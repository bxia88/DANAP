        <h1>다이빙 사고 발생시 따라야 할 절차</h1>
        <p>모든 DAN 아시아 태평양 회원들은 사고시 신속한  행동을 취할 수 있도록 본 절차들을 잘 익혀두셔야 합니다. <br />
          <br />
          <em>참고</em><em>: </em><em> DAN </em><em>회원</em><em>이외에도</em><em>부상를</em><em>입은</em><em>다이버들은</em><em>누구나</em><em> 1</em><em>단계를</em><em>이행하실</em><em>수</em><em>있습니다</em><em>.</em> </p>
        <h6>다이빙 응급상황의 경우     </h6>
        <h2>1단계 - DAN이 지원하는 다이빙 긴급전화에 전화하십시오 </h2>
        <p>이 전화번호들은 회원카드, 탱크 스티커, 알러트 다이버 잡지 및 본 웹사이트에 기재되어 있습니다. </p>
        <p>          이 중 어떤  전화번호를 이용하는 것이 최상인지는 다이버가 있는 위치 및 사용언어에 따라 달라집니다. 다이버 또는 다이버와 함께 있는 이가 영어를 구사할 경우 아시아 태평양  지역에서 주로 이용되는 번호는 DAN 다이빙 응급서비스(DES) 긴급전화입니다. </p>
        <p>          DAN 아시아 태평양이 지원하는 긴급전화 외에도 다이버가 미국, 유럽, 남아프리카 지역에 있는 경우에는  DAN 미국, DAN 유럽 및 DAN 남아프리카 긴급전화를 이용하시면 더 쉽게 연결될 수 있습니다. 해당 전화번호들은 아래 도표를 참조하세요. <br />
        </p>
        <h1><a href="emergency/hotline_numbers.php">DAN 24-시간 긴급전화번호</a> </h1>
        <h2>이 통화의 목적은? </h2>
        <p>&nbsp;</p>
        <ul type="disc">
          <li>다이빙 의학 전문훈련을 받은 의사나 수련의로부터 신속한 자문을 구하기 위해서입니다.  의사/수련의는       여러분이 처한 상황을 검토한 뒤 즉각 취해야 할 조치를 알려드립니다.  </li>
        </ul>
        <ul type="disc">
          <li>일반적으로 의사/수련의는 다이버가 검사를 받기위해 현지 병원으로 가야하는지의 여부 또는 후송 및/또는 치료가 필요한지의 여부를 알려드립니다.         </li>
          <li>의사/수련의는 이송이나 치료 관련 주선은 해드리지 않으며, 특정 의료시설이DAN보험에 가입된 다이버를 치료하도록 승인하거나 DAN을 통한 치료비 보상을 보장할 권한은       없습니다. 이 첫 통화의 목적은 즉각 취해야 하는 조치가 무엇인지를 알려드리는 것입니다.</li>
        </ul>
        <p><strong>참고</strong><strong>: </strong><strong>DAN 회원권과 관련된 세부정보를 소지하신 경우애는 이를 담당의사/수련의에게 알려주시고  여러분을 대신하여 DAN에 연락해달라고 요청하는 것이 중요합니다. 또한 여러분이 연락받을 수 있는 전화번호를 DAN 상담원에게 알려주는 것도 중요합니다.</strong> </p>
        <h2>응급의료상담전화 이용하기 </h2>
        <ul type="disc">
          <li>일반적으로 긴급전화 통화료는 무료가 아닙니다. (호주/뉴질랜드 내       DES/DAN 라인은 예외) <br />
          </li>
             <li>전화를 거실 때 통화요금을 충당할 수 있는 충분한 돈 또는 전화카드가 있는지 확인하시기 바랍니다. </li>
             <li>통화요금이 없거나 다이빙 긴급전화 이용에 어려움을 겪는 회원은 수신자 부담으로TravelAssist (+1-919-684       3483)로 전화하여 상세정보를 알려주시면 다이빙 긴급전화와 연결될 수 있도록 주선해드릴 수 있습니다. </li>
             <li>어떤 이유로든 기타 모든 옵션들을 이용할 수 없는 DAN 회원은  +61-3-9886 9166 (근무시간)       또는 +61-3-8508 9958 (근무외 시간)번으로DAN 아시아 태평양 사무실에 전화하시면(필요한 경우 수신자 부담으로) 필요한 지원서비스와 연결될 수 있도록 주선해드립니다.</li>
          </ul>
