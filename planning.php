        <h1>Planning a Dive Trip?</h1>
         
        <p><em>By DAN  AP Founder, Chairman and Director of Training, John Lippmann, with  contributions by DAN Director Stan Bugg</em></p>
        <h6><strong>Following are some considerations for travelling divers: </strong></h6>
        <p><strong>Is the  dive operator affiliated with a reputable dive training organisation?</strong><br />
Such affiliations can provide some level  of overview of the services provided. However, this cannot guarantee the safety  of an operator as it is difficult for agencies to monitor operations,  especially in distant places.</p>
        <p><strong><br />
          Is the  quality of the breathing gas supplied acceptable?</strong><br />
        This issue is often overlooked. There  have been some serious accidents, some fatal, as a result of contaminated  breathing gas. In developed countries, there are often stringent standards and testing  requirements. However, this is not the case in some developing countries where  such requirements do not exist. This can be an expensive exercise but not impossible  for the operators to do. It is an important safety measure and should be done.</p>
        <p><strong><br />
          What  is the condition of the diving cylinders and valves? Are these cylinders ‘in  test’? How often are they hydrostatically tested?</strong><br />
        The recommended test period varies  between countries, from about one to five years. Visual inspections for  corrosion and cracking are required more frequently. On rare occasions, cylinders  have exploded and have killed or maimed nearby persons.</p>
        <p><em>Are  the cylinder valves in good condition? Do they operate smoothly and are they free  of leaks?</em></p>
        <p><strong><br />
        What  is the condition of the hire regulators, BCDs, wetsuits, masks, snorkels, boots  and fins? </strong><em>Are they well-maintained and functioning properly?  Are they appropriately cleaned and sterilised between uses?</em></p>
        <p>Infections can occasionally be transferred  between users of such equipment and should therefore be cleaned and sterilised  using appropriate procedures after each use.</p>
        <p><strong><br />
          Do  they have appropriate oxygen equipment and is the staff adequately trained to provide  oxygen first aid? </strong><br />
        Appropriate oxygen equipment should  be capable of supplying near-100% oxygen to a breathing diver by both a demand  valve and a high concentration constant flow mask (e.g. non-rebreather mask). It  should also be capable of supplying at least 50% oxygen to a non-breathing  diver. Can the oxygen delivery system provide oxygen to more than one diver at  a time?</p>
        <p><strong><br />
          Is  this oxygen available at the dive site?</strong><br />
          It is important that oxygen equipment  and a trained oxygen provider are available where the diving is taking place.  This often means on the dive boat. In a serious case of decompression illness  (e.g. 
          an arterial gas embolism), it is important  that the victim begins breathing high concentration oxygen<br />
        as soon as possible.</p>
        <p><strong><br />
          Do  they have a sufficient oxygen supply?</strong><br />
          This needs to provide near-100% oxygen  to a diver or divers for the likely period required before an evacuation team  arrives. In some places, this means a supply that will last more than 24 hours  of constant oxygen administration.</p>
        <p><br />
          <strong>Do  they have appropriately trained and competent staff to provide oxygen and other  first aid if required?</strong><br />
        There should be staff available who is  trained and currently competent in oxygen provision, basic first aid and CPR.  It is not enough to simply have had basic training. The staff must practice to  maintain familiarity and competency in these skills. This is more essential in  remote locations without prompt access to good medical care. DAN offers appropriate  training, often at a higher level than many others.</p>
        <p><strong><br />
          Do  they have an appropriate protocol in the event of an accident?</strong><br />
          This should include a prompt call to  a DAN hotline for  advice, provision of basic life support and<br />
        first aid, and knowledge of the local  medical centres and suitable chambers. Be aware that most medical personnel  have little or no knowledge of diving medicine, and may therefore find it very  difficult to diagnose and provide correct management for some potentially serious  diving injuries. By calling a DAN hotline, expert diving medical advice is  accessible 24 hours a day for a diving emergency.</p>
        <p><strong><br />
          Is  there an effective and reliable means of communication between the boat and the  land base and/or emergency services and/or DAN?</strong><br />
        This is important anywhere, but especially  important when distant from land.</p>
        <p><strong><br />
          Do  they provide adequate, clean drinking water on the dive boat?</strong><br />
        Adequate hydration is important to reduce  the potential risk of DCI, as well as conditions such as heat exhaustion, a  common potential problem in warm climates. Divers should be actively encouraged  to hydrate sufficiently.  Clear or  nearly- clear urine can provide a rough indication of adequate hydration.</p>
        <p><strong><br />
          Does  the boat have sun protection?</strong><br />
          This is important in warmer climates  and even more important if the dive sites are distant from the 
        base.</p>
        <p><strong><br />
          Does  the dive boat have suitable, easily accessible and safe entry and exit points  for divers?</strong><br />
        Divers can easily be injured entering  the water if the entry point(s) are not suitable. Exertion on exiting the water  should be minimised to avoid precipitating DCI or cardiac stress after the  dive.</p>
        <p><strong><br />
          Are  decompression arrangements suitable for the type of diving?</strong><br />
          Such arrangements should allow comfortable  decompression for a group of divers and should be 
        appropriate to the level of  difficulty of the dives, the number of divers, and the decompression  obligations that might be expected. If compulsory decompression is expected, a  more complex system will be required which may need to incorporate  decompression stations at multiple depths.</p>
        <p><strong><br />
          What  is the level of underwater supervision provided/required?</strong><br />
Travelling divers have a widely varying  range of expectations when it comes to being led underwater. Some prefer to be  left alone, while others prefer to have the dive closely monitored by the dive guide.  It is helpful to know if the dive operator’s procedures will match the needs  and expectations of your group.</p>
        <ul>
          <li>Is a dive plan imposed and enforced by the  diver leader(s)?</li>
          <li> Is the supervision close or remote?</li>
          <li> Is there any underwater supervision?</li>
          <li> Does the customer have sole responsibility  for their planning and implementation? </li>
        </ul>
        <p>          If the latter, it is essential to obtain  prior knowledge of local conditions and to ensure that divers have adequate  experience for this.</p>
        <h5>Enjoy your diving but be responsible and  prepared. </h5>
        <p>The above  suggestions are just some of the factors to be considered in an effort to  enhance the safety of diving in remote locations. However, most apply equally  to routine diving circumstances. Diving is a wonderful and rewarding activity  and is relatively safe as long as the diver is sufficiently healthy, well-prepared,  and due care is taken by those involved. However, accidents do happen,  sometimes with tragic results. Some are unavoidable while others are preventable.  When accidents do happen it is essential to have suitable management strategies  in place, especially when remote from high- level medical care. Although sometimes  a poor outcome is inevitable, on many occasions injury can be minimised, and  death avoided, by swift and appropriate action.</p>
