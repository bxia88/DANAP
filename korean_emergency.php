        <h1><strong>DAN </strong><strong>응급절차</strong></h1>
        <p>다이버로서 여러분은 다이빙사고로 인해 응급처치가 필요한 상황에 처하는 일이 절대 없기를 바라실 것입니다. 그러나 심지어 가장 경험이 많은 다이버들에게도 사고는 일어날 수 있습니다. 이 섹션은 여러분이 불의의 사고에 대비하는 데 필요한 정보를 제공합니다.      </p>
        <p> 국내 또는 국외로 다이빙 여행을 떠나실 때, 본 응급절차를 인쇄하여 소지하십시오. 응급의료상담 전화번호(Emergency Medical Hotline Numbers)는 DAN 회원카드와 인증서, ID 태그(발급받은 경우) 및 알러트 다이버(Alert Diver) 잡지에도 기재되어 있음을 기억하십시오. </p>
        <p> 어려운 상황에 처한 전세계 수천명의 다이버들에게 도움을 제공해 온 다이버 알러트 네트워크는 세계에서 가장 큰 규모와 가장 풍부한 경험을 자랑하는 다이빙사고 관리전문기관으로서 회원 여러분의 든든한 지원자가 되어드릴 것입니다. </p>
        <h2>사고 후 따라야 할 절차 (Procedure to be used following an accident) </h2>
        <p>아래의 절차는 DAN 회원(또는 회원을 대신하는 사람)이 다이빙 사고 또는 비다이빙 사고 발생시 따라야 하는 절차입니다.</p>
        <p> 모든 회원들은 사고시 발생할 수 있는 혼란을 최소화하기 위해 본 절차들을 잘 익혀두셔야 합니다. 아래 단계들을 클릭하시면 더 자세한 정보를 얻으실 수 있습니다.<br />
        </p>
        <h1><strong>다이빙</strong><strong> </strong><strong>응급상황</strong><strong>의 경우</strong><strong> </strong></h1>
        <p>1단계 - DAN이 지원하는24-시간 다이버 긴급전화에 전화하십시오. <a href="emergency_procedure_step1_korean.php">이곳을 클릭하세요</a><br />
          <br />
2단계 - 담당의사가 긴급후송을 권장한다면, 반드시 DAN  TravelAssist에게 전화하십시오. 
 <a href="emergency_procedure_step2_korean.php">이곳을 클릭하세요<br />
</a></p>
        <h1>비다이빙 응급상황의 경우 </h1>
        <p>다이빙과 관계없는 응급상황이 발생한 경우, DAN 회원은DAN TravelAssist에 연락을 취해야 합니다.  이 때 국제전화 교환원을 통하여 수신자 부담으로 전화하시나 직접 전화 하실 수 있습니다. DAN TravelAssist 상담원에게 여러분이 처한 상황에 대한 상세정보를 주시면 의사와의 상담을 주선해드립니다. DAN TravelAssist는 의사의 소견에 따라 최상의 해결방안을 결정하고 그에 따른 조치를 취해드릴 것입니다.  </p>
