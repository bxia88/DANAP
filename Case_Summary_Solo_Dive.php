      <h1>An Unexpected Solo Dive</h1>
      <p><em>Strong current causes a diver to become separated from his buddy and group. </em></p>
      <h2>Reported Story </h2>
      <p>My usual dive buddy was not going on this particular dive and I got   paired up with a buddy I had never been diving with before. Because my   new dive buddy was also an experienced diver, I didn't think I had   anything to worry about. There was a very strong current and during the   descent, I became separated from the rest of my dive group. My dive   buddy and I were in sight of each other at approximately 15m apart. He decided to continue with the rest of the group without   me. My mistake was that I thought I would see everyone once we   descended. Because of the current I ended up in a different location   than my buddy and dive group.<br />
          <br />
        I found myself at 29m,   in the middle of the Pacific Ocean by myself. No one else was around. My   first feeling was panic, and my immediate thought was to surface.   Fortunately, I did not rapidly ascend to the surface and I relied on my   dive experience and training to know what to do. First off, I had to get   out of panic mode and into safety mode. I told myself, &quot;I was the only   one that was able to get myself out of this mess.&quot; Being at 30m or more is nothing new to me, I love deep dives, but it is scary   when you are in unfamiliar waters unexpectedly solo. I did a controlled   and slow ascent, and remembered to take my safety stop at 4.5m. I surfaced and waved down our dive boat to come and get me.<br />
        <br />
        It   was not until about 30 minutes after I had boarded the dive boat and   disassembled my equipment when my dive master came up to see if I was   OK. He then asked if I wanted to immediately go back down. I was shocked   by how long it took him to check on me and by his question to dive   again. I told him I could not descend because I had already been down to   29m and needed to complete a surface interval.<br />
        <br />
        My   dive buddy got back on the boat, and told me that when he saw me it   looked like I was in trouble and was going to surface so he decided to   continue with the dive. I told him that if he thought I was in trouble   he should not have separated from me. I explained to him that because he   continued without me I could not have helped him either if he was in   trouble. I reported this incident to the main divemaster who at the time   was with a group of inexperienced divers. He said I would not dive with   this buddy or the other divemaster on any future dives.<br />
        <br />
        As if   this was not bad enough, the scenario happened the very next day to   another experienced diver who went diving with the same dive buddy and   divemaster as I had. Fortunately all turned out well, but it could have   been a huge disaster. I consider this incident as a learning experience.   I learned I am capable to use my dive experience and training in   emergency situations. I later reported the dive incident to the main   divemaster at the resort I was staying at. </p>
      <h2>Comment</h2>
      <p>Diving   with a buddy is an important safety measure that increases dive safety   when best practices are followed. This process needs to begin at the   surface, before divers enter the water. Dive buddies should plan the   dive together. Discuss important elements of the dive such as depth,   bottom time, breathing gas management, and what to do if separated (which should have been part of the DM's pre-dive brief).. Also, if either or both   buddies plan any underwater activities such as photography or fishing,   this must be communicated and plan your dive accordingly. If you and   your buddy cannot agree on the dive plan, it is best to uncouple and   find another buddy with the same dive objectives.</p>
      <p>        Complete your   pre-dive check with your buddy and make sure you both are familiar with   the placement and functionality of each other's dive equipment. It's a   good idea to review dive hand signals to avoid any miscommunication.   Good diving practice includes buddies staying in close contact   throughout the entire dive. The ability to see each other from a   distance is not enough to ensure safety; buddies should be able to reach   each other quickly in case of a dive incident or emergency. Buddies   should be able to rely on each other, but not fully depend on one   another or the dive master to complete the dive. Each diver is   ultimately responsible for their own safety. The culture of dive safety   is improved when divers speak up about unsafe situations. Alerting   fellow divers of incidents and potentially hazardous situations can help   promote dive safety and possibly avoid future incidents.</p>
      <p>And to help yourself be found, should you find yourself on an unexpected solo dive, make sure you have on you a couple of signalling devices that will help alert a dive boat to you, such as a surface marker buoy (SMB), whistle and mirror. </p>
      <h1>Have You Experienced a Diving Incident?</h1>
      <p>As part of DAN AP's Non-Fatal Dive Incident Reporting Project (NFDIR)  we are keen to receive information on various problems including     non-fatal  drowning, DCI, equipment problems, oxygen toxicity, boat     encounters, marine  animal injuries, and many others. Please let us know     when you are involved in,  or observe, a potentially dangerous diving     incident. Information from such incidents can  provide a wealth of   important insights about dive   accidents and greatly  assist in   determining good preventative   measures. <em>Note: The incident does not need to have resulted in injury. </em></p>
