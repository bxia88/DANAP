      <h1 class="style1"><span class="style3"></span><em>Sally</em></h1>
      <p>As you have  just read, this was obviously a tragic accident. It is important to review any  such accident and to try to determine what, if anything, could have been done  to prevent it, or to manage it in the best possible manner. In doing so, it’s useful  to ask yourself, what would I have done in similar circumstances?  </p>
      <p> A few  thoughts have occurred to me and I’ll share these. Maybe many readers would  have had similar thoughts.</p>
      <h2>        <strong>Making decisions about the severity  of a problem</strong></h2>
      <p>        I have often  mentioned in talks and articles how important it is for dive professionals not  to try to diagnose whether a diver in their care has suffered a significant  diving injury. Unless medically qualified and trained or adequately orientated  in diving medicine, most dive professionals simply don’t have the expertise and  experience to diagnose a medical problem. </p>
      <p>        There are a  variety of first aid-related courses available through diver training agencies as  well as general first aid training organisations, and DAN. Although these are  valuable for all divers and essential for dive professionals, they are focussed  on the provision of first-line care, which is by definition first aid. They do  not provide sufficient knowledge or training to diagnose anything but a very  basic illness or injury with certainty. That is why, unless the problem is very  minor and it is blatantly obvious what the issue is, it is important to contact  a diving medical specialist for advice for any medical problems that have  developed during or after scuba diving.  </p>
      <p>        You might  wonder why advice from a diving doctor is usually preferred …?</p>
      <p>        The diving  environment exposes a diver to a variety of physical and physiological changes  as a result of increased pressure, the effects of immersion, breathing  compressed gas, and certain other factors. Most medical training includes  little or no education on diving medicine and, as a result, the vast majority  of doctors have little idea of the impact of these changes, especially in  divers with certain pre-existing medical conditions (e.g. epilepsy, diabetes,  heart disease, among others). </p>
      <p>        Sally’s  mother later told me that the dive operator did not seek medical advice. I have  no idea whether or not this was the case. However, it seems unlikely that they  had, given the fact that Sally was diving again two days after becoming  unconscious. In such circumstances, it is essential that the diver be properly  examined and assessed. Unconsciousness soon after diving is often associated  with decompression illness, but can also result from a variety of other  factors, including non-diving-specific medical conditions. All of these need  proper medical investigation, especially before further diving is contemplated.</p>
      <p>        DAN  Asia-Pacific funds a Diving Emergency Hotline which is available 24 hours a  day, 7 days a week to provide advice to divers, dive professionals and medical  professionals in a situation where a diver has symptoms after diving. The call  is directed to a diving medical specialist (who is usually a diver themselves)  who will give advice on the appropriate first aid and appropriate follow-up  assessment and treatment. The injured diver or caller doesn’t need to be a DAN member  to get advice – it is available to anyone for free. The difference is that with  a DAN Member, DAN will then become involved in the management of the situation.</p>
      <p>        It is  certainly wise for dive professionals to use this service to help achieve the  best outcome for divers in their care. It also transfers the decision-making  from the dive professional to the doctor, removing the opportunity for the dive  professional to make a bad decision and reducing potential liability.  </p>
      <p>        Calls to the  hotline are <strong>toll-free within Australia</strong> by dialling <strong>1800-088200</strong>. The hotline  can be reached from <strong>outside Australia</strong> by dialling <strong>+618-8212 9242</strong> although  these calls aren’t toll-free. There is no charge for the service as the doctor provided  their services for free.  </p>
      <h2><strong>The importance of good oxygen first  aid</strong></h2>
      <p>        The administration of as near as possible to 100% oxygen is  the cornerstone of first aid for decompression illness. Prompt and appropriate  oxygen first aid will likely reduce the number of required treatments and  accelerate recovery from DCI. </p>
      <p>        The only  encouraging thing to read in Anny’s email was that oxygen first aid was  available and that Sally was given oxygen. However, no details are provided to  determine the adequacy of this.</p>
      <p>        After many  treatments, Sally can manage to “hobble a few steps at a time” and is currently  waiting to be repatriated to her home country for on-going treatment and likely  many months of constant physiotherapy. It is far too early to know to what  level she will recover.</p>
      <p>        I sincerely  hope that this case report provides some good “food for thought” and discussion  and will serve to reduce the chances of a problem like this re-occurring.</p>
      <p>John  Lippmann<br />
      DAN AP  Founder, Chairman &amp; Director of Research</p>
      
          
