      <h1 class="style1"><span class="style3"></span><em>Update on Rachel following her DCI experience</em></h1>
      <p>As we know, Rachel underwent  testing for a PFO (now referred to as a Persistent Foramen Ovale, rather than a  Patent Foramen Ovale) whilst in South Africa. A transoesophageal echocardiogram  (with contrast) was performed, which until recently was the preferred method in  determining whether an individual has a PFO. </p>
      <p>A transoesophageal echo with  contrast involves the patient being injected with a contrast solution (e.g.  saline solution which has been shaken to create bubbles); an ultrasound probe  is placed down the patient’s throat, at which point they are asked to swallow  at regular intervals. The technician, who is watching a picture of the heart on  a screen, can see the bubbles entering the right side of the heart. If some  bubbles are then seen in the left side of the heart it is evidence of a shunt,  usually a PFO.</p>
      <p>That test can reportedly be less  accurate than a well-conducted transthoracic echo, which is performed through  the chest with Valsalva and sniffing.</p>
      <p>Valsalva is performed by asking  the patient to perform an attempted exhalation against a closed airway, which  is usually done by closing one's mouth, pinching one's nose shut while pressing  out as if blowing up a balloon. This is usually the most common and effective  way of equalisation while diving. </p>
      <p>Upon recommendation from DAN AP,  Rachel contacted a specialist at the Prince of Wales Hospital in Sydney to  undergo further testing. A transthoracic echo (with contrast) was performed and  a PFO was found. As a next step, Rachel will follow up with another specialist  to learn more about the PFO (e.g. its size and whether it can be fixed with  surgery) in order to make an informed decision as to how she will proceed.</p>
	      <p align="center"><img src="_images/pfo_anatomy.jpg" alt="DAN Safety Campaign" width="422" height="459" /></p>
      <p>The presence of this PFO provides a good explanation as to why Rachel  became ill after a relatively non-provocative dive.  However, in Rachel’s case, another rare  condition may also be the answer, which the further testing will reveal. </p>
      <p>Hopefully  for Rachel the PFO will be determined as the issue and it will be large enough  to explain the DCI incident, but not too large to repair, so Rachel can proceed  with the surgery and return to diving. </p>
      <p>PFO repairs  are now relatively common – I haven’t come across many divers with  complications (although some are possible) and most are diving again without  issues post-repair.</p>
