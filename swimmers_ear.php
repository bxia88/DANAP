        <h1 id="storyTitle">Swimmer’s Ear (Otitis Externa)</h1>
        <p><em>Acute inflammation or infection of the outer ear (pinna and ear   canal) caused by prolonged exposure to wet and warm conditions is known   as swimmer's ear.        </em></p>
        <p align="center"><a href="http://www.chinadiveshow.com"></a><img src="_images/swimmers_ear.jpg" alt="Swimmers Ear" width="600" height="523" /></p>
        <h2 align="justify">Statistics</h2>
        <p align="justify"><span title="">Otitis externa</span> affects one in 200 Americans every year and is present in chronic form   in 3-5 percent of the population. Swimmers, surfers and other   individuals who are exposed to wet and warm conditions are at an   increased risk.</p>
        <h2 align="justify">Mechanism</h2>
        <p align="justify">The external auditory canal is the tubular opening between the outside   environment and the eardrum (tympanic membrane). It is covered by skin   and secretes earwax (cerumen), which helps protect against infection.<br />
          <br />
          Otitis   externa, commonly referred to as swimmer's ear, is the acute   inflammation or infection of the external auditory canal, resulting in   ear pain and pus discharge.<br />
          <br />
          Breakdown of the external ear canal's   protective barrier leads to infection. Excessive moisture, mechanical   trauma or underlying skin conditions are contributing factors. The   bacteria normally found in the external ear canal often trigger the   infection. With frequent immersion, water swells the cells lining the   ear canal. Eventually, these cells separate far enough for the bacteria   that are normally found on the surface of the ear canal to penetrate the   skin, where they find a warm environment and start to multiply. Otitis   externa is more likely to develop if the skin in the ear canal is   already chafed and cracked by excessive moisture from showering or   scratching. Bacteria or fungus from the water can easily invade damaged   skin.<br />
          <br />
        Seborrheic dermatitis, psoriasis and excessive cleaning of   wax from the ears that injures the skin lining the external ear canal   may increase susceptibility of the ear canal to infection. Excessive   debris or cerumen may trap water in the canal.</p>
        <h2 align="justify">Manifestation</h2>
        <p align="justify">The chief complaint is generally itching often accompanied by pain,   tenderness and swelling of the ear canal. If left untreated, the   swelling can increase to include nearby lymph nodes and produce enough   pain that moving the jaw becomes uncomfortable.</p>
        <h2 align="justify">Management</h2>
        <p align="justify"><em>First Aid</em></p>
        <ul>
          <li> Avoid getting in the water until after the problem clears up.</li>
          <br />
          <li> Use a hair dryer to carefully dry the ear after you shower (take care to ensure the air is not too hot).</li>
          <br />
          <li> In case of pain, over-the-counter pain medications can be effective.   Examples of such medications include acetaminophen (Tylenol), ibuprofen   (Advil or Motrin) or naproxen (Aleve). Read and follow all instructions   on the label.<br />
        </li>
        </ul>
        <p><em>Treatment</em><br />
          Stop swimming and diving;   schedule an appointment with your physician. Do not put anything in   your ear unless instructed to do so. If you have diabetes or take   medicine that suppresses your immune system, swimmer's ear can cause   severe problems that require immediate medical attention.<br />
          <br />
          It is   important for your physician to rule out external ear squeeze, otitis   media and mastoiditis (infection of the bone just behind the ear).</p>
        <h2>Fitness to Dive</h2>
        <p>Return to diving is possible once your physician determines that the infection 
        has resolved. </p>
        <h2>Prevention</h2>
        <p>Keep your ears clean and dry.<br />
        </p>
        <ul>
          <li> Dry ears with a towel   after swimming or showering by tilting your head and pulling your   earlobe in different directions while your ear is facing down.</li>
          <br />
          <li> Refrain from putting objects — such as cotton swabs or your finger — in   the ear canal or removing ear wax yourself; both actions can damage the   skin, potentially increasing the risk of infection.</li>
          <br />
          <li> You   can dry your ears with a blow dryer if you put it on the lowest setting   and hold it at least a foot (about 0.3 meters) away from the ear.</li>
          <br />
          <li> Talk to your doctor about whether you should use alcohol-based eardrops after swimming.</li>
          <br />
        </ul>
        <p>If   you know you don't have a punctured eardrum, you can use   over-the-counter eardrops or homemade preventive eardrops before and   after swimming. This mixture of one part white vinegar to one part   rubbing alcohol may help promote drying and prevent the growth of   bacteria and fungi that can cause swimmer's ear. At the end of each day   of diving, put five drops of the solution into each ear. Let it stay for   five minutes before draining back out.</p>
