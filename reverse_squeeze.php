        <h1 id="storyTitle">Middle-Ear Barotrauma on Ascent</h1>
        <p>A reverse squeeze is barotrauma due to an inability to release pressure from the middle ear on ascent. </p>
        <p align="center"><img src="_images/reverse_squeeze.jpg" alt="Reverse Squeeze" width="600" height="390" /></p>
        <h2><strong>Mechanisms</strong></h2>
        <p>Pressure must be released from the middle ear as the diver ascends, or   the expanding air will bulge and even rupture the eardrum. Expanding air   normally escapes down the Eustachian tubes, but if the tubes are   blocked with mucus at depth (usually the result of poor equalization on   descent, diving while congested or relying on decongestants that wear   off at depth), barotrauma can result.<em>&nbsp;&nbsp;&nbsp;</em></p>
        <h2>Manifestations</h2>
        <ul>
          <li> Pressure, fullness in ear</li>
          <li> Ear pain</li>
          <li> <span title="">Vertigo</span></li>
        </ul>
        <h2>Management</h2>
        <p><em>While diving:</em> Sometimes one of the equalizing techniques used on   descent will clear your ears on ascent. Pointing the affected ear toward   the bottom may help, too. Ascend as slowly as your air supply allows.   Increasing pressure usually opens the Eustachian tube and relieves   overpressure. However, in rare cases it may persist all the way up. In   that case, you will have to endure the pain to reach the surface. Notify   your buddy, and stay in close proximity.<br />
          <br />
          <em>First aid:</em> Nasal   decongestant spray may help open the Eustachian tube. A physician   evaluation is advised if you experience vertigo, protracted pain and   fullness of the ears.</p>
        <h2>Fitness to Dive</h2>
        <p><em>&nbsp;</em>Repeated episodes require an ENT evaluation. For an ENT referral in your area contact DAN Asia-Pacific: info@danap.org or call +61-3-9886 9166.</p>
        <h2>Prevention</h2>
        <p>Prior to diving, try equalising on the surface to ensure Eustachian tube function 
        is adequate.,</p>
