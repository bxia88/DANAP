         <h1>My Diving Incidents </h1>
		 
		 <p><em>I have been diving since 1985 and I have been an Instructor since 1988. Throughout this time I have logged more than 600 dives. My history  of &ldquo;incidents&rdquo; over this 25+ year period highlights that it is all too easy to  disregard something minor, but that it can very well be a warning sign for  something much more serious.</em></p>
		 <h2>2005  Derawan, Maratua</h2>
		 <p align="justify">Second  dive of the day, depth 26.2m, 32 minutes, fast current.&nbsp; Several hours after the dive the skin on my  abdomen and chest started to itch and my abdomen was swollen and a bit bruised.  I also felt unwell so went to bed. </p>
          <p align="justify">A  surgeon friend on the trip diagnosed it as an allergic reaction, gave me an  antihistamine that night and the next morning, and advised skipping the first  dive. I felt fine when I woke up and went for the second dive, but my buoyancy  was out, I felt full of air, so abandoned the dive.<br />
            <em><br />
            Diagnosis:  Allergic Reaction.</em></p>
          <h2>2008  Bunaken, Manado, Indonesia</h2>
          <p>I  undertook two days of diving. In the evening of the third day the skin on my  abdomen felt itchy, similar to the first incident in 2005. I felt a bit unwell.  I took an antihistamine and was fine by morning.<br />
            <br />
            <em>Self  Diagnosis: Allergic Reaction.</em></p>
          <h2>2009  (January) Similan Islands Liveaboard</h2>
          <p>Felt  well but on one evening I experienced itchy skin on my abdomen.<br />
            <em>Self  Diagnosis:&nbsp; Allergic Reaction.</em></p>
          <h2>2009  (April) Sogod Bay, Philippines</h2>
          <p>After  three days of diving and being out at sea all day, I didn&rsquo;t drink enough water,  felt unwell, needed the toilet, and fainted. I was given oxygen and taken to  the local clinic. <br />
            <em><br />
            Diagnosis:  Dehydration</em><br />
            <br />
          I  then had the itchy abdomen and chest again, and asked the doctor to check for  DCI, however, he concluded it was dehydration.</p>
          <h2>2010  (March) Pulau Weh</h2>
          <p align="justify">Five days of diving, 12 dives, no problems, until the last  evening when my abdomen commenced itching and I felt unwell. Took an antihistamine and felt fine the next morning.<br />
            <em><br />
          Self diagnoses: Allergic Reaction.</em></p>
          <h2>2010 (August) Anilao, Philippines</h2>
          <p>Six days of diving with 12 dives, varying between 15 and 31  metres, but long dives between 51 and 72 minutes, with long deco stops. On the  third night I had the itchy abdomen and felt a little unwell. Again, I took an antihistamine,  rested and was fine the next day.<br />
            <em><br />
          Self diagnoses: Allergic Reaction.</em></p>
          <h2>2010 September &ndash; MEDICAL REVIEW </h2>
          <p align="justify">As I was about to undertake a trip to Tavueni in Fiji and was  feeling uneasy about the incidents on my previous trips, I decided to visit my  GP. </p>
          <p align="justify">The doctor concluded I was fit and well and as the antihistamines  were working for me he also assumed my problem was  an allergic reaction. I was instructed to take an antihistamine first thing in the morning, two hours before I entered  the sea.</p>
          <h2>2010 (October) Taveuni, Fiji</h2>
          <p>For this trip I followed the doctors advice and took an antihistamine  two hours before entering the water each day.</p>
          <p align="justify">I undertook nine dives over four days. The depths varied between  16.1m and 28.9m and dive times were between 30 and 64 minutes. I followed my  dive computers deco stops.</p>
          <p>I felt completely well with no itching or any other problem so  felt the doctor had solved my problem.</p>
          <h2>2010 (December) Raja Ampat, Indonesia </h2>
          <p align="justify">I forgot to take the antihistamine before diving on the first day. As a consequence I had an  itchy abdomen but otherwise felt OK. I took an antihistamine on the second and third day. 
On the second day I still had an itchy abdomen and also felt a  little unwell after dinner.</p>
          <p align="justify">On the third day I decided to skip the first dive, but I  undertook the afternoon and night dive.<br />
            I felt fine throughout the diving, but on return to shore after  a 40-minute boat trip, I suddenly began to feel unwell as we were about to get  off the boat.&nbsp;</p>
          <p align="justify">I felt that my right leg couldn&rsquo;t reach the top of the boat to  climb out. I was helped out of the boat, but said that I needed to lie down, as  I felt faint. I was put on oxygen and carried to my room. My son, who is a  surgeon, stayed with me the whole time, ensured the oxygen was connected, and  that I was kept hydrated. I began to feel slight itchiness on my abdominal  skin. My son asked me questions about my alertness, sight and strength. I had a  little tickle in my throat and I also felt a little nauseous. I was fully aware  of what was happening the whole time. </p>
          <p align="justify">The skin on my abdomen was itchy and felt stretched, swollen,  and a bit bruised; but there were no bruises, rashes or swelling visible to the  eye. I asked my son to phone DAN to get help.</p>
          <p align="justify">My son spoke to DAN AP Executive Director, John Lippmann who  advised oxygen, Nurofen after food, hydration, bed rest, and NO MORE DIVING.</p>
          <p align="justify">John asked my son to monitor me every hour throughout the night  for fear my condition may worsen. There was added concern given the remoteness  of our location, which makes evacuation difficult. In fact, evacuation is not  possible at night.</p>
          <p align="justify">In the morning my son was in contact with John again, and  reported the improvement in my condition. John advised that I maintain his  previous instructions from the previous night. By the evening I felt fine  again, not 100%, but I was able to walk down to dinner.</p>
          <p align="justify">By the next morning I felt 100%. As instructed I did not  undertake any further diving on this trip. While I missed two days of diving, I  still had my life. On the last day I enjoyed snorkelling in the house reef and  mangroves and was able to fly out.</p>
          <p align="justify">On return to Penang I went for a full check up of my heart,  lungs, brain and blood vessels. A small old healed infarct was found in the  left side of my brain, which corresponds with control, and this we all decided  was the reason my right hand and leg didn&rsquo;t work properly.</p>
          <p align="justify">I did not have a hole in my heart but somehow a bubble must have  reached that bit of my brain. The immediate use of oxygen, bed rest, hydration  and nurofen, plus help from my son and advice from DAN, all contributed to my  survival of a very scary incident. <em>(JL: We had no doubt that she would  survive!)</em></p>
          <h2>2011 (February/March) Anilao, Philippines </h2>
          <p>One week diving on Nitrox 32. I had no signs or symptoms of  anything wrong.</p>
          <h2>2011 (May) Manado, Indonesia </h2>
          <p>One week diving also on Nitrox 32. Again, no signs or symptoms  of anything wrong.</p>
          <h2><strong>CONCLUSION</strong></h2>
          <p align="justify">I  think the longer dive times allowed for an accumulation of nitrogen bubbles in  the fatty tissue of my abdomen, leading to the symptoms and problems I  experienced.&nbsp;For me, it is important that I lose the fat and keep fit. I  also need to continually be aware of all the factors that may contribute to DCI  incidents, and ensure safe and more conservative diving from now on.</p>
          <p><em>My Advice</em></p>
          <ul>
            <li><strong>REST WELL BEFORE YOU BEGIN DIVING</strong>. Especially  after a long journey to your dive destination.</li>
            <li><strong>REST BEFORE AND AFTER DIVING</strong></li>
            <li><strong>KEEP WELL HYDRATED</strong></li>
            <li><strong>USE NITROX AND DIVE CONSERVATIVELY</strong>. Keep to  conservative tables and dive computer settings.</li>
            <li><strong>DON&rsquo;T MISS DECO STOPS</strong></li>
            <li><strong>ASCEND SLOWLY</strong></li>
            <li><strong>DON&rsquo;T UNDERTAKE VERY LONG DIVES</strong>. Even if you  have air remaining.</li>
          </ul>
          <h2>COMMENT FROM JOHN LIPPMANN</h2>
          <p align="justify">I received a call from the on-call DAN DES hotline doctor  who passed on the relevant details. It was impossible to evacuate this diver at  this time due to remoteness and the time of night. She was advised to breathe  oxygen by demand valve and mask for 4-5 hours which she did with much  improvement. She was also advised to continue oxygen-breathing through the  night using a constant flow mask. Her son was a doctor and he was to carefully  monitor her throughout. It seemed likely this diver would need to be evacuated  in the morning so options were explored. However, she felt completely well in  the morning and, after a through neurological examination by her son, it was  decided that she could continue her holiday, albeit with no further diving.</p>
          <p align="justify">She was checked for a patent foramen ovale (PFO) and none  was found, although there test can easily be negative unless well-conducted so  it is still possible that a PFO could be present as it is often associated with  the rash described, as well as neurological symptoms.</p>
          <h2>DAN: Issues to Consider</h2>
          <ul type="disc">
            <li>
              <div align="justify"><strong>Don&rsquo;t Self Diagnose.</strong> DAN mans and/or funds 24-hour emergency hotlines       for the purpose of helping divers in trouble. Divers should call and speak       to a doctor or medic trained in diving issues to discuss any symptoms they       may be experiencing.&nbsp; </div>
            </li>
          </ul>
          <ul type="disc">
            <li>
              <div align="justify"><strong>Most GP's Know Little About Diving Injuries</strong>. Diving-related medical issues should be       addressed by a doctor trained in diving medicine who would be more aware       of the stressors of the diving environment. Furthermore, if you are       looking to undertake a medical to determine whether you are &ldquo;Dive Fit&rdquo;       this should be undertaken by a diving doctor. If you need help locating a       diving doctor in your area contact DAN or visit the SPUMS (<a href="http://www.SPUMS.org">www.SPUMS.org</a>.au) website.</div>
            </li>
          </ul>
          <ul type="disc">
            <li>
              <div align="justify"><strong>Use of Antihistamines and other medications when       diving should not be taken likely. </strong>The       effects these medications have on a diver in the underwater environment       can sometimes be different to those experienced on land. It is important       that the doctor prescribing medication understands the environment in       which you intend to use the medication so that you will not be in danger       when diving, e.g. drowsiness is a side effect that must be avoided.</div>
            </li>
          </ul>
