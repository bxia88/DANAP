        <h1>Advice from a DAN Member </h1>
        <h3><em>Continued from Issue 20, Deeper with DAN </em></h3>
        <p align="justify">I was kept out of the emergency room by the  staff while Hazel was being &ldquo;treated&rdquo; and wasn&rsquo;t able to speak to the doctor  about what Dr Roberts had discussed with me regarding the correct treatment for  DCI.</p>
        <p align="justify">After an hour I was told Hazel could be  discharged and I had the opportunity to briefly speak to this doctor who had  taken it upon himself to liaise with a local diving doctor, rather than with  DAN. His conclusion was &ldquo;Grade 1 bends&rdquo;<em> (JL: Musculoskeletal DCI). </em></p>
        <p align="justify">His treatment, I found out later, was five minutes  of oxygen, an injection for pain and a script for anti-inflammatory medication.  Also, while Hazel was in the ER, a certain nurse asked her about the dive and  then stated,<em> &ldquo;You can&rsquo;t get bent from diving to 14m&rdquo;</em>. Say what?</p>
        <p align="justify">I called Dr Roberts and she was concerned about  this outcome, recommending six more hours of oxygen. </p>
        <p align="justify">What this incident did highlight to me was how  ill-informed most doctors are about diving medicine. It is definitely a  specialised field and treatment should not be undertaken without consulting  DAN.</p>
        <p align="justify">On the other hand, it was a good exercise on  what to do and what not to do in an emergency situation.</p>
        <p align="justify">I have the utmost faith and appreciation for  DAN. If you should ever be in a similar situation I strongly suggest you insist  that the doctor attending to the patient speak directly to DAN.</p>
        <h2>Comment from DAN Asia-Pacific</h2>
        <p align="justify">As you know, the effects that scuba diving has  on the body are quite unique. Specialised training in Diving Medicine is  required to be able to effectively diagnose and provide advice about  appropriate treatment for a diver exhibiting symptoms commonly associated with  DCI. When you call a DAN Hotline, you will speak to a doctor or medic trained  in diving medicine. As such, at the first onset of symptoms it is essential to  get DAN involved immediately. DAN will co-ordinate your treatment with the  local treatment facility to ensure the correct treatment is provided.</p>
