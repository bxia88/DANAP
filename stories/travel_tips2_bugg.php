        <h1>Dive Travel Tips  </h1>
         
        <p><em>By DAN Asia-Pacific  Director, Stan Bugg</em></p>
        <h2>Preparing Your Gear for a Dive Trip </h2>
        <p align="justify"><strong>Mask:</strong> Is the strap OK? Look for signs that it is  perishing, or likely to break. Replace if in doubt. Check that it seals onto  your face by using the standard &ldquo;fit&rdquo; test of putting it on without straps, and  inhaling.</p>
        <p align="justify"><strong>Fins:</strong> Are the buckles intact? Are the straps damaged?  Replace if in doubt.</p>
        <p align="justify"><strong>Thermal Suit:</strong> Check for fit, especially if it  has been a while since you dived, and the suit has shrunk (!!!) Check and  lubricate zippers.</p>
        <p align="justify"><strong>Buoyancy Compensator:</strong> Inspect the bladder for leaks by  inflating it and immersing it into a bath. Check the dump valve for function.  It should be airtight unless opened manually. Check the hose for perishing. The  inflator mechanism should be checked for function, and freedom from leaks. If  there seems to be a problem, unless you are sure of what you are doing, it  might be time to find a qualified technician.</p>
        <p align="justify"><strong>Computer: </strong>Check the strap for damage. Ensure  the battery life is sufficient for your intended dive schedule.</p>
        <p align="justify"><strong>Torch:</strong> Inspect O rings for damage, then clean and  lubricate them. Clean seal faces thoroughly. Buy fresh batteries for the trip. </p>
        <p align="justify"><strong>Regulators:</strong> If any of these tests give you  cause for concern, and you do not possess the skills to sort it out, it is time  to visit the experts again. Inspect all hoses for damage, looking especially at  where hoses join fittings. Replace if you are in any doubt. Check all demand  valves for ease of breathing. Next, check for leaks by immersing the entire  regulator and valve while attached to a cylinder and turned on, and look for  bubbles.</p>
        <p>These basic steps are of special importance if you  and your gear have been idle for an extended period.</p>
