	      <h2><strong>On the Road to Recovery </strong></h2>
	      <p align="justify">I flew back home with huge improvement but even today, more than 3 months away from the accident I am still only partially back on track. I can only stay in front of the computer for no longer than 3-5 hours a day with long intervals. I drive in periods of 30 minuted with 10-15 minute long breaks. It is clear that it will take me one or two more months to recover completely.</p>
	      <p align="justify">As I continue to recover my eyes struggle to deal with very basic tasks. For example, going outside to the supermarket one day the wind blew something into my eye and this started a new infection and created the need for a new set of antibiotics. I should probably be wearing my diving mask every time I go outside!	        </p>
	      <p align="justify">It wasn't without complications either. I got keratitis in course of all this and for a period of 2 weeks my vision dropped dramatically (thanks to Dr Liu of 421 Navy Hospital, Guangzhou, we were able to suppress it and my vision fully restored). I am still on medication but getting better and better and I hope to return to diving in a few weeks. Oh yes, people keep asking me if I would quit diving after all this. Answer is NO. </p>
