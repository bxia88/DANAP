         <h1>It Happened To Me </h1>
		 
		 <h2><strong>Evacuation to Manila </strong></h2>
		 
          <p align="justify">I want to stress that a call from John Lippmann arrived within 15 minutes after I finished talking to Dr Wilkinson and ever since this moment till I was on board of a plane back home John was literally by my side calling and messaging me several times a day; this was a very, very powerful support and I never felt alone. I was advised to get to Manila immediately, and during my time in transit the arrangements were to be made for me to be seen by some reputable doctor there. Coron has pretty fine air connection to Manila but the timing was bad &ndash; Chinese New Year &ndash; so the earliest I could fly was early next afternoon. Very important thing was that there is some endemic eye disease in the Philippines that the locals are afraid of, so I had to visit a local, errr&hellip; &quot;hospital&quot; and obtain a paper from there stating that it wasn't my case. Otherwise I would not be allowed to fly regular airlines and DAN would have to snatch me from Coron by a chartered craft.<br />
            <br />
            Before I took off, John was back to me with directions to the Asian Eye Institute in Makati area, Manila. It was a decent facility staffed with personnel whose qualification arose no doubt. A doctor saw me within 10 minutes of my arrival as John had called ahead and arranged the appointment. Fact is that after 5 days it was practically impossible to pinpoint the exact reason of my eye inflammation (invisible jellyfish tentacle? venomous plankton? micrograin of bottom sediment?) so we had to focus on treatment of secondary infection rather than find a specific treatment for my particular case. Hence I was given tons of antibiotics and anti-inflammatory steroids and that was it. We did no lab tests and the diagnosis on paper was something very generic, like &quot;infected conjunctivitis&quot;. I had to return to the Institute a total of three times because I had a very bad day after my first night in Manila and I needed urgent attention (I got a dozen of evil dwarves restlessly throwing shovels of abrasive sand right into my eyes and that was really really unbearable). One day before leaving I returned for the removal of fiber membranes from my eyes and to pick up a fit-to-fly certificate.
