	      <h2><strong> What	You Can Learn From My Experience </strong></h2>
          <ul>
  <ul>
    <li>Try to keep your eyes closed while purging your mask.</li>
    <li>      Try not to open your eyes at all when you are underwater and your mask is not on.</li>
    <li> Always wear maximum protection: No shorties and wear gloves and hood. While it was not my experience, a naked hand brought to the eye after touching bottom stuff could lead to a similar result.</li>
    <li>Never go near the water without proper insurance and never settle down with the basic options &ndash; always top it up. If I had been insured to the top I would have been compensated for a ruined holiday just for extra few tens dollars a year, a cost of a single business lunch&hellip;</li>
    </ul>
