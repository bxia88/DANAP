        <h1>Dive Travel Tips  </h1>
         
        <p><em>By DAN Asia-Pacific  Director, Stan Bugg</em></p>
        <h2><strong>Traveller&rsquo;s  Diarrhoea </strong></h2>
        <p align="justify"><em>... Continued from Issue 20 Deeper with DAN </em></p>
        <p align="justify">First of all, carefully consider what you eat and  drink. Stick to food that has been thoroughly cooked, and is still hot. Avoid  lukewarm &ldquo;bainmarie-type&rdquo; meals, or pre-prepared food that has been standing  for a long time. </p>
        <p align="justify">Raw food (fruit, salads) should be avoided unless it  can be peeled, or washed in water guaranteed to be clean. If at all possible,  perform the washing or peeling yourself, as food can be contaminated during  this phase if the water is suspect.</p>
        <p align="justify">Avoid drinking tap water or its derivatives. Most  places claim that their water is safe to drink, but if you are not sure, err on  the safe side. Many travellers drink only bottled water &hellip; a wise choice if  there is any doubt &hellip; but they allow ice to be added to mixed drinks, or accept  a jug of water with their meals. Both are more than likely from the tap. Insist  on drinks being ice-free, and ask for bottled water at restaurants if you are  at all unsure.</p>
        <p align="justify">At your hotel, use bottled or sterilized water to  brush your teeth, and to rinse your toothbrush.</p>
        <p align="justify">If, despite all precautions mentioned above, you  succumb to an attack, prompt action can reduce the severity and duration of the  affliction. </p>
        <p align="justify">At the first sign of a problem, hydrate regularly,  and consider taking over the counter medication to settle the situation.  Imodium is an effective option here, but use it cautiously, and within  recommendations. Maintain liquid intake, but if you do not feel like eating,  there is no harm in missing a meal or two.</p>
        <p align="justify">If the problem persists for more than a few days, or  if there is fever present, seek medical advice.</p>
        <p align="justify">Meanwhile, be scrupulous with your personal hygiene,  and ensure that you do not pass the problem on to others. Do not share drink  bottles, and avoid handling any food for consumption by other people. </p>
        <p align="justify">Although these steps cannot entirely guarantee that  you will not became affected, they should help most people avoid, or reduce the  severity of an attack.</p>
