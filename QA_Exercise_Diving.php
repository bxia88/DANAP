        <h1>Member Question</h1>
         
        <h2><strong>Exercise and Diving</strong></h2>
        <p><em><strong>Question:</strong> I wanted to get some  clarification about exercise and diving. I was taught not to exercise for 6  hours after a dive because it increases blood movement around the body.&nbsp;Is  this still correct? </em></p>
        <p><em>Also, what about exercising before diving?</em></p>
        <h1>DAN Answer</h1>
        <p>        Thanks for your question about the effect of exercise before, during and after  diving. Following is some advice: </p>
        <h2>Before Diving</h2>
        <p>          Exercise  immediately before diving might be a problem if the heart rate has not settled  to close to normal early into the dive. However, some more recent studies  indicate that vigorous exercise before diving can possibly reduce the chances  of DCI but the time period before diving has yet to be determined.&nbsp;</p>
        <h2>During Diving</h2>
        <p>          Exercise  during the dive increases the circulation so more nitrogen is delivered to, and  absorbed by, tissues with increased blood flow. Exercise also increases carbon  dioxide production, which in turn causes blood vessels to widen, further  increasing blood flow to the tissues.&nbsp;There is strong evidence that  moderate to heavy exercise during a dive increases decompression requirements  and the likelihood of DCI. On the other hand, there is also evidence that mild  exercise during decompression can increase inert gas elimination at that time. <em>(taken from &ldquo;Decompression Illness&rdquo;  by John Lippmann) </em> </p>
        <h2>After Diving</h2>
        <p>          Exercise  after diving has been shown to increase the likelihood and sometimes the  severity of DCI. The exercise will increase turbulence in the blood and tissues  and this can encourage bubble formation (somewhat like shaking a carbonated  drink)</p>
        <p><em>Taken from the  book &ldquo;Decompression Illness&rdquo; by John Lippmann.&nbsp;&nbsp;&nbsp;&nbsp;</em></p>
