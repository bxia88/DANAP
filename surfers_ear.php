        <h1 id="storyTitle">Surfer's Ear</h1>
        <p>Surfer's ear is the narrowing of the outer ear canal due to an   abnormal growth of bone caused by exposure to cold and wet conditions. </p>
        <p align="center"><a href="http://www.chinadiveshow.com"></a><img src="_images/surfers_ear.jpg" alt="Surfers Ear" width="600" height="360" /></p>
        <h2 align="justify">Mechanism</h2>
        <p>The external  ear canal is a tubular structure that conducts sounds and protects the middle  ear. Exostosis is a chronic condition characterized by narrowing of the inner  half of the ear canal as a result of bone growth. The bony wall grows outward  slowly over a period of years in response to local irritation by cold water.  These growths are called swimmer's nodes and are common in swimmers, surfers  and divers. This condition is not related to infection nor is it caused by  infection; however, the narrowing of the ear canal may prevent water from  draining out, which increases susceptibility to outer-ear infections. The bony  swellings continue to grow while there is a continued exposure to cold water  (such as that found in seawater and outdoor swimming pools in temperate  climates). Exostosis often occurs in outdoor enthusiasts in their mid- to  late-30s, but individuals who experience significant cold-water exposure — such  as surfers, swimmers and divers — can develop the condition earlier.<br />
          <br />
The narrowed ear canal is more prone to blockage by earwax or debris and more  susceptible to swimmer's ear (otitis externa). An exostosis on the floor of the  ear canal can form a sump that retains moisture and is susceptible to  infection. Exostosis is seen as a narrowing of the ear canal. The average ear  canal is about 0.25 inches wide (7 milimeters). The bone growth may cause it to  narrow to 0.04 inches (1 millimeter). Early signs include water trapping in the  ear canal after swimming. Debris trapping and infections may make surgery  necessary.</p>
        <h2>Manifestations</h2>
        <p>External ear  infections and difficulty removing water from the external ear canal may be  recurrent. Exostosis symptoms in advanced cases include a decreased hearing  possibly combined with an increased prevalence of ear infections.</p>
        <h2>Differential Diagnosis</h2>
        <p>Other causes  of external ear-canal obstruction could include infection or earwax (cerumen)  impaction.</p>
        <h2>Treatment</h2>
        <p>In case of  decreased hearing or repeated infections, exostosis may be removed surgically.</p>
        <h2>Fitness to Dive</h2>
        <p>Exostoses do  not affect fitness to dive unless they are occluding the ear canal or causing  recurrent infection.</p>
        <h2>Prevention</h2>
        <ul>
          <li>Wear a hood in cold water.</li>
          <li> After diving, rinse both ears with freshwater to  flush contaminated water and salt.</li>
          <li> If prone to ear infections, blow warm air into  external canal using a hair dryer (take care to make sure the air is not too  hot).</li>
          <li> If your ears have a natural tendency to build up a  blockage of earwax, have them checked regularly, particularly before a  prolonged diving trip.  </li>
        </ul>
