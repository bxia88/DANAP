      <h2>Oxygen First Aid Awareness and Preparedness Campaign </h2>

        <h1>&nbsp;</h1>

        <p align="center"><img src="_images/O2_First_Aid.jpg" width="619" height="888" class="imgCentre" /></p>

             <p align="left">.</p>
             <h1 align="left">Steps to Get Your Business Oxygen Prepared</h1>
      <h2>Step 1: How Many Oxygen Units Does Your Business Require?</h2>
      <p>This is determined by:</p>
      <ul>
        <li> The distance/time from land to the nearest appropriate treatment facility (with more oxygen)</li>
        <li>          The distance/time from the furthest dive site to shore</li>
        <li>          The number of boats in operation<br />
        </li>
      </ul>
      <p>It is also important to have backup oxygen available in case, for example, a unit needs to travel with a diver to a treatment facility, whilst others continue to dive.</p>
      <h2>Step 2: Make Sure You Have a Sufficient Supply of Oxygen</h2>
     <p>There must be enough oxygen for a diver to breathe from surfacing until reaching high-level medical care 
       with more oxygen. Depending on your location, this could mean a few hours, but in remote difficult to reach areas, 
       it could mean that a 24-hour supply of constant oxygen administration is required while awaiting an evacuation.</p>
     <h2>Step 3: Effective Oxygen Equipment that is Well Maintained</h2>
     <p>As shown on the above poster, equipment must be capable of supplying near-100% oxygen to a breathing diver by both:</p>
     <ul>
       <li> A demand valve; and</li>
       <li> A non-rebreather mask, with a flow rate of at least 10-15 litres per minute (lpm).</li>
     </ul>
     <p>       It should also be capable of supplying at least 50% oxygen to a non-breathing diver (e.g. resuscitation mask 
       with oxygen).</p>
     <h2>Step 4: Make Sure Oxygen is Available at Each Dive Site</h2>
     <p>Ensure oxygen and equipment are readily available, functioning properly and ready to use at each dive site.</p>
     <h2>Step 5: Staff Trained in Oxygen First Aid</h2>
     <p>Make sure you have trained staff competent to provide oxygen (as well as first aid and CPR) at each dive site 
       so an injured diver can be quickly assisted.</p>
     <h2>Step 6: DAN Can Help You</h2>
     <p>The DAN Team is available to help you implement an effective <strong>Oxygen Preparedness Plan</strong> that meets your 
       business needs. Email <a href="mailto:sales@danap.org">sales@danap.org</a> with the following details and you will receive advice regarding the 
       equipment you need to ensure your business is Oxygen Prepared:</p>
     <ul>
       <li>         Your location</li>
       <li>         Distance to the nearest treatment facility (with basic information about this facility)</li>
       <li>         The number of boats you operate</li>
       <li>         The distance (km/time) of each dive site to land.</li>
     </ul>
