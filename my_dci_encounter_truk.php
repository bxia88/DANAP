        <h1>My DCI Experience - Truk Lagoon </h1>
        <h2><strong>The Diver</strong></h2>
        <p> A Divemaster  with 800+ logged dives. </p>
        <h2><strong>The Dives</strong></h2>
        <p>Four days of  diving with three dives per day. Maximum depth was 54.5m on Day 3. Surface  intervals ranged from 1.5 – 2.5 hours between dives. On days 1 &amp; 2 dives  were performed on air with a minimum safety stop of 10 minutes at 6m. On Day 3  all dives were performed using 30% nitrox with a minimum 10 minute safety stop  at 6m using a 53% mix to off gas on. On Day 4 dives were performed using 30%  nitrox with a minimum 10 minute safety stop at 6m using a 53% mix to off gas  on.</p>
        <h2><strong>The Complications</strong></h2>
        <p>On the  evening of Day 2 I noticed a rash on my chest. I had no other signs of DCI so I  was not too concerned by the rash, however I decided to use nitrox just to be  safer, and to reassure myself that I  was okay.At the end of diving on Day 3 I still had the rash but  I was feeling great.</p>
        <p>By the end of Day 4 I was feeling  lethargic and had a headache. At dinner I have been informed that I was talking  in riddles and was disoriented. I have no memory of this.</p>
        <p>Fortunately my dive leader  quickly called the DAN DES Hotline. My symptoms were conveyed to the diving  doctor who recommended immediate oxygen first aid and for my condition to be  monitored. I was put through some neurological tests, which I failed miserably.  The doctor advised that it appeared to be a neurological bend and I needed  treatment in the local chamber.</p>
        <h2><strong>The Resolution</strong></h2>
        <p>The next  morning I underwent a Table 6 recompression. I left the chamber feeling great.  The following morning I returned to the chamber for a Table 5 treatment and all  symptoms resolved.</p>
        <h2><strong>The Realities</strong></h2>
        <p>For those  that have not been to Truk, things like getting a phone to work or even a  reliable power supply can be a challenge, and this applies for many remote dive  destinations.<br />
          <br />
          Further complicating matters, the  chamber could not receive the patient until morning as some routine maintenance  had to finalised that evening. It was a great relief that sufficient oxygen  supplies were available so this diver could receive oxygen all night until he  entered the chamber. Divers in his tour group sat with him throughout the night  to monitor his condition and change the oxygen bottles as they emptied. </p>
        <p><strong>Neurological DCI</strong><br />
        Divers with  decompression illness (DCI) quite often have some damage to their nervous  system. The signs of this are often vague and may go unrecognised, causing  delays in treatment. To determine neurological DCI tests are conducted on the  divers orientation, eyes, face, hearing, swallowing reflex, tongue, muscle  strength, sensory perception, as well as testing their balance and  co-ordination. </p>
        <h2><strong>          Considerations</strong></h2>
        <p><strong>Ensure Oxygen is Readily Available. </strong>If suitable oxygen &amp;  equipment (including a demand valve and mask as well as a non-rebreather mask)  was not available, or the supply was not sufficient for the extended period,  the outcome of this divers’ DCI could have been very different. Before booking a trip with a  dive operator take a few moments to ask about the operator’s preparedness for  emergency situations. This case highlights how vital oxygen first aid is for  diving illnesses. </p>
        <p><strong>Don’t Ignore Symptoms. </strong>Divers will often deny or underplay symptoms in an effort to return to  the water. Continued diving may worsen the condition and make treatment more  difficult. Some common symptoms of DCI are numbness, tingling, headache,  dizziness, nausea, pain, rash, weakness, and unusual fatigue. Other signs and  symptoms include difficulty breathing, visual disturbance, itching,  unconsciousness, and convulsions, amongst others. </p>
        <p><strong>Extend Surface Intervals: </strong>Bubbles can often be found in a diver’s venous  blood after surfacing from a dive so it is a good idea to extend surface  intervals as long as possible between dives: Two – three hours, preferably longer, especially after deeper dives.<strong> </strong></p>
        <p><strong>Costs Involved: </strong>In this case, two-recompression  sessions cost $6,000. This diver incurred no out of pocket expenses as the  treatments were full covered under his DAN Asia-Pacific Membership &amp;  Treatment Insurance.</p>
