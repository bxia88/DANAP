      <h1>DAN Helped Me</h1>

        <h2><strong>Where</strong> </h2>
        <p>Malapascua, Philippines.</p>
        <h2><strong>The Diver</strong></h2>
        <p>Very experienced having completed 700+ dives.</p>
        <h2><strong>The Dives leading up to the day of the incident</strong></h2>
        <p><strong> Day 1</strong> </p>
        <p>Dive 1: 25m, surface interval (SI) of 2h 57 mins  </p>
        <p>Dive 2: 24m, SI of 3h 47mins</p>
        <p> Dive 3: 20m, SI of 2h 51mins </p>
        <p>Dive 4: 9m.</p>
        <p><strong>Day 2</strong></p>
        <p>Dive 1: 24m, SI of 3h 35 mins</p>
        <p>Dive 2: 21m, SI of 1h  33 mins </p>
        <p>Dive 3: 22m.</p>
        <h2><strong>Day of the Incident </strong></h2>
        <p>Not wanting to miss the chance to  see the Thresher Sharks again, it was the third early morning start for the  diver, who was feeling tired. The dive was to 28m for 59 minutes and she was  breathing EAN32.</p>
        <p>When tired  the diver sometimes experiences sinus pressure problems, which gives her discomfort  when descending. That morning she experienced no discomfort. <br />
            <br />
          The diver  says: <em>“The unusual part of the dive is  that this time I was feeling sinus pressure when I was ascending, so we managed  to ascend even slower than usual to deal with the sinus pressure, while still  feeling discomfort”.</em></p>
        <h2 align="justify"><strong>The Onset of Symptoms</strong> </h2>
        <p align="justify">On the way back to shore the  diver started to feel a minor tingling&nbsp;in her knees.&nbsp;She asked for oxygen.  Fortunately the boat was equipped and she started breathing O2 and continued  doing so until the boat arrived at shore (approx 25mins), at which point the dive  shop manager checked the diver’s symptoms and immediately called&nbsp;the DAN  Diving Emergency Service hotline.&nbsp;</p>
        <p>The diver  was advised to continue breathing 100% O2 for a few hours, rest and see if  symptoms decreased. During this time she drank&nbsp;lots of water and took&nbsp;Ibuprofen  on the DAN doctor’s suggestion. Unfortunately the diver’s condition&nbsp;did  not improve. The diver says: <em>“I&nbsp;started  to feel very weird, like if I was walking on the&nbsp;clouds.&nbsp; I&nbsp;was  awake and talking to people, but&nbsp;I&nbsp;didn’t feel like it was&nbsp;real”.&nbsp;</em></p>
		<p align="center"><img src="_images/psh_photo.jpg" width="423" height="317" /></p>
        <p>DAN advised  that Chamber Treatment was required and as such the diver, with the assistance  from the dive centre, travelled to Cebu, the nearest appropriate facility, by  boat and then car. She was provided with O2 all the way to the chamber.&nbsp;</p>
        <p>The total  journey time was close to 4 hours. This was longer than usual due to a local  festival taking place. The diver arrived at the Chamber at 10:00pm where the  doctor, nurses and chamber operators were ready and waiting, following  co-ordination by DAN. The diver was examined and then a voluntary military  nurse joined the diver in the chamber for the four hours and 50 minutes  treatment. Being locked in a small and confined environment and not being  allowed to sleep (in order to ensure that the oxygen was been properly  administered) was not easy for the diver. Fortunately, only one treatment was  required. </p>
        

        <h2><strong>The Diver Says</strong></h2>
        <p><em>I</em><em>&nbsp;never  thought&nbsp;I would&nbsp;have DCI and I never thought&nbsp;I&nbsp;would need  to be treated in a chamber.&nbsp;My diving profiles are always conservative:  diving within my limits, always with the assistance of a dive computer and  using EAN Nitrox for most of my dives.&nbsp;I&nbsp;now realise&nbsp;that you cannot  be 100% sure as to how your body will respond.&nbsp;I was quite tired and had I  decided not to undertake that dive, maybe&nbsp;I would&nbsp;not have had  DCI.&nbsp;My advice is to listen to your body signals carefully.&nbsp;If your  body says “I’m tired”, don’t ignore it, no matter what.&nbsp;In the end you are  there to enjoy your holiday, not to end up in a chamber. </em></p>
        <p><em>Thanks to&nbsp;everyone&nbsp;that helped me. It is  because of them that I am well and healthy today. DAN was of great help from  the moment they received the call about my condition; their professionalism and  care was so efficient and touching.</em></p>
        <h2><strong>Final Comment by DAN's John Lippmann</strong></h2>
        <p>          When the  doctor on the hotline received the call he diagnosed that the diver’s headache  was likely due to sinus barotrauma given her description of how it occurred. He  suspected that her tingling was likely DCI. Ibuprofen and other similar  non-steroidal anti-inflammatory medications can be helpful in the management of  both of these. She initially improved with oxygen breathing and this is not  uncommon as the oxygen breathing helps to eliminate the excess nitrogen in the  bubbles and body tissues. However, the fact that further symptoms then arose  was concerning and we soon advised that she go to the chamber. There was some  initial difficulty gaining access to this chamber but this was overcome.  Fortunately she responded very well to treatment.</p>
        <p>The diver  herself has given good advice in that we shouldn’t dive if we are feeling  overly tired or unwell. She might have already been suffering very mild DCI  from the earlier diving and exacerbated it on this dive.</p>
