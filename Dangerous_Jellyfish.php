        <h1>Dangerous Jellyfish Safety Campaign</h1>
        <p align="justify">For sometime now DAN  Asia-Pacific has been undertaking an investigation in to dangerous Jellyfish in  the Asia-Pacific in an effort to reduce the increasing incidence of fatalities  and serious stings. </p>
        <p align="justify">For many years it has been  known that dangerous jellyfish are present in northern Australian waters,  especially during the wet season, however, less known, and less publicised, are  the potentially deadly species of jellyfish that are prevalent in various  waters throughout the Asia-Pacific. Sadly, this lack of awareness has lead to  numerous unnecessary deaths in countries including Thailand, Malaysia, the  Philippines and Indonesia. </p>
        <p align="justify">These countries are  must-visit destinations for many divers and as such there is an increasing need  to learn more about the presence of Jellyfish in various countries, the  different species and how to effectively treat stings from the different  species. </p>
        <p align="justify">DAN AP is part of a group  of interested researchers who are trying to track the presence of dangerous  jellyfish in various parts of the Asia-Pacific. </p>
        <p align="justify">DAN AP Executive Director,  John Lippmann, is encouraging divers to participate in this important research  by passing on information of jellyfish sightings, stings and where possible  photographic evidence. This information will enable the various jellyfish to be  identified and on occasion new species documented. </p>
        <h2 align="justify">Asia-Pacific Map of Jellyfish Stings</h2>
        <p align="justify">To further promote the  seriousness of this issue DAN Asia-Pacific has created a dedicated <a href="research/jellyfish/jellyfish.html">Jellyfish</a>  section on our website. </p>
        <p align="justify">We are progressively  adding reports to the site of the various jellyfish reports we receive.  Malaysia, Thailand and Vanuatu are the first countries that have been  documented and included is a history of the fatal and non-fatal stings  throughout the area. </p>
        <p align="justify">Creating awareness of  dangerous jellyfish in the areas will allow divers frequenting these areas to  arm themselves with first aid techniques and information concerning the various  species and where they are likely to be encountered. Documenting this  information also enables the Governments and water-safety authorities in these  areas to develop safety protocols. </p>
