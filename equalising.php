        <h2>Continued from &quot;Equalising&quot; Post</h2>
        <p align="justify"><a href="http://www.chinadiveshow.com"></a>The  most common and usually the most effective method of equalising involves the  diver holding his/her nose and blowing gently. This is known as the &ldquo;Valsalva  Manouvre&rdquo;. Although this method is very effective, damage can occur if it is  done too vigorously. Overly-forceful equalisation should be avoided. Other  methods include swallowing (with or without the nose blocked), wriggling the  jaw, squeezing the tongue against the soft palate, or a combination of these.  Divers need to ensure that whatever technique they use is effective in  preventing pressure build-up.</p>
        <p align="justify">If  the pressure is not equalised, swelling and bleeding of the lining within the  ear will occur, which helps to equalise the pressures and relieve the pain,  although this can often lead to infection. In some cases the eardrum can  rupture and, in very severe cases, delicate membranes within the inner ear can  tear, enabling fluid to leak from the inner ear and creating the potential to  damage this delicate organ, possibly affecting hearing and balance. </p>
        <p align="justify">Bubbles  from decompression can also form in or around the inner ear and so disrupt its  function - inner ear decompression illness. In addition, a variety of other  non-diving-related conditions affecting the ear can cause symptoms in divers  during or after a dive.</p>
        <p align="justify">Signs and symptoms associated with inner  ear dysfunction may include hearing loss, vertigo (spinning), dizziness,  ringing or other ear sounds (tinnitus), nausea and vomiting.</p>
        <p align="justify">Any  diver with these signs or symptoms after diving should promptly call a  DAN-supported diving emergency hotline for advice. The doctor or medic will  take a thorough history including the dive profile, the mode of equalisation  and any equalisation issues, and the time of onset and progression of symptoms.  A suitable course of action will then be determined.</p>
        <p align="justify">An  inner ear barotrauma or decompression illness needs to be managed swiftly and  appropriately to minimise the potential for permanent injury.</p>
        <p align="justify">Although  ear injuries very commonly result from diving, especially with novices,  historically, DAN AP has had relatively few Members who sustained severe ear  injuries. <strong>However, this appears to be changing, and over recent years we are  receiving an increasing number of reports of divers with serious ear injuries. Over the next few days we will look at three recent incidents involving ear injuries.</strong></p>
