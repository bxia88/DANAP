        <h1>Do you have DAN Dive Injury Insurance as well as DAN Membership?</h1>
         
        <p><em>Continued from DAN's Safety Stop (May 2015) </em></p>
        <p>After continuing to feel very tired and “cloudy-headed” for  the next six days he finally contacted one of the local hyperbaric units. The  doctor diagnosed likely DCI, as although symptoms were vague there was no other  obvious alternative, and recommended recompression treatment. The doctor then  called the DAN Diving Emergency Service Hotline (DES) and discussed this with  the on-call doctor who agreed that recompression was indicated.  The DES doctor then called the case in to DAN  AP.   </p>
        <p> When the diver’s details were checked it became clear that,  although he was a DAN AP Member, he had not taken out an optional dive injury  insurance plan at the time of joining. <strong>This  meant that he had no coverage from DAN for the costs of the treatment</strong> he  required.</p>
        <p> A DAN AP staff member called both the diver and the local  diving doctor to explain this. The diver was told that treatment would likely  cost around US$3500. As the diver had no other insurance and was short of funds  he decided not to have any treatment. Fortunately, his symptoms abated after a  few more days.</p>
        <p><strong>Comments from DAN AP's John Lippmann </strong><br />
          While DAN Membership includes coverage for emergency medical  evacuation (in accordance with the conditions), it does not provide cover for  treatment for a dive injury. This is where the optional DAN Dive Injury  Insurance may be necessary. In some countries, recompression may be covered  under a National Health system but this is not the case in many places and  insurance is then a real benefit. </p>
        <p> We have tried hard to make it clear when people sign-up  exactly what membership includes and explain that this does not include  treatment insurance. Despite this, some people end up in the position of this  Member, either through not fully understanding the situation or deciding to  take the risk of not taking the extra cover. The belief that “it will never  happen to me” is fortunately true most of the time … but not always!</p>
        <p> It’s obviously unwise to dive or continue diving without any  ability to monitor depth and decompression status. We have no idea if the diver  breached any decompression obligations. However, remember that the vast  majority of divers who are treated for DCI had been diving within their  computer’s limits.</p>
        <p> Once this diver noticed symptoms after diving, the wise  thing to do would have been to call the DAN DES hotline and get advice. It is  likely that prompt oxygen first aid would have been advised, and, if done soon  enough and for long enough, the diver’s symptoms may well have been relieved.  In any case, he would have been linked with a local hyperbaric doctor for  further evaluation.<br />
          There are always lessons for us to learn in life, and in  diving – learn from others.</p>
        <p><strong>If you have any questions regarding your DAN AP coverage send an email to </strong><a href="mailto:info@danap.org">info@danap.org </a></p>
