          <h1>Code of Practice For The Provision of Recognised Training</h1>
          <p>DAN Asia-Pacific Instructors are committed to the following  Code of Practice as an assurance to intending trainees of quality in the  provision of recognised education and training.</p>
          <h2>Educational Standards</h2>
          <p>            DAN Asia-Pacific has implemented policies and management  practices which maintain high professional standards in the marketing and  delivery of education and training services, and which safeguard the interests  and welfare of trainees.</p>
          <p>DAN Asia-Pacific Instructors are committed to deliver  training in a learning environment that is conducive to the success of the  trainees. Trainees will have access to individual counselling/support to assist  them to achieve the outcomes of the training and the training will be delivered  using facilities and resources, both human and physical, appropriate to the  level and type of training.</p>
          <h2>Marketing Strategy</h2>
          <p>            DAN Asia-Pacific Instructors will market the DAN  Asia-Pacific training programs with integrity, accuracy and professionalism,  avoiding vague, ambiguous, false or misleading statements. </p>
          <h2>Recruitment</h2>
          <p>            Recruitment of participants will be conducted at all times  in an ethical and responsible manner and be consistent with the requirements of  the curriculum. DAN Asia-Pacific Instructors will ensure that trainee selection  decisions comply with equal opportunity legislation. </p>
          <h2><strong>Recognition of other qualifications</strong></h2>
          <p>          DAN Asia-Pacific will recognise AQF qualifications and  Statements of Attainment issued by other Registered Training Organisations. DAN  Asia-Pacific will also provide a Recognition of Prior Learning (RPL) process to  all course participants.</p>
          <h2>Participant Information</h2>
          <p> Asia-Pacific Instructors will provide  accurate, relevant and up to date information to participants prior to  commencement of delivery of training. This may include, but not be limited to:</p>
          <ul>
            <li>Overview of the training to be delivered  including entry requirements, learning outcomes of the training; details of the  credentials to be issued on achievement of the learning outcomes of the  training and assessment procedures.</li>
                      <li>Cost of training. </li>
                      <li>Arrangements       for the recognition of prior learning.</li>
            <li>Admission       procedure.</li>
            <li>Dates,       times and locations of training sessions.</li>
            <li>Grievance       procedure/appeal procedure.</li>
            <li>Copy       of cancellation/refund policy.</li>
          </ul>
          <h2>Cancellation/Refund Policy</h2>
          <p>            Payment for courses is generally made on the day of the  course. However, at times a deposit may be requested to secure a place on a  program. When a deposit is accepted by a DAN Asia-Pacific Instructor, the money  may be refunded if the person fails to attend the program. The following  conditions apply:</p>
          <ol start="1" type="1">
            <li class="ordered">The       person must provide a medical certificate providing a reason for       non-attendance; <br />
            or</li>
            <li class="ordered">The       person must provide an explanation, deemed reasonable by the DAN       Asia-Pacific Instructor, for the non-attendance at the program.</li>
          </ol>
          <p>If the Instructor cancels the course he/she will refund in full any deposits paid. Approved refunds will be made within 3 weeks of application and will generally come directly from the Instructor. In exceptional circumstances (e.g. in the event of a dispute between enrollee and the Instructor and where DAN is contacted) it may come from DAN AP. </p>
          <h2>Access &amp; Equity</h2>
          <p>Trainees will not be discriminated against in any way on  the basis of gender, race, culture, age or disability. All participants will be  treated with equity. </p>
          <p>It is important to identify any participant with special needs and attempt to accomodate these. This can initially be assessed by careful observation of the trainees during the enrolment process to detect any difficulties in reading and comprehension of the registration materials and recording the requested information. It is also importat to discretely ask easch trainee about any physical disability that might impair their ability to oerform the practical requirements, or medical condition (e.g. cold or flu) that may require changes to equipment use (e.g. individual manikin). The DAN AP Director of Training can be contacted for advice on achieving this. </p>
          <h2><a name="grievance" id="grievance"></a>Complaints and Appeals</h2>
          <ul>
            <li>
              <div align="justify">In   general, if a trainee has a grievance about his / her training, the first   approach should be to the DAN AP Instructor to discuss the issue. If a   satisfactory resolution cannot be achieved, the Instructor is obliged to advise   the trainee to contact the DAN AP Training Manager. </div>
            </li>
            <li>
              <div align="justify">If a   trainee contacts the DAN office with a complaint, it will be recorded in writing   initially by the Office Manager. If deemed appropriate, the Training Manager   will be notified. </div>
            </li>
            <li>
              <div align="justify">The   Training Manager will assess the circumstances and act accordingly. Such actions   may include counselling of the Instructor, disciplinary action against the   Instructor, including the imposition of a fine, and, in exceptional   circumstances, legal action against the Instructor. </div>
            </li>
            <li>
              <div align="justify">DAN AP   will act to ensure that the trainee is in no way disadvantaged, where   possible. </div>
            </li>
            <li>
              <div align="justify">If   there is an Appeal, it will be heard by the DAN AP Board of Directors. </div>
            </li>
            <li>
              <div align="justify">Each   Appellant will be provided the opportunity to formally present their   case. </div>
            </li>
             <li>
              <div align="justify">The complainant has the right to have their complaint heard by an independant arbitrator if they feel they have not been given a fair hearing by the DAN AP Board of Directors. </div>
              <li>
              <div align="justify">All   Appellants will be issued with a written statement of the outcome of the Appeal,   including the reasons for the decision. </div>
            </li>
               <li>
              <div align="justify">If, for some reason, the complaint is not resolved within 60 days the complainant will be notified in writing the reasons for the delay and will receive further updates weekly until the complaint is resolved.  </div>
            </li>
            </li>
            
          </ul>
          <p align="justify">DAN AP will act promptly to rectify the subject of any   complaint found to be substantiated.          </p>
          <p align="justify"><a href="trainee_grievance_form.doc">CLICK HERE</a> to download a copy of the Trainee Grievance form. </p>
          <h2>Guarantee</h2>
          <p>            DAN Asia-Pacific Instructors will be expected to honour all  guarantees outlined in the Code of Practice.</p>
          <h2>Sanctions</h2>
          <p>          If the obligations of the Code of Practice or supporting  requirements, where applicable, are not met, registration as a DAN Asia-Pacific  Instructor may be withdrawn and penalties may be applied and/or appropriate legal action may be taken. </p>
