          <h1>DAN Oxygen Instructor Training</h1><span class="emstrong"></span>
	      <p class="subHeading">DAN Asia-Pacific offers two separate one-day modules: </p>
	      <h2>Instructor Module 1 - DAN  Oxygen Instructor</h2>
	      <p> This includes all of the components necessary to enable the  successful participant to teach the 4-hour DAN Oxygen Provider Module. It is  compatible with the DAN America and DAN Europe Oxygen Instructor Courses and is  presented by a DAN Asia-Pacific Instructor-Trainer.</p><img src="../_images/oxygen_first_aid_instructor_pack.jpg" width="220" height="154" class="imgRight" />
	      <p> Topics covered include recognition of a diving injury; benefits of oxygen  provision; handling and storage of oxygen; oxygen delivery systems; teaching  the use of the DAN Oxygen Unit; legal issues; and course preparation, standards  and procedures.</p>
	      <p> A person who successfully completes Instructor Module 1  becomes a <span class="strong">Level 1 DAN Oxygen Instructor</span>. As previously mentioned, a  Level 1 Instructor can conduct the DAN Oxygen Provider Module but cannot teach  the DAN Resuscitation Module.</p>
	      <h2>Instructor Module 2 -  Resuscitation and Diving Medicine.</h2>
	      <p> This module, which is presented by both a DAN Asia-Pacific  Instructor-Trainer and a Diving Medical Specialist (often with anaesthetic  experience), includes advanced physiology, resuscitation theory and practice,  injury recognition and management for diving and certain non-diving illnesses  and hands-on practice with a variety of oxygen delivery systems. </p>
	      <p> A person who successfully completes both Instructor Modules becomes a<span class="strong"> Level  2 DAN Oxygen Instructor</span>. A Level 2 Instructor is sanctioned to teach both  the Oxygen Provider and Resuscitation Modules to the diving community, and beyond.  In other words, they are a DAN Oxygen Instructor as well as a DAN Asia-Pacific  CPR Instructor.</p>
	      <p> Although DAN Asia-Pacific believes that DAN-style units  fulfill the needs of most divers and dive professionals, it has developed  Extension Modules to train people in the use of more advanced oxygen equipment  for those who have a specific need or desire for such training. Extension  Modules include MTV-100, BVM and Closed Circuit Oxygen Resuscitator training.</p>
	      <p>By attending an appropriate extension program,  which vary from 4-6 hours in length (in addition to the initial provider  training), Oxygen Instructors can gain an endorsement to teach such devices, if  desired.</p><a id="back" href="javascript:history.go(-1)" onMouseOver="self.status=document.referrer;return true">BACK</a></div>
