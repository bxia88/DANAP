	      <h2>Purpose of This Call?</h2>
	      <ul>
            <li>To obtain rapid medical advice from  a doctor or medic trained in diving medicine. The doctor/medic will assess the  situation and advise the  immediate course of action that should be taken.</li>
          </ul>
          <ul>
            <li>The doctor/medic will generally  advise whether the person needs to go to a local hospital for assessment or  whether an evacuation and/or treatment is required. <br />
            </li>
            <li>The doctor/medic usually will not make the arrangements for transportation or treatment and will not be in a position to approve for a particular facility to provide treament for a diver with DAN insurance and guarantee for DAN to cover the cost of treatment. This first call is to determine the immediate course of action. </li>
          </ul>
          <p align="justify"><strong>NOTE: It is  important to provide the doctor/medic with your DAN Membership  details if available, and request that they contact DAN on your behalf. It is also important to have a call back number ready to give to the operator.</strong></p>
            <h2>Calling  an Emergency Medical Hotline</h2>
            <ul>
            <li>The Hotlines are generally not  toll-free (except for the DES/DAN line within Australia  and <br />
              New Zealand). </li>
            <li>The caller should try to ensure that  he/she has ample money or a phone card with sufficient credit for the call. </li>
            <li>If no money is available, or if  there is difficulty in accessing the diving emergency line, a Member can make a  collect call to TravelAssist (919-684 9111 ) who would take the  details and may be able to connect the caller to the diving hotline.</li>
            <li>If the other options are not accessible for some  reason, the Member can contact the 
              DAN Asia-Pacific Office on +61-3-9886 9166 (Business Hours)  or +61-3-8508 9958 (After Hours, collect if necessary) and we will endeavour to arrange  the necessary contacts.</li>
          </ul>
