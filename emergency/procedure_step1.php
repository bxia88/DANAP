          <h1>Procedure  to be Used in the Event of a Diving Accident</h1>
	      <p>All DAN Asia-Pacific Members should familiarise themselves with these procedures so quick action can be taken should an accident occur. </p>
	      <p><em>Note: Step 1 can be followed by any injured diver, not just a DAN Member. </em></p>
	      <h6>For Diving Emergencies </h6>
	      <h2>Step 1 - Call a DAN- Supported Diving Emergency Hotline</h2>
	      <p align="justify">These  numbers are listed on your Membership Card, your Tank Sticker, in <em>Alert Diver</em> magazine, and here on our website.</p>
	      <p align="justify">The best number to use will depend on where the diver is and the language requirements. The preferred number of use in the Asia-Pacific Region is the DAN Diving Emergency Service (DES) Hotline as long as the diver, or someone with the diver, can speak English. </p>
