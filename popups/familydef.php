<h1>Family Membership Definition</h1>
<p>A Family Membership is  defined as: Spouse or Domestic Partner or dependent; child if unmarried  and under 18 yrs old or; your unmarried child who is 18 yrs old or  until his or her 24th birthday if that child, <br />
  <br />  
  a) Is enrolled  as a full-time student at an accredited school or college and;</p>
<p> b) Is not  employed on a full-time basis; and </p>
<p> c) Has the same home  permanent address as the parent.</p>
<p><strong>DOMESTIC PARTNER</strong> means a person who is at least eighteen years of age  and the Eligible Person can show:1) evidence of financial interdependence,  such as joint bank accounts or credit cards, jointly owned property, and mutual  life insurance or pension beneficiary designations; 2) evidence of cohabitation  for at least the previous <strong>{</strong>6<strong>}</strong> months; and 3) an affidavit of  domestic partnership if recognized by the jurisdiction within which they  reside.</p>
