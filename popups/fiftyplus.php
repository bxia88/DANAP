<h1>A Second Chance</h1>
<p>Recently my DAN Membership came up for renewal - no problem I thought, hop on the internet, type the requested information - done! But it was not as straightforward as I thought it would be.

There it was, an email from DAN requesting a Dive Medical because of my age! At first I was a bit annoyed at the inconvenience but then I thought, it actually makes sense, because I am pushing 75 years of age.

Due to the fact that I had not been for any checkups for such a long time, the GP wanted to run all the tests modern medicine can throw at us. The result of this test was reported back to my doctor and to my shock showed some abnormalities with my heart.

After the Angiogram I could see the concern in the Cardiologists face and I realized, things did not look as good as I had expected. I was required to undergo open heart multiple bypass surgery.

So in hindsight, I am a very, very lucky man to have followed up on the request by DAN to have a medical. The surgeon and cardiologist assure me, that after my full recovery in another few months, I will feel as good as gold and nothing should stop me going diving and pursuing all my other activities and live to tell the tale of my "second chance".</p>
