        <h1>Omitted Predive Buoyancy Check Leads to Trouble</h1>
        <p><em>Scuba diver survives a life-threatening hazard caused by the omission of the predive buoyancy check.</em></p>
        <h2>Reported Story </h2>
        <p><em>The diver is a 51-year old male, measuring 71 inches (180.5 cm) tall   and weighing 270 pounds (122.5 kg). His dive experience consisted of 72   lifetime dives up to a maximum depth of 120 fsw. He considered his skill   level as intermediate. He reported an incident that occurred on a wreck   dive while using a drysuit and compressed air.</em><br />
          <br />
          <em>Here is his report.</em></p>
        <h2>I Failed to do a Predive Buoyancy Check </h2>
        <p>          I   failed to do a buoyancy check before the dive, and immediately it   became obvious that I was overweighted as I entered the water. I tried   to swim directly down to the wreck instead of going to the front of the   boat and descending along the line that was fixed to it. I got swept in   the current and dropped to the bottom away from the wreck. The other   divers could not find me. I eventually made a safe ascent, did my safety   stop, and all was OK.<br />
          <br />
          A look at the log below shows that I started sinking rapidly.</p>
        <p align="left"><img src="_images/log2.jpg" width="240" height="150" /></p>
         <p>I was at 77 feet one minute into the dive. I added air but began   ascending too quickly. My computer issued a rapid-ascent warning at   about 60 feet. I started venting but continued ascending until about 45   feet, then I started sinking again. I reached 98 feet in four minutes.<br />
           <br />
           At   this point, I was able to see the bottom and look around. I swam over   to the wreck and grabbed on so that I wouldn't be swept away by the   current while gathering my thoughts. At five minutes into the dive I was   at the bottom at a depth of 102 feet. I hung out there for about five   minutes, assessing the situation; then I started swimming toward the   shoal, which gradually rises to 60 feet. I noticed the line going up and   reached it at about 12 minutes; I followed it up to about 43 feet below   the surface. I stayed there for a few minutes to off-gas somewhat.<br />
           <br />
           Trying   to get my buoyancy right, I was up and down the line a bit until I   decided to go for the surface at about 17 minutes. I started ascending   too quickly, vented and dropped down to 60 feet. The rest of the dive is   up and down, trying to stay down long enough to get my three minutes at   10 feet.<br />
           <strong><br />
           This is how another diver saw my incident:</strong><br />
           <br />
        <em>D   entered the water overweighted. The tank he rented had a different   buoyancy than the one he was used to, and he wore a drysuit that he was   not familiar with. Upon entering the water, D failed to do a weight   check. When D vented his suit, the overweighting took over, and he   descended uncontrolled straight to the bottom. D attempted to ascend,   but because of the overweighting he had to apply a large amount of air.   The large volume of air expanded quickly and caused a runaway ascent. To   resolve this, D vented air, but he vented too much and sank again. This   process happened repeatedly until D was able to control the venting and   adding of air better and safely make it back to the surface.</em></p>
        <h2>Comments </h2>
        <p><strong>Buoyancy problems</strong><br />
          Buoyancy problems are often the root   cause of diving incidents. In this case, the diver was at risk of   barotrauma due to the rapid ascent, running out of air due to frequent   adjustments of the buoyancy vest, overexertion, drowning and   decompression sickness. Fortunately, the situation resolved without   injury. The need to practice buoyancy-control skills regularly and   conduct a predive buoyancy test whenever a new configuration of   equipment is used cannot be overstated.<br />
          <br />
          <strong>Fitness to dive</strong><br />
          This   was an obese diver using medication for several medical conditions   along with vitamins and supplements. Based on the information provided   in the report, it appears that there are several issues that may affect   fitness to dive in this case:<br />
          <br />
        1. Obesity (BMI 37.7) </p>
        <p>2. Indications  of cardiovascular disease; risk of cardiovascular disease</p>
        <p>3. Combination of drugs that may be dangerous</p>
        <p>          However,   it is up to the diver's physician, who has full insight into his health   and physical fitness status, to decide the diver's fitness to dive.<br />
          <br />
        We   asked for more details about the diver's medical history, which were   readily provided, but it is not appropriate to discuss them in detail on   a public forum like this when identification of the diver is possible.   Here are some of the questions and answers that may add some but not   complete insight:<br /></p>
        <ul><li><p><em><strong> Did you have an evaluation for possible heart disease?</strong></em></p>
          </li>
          <p><em>Maybe.   I recently had what is called a heart scan. Using ultrasound, they   listen for blockages in your neck, arms and legs to make sure that the   blood is getting everywhere and not blocked. I had no issues.</em></p>
          <li>
            <p><em><strong> Do you exercise? What kind of exercise, how often, and how long? </strong></em></p>
          </li>
          <p><em>No, not really. Every once in a while I will exercise for a week or so, but I never stay with it.</em></p>
          <li>
            <p><em><strong> How many dives using a drysuit did you have before this incident? </strong></em></p>
          </li>
          <p><em>About   a dozen or so, and I did take and pass a drysuit course. I have never   really felt comfortable in mine though. It has always leaked, and I   continually have problems with it. The dive shop where I bought it is   very good about helping me with it, but there is a huge frustration   factor.</em></p>
        </ul>
        <p>We learned that this diver is doing pretty well   when it comes to medical control, but he could do more when it comes to   managing his lifestyle. He has been under thorough medical supervision.   His personal physician prescribes all his medications, and it's   important to control the risk of possible bad interactions between the   drugs. He consistently has an annual medical checkup.<br />
          <br />
        Recently, he did a more extensive evaluation of his cardiovascular risk factors by undergoing a heart scan,   which was within the normal range. He does not smoke and does not drink   excessively (up to two drinks per week), but he does not exercise   enough. His energy balance <a xtarget="_blank" href="http://www.nhlbi.nih.gov/health/public/heart/obesity/wecan/healthy-weight-basics/balance.htm"></a> is on the side of weight gain because his food intake exceeds his   energy consumption through physical activities. This is an area in which   he should consider improvement to increase his safety margin in diving.   <br />
        </p>
        <p><em>— By DAN's Dr. Petar J. Denoble</em></p>
