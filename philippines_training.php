        <h6 align="justify"><strong>DAN Training: On-Site Neurological Assessment</strong></h6>
        <p align="justify"><em>Divers with  decompression illness (DCI) quite often have some damage to their nervous  system. The signs of this are usually vague and often go unrecognised causing  delays in treatment. This program enables participant to obtain essential  information about a diver who has a possible diving injury. It also teaches the  participant what information should be relayed to emergency services when they  arrive. Topics include: Identifying a diving emergency, identify when it is  appropriate to conduct an On-Site Neurological Assessment and how to conduct an  On-Site Neurological Assessment.</em></p>
