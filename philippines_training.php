        <p align="justify"><em>This fascinating 6-hour program, conducted here over two  nights, teaches divers about physical and medical fitness to dive, safety  planning, assessment of an injured diver, including the use of a stethoscope  and taking blood pressure, and many other valuable skills. Although anyone can  attend the course and receive credit for attendance, certification is only  provided to those with the required prerequisites, which include CPR and DAN  Oxygen, Neuro, HMLI and AED Provider. </em><br />
