            <strong>To Register:</strong> <a href="_pdf/neuro_provider.pdf">CLICK HERE</a> to Download the Registration Form for the Provider Level Course only.<br />
            <strong>Location:</strong> Asia Divers, Small Lalaguna, Puerto Galera.</p>
        <h5 align="justify"><strong>On-Site  Neurological Assessment: &ldquo;Instructor&rdquo; Level Training</strong></h5>
        <p align="justify"><em>Having completed  the &ldquo;Provider&rdquo; level training you can go on and complete Instructor Level  course. This course is designed to extend your knowledge of the assessment and  first aid management of decompression illness and prepare you to teach the DAN  AP On-Site Neurological Assessment for Divers (Provider) Course. Topics include  further insights into the mechanism of DCI, the recognition of DCI using the  field, neurological examination techniques, as well as appropriate first aid  management.</em><br />
