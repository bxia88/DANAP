       <h1>Stinging Coral  </h1>

         

        <p>Q: When I was diving some  time ago, I apparently came in contact with a type of stinging coral. As soon  as I got out of the water I noticed a few cuts and scrapes on the outside of my  left wrist and forearm. Later that evening I left an itching and burning  sensation around the affected skin; it became red and tender to touch.</p>

        <p>I washed it with soap and  hot water, which actually seemed to help. However, the redness, itching and  burning lasted for three to four days. The local doctor prescribed a steroid  cream twice a day, and everything seemed to heal during the next four to five  days.</p>

        <p>Since I returned from my  dive trip, the redness and itchy sensations have recurred. The area is  uncomfortable and unsightly. Do I need special care?</p>

        <h2>DAN Answer</h2>

        <p>It  is not uncommon for a diver to have a red raised rash resulting from a coral  encounter. A burning sensation, pain and itching may also accompany a rash if  the coral is actually a hydroid, such as fire coral, which is not a true coral  but a stinging creature. The extent of the reaction to fire coral depends on  the amount of exposure to toxins, the extent of the abrasion and victim&rsquo;s  pre-existing sensitivity. </p>

        <p>You  did the correct thing to clean the wound with soap and water: Reactions may be  reduced by immediately cleansing the affected area. The most frequent  complications from non-stinging coral scrapes are inflammation, which leads to  poor healing, and less commonly, a secondary infection. Proper cleansing,  therefore, is very important.</p>

        <p>If  fire coral is the culprit, then treatment with hot water 45&deg;C is recommended.  This can be applied as a compress or as a soak to reduce the pain. Other  substances that have been used with variable effect are rubbing alcohol and  baking soda. </p>

        <p>It  is not uncommon for the rash, itching and burning to recur after these symptoms  have resolved initially because tiny, microscopic fragments of living coral can  remain in the wound. In addition, a cyclic immune response (redness,  inflammation, itching and swelling, itching or burning) can persist for a few  weeks after the incident.</p>

        <p>Treatment  for the inflammatory response is symptomatic. Steroid creams are rarely helpful  and they can prolong a skin infection. If the inflammation is severe,  systematic steroids in a moderate, tapering dose may be administered under the  supervision of a trained medical provider. Oral antihistamines can sometimes  help reduce the itching or burning sensation.</p>

        <p>When  you are under the water avoid contact with coral or any other living creature.  Whenever possible wear a wetsuit or diveskin to protect yourself.</p>

        <p>Ocean  divers should consider a marine animal first aid kit for their travels. For  divers who want to learn more about the various marine life injuries, DAN has a  course as well as a variety of books on this topic. </p>

        <p><a href="http://www.danasiapacific.org/main/training/haz_marine_life.php"><strong>DAN&rsquo;s  First Aid for Hazardous Marine Life</strong></a> course is designed to provide divers with  knowledge regarding specific types of marine creature injuries and the general  first aid treatment for those injuries.</p>

        <p><strong>Visit  the DAN <a href="http://www.danap.org/main/training.php">website</a> for more information or email <a href="mailto:training@danasiapacific.org">training@danasiapacific.org</a></strong>.</p>

        <p><em>Answer provided by Dr John Parker who is a Diving  physician and Senior Dive Medical Consultant for DAN Asia-Pacific.</em></p>

          
