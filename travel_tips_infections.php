      <h1>Dive Travel Tips  </h1>

         

        <p><em>By DAN Asia-Pacific  Director, Stan Bugg</em></p>

        <h2><strong>Travel Tips: Coping with Fungal Infection</strong></h2>

        <p>While it is never going to put your life at risk, a  case of tinea can make an overseas trip a misery. Tinea is a fungal infection,  and if it flares up while you are overseas, you probably already had it in a  dormant state.</p>

        <p>Fungi thrive in warm, dark and moist environments,  so the problem usually attacks the groin, armpits and between our toes. A  tropical dive destination provides the extra warmth, and the &ldquo;never quite dry&rdquo;  state of your feet which spend hours at a time in damp wetsuit boots, and your  groin which is encased in constantly damp swimmers, allows the infestation to  thrive. The macerated state of the epidermis after long and frequent immersion,  leads to more rapid development of the problem.</p>

        <p>Symptoms include moderate to  severe itching, redness, and flaking skin. The itch can cause the sufferer to  scratch, and further damage the skin, allowing infection to set in.</p>

        <h3><strong>Reduce the severity of an attack by:</strong></h3>

        <ul>

          <li>Wash and completely dry all swim  suits, wetsuits, underwear, socks and boots each day.</li>

          <li>Thoroughly clean all infected  areas, and dry thoroughly afterwards. Take extra time to dry between and under  the toes.</li>

          <li>Allow as much air to circulate the  infected areas as decency will allow. Wear loose clothing to increase air  circulation.</li>

          <li>Apply anti-fungal cream or lotion  at the first hint of itching, redness or flaking, and continue use for the rest  of your trip. Often a few days application of medication will subdue the  symptoms to the extent that you might think you are cured. This is rarely the  case, and it will probably flare up again if you discontinue treatment.</li>

          <li>Do not share towels, dive bootees etc  with others.</li>

        </ul>

        <p align="justify">If the abraded skin becomes infected,  apply disinfectants as necessary. Seek medical assessment if improvement is  slow, or if the condition worsens.</p>

