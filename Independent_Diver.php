        <h1>The Importance of Being an Independent Diver </h1>
         
        <p><em>Continued from DAN's Safety Stop (May 2015) </em></p>
        <p>Some will claim it is  sacrilege for me to say it, but I believe that all divers should strive to  conduct their dives independent of other divers, irrespective of their level of  training, or the type of diving they are doing. </p>
        <p><strong>Before you get your ‘speedos’ in a twist, I am NOT  implying that they are diving solo, or that there is no buddy. Nor am I saying  that they must refuse any assistance from a partner. It merely affirms that ALL  divers, regardless of their experience and/or training, should have the skills,  knowledge and mindset to be capable of looking after themselves in most  circumstances.</strong></p>
        <p>In addition to having  someone to share your dive with, the buddy then becomes a genuine safety backup,  and not the primary solution to problems that many expect the buddy to be. Remember,  they may not always be at hand when you need them.</p>
        <p>We call this state <strong>Solo independent.</strong> It is the common  approach of technical divers and cave divers. Being <strong>solo independent</strong> assumes that YOU are taking responsibility for  your safety, and not just handballing it to your companion.</p>
        <p>While diving’s safety  protocols call for the presence of a buddy, many other risky pastimes do not.  Rock climbers, pilots, hang gliders, bushwalkers, all routinely go solo, and  assume that they will deal with any difficulties encountered. If problems arose  in these activities, they would not reject an offer of help from another  person, but the mindset is not geared towards the assumption that a buddy will  automatically bail you out of trouble. <br />
          <br />
          So I am suggesting that  divers adopt a similar mindset; without actually doing away with the buddy, you  up-skill yourself to a point where you are not totally reliant upon assistance  from that partner, and, if his/her ability to help is diminished, you can still  get yourself sorted out. <br />
          There are no new skills  that are required for you to be considered solo independent. I merely call on  the diver to be TRULY capable of performing all of the skills they learned (  ..or should have learned!!!)  in their  basic diver training. It also assumes the diver is totally honest when  assessing his/her own abilities. </p>
        <p><em><strong>So how do you measure up?</strong></em></p>
        <p>If you can answer YES to  ALL of the statement below, you are well on the way to being considered an  independent diver:</p>
        <ul>
        <ul>
          <li>I am able to gear  up without assistance.</li>
          <li>I can check my  gear prior to a dive to ensure it is all functioning correctly.</li>
          <li>I can plan a  series of dives, taking into account depth, time, deco obligations, air consumption. </li>
          <li>Once underwater,  I can implement this plan, and not exceed planned limits.</li>
          <li>If underwater  circumstances demand it, I can modify this plan while underwater, to stay  within safe limits. </li>
          <li>I can read and  use deco tables or computer to calculate deco obligations and safe limits for a  multi dive schedule.</li>
          <li>I know my personal  limits of skill and comfort, and I plan and conduct my dives to ensure I do not  exceed these limits. </li>
          <li>I am prepared to  CALL a dive if I do not feel comfortable with conditions, equipment, dive  limits, or anything else that causes me discomfort. </li>
          <li>I constantly  monitor my air, and when I reach my air cutoff, I CALL the dive.</li>
          <li>When I check my  depth, time and air, I am never taken by surprise.</li>
          <li>I am able to  remain calm when under pressure</li>
          <li>I communicate  constantly with others in the group while underwater. </li>
          <li>I have control of  my buoyancy throughout the dive, including the ability to hover mid water  without needing a shot line or other physical reference point. </li>
          <li>I am capable of  making a controlled ascent, including a safety stop or two.</li>
          <li>When I am on the surface,  I am able to make myself positively buoyant and comfortable. </li>
          <li>I can remove and  replace my gear while in the water. </li>
          <li>I do not blindly  accept other divers imposing their dive plan on me.</li>
          <li>I can surface  snorkel at least 200m while wearing SCUBA.</li>
          <li>I accept total  responsibility for ME while I am diving. If my actions cause things to go  wrong, it is MY fault. </li>
        </ul>
