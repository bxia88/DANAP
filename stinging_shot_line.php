	      <h1>Beware the Stinging Shot Line </h1> 
	      <p><em>By DAN Asia-Pacific&rsquo;s  Stan Bugg</em></p>
	      <p>As a regular dive traveller to tropical locations, I have often been the  victim of unidentified stingers which have grown onto permanent mooring lines.  I suspect I am not the only one thus affected.</p>
	      <p>These mooring ropes give surface access to reefs and wrecks, and obviate  the need to drop an anchor. The moorings can remain in place for years, and not  surprisingly they rapidly become habitats in their own right when a wide  collection of marine animals attach themselves.</p>
	      <p>The heavy handed diver who grabs onto, or brushes against the rope in a  typically unprotected state commensurate with tropical diving, should not be  surprised to incur minor injuries from contact with these animals. </p>
	      <p>The most common injuries are stings from hydroids and other  coelenterates. Typically these present with degrees of discomfort including  itching and localised pain, which can linger for several days.</p>
	      <p>Encounters with a well-encrusted line can also produce and abrasions from  accumulated molluscs, and even embedded fish hooks. Divers seem to be most  vulnerable on ascent when the skin is macerated from long immersion.</p>
	      <p>Some travellers have even reported more severe stings from tropical  jellyfish tentacles that have drifted onto the line.</p>
	      <p><strong>The  solution:</strong></p>
	      <ul>
            <li>Avoid or reduce direct contact with mooring lines that are overgrown, or  which have been in place for long periods.</li>
	        <li>With a bit of practice you should be able to perform free floating ascent  and decompression stops without actually touching the line.</li>
	        <li>If you MUST hold onto the line, either due to lack of experience, or if a  current is running, wear a decent pair of gloves, or use a JON line*.</li>
          </ul>
	      <p>* A JON line is a 1 &ndash; 2m line with a clip at each end, allowing the line  to be clipped to the diver&rsquo;s BCD and onto the shot line. This helps the diver  to hold position for deco while being hands free, even if a current is running.  It is especially useful if the shot line is crowded.</p>
	      <p><strong>First aid  for hydroid stings:</strong> Resist rubbing or scratching the affected area, apply cold packs for  some relief from symptoms. Cortisone cream may also relieve symptoms.</p>
	      <p><strong>First aid  for cuts from shells etc:</strong> Wash the wound thoroughly, ensuring no shell fragments remain in the  wound, apply antiseptic e.g. betadine, apply pressure to control bleeding, monitor  for infection over subsequent days.</p>
	      <p><strong>If tropical  jellyfish stings</strong> are suspected, liberally irrigate with vinegar, monitor closely, and  seek medical attention if symptoms deteriorate. If no vinegar is available,  wash area with sea water, apply cold packs and seek urgent medical aid if  severe</p>
