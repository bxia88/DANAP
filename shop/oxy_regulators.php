          <h1> Adaptors for Oxygen Regulators</h1>
          <p>Oxygen cylinder valves vary somewhat from country to country and this means that oxygen regulators will not attach directly to all available cylinders. To this end, some adaptors have been manufactured to enable various oxygen regulators to be fitted to a variety of cylinder valves. It is essential that such adaptors are correctly manufactured and maintained to minimise the risk of an oxygen fire. </p>
          <p>The most commonly available oxygen cylinder valve is the &ldquo;pin-indexed&rdquo; valve. Therefore, an oxygen regulator with a pin-index fitting will generally be the most universally used. </p>
          <p>However, in Australia, some parts of Indonesia, PNG and certain some other Pacific islands, larger cylinders are fitted with a Type 10 &ldquo;bullnose&rdquo; valve. A pin-indexed regulator will require a pin-index to bullnose adaptor to enable its use.  Both of these adaptors are available from DAN Asia-Pacific. </p>
