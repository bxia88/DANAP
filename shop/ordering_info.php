          <h1>Online Inquiry / Ordering Information</h1>
          <p>The DAN Asia-Pacific (DAN AP)  On-line Product Pages present you with a variety of diving and safety related products.</p>
          <p>To place an inquiry/order from the DAN AP Product Range you are not required to  be a  
            DAN Member.&nbsp; However, you will be missing  out on the reduced pricing that DAN Members receive.&nbsp; Therefore, you might like to consider <a href="../Membership_Insurance/residencycheck.php">becoming a DAN Asia-Pacific Member</a> today.</p>
          <p>Prices for DAN AP products vary depending on whether or not you are a  DAN AP Member and whether you are located within Australia or overseas.&nbsp; As such, we determined it was best for  visitors to submit an initial inquiry, rather than an order, to allow the DAN  Team to prepare a quote taking into account these variable factors.</p>
          <p>Further, as the cost of freight is dependant upon your specific order,  submitting an inquiry will allow us to accurately calculate the cost of  freight. </p>
          <p>Finally, orders from within Australia will attract an additional 10% <em>(for  the majority of products)</em> for GST.</p>
          <h2>Making an Inquiry</h2>
          <ul>
            <li>Navigate through the range of DAN AP Products by selecting either view <span class="strong">All Products</span> or alternatively searching via the <span class="strong">product categories</span> presented.</li>
            <li>For each product you would like to inquire about click the <span class="strong">Add to Inquiry</span> button.  Doing so will add one of the selected products to your inquiry.</li>
            <li>Once you have selected all  your products click the &ldquo;Finalise Inquiry&rdquo; button at the top of the  page.</li>
            <li>Complete all the required  fields on the form and submit.</li>
            <li>The DAN Asia-Pacific Team  will respond to your inquiry within 48-hours as long as the inquiry is received before 1pm on a Friday.</li>
            <li>The DAN Team will send you an email with advice on how to proceed if you would like to place an order.</li>
          </ul>
          <h2>Delivery</h2>
          <ul>
            <li>Orders placed prior to 11.00am on a weekday will be processed the same  day, where possible, but will be subject to product availability.&nbsp; </li>
            <li>Order placed after 11.00am on a weekday will usually be processed the  next business day.</li>
            <li>Please allow 7-10 business days for delivery depending on your location  and the mode of delivery chosen.&nbsp; </li>
            <li>All orders are dispatched via courier or express post to allow for the  delivery to be tracked.</li>
            <li>Airmail postage is available on request.&nbsp;  This amounts to approximately a 10% saving on freight charges and  requires 2-3 weeks for delivery.</li>
            <li>Freight prices do not include local taxes and duties.</li>
          </ul>
          <h2>Returns Policy</h2>
          <ul>
            <li>Unless a product is faulty or  does not do the job as described, there are no refunds.</li>
            <li>Where a refund is approved  the cost of return freight is the purchasers responsibility.&nbsp; The original freight component is also the  responsibility of the purchaser and will not be refunded.&nbsp; </li>
          </ul>
          <h2>Disclaimer</h2>
          <p class="em">Although DAN AP will attempt to respect delivery times,  we shall in no way be responsible for any delay following the consignment of  the goods to the courier company.&nbsp;  Shipping of goods, by any means, shall be at the sole risk of the  addressee.</p>
          <h2>Payment</h2>
          <p>Payment is required before  goods are dispatched.&nbsp; Payment options  are as follows:</p>
          <h4>Cheque/Money Order</h4>
          <p>Please make out to DAN Asia-Pacific<br />
            Post cheque/money order with print out of order form to:<br />
            DAN AP, PO Box 384, Ashburton, Victoria, 3147 Australia.</p>
          <h4>Credit Card</h4>
          <p>Visa, Mastercard or AMEX.&nbsp; Please enter all required details on the  online application form.</p>
