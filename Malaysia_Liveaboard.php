        <h1>DAN Helped Me </h1>
         
        <h6><strong>Where</strong></h6>
        <p>Six-day liveaboard trip in Malaysia.</p>
        <h6><strong>The Diver</strong></h6>
        <p> At the time of the incident the diver had completed 50 dives.</p>
        <h6><strong>The Dives</strong></h6>
        <p>          Day 1 (Sunday): Two dives to 20m for  45mins. SI of two hours.<br />
          Day 2 (Monday): Dive 1 to 29m for 50  minutes. SI of 1.5hours. Dive 2: 28m for 50 mins. SI of 2 hours. Dive 3: 30m  for 50 mins.</p>
        <h6><strong>The Symptoms</strong></h6>
        <p>It was during the extended  surface interval after lunch that the symptoms commenced with chest pain that  felt like muscle strain. Upon laying down the diver felt nauseous, the chest pain  intensified, and then severe itching of the chest and stomach began. Red welts  appeared and spread covering most of the torso (front).</p>
        <p>          Advice was  sought from the divemaster. The diver suggested breathing oxygen might be  beneficial but the divemaster suggested the rash was an allergic reaction. As  the diver had consumed some unfamiliar seafood at lunch, the explanation seemed  plausible, so she adopted a ‘wait and see’ approach. </p>
        <p>          Over the  coming hours the rash became dark red and very painful (with touch and body  movement); although the itching had subsided. Advice was then sought from the captain  (also a divemaster). The diver again suggested breathing O2 but the captain was  positive it was an allergic reaction. Again, although doubtful, the diver  allowed herself to be persuaded to ‘wait and see’.</p>
        <p>          By the next  day (Tuesday) the rash had improved but there was still some pain. The divers  husband spoke to the captain and suggested that although it might not be a  bend, some oxygen ‘was worth a try’. The captain agreed<strong>. </strong>It was discovered that <strong>the  boats “emergency” oxygen cylinder was empty </strong>so some was decanted from their  Nitrox supply. The diver spent an hour breathing O2. She improved a little more  during the day, but couldn’t be sure it was attributable to the oxygen.</p>
        <h6><strong>Back in the Water</strong></h6>
        <p> By Wednesday the diver felt better  and by Thursday morning the rash had all but disappeared. The diver only had  slight pain so she decided she was fit to continue diving. With encouragement  from the captain and divemaster the diver completed three dives, each around 50  mins to a max depth of 18m. The diver chose not to push the depth.</p>
        <p>          On the  Friday, the diver felt fine and completed all 4-dives on offer. Her max depth  was kept to 20m and each dive was for approx. 50mins. </p>
        <h6><strong>The Symptoms Return</strong></h6>
        <p>That evening the diver started  to feel nauseous and her skin began prickling. They now knew diving was the  direct cause. So after much delay, and insistence again from the captain that  it was not a bend, the diver called DAN for help/advice.</p>
        <h6><strong>The Call to DAN</strong></h6>
        <p> <em>“After all the worry it was wonderful to be able to speak with such a  friendly, reassuring, and most importantly, expert person. It was confirmed  that I definitely had a ‘skin bend. I was told to breathe oxygen for as long as  possible that night until the boat returned to port on the Saturday at which  time we would re-assess if there was a need to visit a Chamber. Fortunately, by  morning my symptoms had fully cleared”.</em></p>
        <h6><strong>My Lesson’s Learnt</strong></h6>
        <ul>
          <li><em>No matter how expensive the trip  or how tempting the diving, don’t dive if you have doubts.</em> Don’t convince yourself you are  fine. Get expert advice from DAN.</li>
          <li><em>Don’t consider the captain, the  divemaster or anyone else on the boat an expert.</em></li>
          <li><em>Ensure your liveaboard or resort  has properly maintained equipment and O2 available. </em></li>
          <li><em>Ring DAN as soon as you have ANY  concerns.</em> You will  get prompt, friendly and expert advice. If you are fine or there is a minor  problem they will tell you that. If there is a serious problem you are going to  be even more glad that you called them!</li>
        </ul>
        <h2>Comment  from DAN Asia-Pacific’s John Lippmann</h2>
        <p>          After completing three 50-minute  dives to around 30m in close succession, the diver’s initial symptoms were not  a great surprise. These dives would accumulate quite a lot of inert gas and  there was relatively little time to eliminate this, despite the dive computer  indicating all was okay. </p>
        <p>It was a sensible action to  refrain from diving and the divemaster/captain should have readily provided  oxygen and called the DAN hotline for advice. It is inappropriate and unwise  for a divemaster/captain to withhold oxygen because they don’t think a diver is  suffering from DCI. Fortunately,  this diver did not deteriorate badly, but she potentially could have,  especially with further diving which can sometimes turn a mild DCI into a  serious one unless well-managed. By  the time DAN was contacted we were of the belief the divers symptoms were  abating and were unlikely to worsen and breathing oxygen for a significant  duration was deemed the most appropriate first aid, which proved successful. </p>
