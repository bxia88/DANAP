	      <h1>Liveaboard Diving and Emergencies at Sea </h1> 
          <p><em>By Marty McCafferty with Contributions  by DAN Asia-Pacific</em></p>
          <p><em>The diver was a 52-year-old female  with 400+ logged dives. She was on a liveaboard traveling near an archipelago  in the northern Indian Ocean. </em></p>
          <p><strong>The Incident</strong><br />
            The  diver undertook 10 dives over four days. All dives were on air and conducted  within her computer’s no-decompression stop limits. The deepest dive of her  series was to 29m. The other dives were to the same or shallower depths. On day  three she chose not to participate in two morning dives due to a headache,  nausea and pain in both hips. Later that same day, feeling better, she  completed the three afternoon dives.</p>
          <p>Approximately one hour after the last dive, her headache, nausea and hip  pain returned along with a new symptom: thigh pain. While returning to her  cabin below deck, she appeared to have difficulty walking down the stairs. The  dive leader noticed this, evaluated her and called a DAN Hotline. </p>
          <p>The symptoms reported to the DAN dive doctor indicated possible  decompression illness (DCI) with neurological involvement. The medic  recommended immediate oxygen first aid and a full evaluation by a dive  physician as soon as possible. </p>
          <p><strong>The Complications</strong><br />
              <em>Oxygen administration was delayed by the crewmembers’ unfamiliarity with  the oxygen equipment. </em>Once resolved, the diver received high-flow oxygen via  a non-rebreather mask.</p>
          <p><em>Oxygen provision was stopped after only 4-hours</em> when the diver noticed a marked  reduction in the severity of her symptoms. </p>
          <p><em>The vessel’s remote location prolonged the transit time required to  reach the medical facility. </em>Ultimately, the boat crew arranged for a local  speedboat to transport the diver quickly and safely to the hospital.  </p>
          <p><strong>The Diagnosis</strong><br />
            The diver was evaluated and underwent treatment in the hyperbaric  chamber following the U.S. Navy Treatment Table 5 (TT5). Her leg and hip  soreness improved with the first treatment. The next day she underwent a  subsequent treatment (TT5), and she noted additional improvement. Over the next  three days, the residual soreness in her legs and hips resolved without any  recurrence of symptoms. </p>
          <h2>The  Discussion</h2>
          <p><strong>Remote  Travel Can Complicate Medical Management.</strong> Limited facilities, long travel times  and language barriers all affect a situation. </p>
          <p><strong>Oxygen  First Aid is Vital:</strong> It must be available in sufficient  quantities onboard and should be provided promptly by staff trained to provide  oxygen first aid. Ideally, oxygen provision should continue until the diver  gets to an appropriate medical facility. Before you book a diving holiday take  the time to learn about the Dive Operators Emergency Plan. <em>Firstly, do they  actually have one? </em></p>
          <p><strong>Don’t  Ignore Symptoms. </strong>Many symptoms can often be subtle and easily  misattributed to non-diving related causes. To complicate matters, people on  dive boats want to dive and will often deny or underplay symptoms in an effort  to return to the water. <br />
              <em>Warning:</em> Continued diving may worsen the condition and make treatment  more difficult. </p>
          <p><strong>A  Word of Caution About Advice from Well Meaning Dive Crew, Buddies etc: </strong>This  diver was fortunate the Dive Leader called DAN for prompt advice. Sadly, in  many cases dive staff have downplayed or misdiagnosed symptoms, leading to  delays in treatment and an increase in the severity of symptoms. For your own  safety, don’t rely on the advice of well-meaning fellow divers / staff unless  they are trained diving medical experts. </p>
          <p><strong>Be Wary of Dive Computers:</strong> Most divers these days use dive computers and appear to be reasonably confident  they will avoid decompression illness as long as they dive within their limits.  The majority of divers DAN has assisted with DCI were using a dive computer and  had been diving within the limits advised by their computers. The fact is dive  computerscannot  take into account variations in each divers age, weight, height and physical  conditioning. It is a good idea to set your dive computer to a more  conservative setting. </p>
          <p>Ultimately, each of us is responsible for our own safety,  and the more prepared we are, the better off we’ll be should trouble occur.</p>
