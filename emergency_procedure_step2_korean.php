        <h1>사고 발생시 따라야 할 절차 </h1>
        <h2>2 단계 - 의사가 후송 및/또는 치료가 필요하다고 조언할 경우에는 DAN AP 또는 DAN <em>TravelAssist</em> 에 반드시 전화하셔야 합니다. (DAN 회원의 경우에만 적용)</h2>
        <p>의사/수련의가 응급후송을 권장한다면 반드시DAN TravelAssist 에게 전화하셔야 합니다. 이 전화를 할 일차적인 책임은 회원에게 있지만, 담당 의사/수련의에게 요청하시면 회원을 대신하여 전화를 해 주는 경우도 있습니다. </p>
        <p> DAN AP 의 직통전화번호는 +61-3-9886  9166 (근무시간)  +61-3-8508 9958 (근무외 시간) 입니다. <em>TravelAssist </em>는 미국 직통전화번호 +1-919-684 9111 (또는 +1-919-684 3483)로 전화하시거나 국제통화교환원을 통해 수신자 부담으로 미국에919-684 9111 (또는 919-684  3483)번으로 전화하시면 됩니다.</p>
        <p>DAN TravelAssist 서비스는 세계 각지에 지사를 두고 있는 Assistance Company(지원회사)를 통해 제공되며, DAN 회원의 후송 또는 치료에 필요한 모든 지원을 해드립니다. </p>
        <p> DAN TravelAssist 전화상담원은 일단 회원의 이름, 회원번호, 사고내용,  위치 및 연락전화번호 등을 포함하여 회원과 관련된 상세정보를 요청할 것입니다. 긴급전화를  통해 의료자문을 구한 경우에는 상담원이 진단내용을 다이빙 전문의사와 확인할 수 있도록 어떤 다이빙 긴급전화를 이용하였는지 알려주셔야 합니다. </p>
        <p> DAN TravelAssist 는 여러분의 회원자격사항을 확인한 후 담당의사에게 연락을 취하여 응급후송의 필요성을 확인하고, 후송조치를 취하거나 후송비용의 지출을 승인합니다. 그 후에도 상담원은 회원 및 기타 관련인들과 지속적인 연락을 취하면서 후송과정이 원활하게 이루어지도록 도와드립니다.  </p>
