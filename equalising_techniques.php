        <h1>What are the best ways to equalise my ears and sinuses?</h1>
        <p>Middle ear and sinus barotrauma   are the most common injuries associated with exposure to increasing and   decreasing pressure. Descent in the water adds approximately one-half   pound of pressure for each foot of descent and diminishes a similar   amount on ascent. According to Boyle's Law, as the pressure increases on   descent, the volume of a gas in an enclosed space decreases   proportionately. As the pressure decreases on ascent, the volume of the   gas increases proportionately. On descent it is imperative that all   enclosed air filled spaces be equalised actively or passively. On   ascent, the increasing volume usually vents itself naturally. <br />
          <br />
          For equalisation to be effective, you should be free of nasal or   sinus infections or allergic reactions. The lining of the nose, throat   and Eustachian tubes should be as normal as possible. If this is true,   the following techniques are effective in reducing middle ear and sinus   squeeze.        </p>
        <ol>
          <p>            1.	Prior to descent, neutrally buoyant,   with no air in your buoyancy compensator, gently inflate your ears with   one of the techniques listed below. This gives you a little extra air   in the middle ear and sinuses as you descend. <br />
            <br />
            2. Descend feet first, if possible. This allows air to   travel upward into the Eustachian tube and middle ear, a more natural   direction. Use a descent line or the anchor line. <br />
            <br /> 
            3.	Inflate gently every few feet for the first 10 to 15 feet. <br />
            <br />
            4.	Pain is not acceptable. If there is pain, you have descended without adequately equalising. <br />
            <br /> 
            5.	If you do not feel your ears opening, stop, try again,   perhaps ascending a few feet to diminish the pressure around you. Do   not bounce up and down. Try to tilt the blocked ear upward. <br />
            <br />
            6.	If you are unable to equalise, abort the dive. The   consequences of descending without equalising could ruin an entire dive   trip or produce permanent damage and hearing loss. <br />
            <br /> 
            7.	If your doctor agrees, you may use decongestants and   nasal sprays prior to diving to reduce swelling in the nasal and ear   passages. Take them one to two hours before descent. They should last   from eight to 12 hours so you don't need to take a second dose before a   repetitive dive. Nasal sprays should be taken thirty minutes before   descent and usually last twelve hours. Take caution when using   over-the-counter nasal sprays. Repeated use can cause a rebound reaction   with worsening of congestion and possible reverse block on ascent. <br />
            <br /> 
            8.	If at any time during the dive you feel pain, have   vertigo (the whirlies) or note sudden hearing loss, abort the dive. If   these symptoms persist, do not dive again and consult your physician. <br />
            <br /> 
            9.	Equalising Techniques</p>
          <ul><li>
              <p> Passive - requires no effort                </p>
            </li>
                    <li>
                      <p> Valsalva - increase nasopharynx pressure by holding nose and breathing against a closed glottis (throat)                      </p>
            </li>
                    <li>
                      <p> Toynbee - swallowing with mouth and nose closed - good for ascent!                      </p>
            </li>
                    <li>
                      <p> Frenzel - Valsalva while contracting throat muscles with a closed glottis                      </p>
            </li>
                    <li>
                      <p> Lowry - Valsalva plus Toynbee - holding nose, gently trying to blow air out of nose while swallowing - easiest and best method!                      </p>
            </li>
                    <li>
                      <p> Edmonds - jutting jaw forward plus Valsalva and/or Frenzel (good method)                      </p>
            </li>
                    <li>
                      <p> Miscellaneous - swallowing, wiggling jaws - good for ascent!                      </p>
            </li>
          </ul>
