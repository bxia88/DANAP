        <h1>Accident Reporting &amp; Research</h1>
        <p>As part of its mission to improve dive safety DAN collects and reports  on diving incidents, injuries and fatalities.</p>
        <p>In addition, DAN is the  industry leader in diving physiology research and is one of the major  organisations in the 
		world that conducts significant medical research on  recreational scuba diving safety.</p>
        <p>DAN's studies are revealing important information that will benefit the entire diving  community and improve 
		recreational diving safety worldwide. Without DAN, many important questions about  recreational diving safety would 
		not only remain unchallenged, but unanswered.</p>
        <p>Our research projects to date have included the effects of flying after  diving, ageing and its effect on dive health, 
		as well as diving with diabetes.</p>
