          <h1>Recompression Chamber Assistance Program</h1>
	      <p>DAN has provided training  and financial support to recompression chambers throughout the Caribbean and the Asia-Pacific Region. </p>
	      <p>This support helps to ensure  they remain in operation and stay properly staffed.&nbsp; Importantly, this means injured divers  receive appropriate treatment with minimal delay.</p>
	      <p>In 1996, DAN broke new  ground in the field of dive injury treatment and insurance by creating a Diving  Preferred Provider Network (DPPN) of hyperbaric chambers to help manage the  costs of recompression treatment and to make it easier for hyperbaric  facilities to receive payment for their services.&nbsp; This program has been upgraded and expanded  in 2006.</p>
	      <p>To date, DAN Asia-Pacific  has provided support for the establishment of decompression facilities in Vanuatu, Papua New   Guinea, Truk Lagoon, Solomon Islands and Thailand. </p>
