          <h1>DAN Dive Injury Insurance</h1>
	      <p>DAN  Asia-Pacific membership automatically provides the Member with <span class="emstrong">TravelAssist</span> benefits, including emergency evacuation, within the conditions of the coverage  to residents of all countries in the Asia-Pacific, except Japan. In  addition, being a DAN Asia-Pacific Member may entitle you to choose from  several optional dive injury insurance plans available exclusively to members  at the time of joining or renewing membership.</p>
	      <p>The  DAN Asia-Pacific plans offer insurance for treatment and certain other costs  associated with covered dive accidents.</p>
	      <p>In  addition, the <span class="strong">80km evacuation restriction is waived in the event of a  covered dive accident with a DAN Member who has taken a DAN Dive Injury  Insurance Plan. </span></p>
	      <p>Coverage  lasts for 12 months from the date of activation. </p>
	      <p>Eligibility for DAN insurance includes any recreational  scuba diver or snorkeller (including instructors and divemasters supervising  recreational diving activities) who is a Member of DAN Asia-Pacific, 12 years  of age or older. It is available to Nationals and Permanent residents of the  Asia-Pacific Region, excluding Japanese Nationals living in Japan. While  proof of residence is not required to take out membership or insurance it will  be required in the event of a claim.</p>
	      <p>DAN  Dive Injury Cover is available for recreational divers, scuba instructors,  divemasters, underwater photographers, or research divers whilst performing  research under the auspices and following the dive safety guidelines of the American Academy of Underwater Scientists (AAUS).  The cover is not available for other commercial diving activities.</p>
	      <h3>IMPORTANT NOTICE</h3>
	      <p>Availability  of services and benefits for evacuation or injury commences on acceptance of an  application for membership of DAN Asia-Pacific. The applicant is required to  complete the application fully and in good faith and with appropriate and full  disclosure of any predisposing medical condition, pre-existing condition or  previous decompression illness. No cover is provided for any evacuation,  medical, paramedical or other injury benefits resulting from or materially  contributed to, directly or indirectly by any diving or other activity in which  the applicant engaged prior to and up to the time of acceptance of the  application by DAN Asia-Pacific, regardless of the date on which any claim for  cover is made by or on behalf of the applicant.</p>
