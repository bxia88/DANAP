          <h1>The Work of DAN Asia-Pacific</h1>
          <p>DAN  Asia-Pacific works hard to improve dive safety services throughout the Asia-Pacific for the benefit of the diving and general community.&nbsp; In endeavouring to do this DAN undertakes  many activities, some of which include:&nbsp; </p>
          <ul>
            <li>The provision of a world class <a href="../membership/benefits.php">Membership Program</a> that includes a valuable range of <a href="../membership/travelassist.php">TravelAssist</a> benefits, <a href="insurance.php">Dive Injury (Treatment) Insurance</a> policies to meet the various needs of divers, as well as producing <a href="alertdiver.php">Alert Diver</a> magazine, to keep recreational divers up-to-date and informed about current dive health and safety issues.</li>
            <li>Provision of funding for <a href="hotlines.php">24-Hour Diving Emergency Hotlines</a> throughout the Asia-Pacific region to assist divers in their time of need. </li>
            <li> First aid and oxygen provider <a href="../training/courses.php">training courses</a> for the diving public as well as the workplace and home environments. </li>
            <li> DAN conducts<a href="daninaction.php"> educational seminars</a> in an effort to refresh the safety skills of divers and to disseminate the latest information on diving health and safety. </li>
            <li> Promoting dive safety via a variety of educational initiatives, such as the invaluable <a href="diverbelow.php">Diver Below </a> promotion. </li>
            <li>	<a href="../accident/research.php">Conducting Research</a> into factors such as repetitive dives, flying after diving, age, and gender, in order to improve the understanding and safety of recreational diving and to facilitate the development of safe dive tables and computers. </li>
            <li>Collecting, analysing and creating a publication of data on diving accidents and fatalities with a goal of improving the understanding of the causes of such accidents and to develop better treatment methods.  </li>
            <li> Providing support to the diving and dive medical community through the <a href="chamber.php">Recompression Chamber Assistance Program</a>.For example, providing support for the establishment of recompression facilities in Thailand, PNG, Vanuatu, the Solomon Islands, Chuuck, and others.</li>
            <li> DAN periodically donates oxygen equipment and training to dive locations to improve dive safety within the region. </li>
            <li> DAN Asia-Pacific has established representatives throughout the region in order to improve the availability of treatment and training to both residents and visitors to those countries, e.g. Thailand, Korea and Hong Kong. </li>
            <li> Training the dive industry in accident management and facilitating the availability of first aid equipment and facilities throughout the region. </li>
          </ul>
