          <h1>The History of Divers Alert Network</h1>
	      <p>DAN is a unique organisation  dedicated to safety, education and research in the scuba diving community.</p>
          <p>DAN was founded in 1980 at Duke University Medical Centre in the United States  with a 24-hour Emergency Hotline. This Hotline assisted in the treatment of  underwater diving accidents by providing information to injured divers and the  medical personnel who cared for them. </p>
          <p>To fund its ongoing existence, DAN  established a membership program, initially providing subscribers with a diving  accident manual and regular newsletter, <em>Alert Diver</em>, which dealt with  various issues of diving safety.</p>
          <p>As DAN grew, it expanded its operations to provide a free evacuation service to  Members, diving injury treatment insurance as well as a variety of other  services, including a highly effective and successful oxygen provision program  designed specifically for divers. Another important function of DAN became the  collation and analysis of data on diving accidents and fatalities to improve  the understanding of the causes of such accidents and to develop better treatment  methods.</p>
          <p>Today, the Divers Alert Network relies on <a href="../membership/benefits.php">Membership</a>, <a href="../sponsors/sponsors.php">supporters</a> </strong>within the dive industry and <a href="../categories.php">product  sales</a> to fund the provision of the high  level of service the dive community has become accustomed to receiving.</p>
          <p>The work of the Divers Alert Network is strongly supported within the  recreational diving community and this is evident by Membership numbers, which  presently exceed 300,000 divers worldwide.</p>
