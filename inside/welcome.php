          <h1>Welcome from Executive Director</h1>
          <p>Dear Fellow Divers,</p>
	      <p>Welcome to the website of the Divers Alert Network (DAN)  Asia-Pacific, your regional not-for-profit dive safety association. Our mission  is to promote safer diving for all recreational divers within this region.</p>
	      <p>Our enhanced website is designed to enable you to easily  find important information about DAN and the variety of services we offer,  24-hours a day, 365 days a year.&nbsp; Our  services include: <span class="emstrong"></span></p><img src="../_images/john_image.jpg" alt="John" width="120" height="150" class="imgRight_10" />
	      <ul>
	        <li class="strong">Diving Emergency Hotlines</li>
	        
	        <li class="strong">Membership</li>
	        <li class="strong">Dive Injury Insurance</li>
	        <li class="strong">Diving medical advisory service</li><li class="strong">Accident management training</li>
	        <li class="strong">Research &amp; data collection</li>
	        <li class="strong">Accident reporting</li>
	        <li class="strong">Product sales</li>
	        <li class="strong">&hellip; and more</li>
          </ul>
	      <p>DAN began in the United States in 1980 in  association with Duke University Medical Centre. Since then it has evolved into  an international group of affiliated but independent organizations with  regional centres in the United States,  Europe, Japan and Southern Africa, in addition to Asia-Pacific. In all, our  &lsquo;DAN Family&rsquo; currently comprises nearly 300,000 DAN Members worldwide.</p>
	      <p>DAN Asia-Pacific was established in Australia in 1994 and we have  worked long and hard to enhance dive safety education and accident prevention  and management within this Region.</p>
	      <p>As a not-for-profit association, DAN Asia-Pacific is not  owned by any individuals - we are here for our Members. This means that any  surplus revenue is directed towards improving DAN services and supporting our Mission. So, by supporting  DAN, you are not only supporting yourself, but the recreational diving  community in general.</p>
	      <p>As your dive safety association we endeavour to provide  the most accurate, up-to-date and unbiased information to the diving public on  dive safety.</p>
	      <p>In the unfortunate event of a dive accident, almost anywhere  in the world DAN Members have the benefit of being supported by the largest and  most experienced group of professionals dedicated to managing diving accidents. </p>
	      <p>If you are not already a DAN Member, we look forward to  welcoming you to the &lsquo;DAN family&rsquo; and opening up to you the unique range of invaluable  DAN Member Benefits. </p>
	      <p>To our existing Members we thank you for your ongoing  support and look forward to remaining <br />
          Your Buddy in Dive Safety.</p>
	      <p><em>Safe Diving,</em></p>
