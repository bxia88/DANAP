     <h1>Nutrition and Diving </h1>

         

        <p><em>By Neal W. Pollock, Ph.D.</em></p>

        <p>Sound nutrition promotes general health and physical fitness, both of   which are critical to diving safety. In addition, there are other   specific ways in which nutrition-related factors may affect diving   performance. <br />
          <br />
          An appreciation of fundamental concepts can help guide   nutritional choices. Energy is primarily provided by fats (9   kilocalories per gram [kcal/g]), carbohydrates (4 kcal/g) and protein (4   kcal/g). Alcohol is high in calories (7 kcal/g), but in a healthy diet   should be a negligible source of total calories. <br />
          <br />
          Diets that exclude major food groups are generally considered   undesirable. While a healthy total intake can be maintained, it requires   much greater effort with exclusionary diets. A recommended dietary   balance is 10-15 percent protein, 25-30 percent fats and 55-65 percent   carbohydrates. Saturated fats, primarily from animal sources, should   constitute less than one-third of the total fats. Complex carbohydrates,   starches and dietary fiber found in beans and grains, should vastly   outweigh simple sugars such as glucose and fructose. <br />
          <br />
          Fats, sugars and sodium are used liberally to enhance flavor in   processed food. Unsaturated fats may be partially or fully hydrogenated   to improve cooking performance and enhance a product's shelf life.   Problematically, the hydrogenation process also creates trans fats,   agents now linked to several health concerns. Fortunately, the trans fat   content has declined in prepared foods since the component was added to   U.S. nutrition labels in 2006. <br />
          <br />
          &quot;Caloric consciousness&quot; and nutrition label reading can   reinforce good dietary habits. Read the nutrition labels and look for   foods low in fat, sugar and sodium, free of trans fat and high in   dietary fiber and vitamins; products that appear very similar can differ   significantly in nutritional value. An even better strategy than simply   comparing labels is to focus diets on scratch-made, home-cooked meals.</p>
        <h2>Nutritional Considerations in Diving</h2>
        <p>Nutritional status can affect diving performance in obvious or subtle   ways. Obvious effects would include sudden fatigue or loss of   consciousness resulting from extreme caloric restriction. Loss of   consciousness has been reported in individuals trying to achieve   dramatic weight loss and others simply not taking time to eat. <br />
          <br />
          Adequate caloric reserves are necessary for appropriate   thermoregulatory responses. The measure of &quot;adequate,&quot; however, is often   misunderstood. There are many misconceptions as to the demands of   diving, referring both to physical work and thermoregulation. It does   not take huge amounts of food to effectively fuel the most energetic   dive in the coldest water. <br />
          <br />
        State of hydration is another important issue, but also one   subject to misconception. Proper hydration promotes effective   circulation that benefits thermoregulation and normal decompression   health. However, dehydration is a far less important risk factor than   the dive profile. Dehydration is probably unfairly blamed for the   negative outcome of many extreme exposures. In addition, excessive   hydration brings its own problems, such as potentially increasing the   risk of immersion pulmonary edema. </p>
        <h2>Studies on Nutrition and Diving</h2>
        <ul>
          <li>The experimental evidence specific to nutrition and diving is   limited but interesting. For example, based on the available data, it   does not appear that ingesting either caffeine or capsaicin (the active   ingredient in hot peppers) enhances thermal protection. </li>
          <br />
          <li>A single study describes a possible relationship between   high total cholesterol levels and &quot;bubble-proneness&quot; following altitude   decompression. Further work is required but the possibility of this   being a real effect should encourage divers to maintain healthy (low)   cholesterol levels. </li>
          <br />
          <li>Observations that marine fish oils (primarily omega-3 fatty   acids eicosapentaenoic acid (EPA) and docosahexaenoic acid (DHA)) might   reduce blood platelet adhesiveness are tantalizing. Further work is   required to confirm this effect and determine whether the biochemical   insult of decompression stress could be reduced. </li>
          <br />
          <li>Drinking alcohol in conjunction with diving is   inappropriate given the potential for impaired decision making and/or   symptom masking. However, animal studies suggest that potential   biochemical interactions deserve further laboratory study. </li>
          <br />
          <li>Questions regarding herbal supplements and decompression   stress generally have not been critically investigated. Some supplements   are known to interact with medical treatments, particularly anesthesia.   It is speculative but possible that some may interact with the pressure   environment or decompression stress in a meaningful way. </li>
        </ul>
        <h2>Nutrition Tips for Consumers</h2>
        <p>Consumers are frequently faced with grandiose marketing claims that   should be viewed cautiously. This is particularly true for products   related to nutrition but outside Food and Drug Administration purview.   Several have prompted questions from the diving community. Oxygenated   water, for example, does not improve aerobic performance. Similarly,   there is no evidence that any commercial drink will provide special   protection against decompression sickness. Finally, high-calorie sport   bars are not essential to meet the modest caloric demands of most diving   activity. <br />
          <br />
        Sound nutrition is an important tool in maintaining general   health. Good nutritional practices, particularly when combined with   regular physical exercise, can contribute to a long and enjoyable diving   life. Developing the habit of making thoughtful dietary choices is the   best place to start.  </p>
        <h2>Recommendations</h2>
        <ul>
          <li>Eat a variety of foods, favoring whole fruits, whole vegetables and whole grains. <br />
        </li>
        </ul>
        <ul>
          <li>Avoid processed foods high in sodium, fat and calories and low in natural nutrients. <br />
        </li>
        </ul>
        <ul>
          <li>Avoid trans fats. <br />
        </li>
        </ul>
        <ul>
          <li>Replace whole fat with low-fat or nonfat milk and dairy products. <br />
        </li>
        </ul>
        <ul>
          <li>Control calorie intake to manage body weight. <br />
        </li>
        </ul>
        <ul>
          <li>Be physically active every day, limiting high-intensity exercise to nondiving days. <br />
        </li>
        </ul>
        <ul>
          <li>If you drink alcoholic beverages, do so in moderation. <br />
        </li>
        </ul>
        <ul>
          <li>If you use multivitamins, do so as a backup, not to replace good dietary habits. <br />
        </li>
        </ul>
        <ul>
          <li>Be cautious in accepting nutritional health claims.</li>
        </ul>
