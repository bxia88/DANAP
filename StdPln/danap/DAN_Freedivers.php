        <h1>DAN Coverage for Freediving</h1>
        
        <p align="justify">DAN Asia-Pacific does provide coverage for Freedivers. Please review the below information for exclusions and limitations which will assist with your DAN Application.</p>
        <h2>Are There Any Limitations with the Treatment  Insurance Plans?</h2>
        <p align="justify">The Master Plan and Preferred Plan  both cover the treatment costs  associated with a covered freediving incident.</p>
        <h2>Are There Any Exclusions?</h2>
        <p>Yes,  Record Attempts and Competitions are excluded. </p>
        <p>For the full list of Exclusions that apply to DAN Coverage <a href="_pdf/austpolicywordingcurrent.pdf">CLICK HERE</a> and review from page 31.</p>
        <h1>How To Apply For DAN Coverage</h1>
        <h3><a href="Membership_Insurance/residencycheck.php">CLICK HERE</a> to commence the application process.</h3>
        <p>Keep in mind there are two components to DAN Asia-Pacific Coverage:</p>
        <p><strong>1. DAN Membership, providing 12-months coverage and includes:</strong></p>
        <ul>
          <li> $150,000 <a href="http://www.danasiapacific.org/Membership_Insurance/travelassist.php">Worldwide Emergency Evacuation Coverage</a></li>
          <li>Travel Assistance Benefits (Personal, Travel, Legal, Medical)</li>
          <li><em><a href="http://www.danasiapacific.org/Membership_Insurance/benefits.php">Alert Diver</a></em><a href="http://www.danasiapacific.org/Membership_Insurance/benefits.php"> Magazine</a>, the Region&rsquo;s  premier dive health and safety magazine</li>
          <li><a href="Membership_Insurance/benefits_DAN.php">and more</a>!</li>
        </ul>
        <p><strong>2. Dive Injury Insurance Plans</strong></p>
        <p align="justify">These plans provide coverage for treatment and certain other expenses following a covered diving incident. <a href="Membership_Insurance/insurance_considerations.php">CLICK HERE</a> for issues to consider when choosing a Treatment Insurance Plan and to review the Comparison Table of Plans. </p>
