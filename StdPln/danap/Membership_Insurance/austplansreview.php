          <h1>Optional DAN Dive Injury Insurance - (For Residents of Australia)</h1>
	      <p align="justify">The following <span class="em">optional</span> DAN Dive Injury Insurance plans are   offered in addition to DAN Asia-Pacific Membership. These plans provide coverage   for treatment and certain other costs associated with covered dive   accidents.&nbsp; DAN Asia-Pacific   exclusively offers Members these dive injury insurance plans <span class="under">only</span> at the   time of joining or renewing Membership.</p>
	      <p align="justify">While emergency evacuation benefits are included automatically   with DAN Membership, it is your choice whether you add the additional dive   injury (treatment) insurance coverage.</p>
	      <p align="justify">For Members electing to take out both Membership and Insurance the 80km   evacuation restriction that applies to Membership only is waived in the case of   a Covered Dive Accident only.</p>
	      <p align="justify">DAN dive injury insurance is available to any recreational diver or snorkeller, (including instructors and divemasters   supervising recreational diving activities), who is a Member of DAN Asia-Pacific   and is 12 years of age or older. It's also available to certain research divers   &amp; underwater photographers. Coverage is not available for commercial diving   other than those activities specified.</p>
	      <h3 align="justify"><a href="insurance_considerations.php">CLICK HERE</a> FOR ISSUES TO CONSIDER WHEN CHOOSING A DIVE INJURY INSURANCE PLAN.</h3>
	      <p align="justify"><span class="strong">The DAN Master Plan (3) </span>  provides up to AUD$150,000 coverage for medical charges for decompression  illness and all covered in-water injuries (including snorkelling) sustained on a dive or a Repetitive  Dive Series to a maximum depth of 50 metres. The Master Plan also offers up to  AUD$10,000 in accidental death and dismemberment  benefits resulting from a covered Diving Accident; plus up to AUD$15,000 in  permanent total disability benefits arising from a covered Diving Accident. </p>
	      <p align="justify"><span class="strong">The DAN Preferred Plan (2) </span>  provides up to AUD$300,000 coverage for medical charges for a covered Diving  (and Snorkelling) Accident. The Preferred Plan also offers up to AUD$10,000 in accidental death   and dismemberment benefits resulting from a covered  Diving Accident; plus up to AUD$15,000 in permanent total disability benefits  from a covered Diving Accident sustained on a dive to any depth,  provided that the Member held appropriate certification for the dive, and was  using appropriate breathing gas mixes and equipment during the dive or  Repetitive Dive Series.</p>
