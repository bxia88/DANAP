<h1 align="justify">Duty of Disclosure</h1>
  <p>Your duty of   disclosure contracts of general insurance subject to the Insurance Contracts   Act. before you enter into a contract of general insurance with an insurer, you   have a duty, under the Insurance Contracts Act 1984, to disclose to the insurer   every matter that you know, or could reasonably be expected to know, is relevant   to the insurer's decision whether to accept the risk of the insurance and, if so, on what terms.</p>
<p>Your duty, however, does not require disclosure of matters: <br>
  <ul>
    <li>that diminish the risk to be undertaken by the insurer</li>
    <li>that are of common knowledge, </li>
    <li>that your insurer knows or, in the ordinary course of its business, ought to know, or </li>
    <li>as to which compliance with your duty is waived by the insurer. </li>
  </ul>
  <h3 align="justify"><strong>Non-Disclosure <br>
    </strong>  </h3>
