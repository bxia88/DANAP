            <p>HLTAID001 Provide CPR, </p>
          </li>
          <li>
            <p>HLTAID002 Basic Emergency Life Support,  </p>
          </li>
          <li>
            <p>HLTAID003 Provide First aid.</p>
          </li>
        </ul>
        <h2><strong>Dates</strong>: You can choose from the following: </h2>
        <h6><strong>July</strong></h6>
        <p><strong>Oxygen</strong>: one day: Friday 1st July 2016.<br />
            <strong>First  aid:</strong> two and a half days. Saturday 2nd,  Sunday 3rd, and Monday 4th July 2016.<br />
            <strong>Combined  program</strong>: three and a half days. See dates above.</p>
        <h6><strong>August</strong></h6>
        <p><strong>Oxygen:</strong> one day Friday 19th August 2016.</p>
        <h6><strong>September</strong></h6>
        <p><strong>Oxygen:</strong> One day. Friday 23rd Sept. 2016.<br />
            <strong>First  aid:</strong> Two and a half days. Saturday 24th,  Sunday 25th, Monday 26th September 2016.<br />
            <strong>Combined  program:</strong> Three and a half days. See dates above. </p>
        <p><strong>NOTE: Courses will only run if sufficient  numbers enrol.</strong></p>
        <h2><strong>Cost</strong></h2>
        <p>Oxygen  provider instructor only: $350-oo.<br />
          First  Aid instructor only: $495-oo<br />
          Both  Oxygen and first aid instructor: $795-oo</p>
        <h2><strong>Pre-requisites</strong></h2>
        <p>18 years of age. </p>
        <ul><li>Hold <strong>TAE40110 Certificate 4 in training and assessment</strong>. </li>
          <li>Be an <strong>instructor with an accredited SCUBA training organization.</strong></li>
          <li>Be a <strong>current member of DAN AP.</strong></li>
          <li>Have or obtain a<strong> USI</strong> (unique student identifier  number.) Go to <strong>www.usi.gov.au</strong></li>
        </ul>
        <p><strong>For  oxygen provider:</strong></p>
        <ul>
          <li>Hold a current DAN <strong>oxygen provision for dive accidents</strong> qualification.</li>
        </ul>
        <p><strong>For  First aid instructor:</strong></p>
        <ul>
