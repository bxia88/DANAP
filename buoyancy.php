	      <h1>Buoyancy 101</h1>
	      <p>Following is a summary of &quot;Buoyancy 101&quot; from basic diver training.</p>
	      <h2>The three states of buoyancy</h2>
	      <ul>
            <li><em>Positive  buoyancy</em>: the tendency of an object to float.</li>
            <li>                <em>Negative  buoyancy: </em>the tendency of an object to sink.</li>
            <li>              <em>Neutral  buoyancy: </em>the tendency of an object to remain at  the same depth while in a column of water. </li>
          </ul>
          <p>Throughout a dive, a diver will change  their buoyancy requirements.</p>
          <p>Positive buoyancy is the desired state  before descent begins to allow us to ensure that all is OK to proceed. It is  also required to float safely while on the surface at the end of the dive.  During ascent, we should aim to achieve slight positive buoyancy to minimize  effort and maintain control.<br />
 <br />
Negative buoyancy is required so we can  descend. Neutral buoyancy is what we should achieve for most of our underwater  time, to enable us to move with minimum effort around a reef, wreck, cave or  other site. </p>
          <p>It takes time and effort to learn to  change from one buoyancy state to another. Changes should be done subtly and  gradually. Skill in this regard will only be achieved with practice. </p>
          <p>The process of buoyancy control begins  with <strong>correct weighting. </strong>If we are  too light, you will never get underwater. But if we are too heavy, we sink like  a brick. </p>
          <p>Incorrect weighting is all too common,  and it invariably means OVERWEIGHTED. This process usually begins during basic  training when new divers SHOULD be taught the need to be correctly weighted,  and be guided through the steps to achieve it. But this is often not achieved.</p>
          <p>We urge all instructors to ensure that  students in their care are weighted correctly. An over-weighted diver is NOT a  comfortable or a safe diver.</p>
          <p>Rapid and uncontrolled descents put  divers at risk of ear and sinus barotraumas, and can lead to collisions with  reef and wreck causing disorientation, reduced visibility, and even physical  injury. 
            An overweight diver is very inefficient,  expends a lot more energy, and will have a higher rate of air consumption. </p>
          <p>The solution to being overweighted is to  add air to your BCD or drysuit. If a lot of air is added, drag increases and  trim can be disrupted. Neither condition enhances the diver’s safety, comfort  or enjoyment. Adding air to offset negative buoyancy needs to be done carefully  and slowly. If you add air too quickly, you can become too buoyant, and ascend  in a dangerous and uncontrolled manner, risking pulmonary barotrauma and  cerebral arterial gas embolism (CAGE). </p>
          <h2><strong>Getting  your buoyancy right</strong></h2>
          <p>Buoyancy control begins with correct  weighting. You should aim to be neutral while on the surface with all air  drained from your BCD. When you exhale, you should begin to sink. </p>
          <p>Now see if you can hover in mid-water. If  you are really neutral, you should be able to hold this position comfortably</p>
          <h2><strong>Practicing  Skills</strong></h2>
          <p>Jump in at your favorite jetty or reef,  and test yourself using this basic series of buoyancy skills. It can be  beneficial to add a shotline for reference. </p>
          <ul>
            <li>Sink from surface without kicking down -  Negative buoyancy.</li>
            <li> Stop your descent and hover every 2-3 m  of descent - Neutral buoyancy. </li>
            <li> Hover without fin or hand movements at  your chosen depth - Neutral buoyancy. </li>
            <li> Slowly ascend - Positive buoyancy.</li>
            <li> Stop your ascent every 2-3 m and hover -  Neutral buoyancy. </li>
            <li> When you are back to 1-2 m, begin to descend  again, and repeat all steps. </li>
          </ul>
          <p>If you are truly in control of your  buoyancy, you should be able to work through this skill set several times  without kicking, without touching the bottom, or surfacing. </p>
