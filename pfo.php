        <h2>What is a Patent Foramen Ovale (PFO)</h2>
        <p align="justify">A PFO is a  very small hole in the heart (often closed under normal circumstances) and  which, during or after diving, can enable nitrogen bubbles to pass back into  circulation in the blood rather than being expelled by the lungs, leading to  DCI.&nbsp; This diver's experience has become increasingly common over the past few years. </p>
		 <p align="center"><img src="_images/pfo_anatomy.jpg" width="220" height="200" /></p>
        <h2><strong>Indications that a diver may have a PFO:</strong></h2>
        <p align="justify">          These can  range from an often mild mottled rash after diving to severe neurological  symptoms, such as collapse, weakness, blurred vision, among others.</p>
        <p align="justify">          If a PFO is  suspected, the treating physician will often refer the diver to be tested. A  negative result on the test does not necessarily mean there is no PFO, however,  if one is present it is likely to be very small. A positive test indicates  there is an obvious route that bubbles in the venous blood can pass through the  heart and enter the arterial blood, from where it can be taken to the brain,  spine and elsewhere in the body. The PFO can often be repaired surgically, and some  divers elect to have the hole closed. As with any surgical procedure there are  associated risks and these need to be balanced against the desire to continue  diving and the risk of diving without having the PFO repaired. This varies  between individuals and is something that needs to be discussed between the  diver, the diving physician and the cardiologist. Many divers continue to dive  despite a small PFO and reduce the chances of a problem by limiting depths,  repetitive diving, extending surface intervals, doing slow ascents and safety  stops and using nitrox (with dive times based on air).</p>
