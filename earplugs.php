        <h2>Member Question: Ear Plugs</h2>
        <p align="justify"><a href="http://www.chinadiveshow.com"></a>In the past  10 years no new information has surfaced to warrant any change in DAN&rsquo;s  recommendations regarding the use of vented earplugs. Although traditional, non-vented  earplugs are never appropriate for diving, some divers report improved  equalization while using vented earplugs. Others, however, report no  improvement. Manufacturers of these products claim they make equalization  easier, but since pressures must still be equalized we do not know why that  would be the case. Although the ear canals may remain drier, this does not  change the fact that the pressure in the middle ears must still equalize to the  surrounding water pressure. Originally, vented earplugs were designed to reduce  the occurrence of otitis externa (swimmer&rsquo;s ear). For this purpose the earplugs  have demonstrated some value. We know of no consistent evidence, scientific or  anecdotal, to confirm significant improvement of equalization.</p>
        <p align="justify">Longtime DAN  consulting physician and ear, nose and throat surgeon, Dr. Cameron Gillespie,<strong> </strong>recently confirmed that users are not likely to be injured by using vented  earplugs while diving. However, Gillespie does have some reservations about  their use on the surface. Earplugs inhibit normal hearing and may hinder buddy  communication and awareness of crew instructions. If earplugs are worn with the  hope of easing the process of equalization, they should not be used in an  attempt to compensate for sinus congestion or other major obstacles to  equalization, as these obstacles preclude diving. In addition, since the risk  of leaking cannot be eliminated completely, diving with a perforated eardrum or  pressure-equalization tubes is not recommended at any time &mdash; with or without  earplugs.&nbsp; </p>
        <p><em>John U. Lee, MSDT, EMT, CHT, DMT, DAN medical information specialist </em></p>
