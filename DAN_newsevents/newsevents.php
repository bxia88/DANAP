        <h2>DAN's Jellyfish Investigation : Can You Help?       </h2>

        <p align="justify">There  has been an increase in reported serious jellyfish stings in Thailand, and  possibly elsewhere in Asia. Since mentioning our jellyfish investigation we  have received several great photos of jellyfish, some being dangerous species  found in Thailand and the Philippines. DAN AP encourages any underwater  photographers in Asia, especially Thailand,&nbsp;to photograph any jellyfish  they see on a dive and send a copy of the photo to us. <em>Please be sure to  document as precisely as possible the locations of each photo. </em>Your  assistance will enable us to increase our understanding, to better catalogue  the potentially dangerous species present in parts of our region and help in  establishing appropriate prevention and management strategies. </p>

        <p>We have  produced a large poster with images of some potentially deadly jellyfish and  useful first aid and prevention advice. We have already begun to distribute  these in Thailand and plan to make them available elsewhere in the Region. </p>

        <p><strong><a href="_pdf/Jellyfish_Poster.pdf">CLICK  HERE</a></strong> to download a copy of this poster.</p>

