        <h1 id="storyTitle">DAN Short Term Insurance Frequently Asked Questions </h1>
        <h2><strong>What are the Coverage Options for my Customers?</strong></h2>
        <p>There are 2 very affordable options for divers:</p>
        <ul>
          <li>
            <p>10 Days Coverage = US$40                          </p>
          </li>
          <li>
            <p>30 Days Coverage =  US$50</p>
          </li>
        </ul>
        <h2><strong>Who can apply for  DAN’s Short Term Plans?</strong></h2>
        <p>All divers, of any age, from any country, including divers undergoing training, e.g. resort dives, try-dives, open water courses. </p>
        <h2><strong>How long does it  take to process an application?</strong></h2>
        <p>          Once an online form is submitted and payment accepted, coverage is <strong>immediate</strong>. Applications can be  submitted at any time on any day of the week: 24/7!</p>
        <h2><strong>Are there any  restrictions for age, depth, equipment, or gas mix?</strong></h2>
        <p>No! <em>Note: Please advise your customer that their short term coverage is only valid within the country of purchase, it does not cross borders. </em></p>
        <h1>Payment and Commission        </h1>
        <h2><strong>How Can  the Short Term Coverage be Paid For? </strong></h2>
        <p>There are two options: </p>
        <ul>
          <li>
            <p>To earn your 20% commission  <strong>immediatel</strong>y (US$8 or US$10), use your business credit card (Mastercard/Visa/Amex/Discover)  or Paypal to register  the diver/s. The sum you pay will be less your 20% commission.<br />
              <em>- For the 10-Day option, you pay only US$32 (retail  US$40), or<br />
                - For the 30-Day option, you pay only US$40 (retail US$50)</em> </p>
          </li>
          <li>
            <p>Alternatively, the diver can pay  with their own credit card. This means your commission will be paid to you <strong>at a  later date</strong> into your provided bank account or Paypal account. Payments will be made quarterly. </p>
          </li>
        </ul>
        <h1>Registration</h1>
        <h2>Registering Your Business is Simple &amp; Free! </h2>
        <p>Visit <strong><a href="http://www.danap.org/ShortTerm">www.danap.org/ShortTerm</a></strong> and Select  “Register”. It will only take a minute to register. The only details required  are your Business Name, Contact Person, Email Address and you need to enter a  Password. Note: You can only offer the Short Term Insurance to your customers once you have registered as a Partner, and the coverage can only be purchased via the Short Term website. </p>
        <h2>Registering a Customer </h2>
        <p>          You are  able to enter just one diver or multiple divers at one time, and choose the  start date. All that is required is each applicant’s name, address, email and  their coverage preference: 10-days or 30-days. <a href="_pdf/Short_Term_Training.pdf">CLICK HERE FOR THE TRAINING  GUIDE</a>.</p>
        <h2><strong>What  will my business and my divers receive to indicate a diver is covered?</strong></h2>
        <p>          You will be sent an automatic confirmation email to your registered business email  address. In addition, an Insurance Certificate will be emailed to the diver.</p>
        <h2>Once You Register as a Partner and Log-in, You Can: </h2>
        <ul>
          <li>Download/print support materials: Insurance  Description Flyers, Waivers and Coverage Certificates.</li>
          <li>View enrolment and commission earnings history.</li>
        </ul>
