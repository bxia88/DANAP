          <h1>DAN의 연구활동 </h1>
	      <p>DAN은 다이빙  생리학 연구 분야의 선두주자입니다. 이 연구활동은 거의 전적으로 회원들의 회비와 기부금으로 지원되고 있으며,  DAN은 이러한 연구를 통해 전체 다이빙 커뮤니티에 이득이 되고 전세계 레크레이션 다이빙의 안전성을 개선하는 중요한 정보들을  밝혀내고 있습니다.</p>
	      <h2>DAN 온라인 연구자료  </h2>
	      <p><a href="http://www.diversalertnetwork.org/research/index.asp">DAN  Research Department Web Site</a> 를 방문하시는 분들은 원하실 경우 다이빙 프로파일 및 다이빙 상해와 관련된 정보를 수집하는 소프트웨어를  무료로 다운로드 받으실 수 있습니다. 또한 다음  프로젝트에 대한 더 자세한 내용도 읽으실 수 있습니다:</p>
	      <ul type="disc">
            <li>프로젝트 다이브 탐험 </li>
	        <li>당뇨병과 다이빙 </li>
	        <li>다이빙 후 비행 </li>
	        <li>다이버 고령화에 대한 연구 </li>
	        <li>상승 속도 </li>
          </ul>
	      <h2>DAN의  감압질환(DCI) 및 치명적 다이빙 사고 보고 </h2>
	      <img src="../_images/diving_fatalities.jpg" alt="Diving Fatalities" width="95" height="118" class="imgLeft" />
	      <p> DAN 아메리카는 감압질환 및 치명적 다이빙  사고들을 폭넓게 다루는 연례 보고서를 발행하고 있습니다. 현재 DAN AP에 가입되어 있는 회원들은 <strong>이곳을 누르시면</strong> 회원전용 포탈을 통해 여러 DAN 아메리카 연례 보고서들을 보실 수 있습니다. <a href="http://www.danasiapacific.org/main/member_only/index.php">CLICKING HERE</a>.	      </p>
	      <img src="../_images/aust_dive_deaths_94-98.jpg" width="110" height="130" class="imgRight" />
	      <p>호주의 치명적  다이빙 사고에 대한 DAN AP의 보고서는 다이빙 및 스노클링과 관련된  사망사례들을 재검토하고 상세한 사고 경위 및 원인들의 목록과 이러한 사례를 통해 얻을 교훈에 대한 토의를 포함하고 있습니다. 이 보고서(1972-1993,  1994-1998 및  1999-2002)들은 구입이 가능하며 이 중 일부는 회원전용 포탈을 통해 다운로드 받으실 수 있습니다.  </p>
	      <p>2004년 자료를 토대로 한 스쿠버 다이빙 상해 및 사망사례에 대한 연례 다이빙 보고서(2006 년판)는DAN회원들에게PDF로 제공됩니다.  <br />
현재 DAN AP에 가입되어 있는 회원들은 <strong>회원전용 포탈을 통해 이 보고서들을 보실 수 있습니다</strong><strong>. </strong><strong> </strong><br />
	      </p>
	      <h2>일반적인 질문에 대한 연구 </h2>
	      <p>DAN은 연구활동을 통해 귀 및 부비동 상해, 다이빙을 위한 건강 및 체력 등, 모든 다이버들에게 영향을 미치는 요인들과 관련된 일반적인 질문에 대한 답을 찾을 수 있도록 도와드립니다. </p>
	      <p>	        <a href="ear&amp;sinus.php">Click here for  an overview of Ear &amp; Sinus Injuries</a></p>
	      <p>	        <a href="health&amp;fitness.php">Click here for  an overview of Health &amp; Fitness in Diving</a></p>
	      <h2>DAN 연구에 대한 재정지원 </h2>
	      <p>DAN의 프로젝트들은 DAN회원비 및 다이빙산업의 지원 등을 통한 민간차원의 재정지원을 받고 있습니다. DAN이 없다면, 레크레이션 다이빙 안정성과 관련된 많은 중요한 문제들에 도전하거나 해답을 찾을 수 없을 것입니다.</p>
