          <h1>DAN Research<img src="../_images/researchlogo.jpg" width="100" height="100" class="imgRight" /></h1>
	      <p>DAN is the dive industry leader in diving physiology research. Supported almost entirely from membership fees and contributions, DAN's studies are revealing important information that will benefit the entire diving community and improve recreational diving safety worldwide. </p>
	      <h2>DAN Research On-line</h2>
	      <p> Interested participants  visiting the <a href="http://www.diversalertnetwork.org/research/index.asp">DAN Research Department Web Site</a> can download free software for collecting information about dive profiles and  diving injuries. You can also read more about the following projects:</p>
	      <ul>
	        <li>Project Dive Exploration</li>
            <li> Diabetes and Diving</li>
	        <li>Flying after Diving</li>
	        <li>Ageing Diver Study</li>
	        <li>Ascent Rate</li>
	      </ul>
	      <h2>DAN Decompression  Illness (DCI) &amp; Diving Fatalities Report</h2>
	      <img src="../_images/diving_fatalities.jpg" alt="Diving Fatalities" width="95" height="118" class="imgLeft" />
	      <p> DAN America produces a  comprehensive annual Report on Decompression Illness and Diving  Fatalities. Current DAN AP Members can view several DAN America Annual Reports  from the Member-Only Portal by <a href="http://www.danasiapacific.org/main/member_only/index.php">CLICKING HERE</a>.	      </p>
	      <img src="../_images/aust_dive_deaths_94-98.jpg" width="110" height="130" class="imgRight" />
	      <p>DAN AP &rsquo;s <span class="emstrong">Reports on  Australian Diving Deaths</span> provide reviews of diving and snorkelling-related deaths and includes detailed incident reports, tables of relevant  contributing factors, and discussions about lessons to be learned from these  deaths.&nbsp; These reports (1972-1993, 1994-1998 and 1999-2002) are available for purchase although some can be downloaded via the <a href="http://www.danasiapacific.org/main/member_only/index.php">Member-Only Portal</a>.</p>
	      <p>The <span class="strong"> Annual Diving Report (2006 Edition) on Scuba Diving Injuries and Fatalities</span> based on 2004 data is available to DAN Members in PDF format.</p>
	      <p>These reports can be accessed by current DAN AP Members via our <strong> Member-Only Portal</strong>. <br />
          <a href="http://www.danasiapacific.org/main/member_only/index.php">CLICK HERE</a> to log in. </p>
