          <h1>Health & Fitness in Scuba Diving</h1>
	      <p class="emstrong">This is meant as a general overview and should not be used in place of a medical consultation.	      </p>
	      <h2>Personal  Fitness in Scuba Diving</h2>
	      <p> Scuba diving offers opportunities for  travel, family fun and friendly interaction with the environment. As divers of  all ages and athletic abilities pursue recreational scuba diving, one must  consider the fitness level required for scuba. Although the relative weightless  underwater environment makes diving appear almost effortless, scuba diving does  require a measure of strength and stamina.</p>
	      <h2>            Learning  to Scuba Dive</h2>
	      <p> If you&rsquo;re considering learning to dive,  keep in mind that not all your diving will be the same - water and weather  conditions vary from dive to dive and even the beginning to end of some dives.  You may be required to manoeuvre through strong currents to reach your dive  site - or perform a long surface swim at the end of a dive. Divers need to be able  to perform unexpected strenuous physical tasks. Also, they should not have any  health conditions or be taking any medications that may cause problems while  diving.</p>
	      <h2>            Restrictions  on Diving</h2>
	      <p>Some medical conditions - such as colds,  flu, injury or pregnancy - will temporarily restrict diving. Why? With colds or  flu, swelling or blockage in the Eustachian tubes or sinuses may prevent  adequate equalisation of these air spaces with the water pressure. Injuries to  joints and muscles may not only reduce diving ability but also increase  susceptibility to decompression illness. Therefore, it is best to postpone  diving until the injuries are fully healed. Because little is known about the  effects of diving on an unborn child, it is recommended that pregnant women wait  to dive until after a pregnancy. Other medical conditions that restrict  movement of a diver&rsquo;s arms and legs may limit in water mobility. Additionally,  some people may experience pain or discomfort with the exertion of diving.  These symptoms may mimic the signs of decompression illness after a dive. Such  health issues require individual evaluation by a physician prior to learning to  dive.</p>
	      <h2>            Pre-dive  Physical Examinations</h2>
	      <p>You should definitely consult a doctor  before diving if you have any of the above conditions, but also other  conditions like a history of heart or lung disease, nausea/vomiting,  unexplained black-outs, shortness of breath, have the appearance of not being  physically fit, are taking any medication, or have repeated trouble clearing  air spaces. No upper age limit for participation in scuba diving exists,  provided a diver is fit and healthy and has no disqualifying medical  conditions.</p>
	      <h2> Diving on  Medications</h2>
	      <p> Let your dive instructor know if you are  taking any medications, whether they are prescribed or not. Most medications  will have no effect on diving, but some may cause drowsiness or fatigue, which  may increase susceptibility to nitrogen narcosis. Others may affect heart rate.  Read the warning labels carefully and consult a doctor if you are unsure.</p>
	      <h2>            Asthma  and Diabetes</h2>
	      <p>	        There is concern about the advisability  of allowing individuals with asthma or insulin dependent diabetes mellitus to  dive. Some individuals with a history of asthma are permitted to dive by their  doctors - after a careful evaluation. Diabetes and many other medical  conditions must be evaluated on a case-by-case basis by an appropriate medical  specialist. DAN membership helps support research into these matters.</p>
	      <h2>            Enjoy  Scuba!</h2>
	      <p> Scuba diving is an activity that anyone with good  health and fitness can enjoy for a lifetime. To get the most out of your  lifetime of diving, practice disease and injury prevention and participate in a  health maintenance program.</p>
