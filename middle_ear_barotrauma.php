        <h1>Middle Ear Barotrauma (MEBT) </h1>
        <p align="justify"><a href="http://www.chinadiveshow.com"></a>Middle-ear  barotrauma is the accumulation of fluid and blood in the middle ear or rupture  of the eardrum as a consequence of failed equalization of pressure in the air  space of the middle ear during diving or flying. </p>
        <h2 align="justify">Mechanisms</h2>
        <p>The air  pressure in the tympanic cavity­ — air-filled space in the middle ear — must be  equalized with the pressure of the surrounding environment. The Eustachian tube  connects the throat with the tympanic cavity and provides passage for gas when  pressure equalization is needed. This equalization normally occurs with little  or no effort. Various maneuvers, such as swallowing or yawning, can facilitate  the process.<br />
            <br />
          An obstruction in the Eustachian tube can lead to an inability to achieve  equalization particularly during a descent when the pressure changes fast. If  the pressure in the tympanic cavity is lower than the pressure of the  surrounding tissue, this imbalance results in a relative vacuum in the middle  ear space. It causes tissue to swell, the eardrum to bulge inward, leakage of  fluid and bleeding of ruptured vessels. At a certain point an active attempt to  equalize will be futile, and a forceful Valsalva maneuver may actually injure  the inner ear. Eventually, the eardrum may rupture; this is likely to bring  relief from the pain associated with MEBT, but it is an outcome to be avoided  if possible.<br />
  <br />
          Factors that can contribute to the development of MEBT include the common cold,  allergies or inflammation — conditions that can cause swelling and may block  the Eustachian tubes. Poor equalization techniques or too rapid descent may  also contribute to development of MEBT.</p>
        <h2>Manifestations</h2>
        <p>Divers  who cannot equalize middle-ear pressure during descent will first feel  discomfort in their ears (clogged ears, stuffed ears) that may progress to  severe pain. Further descent only intensifies the ear pain, which is soon  followed by serous fluid buildup and bleeding in the middle ear. With further  descent, the eardrum may rupture, providing pain relief; this rupture may cause  vertigo, hearing loss and exposure to infection.</p>
        <h2><strong>Management</strong></h2>
        <p><em>While  diving:</em> When  feeling ear discomfort during descent, you should stop descending and attempt  equalization. If needed, ascend a few feet to enable equalization. If  equalization cannot be achieved, you should safely end the dive.<br />
          <br />
          <em>First aid:</em> When feeling fullness in one's ears after diving, abstain  from further diving. Use a nasal decongestant spray or drops. This will reduce  the swelling of nasal mucosa and Eustachian tube mucosa, which may help to open  the Eustachian tube and drain the fluid from the middle ear. Do not put any  drops in your ear.<br />
          <br />
          <em>Treatment:</em> Seek a physician evaluation if fluid or blood discharge from  the ear canal is present or if ear pain and fullness lasts more than a few  hours. If vertigo and dizziness are present, which may be a symptom of  inner-ear barotrauma, you should seek an urgent evaluation. Severe vertigo and  nausea after diving require emergency medical care.</p>
        <h2><strong>Fitness to Dive</strong></h2>
        <p>Return to  diving may be considered if a physician determines that the injury is healed  and the Eustachian tube is functional.</p>
        <h2><strong>Prevention</strong></h2>
        <ul type="disc">
          <li>Do not dive with congestion       or cold.</li>
        </ul>
        <ul type="disc">
          <li>Descend slowly. If unable to       equalize after a few attempts, safely end the dive to avoid significant       injury that may prevent you from diving the rest of the week.</li>
        </ul>
