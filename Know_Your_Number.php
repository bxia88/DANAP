        <h1>Know Your Number </h1>
         
        <p><strong>Make  Sure You Are Reachable In An Emergency</strong></p>
        <p><em>Did  you know that having a call back phone number ready to give to DAN Emergency  Staff in the event of a diving accident could be the most important part of  your personal emergency plan? </em></p>
        <p>One of the most frequent challenges faced by all DAN organizations  is dealing with incorrect phone numbers.&nbsp;All it takes is for one digit in  a call-back number to be wrong to prevent DAN from reaching a diver and delays to  ensue. I have personally spent hours trying to call a diver back when the  number they provided was wrong.</p>
        <p>Should you call a DAN Emergency Hotline you will quickly be asked  for a number we can call you back on, just in case the call drop-outs or is disconnected,  which happens often as phone connections in many parts of Asia, the Pacific,  and the world, can be unreliable. </p>
        <p>The below examples demonstrate how smooth or unnecessarily difficult case management can be, simply  due to a phone number.</p>
        <h2><strong>Case 1 : Number Known</strong></h2>
        <p> The caller had an  international calling plan and their cell/mobile phone worked where they were  travelling. After the initial call to DAN was made, we were repeatedly and  easily able to reach the diver so their condition could be assessed quickly,  first aid advised and corrected and transportation and referral to the chamber  arranged and treatment provided. This all happened smoothly and in just a few  hours, all because we were able to communicate quickly and easily. </p>
        <h2><strong>Case 2 : Wrong Number </strong></h2>
        <p> The caller did not know how  to be called in the country from where they were diving and when DAN was called  they provided the calling sequence for calling their mobile phone from within  their home country.&nbsp;As such, when the DAN Emergency Hotline staff member  repeatedly attempted to call the diver they were unsuccessful.&nbsp;Over 90  minutes ensued while many possible combinations of the wrong numbers were  attempted. The caller had only stated they were in the Philippines but neglected  to say the phone number they provided was from China or that it was a mobile  number. </p>
        <p>The DAN Emergency staff  member consulted with a representative familiar with Chinese mobile numbers who  suggested adding ‘1’ after the country code.&nbsp;Fortunately, this enabled DAN  to connect with the diver but even then the connection was poor and the call  dropped twice before a better connection was made.&nbsp;The chamber staff was  contacted and authorisation given to treat the diver but with the correct phone  number provided this could have happened much, much faster. </p>
        <p><strong><em>The Lesson?</em></strong> Making  sure you are reachable on your cell phone is a simple but vital action divers  can take to help themselves prepare for an emergency situation, and eliminate  unnecessary delays. Fortunately, in the above case, the diver was already at  the hospital and already receiving important emergency care. The situation  becomes much more critical in an emergency situation, distant from medical care.</p>
        <h2><strong>Know Before You Go </strong></h2>
        <ul>
          <li>Check that you can make and receive  international calls from the country you are travelling to. </li>
          <li>Remember to bring your phone charger and, if  you have one, an extra battery.</li>
          <li>Have sufficient international credit for an  emergency call to be made to DAN.</li>
          <li>Know how to make an international call from  the country you are visiting.</li>
          <li>If your phone will not work (or to save more  expensive toll charges) buy a local SIM card. </li>
          <li>Know how you can be called from outside that  country: What is the International Calling Code for DAN to reach you? At the  very least, when speaking to a DAN hotline operator, be sure to identify the location  you are calling from (e.g. country and city).   </li>
          <li>If your phone doesn’t work find out who on  the liveaboard or at the dive resort can make and receive international calls.  Know this prior to your first dive. </li>
          <li>Have a back-up call-back number also ready in  case your number or the primary number at the resort or live-aboard does not  work.</li>
        </ul>
        <p><strong>Be  prepared:</strong> Knowing how DAN can reach you in the event of a diving accident could  save your life or your limbs.</p>
