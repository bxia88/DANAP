        <h1><em>Alert Diver</em> Asia-Pacific Media Kit</h1>
        <p><img src="_images/adcover42.jpg" width="110" height="145" /> <img src="_images/adcover45.jpg" width="110" height="145" /> <img src="_images/adcover40.jpg" width="110" height="145" /> <img src="_images/adcover41.jpg" width="110" height="145" /> </p>  
        <h2>Who is DAN? </h2>
        <p align="justify">The Divers Alert Network (DAN) is  a global network of not-for-profit, member-based organisations dedicated to  improving diving safety for all divers. </p>
        <p align="justify">          DAN serves as a lifeline for the  scuba diving industry. The DAN brand is recognised worldwide. Thousands of  divers have witnessed or personally experienced DAN in action and the  organisation has gained a much-deserved reputation as <em>the experts</em> <em>in dive accident  management</em>.</p>
        <p align="justify">          As part of its mission, DAN provides or funds 24-hour  diving emergency hotlines throughout the world; offers emergency evacuation  cover and dive injury insurance to members; provides diving medical information  services; offers quality training in accident management including first aid  and oxygen provision; collects data and reports on dive accidents; funds  research into dive medicine and dive safety; and provides support for  recompression chambers in remote locations.</p>
        <h2>What is <em>Alert Diver</em> Asia-Pacific?</h2>
        <p align="justify"><em>Alert Diver</em> Asia-Pacific is the magazine of the Divers Alert  Network Asia-Pacific. It is the only dedicated dive health and safety magazine  in the Asia-Pacific region. This exclusive publication is sent to the entire  DAN Asia-Pacific membership database.</p>
        <p align="justify"> Each issue of <em>Alert Diver</em> AP contains quality content  comprising medical, research and training articles, designed to equip Members  with the skills and knowledge that will make them safer divers. </p>
        <p align="justify">In essence, the magazine is made up of content  that our readers can use; it&rsquo;s for everyone from the beginner to the seasoned  diver, and it teaches valuable lessons and provides valuable insights to  empower the diver. </p>
        <h2>Why Partner with DAN?</h2>
        <p>The key goal for advertisers is:</p>
        <ul>
          <li>To place their ads in publications that will be read by  their target market; </li>
          <li>To gain maximum exposure with this audience; and</li>
          <li>To achieve this targeted exposure at an affordable cost  with a high return on investment.</li>
        </ul>
        <p align="justify">DAN&rsquo;s highly targeted market is  made up of active divers with a disposable income ready to be spent on new kit  or their next dive adventure. </p>
        <p align="justify">          Placing your advertisement in <em>Alert Diver</em> <em>Asia-Pacific</em> will ensure your product  is seen by the right regional audience.</p>
        <h2><em>Alert Diver </em>Asia-Pacific Circulation</h2>
        <p>Published three times per year,  and comprising two print issues and one electronic issue.</p>
        <p align="justify"><em>Alert Diver</em> AP has a high pass-on value due to the quality of the  publication. The electronic issue achieves an even greater reach due to the  simplicity of forwarding the e-magazine to other divers.</p>
        <p align="justify"><em>Alert Diver</em> is sent to the entire DAN AP membership, currently   approaching 10,000 unique divers. </p>
        <h2 align="justify">Demographics</h2>
        <p align="justify"><strong><em>ACTIVE:</em></strong> Wouldn&rsquo;t your  print advertising work better if you knew it was reaching divers who actually  go diving? DAN Members are often the most active of divers in the industry and  are likely to travel more. Typically, they join to receive the membership  perks, including the Emergency Medical Evacuation assistance, access to dive  accident insurance and other such programs.</p>
        <p align="justify"><strong><em>AFFLUENT:</em></strong> For  advertisers, an important characteristic of a potential audience is whether  that audience can afford your product. DAN Members undertake regular international  dive trips and spend money on their kit. A large percentage of DAN Members are  30+ years of age, economically stable, with a disposable income to be spent on  pursuing their passions.</p>
        <p align="justify"><em><strong>V</strong></em><strong><em>ISIBILITY:</em></strong> Unlike many other publications, <em>Alert Diver</em> AP restricts advertising to  a maximum of 20% in each issue, leaving the remaining 80% for editorial  content. This advertising restriction will ensure that your advertisement is  visible to readers. <em>Your ad will be  noticed!</em>        </p>
