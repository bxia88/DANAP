         <h1>           Costs Involved in Treating an Injured Diver         </h1>
		 
		 <p>The costs associated with treating a sick or injured diver vary greatly  based on several variables, such as:</p>
		 <ul>
		   <li>The location of the incident</li>
	       <li>Proximity to  the nearest appropriate chamber</li>
		   <li>Proximity to a suitable medical treatment  facility for the injury/illness</li>
		   <li>Whether an air evacuation is required.</li>
		 </ul>
	      <p>If an aircraft  needs to fly in from another location with a medical team on board then  naturally the associated costs will be significantly higher than if the diver  needs to be driven to a local chamber.</p>
	      <p> The cost of  treatment that is required in a chamber and/or hospital for the  accident/illness is additional. <strong>This can vary from $1,000 - $20,000 or more depending on  where this treatment is and how much treatment is required.</strong> </p>
	      <p>Suffice to say that  the costs involved in evacuating and/or treating a diver can be significant.<strong> The reality is that it can cost more than  US$150,000 for the evacuation alone!</strong>  This is why it is so important that your divers have DAN protection. </p>
	      <p>And the  fact that DAN are the experts in diver accident management means the diver will  receive the most effective response in co-ordinating their treatment/evacuation. </p>
	      <h2>Example </h2>
	      <p><strong>DAN was  involved in the evacuation of a paralysed diver from the Solomon Islands to  Australia. The evacuation cost was around AUD$90,000 (as an aircraft had to be  sourced from far away). In addition, the treatment costs were in excess of US$30,000,  plus on-going expenses. </strong></p>
	      <p>DAN  protection is about being prepared.  DAN offers members peace-of-mind, which comes from knowing they have the  experts in diver accident management to call in the event of a diving accident  or illness<strong>. </strong></p>
	      <p>If you have any questions about any aspect  of DAN Membership and Treatment Insurance please email  <a href="mailto:marketing@danap.org">marketing@danap.org</a>.</p>
