          <h1>DAN Asia-Pacific  Promoter Program</h1>
          <p>            The DAN  Asia-Pacific Promoter Program is our way of thanking dive businesses for their  support of our not-for-profit dive safety organisation.&nbsp; Our Promoters work alongside us to:</p>
          <ul>
            <li>Increase diver&rsquo;s awareness of the  importance of dive safety; and to</li>
            <li>Highlight the significance of being  prepared in the event of an emergency.</li>
          </ul>
          <p>The Promoter  Program is free to join and all dive businesses and Instructors throughout the  Asia-Pacific region are welcome to join the program and take advantage of the  rewards on offer.</p>
          <h2>Promoter Benefits          </h2>
          <p class="sred">1. Participation in the Lucrative Member Sign-Up Incentive Program</p>
          <p>The Incentive Program is our way of rewarding our  Promoters/Sponsors for signing up new DAN Asia-Pacific Members.&nbsp; So, by encouraging others to join DAN, you  are not only providing us with much needed revenue, you are also working  towards gaining extra rewards for yourself. </p>
          <p>Each  new DAN Asia-Pacific Member sign-up  equates to one credit point with a value of AUD$7.50.&nbsp; Points are accrued over the calendar year at  which time you are given the opportunity to exchange* these credits for:</p>
          <ul>
            <li>DAN Asia-Pacific Products;</li>
            <li>Advertising  in Alert Diver, the regions only dedicated dive health &amp; safety magazine;  or</li>
            <li>Cash</li>
          </ul>
          <p class="sred">The  Choice is Yours!</p>
          <p>And  with a whole year to accumulate points this is an excellent opportunity to  receive some great rewards simply for promoting dive safety and the importance  of DAN Membership with your divers.</p>
          <p class="fine">*Sponsors  and Promoters must accumulate a minimum of 5 points in one calendar in order to  redeem points for rewards.</p>
          <p align="left">Note: Points are only  rewarded for new Member sign-ups for divers who are residents/Nationals of the  Asia-Pacific region (excluding Japan).</p>
          <p class="sred">  2. Ability to Use the DAN Asia-Pacific Logo</p>
          <p>DAN Promoters and  Sponsors are given permission to use the DAN logo in their advertising and  promotional efforts, following specific guidelines provided by the DAN  Asia-Pacific Marketing Department. To obtain a copy of these guidelines please  send an email to <a href="mailto:marketing@danasiapacific.org">marketing@danasiapacific.org</a>. </p>
          <p class="sred">            3. Access to DAN Promotional Materials</p>
          <p>To  highlight your commitment to dive safety within your dive centre we will send  to you a Welcome Pack with a range of materials including posters, stickers, brochures  and flyers.&nbsp; Displaying these materials  throughout your centre will assist in your efforts and success in signing-up  new DAN Asia-Pacific Members.&nbsp; </p>
          <p class="sred">How Do I Become a DAN Asia-Pacific Promoter?</p>
          <p>Select the link below to complete the DAN Promoter sign-up form. Once we receive your  application we will establish your dive centre as a DAN Asia-Pacific Promoter  and provide you with a Promoter Number, promotional pack, and helpful tips to  get you started.</p>
