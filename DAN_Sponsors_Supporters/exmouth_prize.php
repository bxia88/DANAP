	        What  makes Ningaloo Reef so special is the consistent congregation of Whale sharks,  Big Fish, Manta, Turtles &amp; Dugong and Humpback Whales. </p>
	      <p align="justify">Exmouth  Dive is the only PADI 5 Star Instructor Development (IDC) Centre on  the&nbsp;Ningaloo Reef, offering a complete diver training service.</p>
	      <p align="justify">	        Don&rsquo;t just come for the Whale sharks as there are many  other seasonal visitors to the Ningaloo reef. Humpback whales are in the area  from June until November and Manta rays can be seen along the Ningaloo Reef  near Exmouth from May to November. As summer arrives, so do the Green,  Loggerhead and Hawksbill turtles and they begin their nesting season after  mating during October and November and can be seen on the beaches right up  until March.&nbsp; </p>
