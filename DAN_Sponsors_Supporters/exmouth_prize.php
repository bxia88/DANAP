          <h2><strong>About Whalesharks  </strong></h2>
          <p align="justify">This fish, the largest of the shark  family and the largest fish in the world, is a harmless plankton eater.  Reported to reach 18 metres (60ft) but averages 12 metres (40ft) in length, the  whale shark is easily identified by its huge size, wide mouth and distinctive  pattern of white spots. Thought to mainly inhabit worldwide temperate and  tropical seas, it is rarely seen in shallow coastal waters, but is a daily  visitor to the Ningaloo Reef every year between late March to July when up to  30 whale sharks have been spotted close to the reef feeding on the plankton in  the nutrient rich waters. </p>
