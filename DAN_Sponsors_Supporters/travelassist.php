          <h1>DAN 30-Day TravelAssist and Rapid-Processing of  DAN Membership</h1><img src="../_images/sponsor_flag.jpg" alt="Sponsors Flag" width="80" height="72" class="imgRight" />
	      <p>As you are no doubt aware, many dive tourists arrive on a  dive expedition without pre-arranging appropriate cover should an expensive evacuation and/or treatment  be required.</p>
	      <p>DAN Asia-Pacific has created the following programs to cater  for such circumstances:</p>
	      <h2>1. Rapid Processing of DAN Asia-Pacific  Membership.</h2>
	      <p>Rapid Processing Privileges will be provided for applications for annual DAN AP Membership submitted by Sponsors. </p>
	      <p>A diver who wishes to join the scheme must first complete  the normal DAN Asia-Pacific membership application, including the provision of  their credit card details for payment. This application is then faxed to us at  least 12 hours prior to commencing diving.&nbsp;  The usual $10 fast processing fee will be waived for these applicants.</p>
	      <p>The diver will be enrolled as soon as his/her credit card  payment is authorised, providing there is no medical issue that would delay  coverage of optional dive injury insurance, if applied for. Membership  materials will be sent to the address on the form (which must be in the  Asia-Pacific Region).</p>
	      <h2>2. DAN 30-Day  TravelAssist (Emergency Evacuation Coverage)</h2>
	      <p>          This scheme was created to provide a temporary cover for  divers who live outside this region and who are visiting this region (eg.  Americans, Europeans and Japanese to whom we cannot offer full DAN Asia-Pacific  membership).</p>
	      <p>It provides evacuation cover in the event of an accident or  illness, diving-related or not, for a period of 30 days from the stated  expedition date. In addition to providing some security during the dive trip,  it can be useful if the person gets seriously ill or injured during travels  subsequent to the dive expedition.</p>
	      <p>A diver who wishes to join the scheme must complete the  correct form clearly and in full (failure to do so can affect acceptance of  cover) and fax it, together with credit card payment at least 12 hours prior to  commencing diving. Alternatively, you can fax through a list of all the names  and addresses of divers to be covered, together with payment details. The  format of this list will need to be agreed to by DAN Asia-Pacific.</p>
	      <p>The diver will be covered as soon as his/her credit card  payment is authorised. No materials will be sent to the diver and the cover  will automatically expire 30 days after the expedition date.</p>
	      <p>You must provide the top section of the TravelAssist Form to  the diver for reference as it contains emergency contacts and information about  various DAN regional offices. On returning home, the diver may be eligible for  a discount if he/she decides to join the regional DAN Branch.</p>
	      <p class="sred">If you, as a dive operator, would like access to these privilieges you must be a DAN Sponsor.</p>
	      <p class="sred">If you have any questions please send an email to <a href="mailto:marketing@danasiapacific.org">marketing@danasiapacific.org</a></p>
