         <h1><strong>DAN Asia-Pacific <em>Optional</em> Dive Injury (Treatment) Insurance </strong> </h1>
		 
		 <p>As mentioned earlier, at the time of becoming a DAN AP Member, the  diver can choose to add on one of several levels of Dive Injury (Treatment)  Insurance. These plans will cover the Member for treatment and certain other costs associated with a covered <strong>diving</strong>  accident/illness. </p>
		 <p>Before you can assist your clients in determining the best level of coverage for their diving needs you need to determine whether they live within Australia or within the Asia-Pacific (excl Aust &amp; Japan).</p>
		 <h3>Residents of Australia</h3>
		 <p><a href="../Membership_Insurance/austinstable.php">CLICK HERE </a> to view the Dive Injury Insurance Plans available to residents of Australia.</p>
		 <h3>Residents of the Rest of the Asia-Pacific </h3>
		 <p><a href="../Membership_Insurance/osinstable.php">CLICK HERE</a> to view the Dive Injury Insurance Plans available to residents of the rest of the Asia-Pacific (excl Japan and Australia)</p>
		 <h2>What to Consider When Selecting a Dive Injury Insurance Plan </h2>
		 <p><strong>All plans provide 12-months coverage</strong> so your divers need to consider their coverage needs for the coming year when applying for coverage. </p>
		 <p><strong>Members cannot simply switch between plans throughout the coverage period.</strong> Doing so will involve additional costs so it is important that your divers take out the correct level of coverage at the outset. </p>
		 <p>Regardless of the Plan that a diver selects they <strong>must stick within the depth limits of their certification</strong>, otherwise they risk voiding their plan. </p>
		 <h2><strong>Can All Divers Join DAN Asia-Pacific  Members? </strong> </h2>
		 <p>No.</p>
		 <p>DAN Asia-Pacific can only sign-up divers  who are residents/Nationals of the Asia-Pacific region. Residents/Nationals of  other regions need to sign-up for DAN coverage via their local office: </p>
