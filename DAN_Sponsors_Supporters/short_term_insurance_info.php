         <h1>Short-Term Insurance Information</h1>
          <h2>Coverage Period </h2>
		  <p> Up to 30-Days </p>
		  
		  <h2> Coverage Provides </h2>
		  <p> Up to US$100,000 Emergency Evacuation Coverage for diving and non-diving accidents and illnesses.</p>
		  <p>Up to US$125,000 coverage for Decompression Illness Treatment Insurance {including both <em>Arterial Gas Embolism</em> (AGE) & <em>Decompression Sickness</em> (DCS)} and all covered in-water snorkelling & scuba diving injuries, sustained on a <em>Covered Dive or Repetitive Dive Series</em>.		    </p>
		  <p>Coverage includes hyperbaric & physician's fees and medical supplies used in the treatment of DCI.		    </p>
		  <p><em>NOTE: Dive injury insurance is underwritten by Accident &amp; General Insurance Company, Ltd (AGI). </em></p>
		  <p>To review the Short-Term Insurance Policy  Document <strong><a href="../_pdf/short_term_policy.pdf">CLICK HERE</a></strong></p>
		  <h2> Some Coverage Conditions </h2>
<p>This policy <strong>cannot</strong> be purchased by residents of the Asia-Pacific region. Residents of the Asia-Pacific need to apply for full DAN Asia-Pacific Coverage. Full DAN Asia-Pacific coverage offers 12-months protection and is very cost effective. For more information and to join <a href="../membership.php">CLICK HERE</a>. </p>
<p>Short-term insurance cannot be purchased for diving within Australia.</p>
<p>Applicants must be between 14 – 60 years of age.  </p>
<p>Applicants must have completed and submitted a <a href="../_pdf/short_term_reg_med_dec.pdf">Medical Declaration</a> and have been accepted for cover.  </p>
<p>Coverage is per person.  </p>
<p>Full details of the policy including all benefits, conditions and exclusions are available by <strong><a href="../_pdf/short_term_policy.pdf">CLICKING HERE</a></strong>. We highly recommend  your divers read this information carefully prior to signing up so they understand what the coverage entails. </p>
<h2> Cost </h2>
<p> AUD$45 per person for up to 30-days.	    
