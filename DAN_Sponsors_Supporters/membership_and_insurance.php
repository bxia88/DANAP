         <h1><strong>Why Should Divers Have DAN Membership &amp; Dive Injury (Treatment)  Insurance? </strong></h1>
		 
		 <p>The simple reason is so they are  prepared for the unexpected, namely dive accidents and illnesses. </p>
		 <p align="justify">It is essential that divers have a reputable organisation to call in the event of an emergency. An organisation that will be able to co-ordinate a rescue effort  and get them to the most appropriate treatment for their injury or illness. </p>
		 <p align="justify">Many insurance companies claim to cover diving but none have the level of experience that DAN has in helping injured/ill divers. With 30+ years worldwide experience, DAN are the <strong>experts</strong> in diver accident management. </p>
		 <p align="justify">On top of this is the issue of cost. Treating a diving accident/illness can also amount to tens of thousands of dollars. DAN Treatment Insurance Plans are designed to cover treatment (and certain other costs) associated with a covered diving accident, so that the diver is not lumbered with the burden of a potentially hefty invoice. </p>
		 <h2>DAN Asia-Pacific Membership </h2>
		 <p>DAN <strong>Membership </strong>provides divers  with 24-hour <em>TravelAssist</em> benefits, for which the primary benefit is <em>Worldwide  Emergency Evacuation </em>coverage for diving <u>and </u>non-diving  accidents/illnesses. This valuable service can evacuate the DAN Member from  most places in the world to the nearest appropriate medical facility.  Membership is annual and can be purchased as an Individual or Family  Membership. This coverage is particularly important when you consider that an  Emergency Evacuation, depending on a divers location, can cost as much as  US$150,000! </p>
		 <p>In addition, members receive a  Membership Card; an annual subscription to <em>Alert Diver</em>, the region&rsquo;s  only dedicated dive health and safety magazine; access to Member-Only programs;  discounts on DAN AP products; and more. </p>
		 <p><strong>IMPORTANT: </strong>DAN does NOT provide &quot;blanket&quot; coverage that will protect all your staff and clients; nor does DAN offer liability protection.  </p>
