      <h1>Invitation to Become  a DAN Dive Safety Supporter  </h1>
          <h2>Who are DAN Supporters? </h2>
          <p>DAN Supporters are dive  businesses that are advocates of dive safety and wish to promote DAN and dive  safety with their divers. </p>
          <h2>What are the Benefits of being a  DAN Dive Safety Supporter?</h2>
          <ul>
            <li>It&rsquo;s <em>free</em> for your  dive business to become a DAN AP Supporter.</li>
            <li>You will be demonstrating to  the diving public that diving safety is important to your business.</li>
            <li>You will receive DAN's range of promotional materials to display in-store.</li>
            <li>You will receive a credit point worth AUD$7.50 each new DAN AP Member sign-up that can be put towards DAN AP Membership/Insurance or DAN AP Products.</li>
            <li>Supporters have the opportunity to receive a business listing on the DAN AP website <em>(business name, contact person, phone number, address, email)</em>. Supporters receive further details upon registering for the program. <em>This  is a unique Supporter only benefit</em> <em>and an exceptional opportunity to  advertise your business.</em></li>
            <li>The top Supporter over each 6-month period receives promotion in the e-newsletter, <em>Deeper with DAN</em>, providing another great promotional opportunity. </li>
          </ul>
          <h2>What Do DAN Supporters Need to  Do?</h2>
          <p>Promote DAN  and dive safety by displaying DAN posters, flyers and brochures prominently  in-store. It is also important that you and your team educate your students and  customers about DAN and actively encourage them to become DAN Asia-Pacific  Members. </p>
          <h2>How to Become a DAN Asia-Pacific Supporter </h2>
          <p class="u">All you need to do is send an  email to <a href="mailto:marketing@danap.org">marketing@danap.org</a> with the following:</p>
          <ul>
            <li>Business Name</li>
            <li>Contact Person&rsquo;s Name</li>
            <li>Contact Phone Number</li>
            <li>Email Address</li>
            <li>Web Address</li>
            <li>Business Address (physical and postal if different). We need your postal address in order to post promotional materials to you.</li>
            <li>Promotional materials are available in English, Korean and Chinese. Please let us know in your email which languages you require. </li>
          </ul>
          <h2>What's Next?</h2>
          <p>A unique Supporter code will  be created for your business. You will receive a confirmation email and a DAN  AP Promotional Pack will follow in the post containing full program details and  outlining ways for you to gain maximum benefit from the program.</p>
          <p>We look forward to welcoming your business as a DAN Asia-Pacific Dive Safety Supporter and working  alongside your team for many years to come.</p>
          <p>IMPORTANT: We can only accept and allocate points for residents of the Asia-Pacific. Divers from other parts of the world will need to be referred to the relevant DAN branch. <a href="../Contact_DAN/internationaloffices.php">CLICK HERE</a> for contact details of the international DAN branches. </p>
          <h2>Permission to use the DAN Asia-Pacific Logo </h2>
