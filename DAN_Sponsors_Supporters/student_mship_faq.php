         <h1>Student Membership Frequently Asked Questions </h1>
		 
		 <h2>Q: How will I  know when DAN&rsquo;s Student Membership has been implemented for my students?</h2>
		 <p>You will be sent a confirmation email with the Name and corresponding  Student Membership Number for each Student listed on the Roster that you submit  to DAN AP. </p>
          <p>You need to add the Student Number and the Policy Expiry Date to each  Student's Confirmation Letter.</p>
          <h2><strong>Q: How long will it take to  implement the Student Membership from the time I submit a roster, student  registration and medical checklist for each student?</strong> </h2>
		  
		  <p>Applications  for Student Membership will be processed within 12-hours provided the  application is received between Monday and Friday and before 2pm on a Friday. <em>No  applications are processed on Saturday and Sunday as the DAN AP office is  closed. </em></p>
		  <p>Should a  problem occur with the processing of payment this will extend the processing time. Make  sure DAN has your current and accurate contact details to ensure lengthy delays  do not occur.</p>
		  <h2>Q: Can you keep  my credit card details on file so I don&rsquo;t have to submit these details with  each Roster? </h2>
		  <p>No.</p>
		  <h2><strong>Q: Do  students under 18 require parental consent? </strong></h2>
		  <p>Yes. A  parent or legal guardian must co-sign the Student Registration Form where the  student is a minor. A minor is defined as a person younger than 18 years of  age.</p>
		  <h2><strong>Q: Are Resort Courses,  Discover Scuba, and courses of a similar nature covered by Student Membership?</strong></h2>
		  <p>No.</p>
		  <h2>Q: What am I required to give my Students  covered under DAN AP Student Membership?</h2>
		  <p>1. <strong>Student Membership Letter</strong> that includes  your details as well as the Students policy number and policy expiry date. Importantly this  letter also directs the Student to the DAN AP website for more information  pertaining to the <a href="../_pdf/student_membership_policy.pdf">Student Membership Policy</a>.</p>
	      <p>2. <strong>DAN Asia-Pacific Membership Flyer</strong>. You must send an  email to <a href="mailto:marketing@danasiapacific.org">marketing@danasiapacific.org</a> when you run low for your stock to be replenished.</p>
	      <h2>Q: What do I do  if my student suffers symptoms of DCI?</h2>
	      <p>DAN <strong>must</strong>  be notified in the event of a diving accident or illness. If your student is  exhibiting symptoms that may indicate DCI call DAN for advice. DAN <strong>must</strong> approve any treatment before it is undertaken to ensure payment is received.  Refer to the <a href="../emergency.php">Emergency Procedures</a> section on the DAN Asia-Pacific website for the latest DAN  Emergency Hotline Numbers and advice regarding what to do in the event of an  accident/illness. As an instructor it is essential  you are aware of these procedures. <a id="back" href="javascript:history.go(-1)" onMouseOver="self.status=document.referrer;return true">BACK</a></p>
