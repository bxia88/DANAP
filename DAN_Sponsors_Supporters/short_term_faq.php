         <h1>Short-Term Insurance  Frequently Asked Questions </h1>
		 
		 <h2>Q: What coverage does the Short-Term Insurance Plan  provide?</h2>
		 <p>Up to US$125,000 coverage (Lifetime Maximum) for decompression       illness (DCI) and all covered in-water injuries sustained on a dive or a       Repetitive Dive Series to a depth not exceeding fifty (50) metres. </p>
		 <p>Up to US$10,000 in accidental death and       dismemberment benefits resulting from a covered Diving Accident; </p>
		 <p>Up to US$10,000 in permanent total disability       benefits from a covered Diving Accident.</p>
		 <p>Up to US$100,000 Emergency       Evacuation Coverage for diving and non-diving accidents and illnesses.</p>
		 <p><em>Note: Coverage under this plan is secondary  coverage.&nbsp; After any other insurance  you may have, the DAN Short&ndash;Term Insurance Plan pays 100% of all remaining Covered  Medical Charges up to the limits of coverage.</em></p>
		 <h2><strong>Q: What is  the duration of the coverage</strong>?</h2>
		 <p>Up to 30-days from  the day the application has been processed byDAN AP.</p>
		  <h2>Q: Can residents of the Asia-Pacific apply for Short-Term  Insurance?</h2>
		  <p>No. The  Short-Term Insurance Plan has been created for divers who cannot apply for full DAN  Asia-Pacific coverage. It is important that you encourage  divers, who are  residents of the Asia-Pacific, to apply for full DAN AP coverage at <a href="http://www.danasiapacific.org">www.danasiapacific.org</a>.</p>
		  <h2>Q: Who can apply for the Short-Term Insurance Plan?</h2>
		  <p>The applicant must be:</p>
		  <ul type="disc">
            <li>Aged       between 14 and 60;</li>
		    <li>A       resident of an eligible Home Country outside the Asia-Pacific Region;</li>
		    <li>Certified       as a recreational scuba diver by a recognised training agency, and<strong> not</strong> a       Commercial Diver.&nbsp; </li>
	      </ul>
		  <p>An applicant who fails to provide sufficient evidence of Residence in a country outside the Region  covered by DAN &ndash; AP is not eligible for Coverage under the Group Policy. This  request from the Underwriter may occur prior to enrollment, at the time of an accident or the  subsequent treatment, or later. </p>
		  <h2><strong>Q: What is an Eligible Home Country</strong>?</h2>
		  <p>An eligible home country is a country located outside the  Asia-Pacific Region. However, Japan is considered an eligible home country. </p>
		  <h2>Q: How will my diver's know when DAN&rsquo;s Short-Term Insurance has been implemented?</h2>
		  <p>Each diver applying for Short-Term Insurance will be sent a confirmation email to the email address provided on their Registration Form. As such, it is essential that applicants write clearly when completing their registration forms to avoid an unnecessary delays in the implementation of their coverage. </p>
	      <h2><strong>Q: How long  will it take to implement the Short-Term Insurance from the time I submit the  Registration Form and Medical Checklist for my diver/s?</strong></h2>
	      <p>Applications  for Short-Term Insurance will be processed within 12-hours provided the  application is received between Monday and Friday and before 2pm on a Friday. <em>No  applications are processed on Saturday and Sunday as the DAN AP office is  closed. </em></p>
	      <p>If problems exist with the processing of payment this  will extend the processing time. </p>
	      <h2><strong>Q: Is parental consent required for applicants aged 18 or younger?</strong></h2>
	      <p>Yes. A  parent or legal guardian must co-sign the Short-Term Insurance Registration Form where  the applicant is a minor. A minor is defined as a person younger than 18 years  of age.</p>
	      <h2>Q: What do I do  if my Diver suffers symptoms of DCI?</h2>
	      <p>DAN <strong><u>must</u></strong> be notified in the event of a diving accident or illness. If your diver is  exhibiting symptoms that may indicate DCI call DAN for advice. DAN <strong><u>must</u></strong> approve any treatment before it is undertaken to ensure payment is received.  Refer to the Emergency Procedures section at <a href="http://www.danasiapacific.org/">www.danasiapacific.org</a> for the latest DAN  Emergency Hotline Numbers and advice regarding what to do in the event of an  accident/illness. As an instructor it is essential that you are aware of what  to do. </p>
