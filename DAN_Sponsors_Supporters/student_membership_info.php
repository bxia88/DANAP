      <h1>Student Membership Information</h1>
          <h2><strong>Who Can Register?</strong></h2>
          <p>- Open Water  Students Only (while undertaking training dives under the direct supervision of  an instructor currently registered with DAN AP for this Program)<br />
            <br />
            - Excludes Advanced  Open Water Course as well as Resort Courses, Discover Scuba and other similar &quot;Try Dive&quot; courses.<br />
<br />
          - Age: 14 - 60 years.     </p>
		
          <h2> Coverage Provided </h2>
<p> Up to US$20,000 coverage for Decompression Illness Treatment, which includes both <em>Arterial Gas Embolism</em> (AGE) & <em>Decompression Sickness</em> (DCS) sustained on a<em> Covered Dive or Repetitive Dive Series</em>.</p>
<p>Coverage includes hyperbaric & physician's fees and medical supplies used in the treatment of DCI.</p>
<p><em>NOTE: Dive Injury Insurance is underwritten by Accident &amp; General Insurance Company, Ltd (AGI). </em></p>
<p><strong> IMPORTANT</strong>: Coverage EXCLUDES any other diving or non-diving related accident. This policy provides coverage for Decompression Illness only.</p>
<h2> Coverage Conditions </h2>
<ul>
  <li> Coverage commences once the DAN AP Team has processed the application, payment has been received and a Student Membership Number has been allocated.<br />
    <br />
  </li>
  <li>Course must be completed within 6 weeks (42-days) from date of commencement. Alternatively, the coverage ends when the student completes the  last dive required for certification, if completed in less than the allowed 42-days. <br />
    <br />
  </li>
  <li>Open Water course must take place in the Asia-Pacific EXCLUDING AUSTRALIA & JAPAN.</li>
    
  <br />
   
   <li> Maximum diving depth is 18 metres. </li>
   <br/>
    
    <li> Policy does not include Evacuation Coverage. Divers wishing to have Evacuation Coverage will need to become full DAN Members.</li>
	</ul>
<h2> Cost? </h2>
<p> AUD$15 per student. </p>

<h2> Other Requirements </h2>
<p>Each student is required to complete a <strong><a href="../_pdf/Student_Reg_Med_Dec.pdf">Medical Checklist</a></strong> which must be returned to DAN Asia-Pacific via <strong><a href="mailto:info@danasiapacific.org">email</a></strong>) or fax (+61-3-9886 9155) along with the <strong>Student Registration Form</strong> and <strong>Roster</strong> <strong>of Applicants</strong>.</p>
<p>A minimum of three students must be registered at one time (max of 8).  </p>
<p><em>Note: Applicants are not covered by DAN Student Membership until you have received a confirmation email advising the coverage is in place and each student has been allocated a DAN AP Student Membership Number.</em></p>
<h2>Frequently Asked Questions</h2>
<p><a href="student_mship_faq.php">CLICK HERE</a> for answers to some common questions concerning the Student Membership Program. </p>
<h2>Policy Document</h2>
<p>To review the full Student Membership Plan policy document <a href="../_pdf/student_membership_policy.pdf">CLICK HERE</a>. </p>
<h2> Why Students Need Cover? </h2>
<p> The fact is that injuries do occur during instruction. Here’s an example:

  <br />
  <br />
  <em>An instructor took his students to 9 metres for 25 minutes to practice basic skills. Following the dive the student experienced symptoms of decompression illness. It was determined later that the student had a heart condition that made him susceptible to decompression illness. The student was treated for decompression illness and made a full recovery.</em></p>

<h2> Benefiting Your Students </h2>
<p> By using the Student Membership Program you can take an important first step in encouraging dive safety among your new divers. The Student Membership ensures the student has valuable protection during their entry-level training so they can relax and enjoy their diving.  </p>
<p>DAN’s aim is to keep every dive, accident and injury free, so by introducing new divers into the DAN community through Student Membership, we are better able to educate and inform them how they may achieve that goal.  </p>
<p>At the end of the Open-Water Course encourage your students to continue their safe diving practices by becoming a full DAN Member. 
