        <h1 id="storyTitle">Sinus Squeeze - Nose Bleeds </h1>
        <h3> I have noticed that divers new to scuba diving frequently have nosebleeds. Could you please tell me what causes them?</h3>
        <p>New divers frequently report nosebleeds after diving primarily because   they are unaware of the importance of equalizing the sinuses and middle   ears (via the Valsalva method). The barotrauma that is produced when the   sinuses are not cleared can cause blood vessels in the lining of the   nose to burst. These vessels lie very close to the top of the mucous   membrane lining in the nose and sinuses, and the blood can come from the   lining in either. This type of barotrauma, generally the result of air   being trapped within the sinuses, is not always painful, though the   presence of blood can be disconcerting to a new diver. With this type of   injury, blood can run down the back of the throat or pool in the sinus   below the eye and emerge at a different time. It can also act as a   growth medium for bacteria and result in sinus infections. <br />
          <br />
          Individuals with a history of sinus trouble, allergies, a broken   nose, or deviated septum, as well as divers who currently have colds   may find the clearing procedure difficult to accomplish and may   experience a problem with nosebleeds. It's always best not to dive with a   cold or any condition that may block the sinus air passages. <br />
          <br />
        We suggest a slow, gentle descent with frequent equalizing to   help decrease the risk of sinus barotrauma. Divers who are unable to   clear their sinuses or have frequent nosebleeds when scuba diving should   see their personal physicians or ear-nose-and-throat specialists for   evaluation.</p>
