        <h2>World Map of 2014 Compressed Gas Diving Fatalities</h2>
        <p align="justify">The figures indicated  on the map are the minimum number of fatalities in the countries shown. These  are taken from reports provided to IDAN organisations (Asia-Pacific, America,  Europe, Southern Africa, Japan) and also include some taken from various media  sources.</p>
        <p align="justify">The number for  certain countries will be relatively accurate (eg. U.S.A., Australia, New  Zealand, U.K., Japan.) However, the numbers for many other countries,  especially less developed countries, are very likely understated.</p>
