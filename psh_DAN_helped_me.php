      <h1>DAN Helped Me: A Reminder to Listen to Your Body.</h1>

        <p><em>By DAN Asia-Pacific Member, Paik Seung Hee (continued from Issue 25 Deeper with DAN)</em></p>

        <p><strong>        Where:</strong> Malapascua, Philippines.</p>

        <p><strong>The Diver:</strong> Very experienced having completed 700+ dives.</p>

        <p><strong>Dives Undertaken </strong><br />

            <em><u>Day 1</u></em><br />

          Dive 1: 25m  for 55mins<br />

          Dive 2: SI  02:57, 24m for 45mins<br />

          Dive 3: SI:  03:47, 20m for 54 mins <br />

          Dive 4: SI:  02:51, 8m for 68mins</p>

        <p><em><u>Day 2</u></em><br />

          Dive 1: 24m  for 56mins<br />

          Dive 2: SI:  03:35, 21m for 60mins<br />

          Dive 3: SI:  01:33, 22m for 59 mins</p>

        <p><em><u>Day 3</u></em><br />

        Dive 1: 28m  for 59mins</p>

        

        <h2 align="justify">Another Beautiful Day         </h2>

        <p>It was a  normal day, like the previous two days of our holiday. Sunrise was beautiful  and the weather was perfect. We woke up at 4:30 AM to go on our first morning  dive.&nbsp;Malapascua&nbsp;is a very special place, where thresher sharks are  one of the main attractions, but only in the very early morning.&nbsp;I&nbsp;had&nbsp;already  been on two early morning dives the previous two days and they were just so beautiful  and breathtaking.&nbsp;I&nbsp;was a bit tired from the trip to Malapascua,  but&nbsp;I&nbsp;couldn&rsquo;t miss the chance to see thresher sharks again.</p>

        <p>On entering  the water I felt quite good. When I am&nbsp;tired I sometimes&nbsp;have sinus  pressure problems, which gives me discomfort when I descend. So as usual, I  descended slower than the other divers. On this morning, I didn&rsquo;t experience any  sinus problem.</p>

        <p>It was a  quiet, nice and calm dive and my husband was my dive buddy. We ascended slower  than our group and way slower than our Suunto D6 dive computers suggested,  doing a 3 minutes safety stop at 5 meters as usual. The unusual part of the  dive is that this time I was feeling sinus pressure when I was ascending, so we  managed to ascend even slower than usual to deal with this, however I still felt  discomfort.</p>

        <h2 align="justify">Onset of Symptoms</h2>

        <p>On the way  back to shore&nbsp;I&nbsp;started to feel a minor&nbsp;tingling&nbsp;in the  joints of my knees.&nbsp;It was definitely not a good sign.&nbsp;It was very  minor but it was there.&nbsp;I&nbsp;never imagined&nbsp;I would have symptoms  of DCI and had&nbsp;never had any&nbsp;during my previous 700+ dives.&nbsp;But  there I was&nbsp;feeling a&nbsp;tingling sensation in my  joints.&nbsp;I&nbsp;asked for 100% oxygen, which every boat of Exotic Island  Dive Resort carries on board, and I&nbsp;immediately&nbsp;started to breathe it  and continued to do so until we arrived at shore (approx. 25mins). The dive  shop manager checked my symptoms and immediately  called&nbsp;DAN.&nbsp;I&nbsp;also spoke to DAN&rsquo;s John Lippmann and he suggested  that I continue breathing 100% O2 for few more hours.&nbsp;I&nbsp;took a rest  while breathing O2,&nbsp;drank&nbsp;lots of water and I also  took&nbsp;Ibuprofen,&nbsp;following DAN&rsquo;s suggestion. </p>

        <p align="center"><img src="_images/psh_photo.jpg" width="423" height="317" /></p>

        <p>Despite all  this my condition&nbsp;was not getting better.&nbsp;I&nbsp;started to feel very  weird, like if I was walking on the&nbsp;clouds.&nbsp;It was like the feeling  of not really waking&nbsp;up properly in the morning, like I was dreaming.&nbsp;I&nbsp;was  awake and talking to people, but&nbsp;I&nbsp;didn&rsquo;t feel like it  was&nbsp;real.&nbsp;Meanwhile Exotic's managers and staff&nbsp;were so helpful  and nice to me.&nbsp;They gave me something to eat before I took the Ibuprofen  pills so my stomach was not empty and they checked on me continuously.</p>

        <p>However, my  condition did not improve despite having breathed O2 for several hours. So DAN  and the managers of Exotic decided it was&nbsp;best that I head to the  Hyperbaric Chamber in Cebu for treatment.&nbsp;</p>

        <p>It is a long  way from Malapascua to Cebu, but the managers at Exotic managed&nbsp;all the  transportation to the chamber: boat from Malapascua to Maya pier and then a car  to Lapulapu military base in Cebu.&nbsp;</p>

        <p>Exotic sent  one of their diving instructors along with my husband and I for the journey to  help with the translation and the&nbsp;other arrangements. </p>

        <h2>Getting to the Chamber</h2>

        <p>First we  took a boat to Maya pier (30-minutes), in the northern side of Cebu&nbsp;Island  then we took a car to the chamber.&nbsp;I was provided 100% O2 all the way to the  chamber, 25 minutes on and 5 minutes off, following DAN's  recommendation.&nbsp;Unfortunately&nbsp;there was some sort of festival between  Maya pier and Cebu, which made the car journey much longer than expected, but  luckily the Exotic Team ensured we had more than enough O2 for the trip. The  road portion of the trip took approx. 3.5 hours. </p>

        <p>When we  arrived at the chamber it was already 10:00pm, but the doctor, nurses and  chamber operators were waiting for us. It was a military chamber. After being  examined by the doctor and nurses, the operators of the chamber explained what  type of treatment I would be given. The chamber looked exactly like the ones I  saw in my diving books and manuals, like a big capsule or a small submarine.</p>

        <p align="center"><img src="_images/psh_chamber_pic.jpg" width="423" height="317" /></p>

        <h2 align="justify">Inside the Chamber</h2>

        <p>The military  base contacted a voluntary military nurse to go with me in to the chamber, who  explained to me the procedures of how to wear the oxygen mask while in the  chamber.</p>

        <p>          We were in  the chamber for four hours and 15 minutes, leaving the chamber at around 4:00am,  at which time the tingling sensation had completely stopped.</p>

        <p align="center"><img src="_images/psh_3.jpg" width="270" height="200" /></p>

        <p>The  experience was not an easy one, being locked in a small and confined  environment and lying down on a narrow bench for several hours, without being  allowed to sleep, in order to ensure that the oxygen was been properly  administered. After treatment the doctor authorized us to go to the hotel,  which Exotic had arranged and which would later be paid by DAN. The next day we  went back to the military base to consult the doctor for a second time and he  confirmed that no further treatment would be necessary. I am happy to advise  that I fully recovered from my DCI experience.</p>

        <h2 align="justify">My Advice</h2>

        <p>Here&nbsp;I&nbsp;am,  sharing my experience and hoping that it can&nbsp;help someone.&nbsp;I  had&nbsp;never thought&nbsp;I would&nbsp;have DCI and I had never  thought&nbsp;I&nbsp;would need to be treated in a chamber.&nbsp;It had always  sounded like a&nbsp;far-away&nbsp;story&nbsp;that existed only in books.&nbsp;My  diving profiles were always conservative, diving within my limits, always with  the assistance of a dive computer and using EAN Nitrox for most of my  dives.&nbsp;Now&nbsp;I&nbsp;realize&nbsp;that nobody can&nbsp;be sure as to how  their body will react.&nbsp;If&nbsp;I&nbsp;had not been on that dive,  maybe&nbsp;I would&nbsp;have not had DCI.&nbsp;I&nbsp;was quite tired  and&nbsp;I&nbsp;thought I felt that way because it was a very&nbsp;early&nbsp;morning&nbsp;dive.  But&nbsp;I&nbsp;wanted to see the thresher sharks again.&nbsp;</p>

        <p>Listen to  your body signals carefully.&nbsp;You can be a better diver if you have a  conservative&nbsp;dive profile, use Nitrox and take a&nbsp;good care of your  body.&nbsp;But sometimes it will not be enough.&nbsp;If your body sends a  signal&nbsp;&ldquo;I&rsquo;m tired&rdquo;, don&rsquo;t ignore it, no matter what.&nbsp;In the end you  are there to enjoy your holidays and to not end up in a chamber. My husband and  I were meant to undertake further diving on this trip, but because I ended up  with DCI this could not happen.</p>

        <p>I want to thank&nbsp;everyone&nbsp;that  helped me: Malapascua Exotic Island Dive Resort diving manager&rsquo;s&nbsp;Joa  L&ouml;fstr&ouml;m&nbsp;and Rebecca&nbsp;Johansson, instructor Tong, doctor, nurses,  volunteers and chamber operators at Lapulapu military base in Cebu and John  Lippmann from DAN Asia-Pacific.&nbsp;It is because of them that I am here&nbsp;well  and healthy.&nbsp;Without their help,&nbsp;I&nbsp;doubt if&nbsp;I would be here  now, in perfect condition, writing my story. DAN was of great help from the  moment they received that first call, their professionalism and care was so  efficient and touching.</p>

        <h2><strong>Comment  from Seung Hee&rsquo;s husband, Roberto:</strong></h2>

        <p>&nbsp;</p>

        <p align="center"><img src="_images/psh_roberto.jpg" width="250" height="180" /></p>

        <p><em>As Seung  Hee mentioned, her husband Roberto was her dive buddy and was with her  throughout the entire experience. Following are his comments: </em></p>

        <p>We were very relieved that we had taken out DAN coverage. Scuba diving  has always been our passion and it is because of scuba diving that Seung Hee  and I met in the first place.</p>

        <p>Having diving insurance is like having car insurance. You hope that you  will never use it, but it is a relief to know you have it, when you actually  need it.</p>

        <p>The assistance we received from DAN was not only a financial one, but  most important we received clear instructions about what steps to take with  regards to the administration of oxygen and medication from when the DCI  symptoms appeared. DAN and Exotic Island Dive Resort gave us all the support we  could expect from the professional companies they are. The transportation by  boat and by car to bring us to the chamber at the military base of Lapu Lapu in  Cebu worked just fine. Upon arrival late in the evening at the chamber there  was a group of doctors, nurses and the chamber professionals waiting for us in  Lapu Lapu. The chamber is managed by the military and it is run mostly by  volunteers, who dedicate their free time to helping others in need. I was very  impressed by the quality of the service and care provided to us by the  operators of the chamber.</p>

        <p>My message to our fellow divers is: dive within your limits and  training. Make use of a good dive computer to guide you through the dive. When  enriched air Nitrox is available, use it! Because even though you take all the  necessary precautions, sometimes unexpected bad things happen and you should be  prepared for them. Dive with well managed and responsible diving operators who  have radio communication and 100% O2 available on all their dive boats, no  matter how small the boat is or how close you think you are from the dive  center. Last but not least, invest in your safety and have a good diving  insurance, like DAN. As I said, one hope&rsquo;s that he/she will never need to use  it, but you will realize how important it is if and when you need it.</p>

        <h1><strong>Issues from Seung Hee&rsquo;s Experience</strong></h1>

        <h3><em>Emergency  Evacuations are not always undertaken by air. </em></h3>

        <p>          Several factors are taken into  account when determining the best mode of transport for an evacuation. These  include the severity of the diver&rsquo;s condition, the location of the incident,  local treatment facilities, weather and many more. Members rely on DAN&rsquo;s  expertise to determine the best course of action. In this case, it was determined  that the chamber in Cebu, accessed by boat and car, was the most appropriate  action as there were no other suitable options available at that time that  would be faster. </p>

        <h3><em>Availability of Oxygen<br />

          </em></h3>

        <p>Seung Hee was fortunate the dive operator was  well equipped with oxygen on-board the dive boat and at the dive centre, and in  sufficient quantities, to get her through the extended period of time before she  received her chamber treatment.</p>

        <p>          If suitable oxygen equipment (including a demand valve and  mask as well as a non-rebreather mask) was not available, or the supply was not  sufficient for the extended period, Seung Hee&rsquo;s symptoms could have been  significantly worse and required further treatment.</p>

        <p>          This story should again reinforce to you the importance of  asking dive operators questions so you can be sure you will be looked after in  the event of a diving illness.</p>

        <h3><em>Dive Computers</em></h3>

        <p>There is a  misconception that if you stick within the limits of your dive computer you  cannot be get. &nbsp;Although dive computers are helpful in guiding  decompression, they are not always accurate. These days, most divers who are  treated for DCI have been diving within the limits of their computers  and still got sick. Dive computers need to be used conservatively. The deeper and/or longer the dive and the more dives in a series, the more  the computers algorithms are stretched. </p>

        <h1><strong>Final Comment by John Lippmann (DAN AP Founder,  Chairman, Director of Research)</strong></h1>

        <p class="style1">          We were  happy to help Sueng Hee in her time of need. That&rsquo;s what we&rsquo;re here for. </p>

        <p><em>When Dr  David Wilkinson, the doctor on the hotline, received the call, he diagnosed  that the diver&rsquo;s headache was likely due to sinus barotrauma given her  description of how it occurred and her past history. He suspected that her  tingling was likely DCI. Ibuprofen and other similar non-steroidal  anti-inflammatory medications can be helpful in the management of both of  these. She initially improved with oxygen breathing and this is not uncommon as  the oxygen breathing helps to eliminate the excess nitrogen in the bubbles and  body tissues. However, the fact that further symptoms then arose was concerning  and we soon advised that she go to the chamber. Fortunately she responded very  well to treatment.</em></p>

        <p><em>The diver  herself has given good advice in that we shouldn&rsquo;t dive if we are feeling  overly tired or unwell. She might have already been suffering very mild DCI  from the earlier diving and exacerbated it on this dive. It is a good idea to  routinely set your dive computer to a more conservative mode that its factory setting  (if the option is available), and dive within those limits. </em></p>

        <p><em>I have just  heard that a brand new multi-place chamber has recently arrived in Cebu and,  hopefully, will be operational in about a month. This is great news for divers  there. Dick Clarke from Hyperbaric Partners Ltd, supported by DAN, recently  conducted a large hyperbaric training program for medical staff in the  Philippines in support of the establishment of several new chambers there. We  are grateful to Dr Jose Bernardo and the PCSSD for arranging this opportunity.</em></p>

        

