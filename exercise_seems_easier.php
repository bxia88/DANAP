 	  <h1>Why Might Diving Make Exercise Seem Easier?</h1>
 	  <p><em><strong>Member Question: </strong>My son and I have been mapping the bottom of a quarry. We did   several dives last weekend; each was about 30 feet deep and 45 minutes   long. The water was cold, so we decided to run (about a mile) around the   quarry between dives to get warm. We started running about 10 minutes   after coming out of the water. To our surprise, we ran much better and   were able breathe more easily than usual. I understand it may be   inadvisable to exercise too soon after diving, but I wondered if you   could explain why we found breathing while running so much easier after   diving. It was a complete surprise to both of us — we thought we'd be   sluggish, but just the opposite was true.</em></p>
 	  <p><em><br />
          Answer provided by Neal W. Pollock, Ph.D. </em></p>
 	  <p>          The factors captured by your question are actually quite complicated   and bridge a number of issues. First, the recommendation against   exercise after diving is based on decompression safety. Vigorous   exercise and/or high joint forces can promote bubble formation and   increase the risk of decompression injury. The simplest way to visualize   this effect is to compare the result of opening a bottle of soda that   has been at rest and one that was shaken before opening. While avoiding   unnecessary exercise after diving is a sound general recommendation, the   relatively benign nature of the dive profiles you described make this   less critical. (Please note that &quot;less critical&quot; does not mean   unimportant, just that the exposure you described is not as high on the   risk scale as many dives might be.)<br />
 	    <br />
 	    The next consideration is the   impact of immersion, breathing equipment and person. The combined   effects of a blood shift to the chest (primarily caused by the   hydrostatic pressure of water, potentially magnified somewhat by   additional cold stress), elevated breathing resistance from the   mouthpiece — which can vary substantially depending on body position,   depth (due to changing gas density), level of exercise and the   characteristics of the regulator itself — state of hydration (being   overhydrated can increase the likelihood of respiratory problems) and   miscellaneous factors such as age, gender, constrictive equipment and   physical fitness can lead to a degree of respiratory compromise after   diving. If enough of these small factors work together, a swimmer or   diver may develop a condition known as immersion pulmonary edema, in   which excess fluid enters the lungs and compromises breathing during or   following a dive. The solution is to try to reduce the impact of each   element, a goal divers may achieve consciously or unconsciously. Some   protection is provided by maintaining a normal pattern of good but not   excessive hydration and by maintaining an optimal body position and low   exercise intensity during dives to reduce breathing effort.<br />
 	    <br />
 	    Immersion,   especially in cold water, can induce some respiratory compromise. Mild   impairment may not be noticed postdive because of the more obvious   benefits of removing the regulator, dropping the heavy gear and having   to work only against air. I suspect that your sense of ease was more   perceptual than actual.<br />
 	    <br />
 	    Ultimately, while you may feel good   postdive, the dive has still stressed your system. For this reason I   would discourage strenuous exercise. The postdive period is the right   time to take it easy. Establishing a habit of keeping physical effort   low after diving may offer important protection since  some dives are   less benign than others. Think of this as inexpensive insurance for   long-term health and safety.</p>
