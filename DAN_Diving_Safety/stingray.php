      <h1>Stingray Injury </h1>
		      <p align="justify"><em><strong>Diver Question </strong></em></p>
	               
              <p><em>My  daughter was walking in the surf near Panama City when she got stung. She was  barefooted and said something wrapped up around her foot right before she felt  the pain. One of the lifeguards pulled out a small spine, and then she saw a  doctor. He put her on an antibiotic, but the cut on her foot doesn't seem to be  healing. What should she do?</em></p>
              <h6>              DAN Answer:  </h6>
              <p>A stingray  does its damage by lashing upward in defense with a muscular tail-like  appendage, which carries up to four sharp, swordlike stingers. The stingers are  supplied with venom, so that the injury created is both a deep puncture or  laceration and an envenomation.</p>
              <p>The pain from a stingray wound can be excruciating and accompanied by bleeding,  weakness, vomiting, headache, fainting, shortness of breath, paralysis,  collapse and occasionally, death. Most wounds involve the feet and legs, as  unwary waders and swimmers tread upon the creatures hidden in the sand. <br />
                <br />
                <strong>The Treatment</strong></p>
              <ol start="1" type="1">
                <li>Rinse       the wound with whatever clean water is available. Immediately immerse the       wound in non-scalding hot water to tolerance (110 to 113 F/43.3 to 45 C).       This may provide some pain relief. Generally, it is necessary to soak the       wound for 30 to 90 minutes. Gently extract any obvious piece of stinger. </li>
                <li>Scrub       the wound with soap and water. Do not try to sew or tape it closed - doing       so could promote a serious infection by &quot;sealing in&quot; harmful       bacteria. </li>
                <li>Apply a       dressing and seek medical help. If more than 12 hours will pass before a       doctor can be reached, start the injured person on an antibiotic       (ciprofloxacin, trimethoprim-sulfamethoxazole or doxycycline) to oppose       Vibrio bacteria. </li>
                <li>Administer       pain medication sufficient to control the pain.</li>
              </ol>
      <p><strong>Prevention of Stingray Injuries</strong></p>
              <ol start="1" type="1">
                <li>Always       shuffle your feet when wading in stingray waters. </li>
                <li>Always       inspect the bottom before resting a limb in the sand. </li>
                <li>Never       handle a stingray unless you know what you are doing or unless the       stingrays are definitely familiar with divers and swimmers (e.g., the rays       in &quot;Stingray City&quot; off Grand Cayman Island in the British West       Indies). Even then, respect them for the wild creatures they are - the       less you handle them the better for them and for you, too. </li>
              </ol>
              <p><em>Answer provided by Paul S. Auerbach, M.D., M.S in an article for DAN's Alert Diver magazine..</em></p>
