      <h1>Deep Vein Thrombosis </h1>
      <p>Deep vein  thrombosis (DVT) is a condition in which a blood clot (a &quot;thrombus&quot;)  forms in one or more of the body's deep veins, usually in the legs. If a clot  breaks free and travels through the circulatory system, it can lead to  life-threatening conditions. For example, if a clot lodges in the lungs, it is  known as a pulmonary embolism (PE) and affects the lungs' ability to oxygenate  the blood. Collectively, DVT and PE are  sometimes referred to as venous thromboembolisms (VTEs).<br />
        <br />
        A clot that originates as a DVT can also cause a stroke in individuals with a  <a href="../pfo.php">patent foramen ovale</a> (PFO); in such a  case, the clot travels through the veins to the right atrium of the heart,  passes through the PFO to the left atrium and then travels through the arteries  to the brain.<br />
        <br />
DVT is not related to diving, but divers often travel, and travel is a  significant risk factor for DVT. In about half of all cases of DVT, the  individual experiences no noticeable symptoms before the onset of the  condition. Most often, it starts in the calf. Symptoms may include the  following:</p>
      <ul type="disc">
        <li>Swelling in the affected       leg, ankle or foot</li>
        <li>Pain in the calf that       spreads to the ankle or foot</li>
        <li>Warmth in the affected area</li>
        <li>A change in the color of the       skin — to pale, red or blue</li>
      </ul>
      <p>Most  VTEs related to air travel occur within two weeks of the flight and are  resolved within eight weeks. If untreated, a DVT that starts in the calf will  spread up into the thigh and pelvis in about 25 percent of cases. An untreated  DVT of the thigh and pelvis has about a 50 percent risk of leading to a PE,  which is the most serious complication of DVT. Many cases of DVT are  asymptomatic and resolve spontaneously. However, DVT often recurs in an  individual who has had one episode of the condition.</p>
      <p align="center"><img src="../_images/DVT.jpg" width="600" height="471" /></p>
      <p align="justify"><a href="https://www.onlineexambuilder.com/cardiovascular-health/exam-38346"></a></p>
      <p>Most DVTs  occur in individuals with pre-existing risk factors for DVT who remain  motionless for a long time — such as when traveling a long distance by plane,  car or train; when doing deskwork over a period of many hours; or when  bedridden. This is because immobility slows down the blood flow in the veins (a  condition known as &quot;venous stasis&quot;); in addition, pressure on the  calf from an inadequate seat can injure the vein walls. If you sit still for 90  minutes, the blood flow in your calf drops by half, and that doubles your  chance of developing a blood clot. For every additional hour you spend sitting,  your risk of a blood clot increases by 10 percent.<br />
          <br />
        The incidence of DVT in the general population is one-tenth of one percent, but  it is higher in those who have risk factors and those who travel often.  Long-distance air travel may double or even quadruple the risk of suffering a  VTE. Although DVT is often called the &quot;economy class disease,&quot;  business-class travelers are susceptible, too. The risk of a DVT occurring on a  flight lasting more than four hours is between 1 in 4,650 flights and 1 in  6,000 flights; this is lower than the risk in the general population, but  that's because people who take long trips are likely to be healthier than  average. The incidence of DVT among travelers with a low to intermediate  pre-existing risk for VTE who take a journey longer than eight hours was found  to be 0.3 percent for symptomatic cases and 0.5 percent when including  asymptomatic cases as well.<br />
  <br />
        Risk factors for DVT include the following:</p>
      <ul type="disc">
        <li>Older age (the risk rises       after age 40)</li>
        <li>Obesity (defined as a body       mass index greater than 30)</li>
        <li>Estrogen use (either       hormonal contraceptives or hormone replacement therapy)</li>
        <li>Pregnancy (including the       postpartum period)</li>
        <li>Thrombophilia (an abnormally       increased tendency of the blood to clot)</li>
        <li>Previous VTE or a family       history of VTE</li>
        <li>Active cancer</li>
        <li>Serious medical illness</li>
        <li>Recent surgery,       hospitalisation or trauma</li>
        <li>Limited mobility</li>
        <li>Central venous       catheterisation (the presence of a catheter in one's chest, for use in       administering medication or nutrients and/or drawing blood samples)</li>
      </ul>
      <p>Between  75 percent and 99 percent of those who develop a travel-related VTE had more  than one of these risk factors.<br />
          <br />
        Height is also a factor in one's risk of developing a travel-related DVT.  People who are either very short — less than 5 feet, 3 inches (1.6 meters) — or  very tall — more than 6 feet, 3 inches (1.9 meters) — appear to be at increased  risk as a result of their inability to adjust their seats sufficiently to accommodate  their height. In addition to effects of immobility, shorter passengers may  suffer greater than usual seat-edge pressure on the backs of their knees, and  taller passengers may be cramped due to insufficient leg room. All of these  factors can contribute to injury of deep veins, venous stasis and activation of  the blood's clotting mechanisms.<br />
  <br />
        Those who are at increased risk of DVT should wear compression socks whenever  they fly or drive long distances and should consult their primary-care provider  regarding the possible benefit of taking a clot-preventative such as aspirin.  Although the risk of DVT for healthy people is small, everybody should be aware  of the factors that can precipitate the condition — and avoid long periods of  immobility. The best way to prevent DVT is to get up and walk around from time  to time. It also helps to flex your feet and calf muscles regularly if you must  remain seated for any length of time. Finally, it is also helpful in preventing  DVT to stay well hydrated.</p>
      <h2>Effects on Diving      </h2>
      <p>Any individual who has been diagnosed with acute DVT or who is taking  anticoagulants should refrain from diving. It may be possible to return to safe  diving after having a DVT, but the evaluation of fitness to dive must be made  on an individual basis.</p>
