      <h1>Basic Instincts </h1>
		      <p align="justify"><em><strong>Diver Question: </strong></em></p>
	               
              <p><em>DAN explores a hypothetical underwater emergency. Aboard the dive   boat your dive buddy seemed relaxed and well prepared. Thirty minutes   into the dive at about  15m he panics and   appears to be hysterical. He removes his mouthpiece. What emergency   procedure should you perform?</em></p>
              <h6><strong>DAN Answer </strong></h6>
              <p>Anytime a diver loses his regulator underwater he is at risk of   drowning, loss of consciousness and, if he breath-holds on his way up,   an arterial gas embolism. That's why we spend so much time in open-water   training learning what it feels like to be underwater and working on   equipment-related skills: it's to prevent the situation from occurring.   Nonetheless, it does happen from time to time and for a variety of   reasons. <br />
                <br />
                Try to make contact with the diver. Do you know whether this   diver has a history of anxiety-related issues when diving, or is he   having a real problem with his regulator or air supply? For example, see   if the tank is attached to the backpack. Is the regulator attached to   the tank? Is the first stage leaking? Is the mouthpiece attached to the   regulator? Does the regulator work? Is the regulator leaking? Does the   octopus work? Can you see other equipment problems? <br />
                <br />
                <strong>Consider Medical or Physical Problems</strong><br />
                <br />
                Has anything   like this happened before? Does the diver have a significant medical   history, such as high blood pressure or cardiovascular illness? Is there   trauma ? i.e., do you see any wounds? <br />
                <br />
                Many callers to the DAN Medical Information Line have said that   just before they panicked, they had experienced the sensation of not   getting enough air; others had noted that they felt like they were   &quot;breathing through a straw.&quot; It is difficult to communicate respiratory   (breathing) distress underwater, and it very quickly turns to panic when   the diver sees no real option except bolting to the surface for air. <br />
                <br />
                <strong>Getting the Diver to the Surface</strong><br />
                <br />
                Time is a   definite concern in a rescue like this. The panicked diver's tissues   will consume oxygen, even while he is not breathing. This means that   during the ascent, as oxygen concentrations in the lung decrease and gas   expands, the diver is at risk of losing consciousness (called   shallow-water blackout) and perhaps in danger of an arterial gas   embolism from lung overexpansion. <br />
                <br />
                On the other hand, if the diver is unconscious, he may relax the   pharynx and allow passive exhalation. This will allow air to vent out   of the lungs without over-pressurization and barotrauma. <br />
                <br />
                The diver, whether conscious or not, may also experience   laryngospasm, a reflex spasm that occurs when water enters the throat.   Because the spasm may completely or partially close the airway, an   ascending diver experiencing this reflex risks arterial gas embolism. <br />
                <br />
                <strong>Dealing With Complications</strong><br />
                <br />
                The central task in the   rescue of a panicked diver involves helping him make a normal ascent   without inhaling water and holding his breath. Assuming this is a   standard no-decompression recreational dive, the best assistance is to   hold onto the diver to help control the rate of ascent. A distressed   diver in this situation will likely do everything in his power to hold   his breath and not breathe in water voluntarily. Expanding lung volume   usually forces some air to escape through the mouth and nose, but the   risk of AGE still exists. <br />
                <br />
                Drowning or near-drowning is also a possibility, but you   shouldn?t have to cover both the mouth and nose during the ascent. The   diver may still have his mask. If he has panicked, however, both the   regulator and mask may come off, making the situation grave indeed. We   have a natural protective mechanism to continue to breathe after we lose   consciousness. If that were to happen underwater, however, it could   result in drowning or near-drowning. <br />
                <br />
                The risk of injury and even death increases once a diver loses   the regulator. In the best scenario, the diver takes it back immediately   or the buddy will offer it to the diver and adjust the equipment, and   the diver continues to breathe. If not, the buddy or dive guide will   have to help the diver to the surface in a controlled emergency ascent,   one would hope, without incident. If the diver can reach the surface   safely, serious injury or death may be averted. <br />
                <br />
                Other reasons besides equipment trouble or panic may cause the   diver to spit out a mouthpiece. Most said another diver assisted them to   the surface. <br />
                <br />
                Once you've reached the surface, establish positive buoyancy for   both yourself and the panicked diver. If you haven't already removed   the diver's weight belt, do it now. If you?re diving in warm water, you   may have to inflate the diver's BCD. <br />
                <br />
                At this point, you should be able to get the diver back to the shore or the dive vessel. <br />
                <br />
                A good diving buddy should be knowledgeable and skillful, should   remain aware of the surroundings in the water, and should be aware of   his partner's medical and diving history. That's what a buddy isfor.</p>
