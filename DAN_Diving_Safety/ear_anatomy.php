      <h1><strong>Anatomy of the Ear </strong></h1>

	      <p align="justify"><em>The ear is the organ of hearing and balance. It consists of a   cavity in the skull structure lined with soft tissue, which encloses   three distinctive spaces filled with air or liquid (external, middle and   inner ear); these distinctive spaces host both sound transmission   mechanisms and sensory apparatuses. </em></p>
	      <p align="center"><img src="../_images/ear_anatomy.jpg" alt="Ear Anatomy" width="550" height="380" /></p>
	      <h2 align="justify"><strong>Structure</strong></h2>
	      <p align="justify">The external ear includes the pinna (auricle) and the ear canal up to   the eardrum (tympanic membrane), which separates it from the middle ear.   The lining of the external ear is skin rich with glands that produce   earwax.<br />
	        <br />
	        The middle ear is a cavity in a temporal bone lined with a   thin layer of tissue similar to that found in the nose and throat. It   is separated from the ear canal by the eardrum and connected to the   throat via the Eustachian tube. It includes three tiny bones (auditory   ossicles) forming the chain attached to the eardrum on one side and to   the oval window membrane on the inner-ear side. The middle-ear space is   filled with air at ambient pressure, which needs to be equalized when   ambient pressure changes (as occurs in diving or flying). This is   accomplished by moving air in or out through the Eustachian tubes, which   connect the throat to the middle ear, using equalization techniques   such as the Valsalva maneuver.<br />
	        <br />
	        The inner ear, or labyrinth,   includes the cochlea (hearing organ) and the vestibule and semicircular   canals (balance organs). The cochlea and the vestibule are the origin of   the auditory and vestibular nerves.
	      <h2 align="justify">Function          </h2>
	      <p align="left">Pressure waves transmitted by air or water are funneled by the pinna and   the ear canal to the tympanic membrane. The pressure waves cause the   tympanic membrane to vibrate, which causes the auditory ossicles to move   simultaneously in response. The stapes (the last bone in the chain)   strikes the oval window of the cochlea. Since this is a closed system,   when the oval window is pushed inward, the round window pushes outward.   The fluid within the cochlea transmits the pressure waves to the   auditory nerve, which in turn, sends signals to the brain that are   interpreted as sound.<br />
	        <br />
	        Parts of the vestibule are projections   known as the semicircular canals. The fluid within this system moves   correspondingly with head movement. Inside the semicircular canals are   hairlike structures called cilia. The cilia detect movement of the fluid   through the canals and send the signals through the vestibular nerves   to the brain, where the movement is interpreted and used to help   determine the position of the head in three-dimensional space.</p>
