      <h1> Decompression Recovery </h1>
		      <p align="justify"><em><strong>Diver Question: </strong></em></p>
	               
              <p><em>I  received eight hyperbaric treatments for decompression sickness six weeks ago.  I was in good physical shape, and I've never had a major injury before. I  experienced numbness and tingling in my left arm, hand, leg and foot. This all  started about one hour after my dive, and I felt very weak and fatigued. I saw  the local doctor and received chamber treatment about four hours after my  symptoms started. All of my symptoms were gone before I flew home to the United  States, four days after my last treatment.</em></p>
              <p><em>In the  last two weeks I have noticed a return of some of the tingling in my left arm  and foot. It comes and goes, but it does not completely go away. Is this a  normal side effect of decompression sickness, and should I seek more treatment  in the chamber?</em></p>
              <h6><strong>DAN Answer </strong></h6>
              <p>According to the DAN 1992 Report  on Diving Accidents and Fatalities, slightly more than 50 percent of all  decompression illness cases that received hyperbaric oxygen therapy were successfully  treated without residual symptoms. The remaining cases had some neurological  symptoms or pain for several days or weeks after hyperbaric therapy was  completed. On average, 16 percent of injured divers will still have symptoms  for up to three months after they have been treated.</p>
      <p> Original symptoms sometimes reappear during this three-month recovery period.  Divers have reported the recurrence of symptoms after a series of long days at  work, decreasing amounts of sleep, sitting in one position for long periods of  time or after drinking too much alcohol. The most often mentioned symptom is  numbness and tingling. </p>
              <p>In general, recurrence of symptoms can occur, but  this is not necessarily normal. It most likely relates to the severity of the  original injury. There are three important issues to remember regarding  recovering from decompression illness.</p>
              <p>In many cases of decompression illness, the response to therapy is related to  the time between symptom onset and chamber recompression.</p>
              <p> Divers must do everything they can to assure rapid first-aid measures - which  includes the use of 100 percent oxygen - and evaluation leading to chamber  therapy. It is also important to note that hyperbaric oxygen therapy is a  treatment and not always a cure. Divers can suffer mild numbness in a hand or  physical impairment that is life-long.</p>
              <p> Thirdly, as in any other injury, some recovery time must be expected before an  injury can completely heal. Unlike a traumatic injury that is obvious to the  eye, injuries caused by gas bubbles are internal.</p>
              <p> The tiny bubbles associated with decompression illness, in sufficient  quantities, can do more damage than being hit by a car. Never underestimate the  potential for a serious injury when symptoms first appear and always seek  immediate medical evaluation.<br />
