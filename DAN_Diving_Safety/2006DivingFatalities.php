          <h1>2006 Diving Fatalities  </h1>
          <h2>          A review of the regional diving-related fatalities recorded by <br />
          DAN Asia-Pacific for 2006 </h2>
          <p><em>By John Lippmann, Dr Douglas Walker, Dr Chris Lawrence, Dr Andrew Fock,  Tom Wodak, and Scott Jamieson</em></p>
          <p align="justify">DAN  Asia-Pacific collects information on diving and snorkeling fatalities  throughout the Asia-Pacific region. However, the effectiveness of this data  collection varies greatly between countries.</p>
          <p align="justify">In Australia,  dive fatalities are comparatively well investigated and most are logged on a  National Coronial Database to which DAN AP has access. It often takes several  years before the coronial investigations are completed and for detailed  information to become available. Although a similar system does not yet exist  in New Zealand, diving fatalities are generally investigated and reported  there. Coronial investigations of diving deaths are usually conducted in  Singapore and Hong Kong but these are rare in other parts of the Asia-Pacific.</p>
          <p align="justify">As a result, it  is often very difficult to get reliable information beyond Australia. Even if  an autopsy is conducted to determine the cause of death, this can often be  unreliable if it has not been performed by a pathologist who is aware of  appropriate autopsy procedures for diving accident victims.</p>
          <p align="justify">Despite these  difficulties and shortcomings, we believe that dive fatality reporting plays an  enormously important role in accident prevention, and DAN AP is committed to  providing substantial resources in pursuing this task.</p>
          <p align="justify">The reports  shown here are only brief summaries of the cases for 2006 for which we have  some information. Detailed reports of the Australian fatalities have been  published in the journal of the South Pacific Underwater Medical Society  (SPUMS).* We are very 
            grateful to the  various Australian State Coroners, the NCIS and Water Safety New Zealand for  their assistance with this project, and to the members of the public who have  provided information. </p>
          <p align="justify">We need divers to contact us when they hear of  diving-related 
            fatalities so  that we can collect as much information as possible.</p>
          <p align="justify"><em>* Lippmann J, Walker D, Lawrence CL, Fock A, Wodrak T, Janieson S. Provisional report on diving-related fatalities in Australian waters 2006. Diving &amp; Hyperb Med 2011; 41(2)70-84.</em></p>
