          <h1>Are You Prepared?</h1>
	      <p align="justify">Despite the best-laid plans, diving injuries can and  do happen. A reality of scuba diving is that it often involves travelling to  remote locations. This is part of the appeal of scuba diving but it also comes  with some challenges, primarily that such destinations are often cut off from  the medical care required for treating serious diving accidents and illnesses. </p>
	      <h2>Risks of Diving</h2>
	      <p>Let&rsquo;s think about the risks involved in diving. The  causes of diving accidents are varied and may include:</p>
	      <ul>
	        <li><em>Lack of Familiarity with the Environment. </em>Divers may encounter problems as a result of currents,  differing water temperatures and visibility, poor and/or changing surface  conditions, amongst others.</li>
            <li><em>Lack of Familiarity with Equipment or Equipment  Failure</em>.</li>
	        <li>	          <em>Inadequate Physical Fitness</em>.</li>
	        <li>	          <em>Inadequate Medical Fitness</em>.</li>
	        <li>	          <em>Human Error and Violations of Safety Rules</em>.</li>
	        <li>	          <em>Encounters with Marine Animals</em>.</li>
	        <li>	          <em>Boats and jet skis. </em>Both pose a potential danger for divers and serious  injuries do occur. Boat operators do not always see divers, while others may  not be aware of, or may ignore, the requirement to stay a safe distance from  divers.</li>
          </ul>
	      <p align="justify">While these risks should not keep us from diving, they  do highlight the need to prepare ourselves for the unexpected.</p>
	      <h2>Do You Have A Plan?</h2>
	      <p align="justify"><em>Do you know what you would do in the event of a diving  accident? Do you know who you would you call? Do you have any first aid  training to assist until professional medical advice and care are received?</em></p>
	      <p align="justify">Having a plan in place can only work to improve a  difficult and scary situation, where time is of the essence. An action plan  must be implemented immediately to lessen the severity of an accident or  illness.</p>
	      <p align="justify">The Divers Alert Network (DAN) is the lifeline for  many divers that have been in need of urgent help. DAN has been involved with  cases or paralysis, loss of hearing, loss of limbs, and unfortunately even loss  of life in locations all around the world. It is the networks that DAN has  established worldwide that divers rely upon. And, as all people who experience  a diving accident or illness will attest to, it pays to be prepared. </p>
	      <h2>What Are the Costs Involved With Evacuating &amp;  Treating of a Diver? </h2>
	      <p>The costs associated with  treating a sick or injured diver vary greatly based on several variables, such  as:</p>
	      <ul type="square">
            <li>The location of the incident</li>
	        <li>Proximity to a chamber</li>
	        <li>Proximity to a suitable       medical treatment facility for the injury/illness</li>
	        <li>Whether an evacuation is required.</li>
          </ul>
	      <p align="justify">If an aircraft needs to fly in from another location  with a medical team on board then naturally the associated costs will be  significantly higher than if the diver needs to be driven to a local chamber. </p>
	      <p align="justify">In addition to this is the cost of  treatment that is required in a chamber and/or hospital for the  accident/illness. This can vary from US$500 - $20,000, or more, depending on  where this treatment is and how much treatment is required. Suffice to say that  the costs involved in evacuating and/or treating a diver can be significant. </p>
	      <p align="justify"><em>The reality is that it can cost more than US$100,000  for the evacuation alone! </em>This is why it is so important that divers have DAN protection. And the  fact that DAN are the experts in diver accident management means the diver will  receive the most effective response in co-ordinating their  treatment/evacuation. </p>
	      <p align="justify">Have you considered how would you pay for an  evacuation or treatment required for a diving accident or illness? If you have  Travel Insurance, are you 100% certain that diving is fully covered? Are there  any restrictions associated with this coverage? Would the travel insurer know  how to effectively manage a diving accident/illness? Let&rsquo;s explore some real  life cases. </p>
	      <h2>Case 1: Location: Solomon Islands</h2>
	      <p align="justify">DAN was involved in the evacuation  of a paralysed diver from the Solomon Islands to Australia. The evacuation cost  was in excess of AUD$90,000 (as an aircraft had to be sourced from far away).  In addition, the treatment costs were in excess of US$30,000, plus on-going  expenses.</p>
	      <h2>Case 2: Location: Papua New Guinea</h2>
	      <p align="justify">At a cost of US$72,000, DAN evacuated a diver form PNG  to Australia. 
	        This diver undertook one dive and some 30 minutes  later commenced experiencing nausea and headache. The symptoms continued to  increase in severity with vomiting, confusion and irrational behaviour. The  diver fully recovered and it was determined that the diver suffered a  mini-stroke. </p>
	      <h2>What Do These Cases Highlight?</h2>
	      <p align="justify">Diving accidents and illnesses can and do happen and it  is essential to have a plan in place so that help can be quickly sought in an  emergency situation. Part of this plan should include being a member of an  organisation, like DAN, where Members have the benefit of being supported by  the largest and most experienced group of professionals dedicated to managing  diving accidents worldwide.</p>
	      <p align="justify">	        Even though DAN has by far the best worldwide network  and undoubtedly the greatest experience in handling emergencies, we cannot  escape the fact that, in the event of a very serious accident, remoteness from  a suitable medical facility can be the main determinant of the outcome.</p>
	      <p><em>Article By John Lippmann, DAN Asia-Pacific Executive  Director</em></p>
