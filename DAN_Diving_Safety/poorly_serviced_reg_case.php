      <h1>Case Summary: Regulator gets harder to breath the deeper a diver goes</h1>
		      <h3>Recently serviced regulators made it harder to breathe the deeper they went</h3>
		      <h2>Reported Story		      </h2>
		      <p align="justify">We took our regulators to the local dive center for their annual   inspection and collected them when my husband and I took enriched air   and dry suit classes. We used our regulators in a pool dive that day and   then later they were used by me for a shallow water dive (less than 6m) without incident. A couple of days later I was scheduled to   make a deep dive along with another student. With us were three   instructor candidates and the dive instructor. After some weight   adjustments we descended to a depth of 18m, when I felt I   wasn't receiving enough air from my regulator. I signaled to my   instructor and pointed up. The instructor thought I was having a problem   with my ears so we slowly started to swim to a shallower depth.   Somewhere around the 15m level my regulator was giving me   very little air and I inhaled water. My instructor attempted to put my   regulator back in my mouth because I had already spit it out because I   was not receiving air from it, then I attempted to locate my secondary   regulator which is integrated into my BCD power-inflator line and the   instructor thought I was going to inflate my BCD and rapidly ascend to   the surface so he attempted to stop me. I pushed him away and started to   swim towards the surface and lost conscious at approximately 12m. My instructor caught up to me, inflated my BCD and sent me to the   surface. At the surface the instructor administered mouth-to-mouth and   yelled to my husband who was on shore. My husband called the Emergency Services and swam   out to assist. After receiving rescue breaths from my instructor and   husband I vomited and coughed up water repeatedly, then started to   regain consciousness. I was pulled into shore by my husband and medical   treatment was given to me by him and the arriving ambulance.<br />
	            <br />
              Later,   after researching online about scuba regulator testing facilities, we   took our regulators to have them inspected. The company put them in a   pressure vessel to test gas flow and found the regulators were out of   factory specifications. We were informed that the regulators were set so   the deeper the dive the more difficult it was to receive air from them.</p>
		      
		      <p align="center"><img src="../_images/reg_case.jpg" alt="Poorly Serviced Reg" width="245" height="224" />
		      <h2>Comment</h2>
		      <p>It sounds as though this diver was lucky not to have drowned. As with   many near miss reports, a number of factors conspired to create the   circumstances needed to enable this event to occur. First, the   regulators were a particular type than can be set incorrectly to make it   harder to breathe with increasing depth, then it sounds as though they   may indeed have been incorrectly serviced, then the diver made some test   dives but they were shallow so no problem was detected. During the   actual dive the diver may not have given clear &quot;OUT OF GAS&quot; and/or &quot;GIVE   ME GAS&quot; signals and, coupled with an unusual type of back-up second   stage, the instructor mistook the diver reaching for the power inflator   to indicate a desire to ascend. Even if the diver had tried this back-up   second stage then it may not have worked, if it was the first stage   that was malfunctioning.  Rarely does a single issue lead to a near   miss. The great majority of incidents involve many factors all coming   together during the one dive. This is why to maximize safety it is   essential for every diver to maintain current skills, (such as knowing   standard dive signals), maintain personal dive gear and practice   emergency procedures such as locating a back-up second stage, dropping   weights and orally inflating the BCD at the surface.</p>
