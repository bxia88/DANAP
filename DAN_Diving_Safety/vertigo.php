      <h1>Vertigo</h1>
		      <h3 align="justify">Vertigo is the persistent feeling of tilting, swaying, whirling or   spinning motion of oneself or of the surrounding world when nothing is   moving. </h3>
		      <h2 align="justify">Description</h2>
		      <p><span title="">Vertigo</span> during or after diving is a common symptom of middle-ear or inner-ear   injury. It is often associated with nausea and in severe cases vomiting.   If vertigo happens underwater, the diver may not be able to tell which   way is up; panic and vomiting may cause choking and drowning. On land,   the patient may not be able to sit or stand<br />
		        <br />
		        There are various   causes of vertigo. In diving, it is most often caused by inner-ear   barotrauma. It can also occur from stimulation of one side and not the   other, such as when the pressure difference in only one ear equalizes   (alternobaric vertigo) or when cold water enters one ear but not the   other (caloric vertigo). This type of vertigo disappears as the   condition equalizes and leaves no lasting effects except that the   associated disorientation, nausea and vomiting while underwater may   contribute to diving accidents.<br />
		        <br />
      Vertigo is an acute symptom of   vestibular injury that may be associated with other symptoms, some of   which may become chronic. Symptoms may include imbalance and spatial   disorientation, vision disturbance, hearing changes, involuntary eye   movement (nystagmus), and cognitive and/or psychological changes, among   others.</p>
		      <h2>Differential Diagnosis</h2>
		      <p>Vertigo is not the same as dizziness, lightheadedness or unsteadiness.   When you're dizzy, you may feel lightheaded or lose your balance. If you   feel that the room is spinning, you have vertigo.<br />
		        <br />
        For vertigo, differentiate between inner-ear decompression sickness (DCS) and inner-ear barotrauma. </p>
		      <h2>General Guidance</h2>
		      <ul>
		        <li> Vertigo occurring briefly during or after a dive and resolving   spontaneously requires evaluation of Eustachian tubes before resuming   diving.</li>
		        <br />
		        <li> Persistent <span title="">vertigo</span> is a sign of serious conditions and requires urgent evaluation by an ENT specialist. For an ENT referral in your area, email <a href="mailto:info@danap.org">info@danap.org</a>, or call DAN AP on +61-3-9886 9166.</li>
		        <br />
		        <li> Severe persistent postdive vertigo is an emergency.</li>
      </ul>
              <h2> Fitness to Dive</h2>
              <p>Damage to vestibular organs by DCS, barotrauma or acoustic shock may be   permanent. In case of single-ear injury, vertigo may go away in two to   six weeks, because the brain learns to compensate and ignores the side   that is damaged, but the canal will not heal. The diver will have   difficulties maintaining balance in the dark when deprived of visual   clues. Damage to both vestibular organs is debilitating and may make   certain life activities (such as driving a car) challenging or   impossible.<br />
		        <br />
		        Persistent or recurrent vertigo, even if controlled by medications, is disqualifying for diving.<br />
		        <br />
		        Return   to diving after inner-ear barotrauma or DCS should be evaluated on an   individual basis depending on the extent of permanent injury of   inner-ear organs. 
