      <h1> Diving After Flying </h1>
		      <p align="justify"><em><strong>Diver Question: </strong></em></p>
	               
              <p><em>Often it  takes a long time to get to some dive destinations, in some cases more than 24 hours. I notice that many tourists begin diving immediately when they   arrive and often start drinking quite a bit of alcohol. Aren't they at   greater risk for decompression sickness after their long flight if they   begin diving right after they arrive? Shouldn't they wait one day before   they begin diving?</em></p>
              <h6><strong>DAN Answer </strong></h6>
              <p>Mild dehydration can occur on long flights, especially when travelers   cross several time zones; alcohol consumption can also contribute to   dehydration. Generally speaking, dehydration is thought to predispose a   diver to decompression illness because the washout of inert gas   (nitrogen, in diving) is less effective in a dehydrated individual.</p>
              <p>                If   there were a relationship between diving after flying and DCI, we would   expect to see a great deal of decompression illness on the very first   day of diving  indeed, some data suggests that there are more accidents   on the first day of a planned multiday dive trip. </p>
              <p>In a study where 88 cases   of DCI were reviewed from the Caribbean, 33  or 37.5 percent  occurred on   the first day. The remainder occurred on days two through seven. Given   that there are thousands of tourist divers who fly to Caribbean and   Pacific dive sites, these numbers are far too small to establish a cause   and effect.</p>
              <p>                Although no one can insist upon a 24-hour waiting period   after flying, such a conservative approach to diving after flying is a   reasonable idea as it gives divers an opportunity to rehydrate, adjust to a   new climate and time zone, and rest up after a long flight.<br />
                <strong><u><br />
                </u></strong><em>Answer provided by the  Divers Alert Network Medical Team</em><br />
              </p>
