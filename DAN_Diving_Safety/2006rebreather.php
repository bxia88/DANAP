          <h1>2006 Diving Fatalities  </h1>
          <h2>          Rebreather Fatality (Australia) </h2>
          <h6>RB 06/6 </h6>
          <p align="justify">The victim was a 31-year-old male,  described as an experienced technical diver who&nbsp; had been diving for 20 years with 75 dives in the past year. He  exercised regularly and was described as &ldquo;fit and healthy&rdquo; without any known  medical conditions.</p>
          <p align="justify">He and two friends planned to dive on a  wreck at a depth of 75 msw. The victim had previous experience with a &lsquo;PRISM&rsquo;  rebreather, however, on this occasion he was using a borrowed &lsquo;Inspiration&rsquo;  rebreather. This rebreather differed from the former in a number of key areas  and the victim had reputedly had these differences highlighted to him during  some retraining and had been encouraged to gain experience with the unit by  conducting air dives only. He had apparently conducted several dives in the  40-50 msw range on the unit post course.</p>
          <p align="justify">The victim was the first to enter the water  and was soon joined on the line by one of his buddies. He then complained that  he felt nauseous (he suffered from seasickness) and 
            decided to abort the dive. However,  shortly afterwards, the buddy saw him underwater at about 5 msw, apparently  unconscious and sinking. He immediately tried to descend but 
            was unable to dump air in his BCD quickly  enough. He called to the other diver on the boat and when he entered the water,  the two descended to try to reach the victim but 
            abandoned the attempt at 55m. His body was  eventually found by a diver eight days later at a depth of 75 msw and with all  equipment in place. It is possible that, due to the relative unfamiliarity of  the rebreather he was using and its difference with his 
            previous unit, he accidently breathed a  hypoxic gas mix which caused him to rapidly become unconscious.<em>Cause of death: </em>Drowning.</p>
