          <h2>World Compressed Gas Diving Fatalities 2014</h2>

          <p align="justify">These figures are the minimum number of fatalities in the 

            countries shown. They are taken from reports provided to 

            IDAN organisations and also include some taken from 

          various media sources.</p>

          <p align="justify">            The number for certain countries will be relatively accurate 

            (eg. U.S.A., Australia, New Zealand, U.K., Japan.) 

            However, the numbers for many other countries especially 

            less developed countries, are very likely understated.</p>

          <p align="justify">            The data from Europe (with the exception on the UK) is 

            predominatly taken from insurance claims so is very likely to 

          be an underestimate.</p>

