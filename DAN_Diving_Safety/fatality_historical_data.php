          <h2>2003 - 2011 </h2>

          <p align="justify">The graphs on the below links illustrate the number of cases of diving fatalities reported to DAN  Asia-Pacific on an annual basis and categorised by country. </p>

          <p align="justify">The accuracy of  this information varies greatly between countries depending on how  well-reported it is from the chambers to DAN. In certain places (e.g.  Thailand), some chambers withhold information for commercial and other reasons.</p>

