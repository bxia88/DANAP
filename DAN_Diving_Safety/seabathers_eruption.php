      <h1>Seabather's Eruption </h1>
		      <p align="justify"><em><strong>Diver Question: </strong></em></p>
	               
              <p><em>I  was snorkelling in the ocean outside my hotel in the Florida Keys. Soon after exiting the water, my  skin started to tingle. I quickly showered in an attempt to remove the  offending agent and noted that the stinging sensation intensified. A bumpy,  itchy rash erupted within the hour and I’m miserable. What is this and what  should I do? </em></p>
              <h6><strong>DAN Answer </strong></h6>
              <p>Seabather’s eruption, often misnamed &quot;sea lice&quot;  (which are true crustacean parasites of fish), refers to dermatitis (skin  irritation) that results from contact with ocean water containing various  larval species of jellyfish and anemones. The incidence of larval blooms  appears higher in warmer oceans and affects people all over the world. Most  noted species in the Western hemisphere are the thimble jellyfish, <em>Linuche  unguiculata</em> and sea anemone <em>Edwardsiella lineate</em>  {1,2,3}. Seasonal peaks  of thimble jellyfish occur in the Caribbean and southern Florida during May,  but are present throughout the summer (April – August) {1,4,5,6,7}.  </p>
              <p>Seabather's eruption predominately affects areas covered by clothing: bathing  suits, swim caps, shirts, rash guards and wetsuits {1}. Those swimming  at the surface may suffer stings to the exposed neck and scalp {1}. In  areas with heavy boat traffic, jellyfish may become fragmented, which can  facilitate stings to any exposed area {1}.</p>
              <p><br />
                Stings are characterized as a tingling sensation that may begin while still in  the water or soon after exiting. While tempting to bathe, the application of  freshwater may intensify the sting {1}.The ensuing rash usually  consists of extremely itchy red bumps, wheals, blisters and papules, which may  become dense and confluent (run together in a mass). Skin eruptions may present  within minutes or up to 12 hours after exiting the water and can persist for  4-weeks (in severe cases) {1}. Severe exposure can result in systemic  symptoms such as fevers, chills, headache, fatigue, nausea, conjunctivitis  (itchy red eyes) and urethritis (burning on urination) {1}.  Susceptibility and presentation will vary by individual. Children, and those  previously sensitized may have more severe skin reactions.<br />
                <br />
                <strong>Treatment</strong><br />
              As with stings from other Cnidarians (jellyfish, fire coral and anemones),  treatment consists of immediate application of vinegar, removal of any  remaining marine organisms, and the use of topical steroids (which reduce itch  and inflammation). In severe cases, systemic corticosteroids and antihistamines  may be helpful1. Signs of infection or severe systemic symptoms  should prompt medical attention.</p>
              <p><br />
              The stinging cells may remain in bathing suits or clothing, even after drying.  To prevent subsequent inoculation, machine wash or thoroughly rinse in vinegar.</p>
              <p><br />
              <em>Answer provided by DAN’s Nicholas Bird MD, MMM and Matias Nochetto, MD</em></p>
