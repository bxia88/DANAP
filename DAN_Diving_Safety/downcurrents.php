        <h1>Beware of Dangerous Downcurrents  </h1>
	      <p>By John Lippmann, Founder,  Chairman &amp; Director of Research, DAN Asia-Pacific</p>
	      <p><em>Currents that present the greatest risk to divers run downwards or  upwards and are often caused when a horizontal current hits an underwater “wall”.  Dangerously, divers can be unexpectedly dragged to depth and risk running out  of breathing gas and/or having very rapid ascents when released by the currents.  Knowledge and skills to prevent or minimise the problem are vital for all divers,  given the number of divers who travel to destinations of which they have little  local knowledge and supervision, if available, may be of a variable standard.</em> </p>
	      <p>It was  interesting to read a while ago that the local police had temporarily banned  diving at Crystal Bay, Nusa Penida in Bali. This was to investigate why so many  diving-related deaths had been occurring there. It is a popular diving site due  to the periodic presence of Sunfish (Mola Mola). However, there are some  serious potential hazards there for divers, especially inexperienced and/or  unwary divers.</p>
	      <p>The area is  subject to very strong currents, including substantial downcurrents which can  suddenly force a diver to descend far deeper than planned - and quickly. The  Sunfish are often already quite deep so this has caused numerous accidents,  including multiple deaths and severe cases of decompression illness. Divers can  be unexpectedly dragged to depth and run out of breathing gas and/or have very rapid  ascents when released by the currents.</p>
	      <p>Such  downcurrents occur regularly in a variety of sites around the world, but the  majority of reports DAN AP has received over the past few years has been from  Indonesia; predominantly Nusa Penida, the Komodo area, Raja Ampat and Bunaken.  These have become popular destinations for divers due to the rich and diverse  marine life. However, unfortunately the safety standards and preparedness of  dive operators varies greatly and unsuspecting divers sometimes find themselves  in perilous situations.</p>
	      <p>Most currents  run horizontally across the earth’s surface. Divers can, and do, occasionally  get stranded and sometimes lost at sea as a result of these. However, the  currents that present the greatest risk to divers run downwards or upwards and  are often caused when a horizontal current hits an underwater “wall” and is  then forced to flow downwards, or upwards.</p>
	      <p>The strength of  the current will vary with the tide and conditions. There is often no obvious  sign of the presence of such a current, although there can sometimes be a  rippling and/or swirling on the surface in the area. Therefore, local knowledge  is very important and experienced local divers are usually a good source of  information.</p>
	      <p><strong>To Prevent or Minimise the Problem:</strong></p>
	      <ul>
	        <li>If  diving independently, get good local knowledge about the planned dive site.</li>
            <li>              If  diving with an operator, choose them carefully.</li>
            <li>              Ask  specific questions about prevailing currents as well as other potential hazards.</li>
            <li>              Be  aware of your location throughout the dive.</li>
            <li>              If  close to a wall when caught in an up-current or down-current, try to grab or  hook onto the wall to avoid being swept away.</li>
            <li>              If  unable to get a hold on the wall, swim away from it as the current should  lessen further away.</li>
            <li>              If  you are being dragged down:<br />
              - keep calm<br />
              - equalise your ears as necessary<br />
              - inflate your BCD<br />
              - swim up &amp; away from the wall<br />
              - if you have no visual reference, watch  your bubbles for orientation (although this can sometimes be misleading in a  strong downcurrent).<br />
              - monitor your breathing gas</li>
            <li>If  you are being dragged upwards:<br />
              - keep calm<br />
              - swim down &amp; away from the wall<br />
              - deflate your BCD<br />
              - exhale appropriately and then continue to  breathe as appropriate<br />
              - spread your arms and legs to slow the  ascent.                </li>
        </ul>
	      <p>Diving in current can be very rewarding due  to the rich marine life often associated with them and the wide territory than  can sometimes be covered with little effort. However, adequate preparedness is  essential to maximise enjoyment and safety.  </p>
	      <p><em>Safe Diving,</em></p>
	      <p>	        John Lippmann<br />
	        Divers Alert Network Asia-Pacific </p>
