      <h1><strong>Chamber Locations </strong></h1>
		      <p align="justify"><em><strong>Diver Question: </strong></em></p>
	               
              <p><em>I  don't understand why DAN doesn't tell divers where the closest chamber is? </em></p>
              <h6><strong>DAN Answer </strong></h6>
              <p>DAN  frequently consults on the care, transport and hyperbaric treatment of injured  divers but does not provide chamber location information. This is an effort to  get divers with a suspected decompression illness into hospital care. <br />
                <br />
In the past, divers would often drive past legitimate health-care facilities to  get to a recompression chamber. Even when divers surface with clear symptoms of  an arterial gas embolism, the treatment of choice is the local emergency  service and hospital. Your best option is to use existing emergency services  for an injured diver. <br />
<br />
<strong>The reasons:</strong></p>
              <ul>
                <li>                Hospitals  and urgent-care facilities have an unlimited supply of oxygen, intravenous  fluids and medications. </li>
                <li> A  physician/emergency care provider needs to rule out other illness such as  pneumothorax (collapsed lung), myocardial infarction (heart attack), and  neurological and musculoskeletal injuries with symptoms similar to DCI.</li>
                <li> An  injured patient needs to be stabilized before and during transport and should  be transferred under medical supervision.</li>
                <li> Transporting  a diver without a proper evaluation may adversely affect the diver's health and  treatment outcome.</li>
                <li> A  chamber's operational status can change. Chambers may close for scheduled  maintenance, staff vacation or a limited staff because of a high daytime  patient treatment load. The chamber you are driving to may not be available.  Prior notification from an evaluating facility is usually necessary to begin the  call-in procedure to staff a hyperbaric treatment. Finally, most hyperbaric  facilities have regular daytime business hours and are not staffed in the  evenings and on weekends. In fact, some chamber facilities choose not to staff  their unit after hours and do not wish to treat divers. Most cases of  decompression sickness report for evaluation after normal business hours.</li>
              </ul>
              <p>                Divers  traveling <strong>outside</strong> the United States (including Alaska and Hawaii) and  Canada may contact the DAN information line for chamber information appropriate  to their destination. Keep in mind that DAN is not the only resource for  chamber information for travel abroad. It also is reasonable and appropriate to  obtain this information from the planned dive operator/resort before arrival.  Please understand that the same issues that affect availability with domestic  chambers can also affect international chambers.<br />
                <br />
                <strong>If you suspect a diver has a dive-related injury and needs evaluation, you  should safely:</strong></p>
              <ul>
                <li>Monitor  airway, breathing, circulation</li>
                <li> Provide  100 percent oxygen if you are a trained oxygen provider.</li>
                <li> Call the  local EMS for transport or assist in the transport of the injured diver to  medical care.</li>
                <li> Call a  DAN Emergency Hotline at for consult and advice.</li>
              </ul>
              <p>                If you are uncertain about symptoms that occur hours or days after diving, and  there is no emergency, or you wish to ask questions about the signs and  symptoms of decompression illness, contact DAN on +61-3-9886 9166, 9 a.m. to 5  p.m. AEST, Monday through Friday.</p>
              <p><em>Answer provided by the DAN America Medical Team </em>
