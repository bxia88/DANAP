      <h1>Tank Valves and Out of Air Emergencies</h1>

	      <p align="justify">By Petar Denoble, M.D., D.Sc.<br />

	      </p>

          <p align="center"><img src="../_images/out_of_air.jpg" alt="Out of Air" width="370" height="200" /><br />

          <p align="justify">Out-of-air situations are the most common cause of fatal diving accidents. Surprisingly, these problems sometimes occur at the very beginning of dives when the diver should still have a full tank of compressed gas. How is this possible? Recently such case was reported through DAN's Diving Incident Report System (DIRS). The following is a brief summary of the case.<br />

            <br />

            <em>A few minutes into the dive, while still descending, the diver dropped his weights and surfaced in a hurry. His dive guide surfaced next and found him at the surface panicking and out of his BCD. The diver's BCD was empty of gas and could not be inflated. His tank appeared to have no pressure. The guide inflated the victim's BCD orally, and the victim calmed down. The guide then checked the valve of the victim's tank and found it closed. He opened the valve and found the pressure in the tank to be 2,200 psi. The victim said that a few minutes into the dive, as he descended to 30 feet, he suddenly had no air. He never explained why he got out of his BCD or whether or not he closed the tank.</em><br />

            <br />

            The diver most likely did not sufficiently open the tank valve before his descent. The valve was open enough to provide some air at the surface and in shallow water. As the diver descended, however, he did not get enough air. He tried to open the tank, but in the attempt he turned the valve in the wrong direction, shutting it off completely. The root cause of this incident may have been the often-suggested practice of opening a valve all the way and then turning it back a half turn. To learn about the prevalence of this practice and the rationale behind it, we conducted an informal survey, using Facebook, of a group of diver medics. The survey questions were:<br />

            <br />

            <em>1.	How do you open your tank valve?</em><br />

            <em>2.	Why are there so many incidents involving incompletely open tank valves?</em><br />

            <br />

            We received 40 responses. In general there were two distinctive answers to the first question: &quot;all the way&quot; and &quot;all the way and then a half-turn (or quarter-turn) back.&quot; Nearly every respondent had his own justification for doing it the way he did it. The second question elicited long and various answers.**<br />

            <br />

          We contacted two experts, both experienced divers and scholars in matters of diving accidents, and asked them for insight. </p>

      </p>

            

              <h2>How might an incompletely opened valve leave a diver without a gas supply? </h2>

          <p><strong>Peter Buzzacott:</strong>&nbsp;The  breathing gas is delivered from the tank to the diver through a valve, then a  first-stage regulator, then a hose and then a second-stage regulator. The  degree to which the valve is open can be important (depending on the valve  type), and it may vary among individual valves of the same type. Even valves of  the same brand vary in the number of degrees the knob has to be turned before  the volume of gas meets or exceeds the diver's needs.<br />

            <br />

A gauge is connected to the tank immediately after  the valve to measure pressure in the tank. Next in the gas line is the  first-stage regulator, which fills the hose to about 130 psi higher than  ambient pressure. Last is the second stage. When a diver inhales, the pressure  in the breathing line drops, and the regulators let more gas flow out of the  tank. If the tank valve is not fully open, then a diver will suck the available  gas out of the line faster than the gas flows out of the tank. In that case one  can see the needle of the gauge move with each attempted breath.<br />

<br />

Recreational divers' average breathing rate over  the duration of a dive varies from 7 to 45 liters per minute. This depends on  how big the diver is, how stressed he is and also how hard he is exercising.  For ventilation of 45 liters per minute, a diver may breathe at rate of 30  times per minute, giving him one second for inhalation and one second for  exhalation of a tidal volume of 1.5 liters. This means that a regulator has to  deliver 1.5 liters in one second.<br />

<br />

However, the deeper a diver is, the more molecules  of gas are necessary to make up the same volume and the more molecules must  pass through the valve and the rest of the gas line into the lungs. Remember,  pressure at the surface is one atmosphere, and it increases by another  atmosphere for every 33 feet of depth. If a diver is breathing 20 liters per  minute at the surface, then he will need 40 normal liters of gas at 33 feet  (compressed down to 20 liters) and 60 normal liters of gas (compressed down to  20 liters) at 66 feet. A partly closed valve may provide sufficient gas to a  resting diver at the surface, but at depth or during exercise the supply will  not meet the demand. At this point there is often a moment of disbelief  whereupon the diver asks himself, &quot;Did I just run out of gas?&quot; He may  take another breath and initially think, &quot;OK, phew, no problem,&quot; but  then it stops short again. If this happens to you, what are your options?<br />

<br />

First, and most important, signal your buddy that  something is wrong (remember, the twisting hand signal), and then tap your  regulator. This happened to a buddy of mine at 66 feet on an artificial reef in  the Indian Ocean. I gave the diver in need my spare regulator. Then we kneeled  on the bottom while I checked his valve and turned it all the way on; he went  back to his own regulator, and we went on to enjoy a great dive. You just need  to keep a cool head. My buddy had been diving for 30 years and was pretty  relaxed underwater. He was able to breathe slowly, and he stopped exercising  and signaled me.<br />

<br />

<strong>Gareth Lock:</strong>&nbsp;An open pillar valve may leave the  diver without a gas supply because an inadequately open valve may deliver  insufficient gas for the ambient pressure. A small opening that provides  sufficient gas flow on the surface may become problematic with increasing depth  due to gas density limiting the flow through the restriction. Despite the  diver's increased breathing effort to suck more gas, the flow does not increase  and the diver remains short of breath, accumulating carbon dioxide (CO2) and,  possibly, panicking. <br />

          </p>

          <h2>What might cause a valve to be incompletely open?</h2>

        

         

          <p><strong>Lock:</strong>&nbsp;There are several reasons a valve may not be completely open. These include failure to open the valve prior to entering the water, which might be a result of not opening the valve in the first place or an interrupted equipment check that wasn't restarted properly. It's also possible that the diver opened the valve before he entered the water and someone closed it in an attempt to be helpful, thinking he was opening it. Finally, the internal components of the valve might fail in a closed position.<br />

            <br />

            <strong>Buzzacott.</strong>&nbsp;The main cause, in my opinion, is the turning of a valve back a half turn. Divers and divemasters check each tank, and there is room for confusion about whether a tank is a half turn from off or a half turn from on. In the early days of scuba, valves were made of brass. If you didn't turn the knob back a half turn then the valve would shrink during the dive due to cooling, and you wouldn't be able to turn it off afterward. This is not a problem today, but old habits die hard, and many divers still turn the valve back half a turn.<br />

            <br />

          Problems may arise when a diver opens a valve fully and then turns it back a half turn, and then later the same diver or someone else turns it the other way. Labeling could help prevent this. Some valve manufacturers label their valve knobs with a two-ended arrow and the words &quot;on&quot; and &quot;off&quot; at either end. This is brilliant. You simply look at the knob, and you immediately know which way to turn it. Not all manufacturers have done this, and not all valve knobs turn the same way. We live on the thin skin of a globe, and while the market may be global, each region has its idiosyncrasies. In France, for example, divers commonly dive with Y-valves, and in other places sidemount units feature left and right valves. The international diving community should consider a uniform practice to optimize safety. </p>

          <h2>What is the best practice to avoid confusion about whether a valve is open or closed? </h2>

            <p align="center"><img src="../_images/tank_open.jpg" alt="Tank" width="400" height="300" /><br />

          <p><strong>Buzzacott:</strong>&nbsp;Always open your tank valve all the way. You don't need to turn the knob back a bit anymore; those days are long gone. Open it up, and breathe easy. Also, a quick predive check and a buddy check should pick up any errors. Look at the submersible pressure gauge (SPG), take a couple of breaths, and if you can breathe and the needle does not move, you are good to go.<br />

            <br />

            <strong>Lock:</strong>&nbsp;First, remember that you don't have gills and that you need a gas supply to breathe underwater. While that might appear to be a trite answer, it means you have to accept personal responsibility for all your predive equipment checks and preparation. You are the one who needs the gas, and therefore you should be the one to check it. The last time you can check is after you get off the bench, seat or beach and just before you enter the water. Before you leave the kitting-up place, reach back and check that the valve is open. If you can't reach it, ask someone else &mdash; preferably your buddy &mdash; to physically open the valve and then back it off a little bit. Breathe the regulator enough to empty the low-pressure hoses and confirm the SPG needle didn't drop. By checking it just before you enter the water, you reduce the possibility of a &quot;helpful&quot; deckhand introducing an error. Always think through the consequences of not completing this valve check. Starting a dive with a cylinder turned off could result in a fatal outcome &mdash; a recent fatality in the U.K. was caused by this sequence of events.<br />

            <br />

            I believe the reason people don't check their valves (and other life-support equipment) before they get into the water is because &quot;nothing went wrong last time, and so it will be OK this time.&quot; This heuristic works well sometimes, but activities that involve life-support equipment are not the place to gamble. Physical checklists help reduce the probability of an action not being completed. Conducting a proper buddy check in which the divers physically see each other throughout the activity further reduces the probability of omissions and failures. This advice applies to instructors and divemasters, too. Holding a supervisory position doesn't exempt one from forgetting something; furthermore, students and guided divers look to dive leaders as exemplars of best practices &mdash; if the leaders do not complete checks, they won't either. Finally, leaders must be able to help the divers they're with, and they may not be able to do so without a fully opened pillar valve.<br />

          </p>

          <h6>Meet the Experts</h6>

          <p><strong>Peter Lee Buzzacott, Ph.D.</strong>&nbsp;obtained his master's and doctoral degrees at the University of Western Australia while studying recreational scuba diving injuries and diving morbidity risk factors. He is certified in advanced trimix and advanced cave diving and has instructed and certified more than 500 divers. Buzzacott has logged more than 2,000 dives around the world and has written more than 100 dive-related articles for publications. He is published extensively in scientific literature and has won awards for his diving achievements and scientific contributions to diving.<br />

            <br />

            <strong>Gareth Lock</strong>&nbsp;is an accomplished technical diver and widely published underwater photographer. In 2010 he formed Cognitas Incident Research and Management with the aim of improving diving safety by challenging current practices and encouraging a &quot;Just Culture.&quot; In support of this effort, Lock started a doctoral program in January 2012, examining the role of human factors in diving incidents. He has presented at a number of national and international diving conferences on improving safety in scuba diving. </p>

