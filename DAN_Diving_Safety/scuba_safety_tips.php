          <h1>Scuba Diving Safety Tips </h1>
	      <p>All diving involves a degree of risk, because, after all, we are air-breathing mammals who have no sensible reason to be underwater. If we accept this premise, and admit to ourselves that we are voluntarily entering an alien environment, we are more likely to approach our diving with a sensible degree of caution. We must also acknowledge that we rely totally on our equipment while diving. </p>
	      <p>These safety hints apply to ALL dives, and should be read in addition to those hints for specific types of diving. </p>
	      <ul>
	        <li>
	          <div align="justify"><strong> Be trained by a recognised agency.</strong> Such training will make you aware of the more common problems you will face underwater, and how to reduce the likelihood of these problems occurring.	          </div>
	        </li>
            <li> 
              <div align="justify"><strong>Be medically assessed by a doctor who has appropriate training in diving medicine.</strong> Some medical conditions are not compatible with safe diving, while other conditions may allow you to dive safely with caution.              </div>
            </li>
	        <li>
	          <div align="justify"><strong> Thoroughly prepare and check your gear prior to diving</strong>. You rely totally on your equipment while underwater.	          </div>
	        </li>
	        <li> 
	          <div align="justify"><strong>Choose dives that match your training, experience and confidence.</strong> Always dive within your comfort zone. * Listen to your inner voice. If you do not feel right while underwater, or you feel that you have exceeded your comfort level, abort the dive.	          </div>
	        </li>
	        <li>
	          <div align="justify"><strong>When you first reach the bottom, establish neutral buoyancy, ensure your ears are OK, check your air status and your depth, tighten your weightbelt, then signal your buddy that you are OK.</strong> Make these actions a standard part of ALL dives.	          </div>
	        </li>
	        <li> 
	          <div align="justify"><strong>Watch your ascent rate on all dives.</strong> You should never exceed an ascent rate of 10m/minute when diving shallower than about 30m. .An ascent rate of 5-6 metres per minute is recommended in the last 10m of ascent.	          </div>
	        </li>
	        <li> 
	          <div align="justify"><strong>Complete safety stops on all dives that exceed 10m depth.</strong> Safety stops assist with reduction of excess nitrogen, which reduces the risk of DCI. They also slow your ascent rate, by forcing you to stop
	          for a period of time. The rule of thumb is 3-5 minutes at 5-6 metres. An additional deeper stop of 2-3 minutes at 10-15m appears to be beneficial after deeper dives.	          </div>
	        </li>
	        <li> 
	          <div align="justify"><strong>Always dive with a buddy. </strong>Your safety and your enjoyment will be enhanced by being with a companion while underwater.	          </div>
	        </li>
	        <li> 
	          <div align="justify"><strong>Plan your dive. </strong>You and your buddy should agree on depth, time, air cut-off, and safety stops.</div>
	        </li>
	        <li> 
	          <div align="justify"><strong>Plan your dive so you surface with a minimum of 50 bar.</strong> Don&rsquo;t look at it as wasted air, but as insurance against the possibility of some emergency that causes your air consumption to increase.	          </div>
	        </li>
	        <li>
	          <div align="justify"><strong> If you have had a layoff from diving, or you have been unwell, do some easier dives to regain 
              your confidence and skill. </strong></div>
	        </li>
	        <li> 
	          <div align="justify"><strong>Revise your skills regularly. </strong>Practise such survival skills as mask-clearing, regulator removal, and air- sharing regularly.	          </div>
	        </li>
	        <li>
	          <div align="justify"><strong> Log your dives. </strong>A record of your diving history may come in very handy should you ever seek higher levels of training.	          </div>
            </li>
	        <li>
