      <h1> DAN Medical Frequently Asked Questions: Hypertension </h1>
		      <p align="justify"><em><strong>Diver Question: </strong>My doctor tells me I have hypertension. Is it a problem for me to continue diving?</em></p>
	               
              <h6><strong>DAN Answer </strong></h6>
              <p>Hypertension, or high blood pressure, is one of the most common medical   conditions seen in the diving population - no surprise, really, since it   is a common medical condition in the general population. Strict   criteria for hypertension can vary depending on the reference cited, but   normal blood pressure is generally accepted to be a systolic pressure   below 140 and a diastolic pressure below 90 mm Hg, depending on age   (cited as systolic first and diastolic second - e.g. &quot;120 over 80,&quot; by   your doctor). A thorough medical evaluation should be performed to find a   treatable cause for hypertension; in most cases, however, none will be   found. <br />
                <br />
                Basically, two different sets of complications face a person with hypertension: short-term and long-term. <strong>Short-term complications</strong> are generally due to extremely high blood pressure; the most   significant is the risk of a stroke due to rupture of blood vessels in   the brain (called a cerebrovascular accident). <strong>Long-term detrimental effects</strong> are more common: they include coronary artery disease, kidney disease,   congestive heart failure, eye problems and cerebrovascular disease. <br />
                <br />
                <strong>Fitness and Diving Issue</strong><br />
                As long as the   individual's blood pressure is under control, the main concerns should   be the side effects of medication(s) and evidence of end-organ damage.   Divers who have demonstrated adequate control of blood pressure with no   significant decrease in performance in the water due to the side effects   of drugs, should be able to dive safely. <br />
                <br />
                A recent report in a diving medical journal citing several   episodes of acute pulmonary edema (i.e., lungs congested with fluid) in   individuals with uncontrolled hypertension while they were diving.   Regular physical examinations and appropriate screening for the   long-term consequences of hypertension such as coronary artery disease   are necessary. <br />
                <br />
                <strong>Medication Used in Treatment</strong><br />
                Mild hypertension may   be controlled with diet and exercise; however, medication is often   necessary. Many classes of drugs are used to treat hypertension, with   varying side effects. Some individuals must change medications after one   drug appears to be or becomes ineffective. Others might require more   than one drug taken at the same time to keep the blood pressure under   control. <br />
                <br />
              Classes of drugs known as beta-blockers often cause a   decrease in maximum exercise tolerance and may also have some effect on   the airways. This normally poses no problem for the average diver. ACE   (angiotensin converting enzyme) inhibitors are the preferred class of   drugs for treating hypertensive divers; a persistent cough is a possible   side effect.
                Calcium channel blockers are another choice, but   lightheadedness when going from a sitting or supine position to standing   may be a significant side effect.</p>
              <p>                Diuretics are also frequently used   to treat hypertension. This requires careful attention to hydration and   electrolyte status. Most anti-hypertensive medications are compatible   with diving as long as the side effects experienced by the diver are   minimal and their performance in the water is not significantly   compromised. Any diver with long-standing high blood pressure should be   monitored for secondary effects on the heart and kidneys. </p>
              <p><em>Answer provided by Dr. James L. Caruso</em><br />
