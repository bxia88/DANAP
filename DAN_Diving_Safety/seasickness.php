      <h1>Seasickness</h1>
		      <p align="justify"><em><strong>Diver Question: </strong></em></p>
	               
              <p><em>Every time we go on  diving trips, my girlfriend has to take anti-seasickness pills to avoid  vomiting during boat rides. These pills cause drowsiness and I was wondering if  it is safe to dive under such conditions? Most doctors in clinics tell me that  she should avoid heavy work because she is feeling drowsy. Is it dangerous to  go diving when feeling drowsy? What are other options available to  present seasickness?</em></p>
              <h6><strong>DAN Answer </strong></h6>
              <p>Seasickness is  a great menace, especially for scuba divers. Diving demands a clear hear and  feeling nauseous in the water is distracting and unsettling. Vomiting causes  dehydration, which increases fatigue and the risk of decompression illness.  Vomiting in the water with a regulator in the mouth can result in blockage of  the regulator, inhalation of water and anxiety, which can lead to panic. It is  advisable not to go diving if suffering seasickness.</p>
              <p>                To avoid seasickness I recommend taking promethazine, a sedating  antihistamine, the night before diving. Its anti-nausea effects last longer  than the sedative effect. On the morning of diving, I then recommend taking hyoscine  an hour before going out on the boat. This is generally well-tolerated but can  cause drowsiness, dry mouth and blurred vision. It is important for each diver  to trial these medications well before going on a diving trip to ensure that  one’s individual reaction is not excessive.</p>
              <p>Any diver  using these medications should avoid deeper diving as such medications can  enhance the effects of nitrogen narcosis. It is important to be aware of this,  and ascend to shallower depths if narcosis becomes apparent.</p>
              <p><em>Answer provided by Dr John Parker, who is  a Diving physician and Senior Dive Medical Consultant for DAN Asia-Pacific.</em></p>
