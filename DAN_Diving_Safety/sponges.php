      <h1>Sponges </h1>
		      <p align="justify"><em><strong>Diver Question </strong></em></p>
	               
              <p><em>On  a recent dive trip I was not wearing gloves and I touched a vase sponge. The  next morning I woke up with my hand swollen and itching. I wasn't sure what  happened. Everyone I asked had a different theory. One person suggested it was  from the sponge. I didn't think sponges were harmful. Can sponges really cause  a rash?</em></p>
              <h6>              DAN Answer:  </h6>
              <p>Most sponges  are harmless and not associated with human illness. However, unprotected  contact may result in an itchy rash which can develop within as little as 10 to  20 minutes or after a few hours following contact and may take several weeks to  resolve {1}. Symptoms can include itching, burning, localized swelling,  blister formation, desquamation (shedding of outer skin layers) and rarely  systemic manifestations to include fever, chills, malaise, nausea, vomiting and  dizziness {1,10}. Fortunately most skin symptoms are relatively mild  and are similar to dermatitis following contact with other mildly toxic marine  animals.</p>
              <p> Skin symptoms may result from allergic, sting or spicule-induced reactions and  differentiating between these potential sources is often difficult {1}. <br />
                <br />
                Skin reactions can result from contact with organisms that colonize the sponge  (sponge diver’s disease) – the principal culprits being Cnidarians (polyps),  bristle worms and other Echinoderms {7}. There are a few species linked  to severe reactions and include the Hawaiian or West Indian Fire Sponge,  Tedania ignis; and the poison Bun Sponge, Fibula (or Neofibularia) nolitangere {2,5,6}.  Symptoms and signs of contact are more likely when the area affected has pre-existing  skin lesions, open wounds and when contact is made with the silica or calcium  carbonate spicules embedded within the sponge’s structural tissue {4,5,6}. <br />
                <br />
                <strong>First aid</strong></p>
              <p>- Gently dry the affected area and remove any foreign  material. Spicules should be removed, if possible, with adhesive tape, thin  layer of rubber cement or products used for facial peels {6,8,9}.<br />
                - Soak the affected area with 5% acetic acid solution  (vinegar) for 10 to 30 minutes 3 to 4 times a day {6,8,9}.<br />
                - Clean the affected area with soap and water and keep the  area clean, dry and well-aerated.<br />
                - Leave blisters intact and allow them to dry out naturally.<br />
                - Monitor for signs of infection.<br />
                - Steroid ointments may prove useful in reducing skin  irritation.<br />
                - Tetanus vaccination/booster is often recommended {3}.<br />
                - Other medical management may include corticosteroids,  antihistamines and other medications used for severe reactions.</p>
              <p><strong>Prevention</strong><br />
                - Avoid direct contact with sponges. Sponges should not be  handled with bare hands. Dry sponges may still remain toxic or regain toxicity  upon rehydration {1}.<br />
                - Those providing 1st aid, should wear protective gloves to  minimize the risk of skin irritation from stings or spicule contact {1}.<br />
                </p>
