      <h1>Case Summary: Savvy Diver Prevents Uncontrolled Ascent</h1>
		      <h5>An experienced diver with an underwater scooter saves an unprepared   diver who overbreathed his regulator and attempted an emergency ascent.</h5>
		      <h2>Reported Story </h2>
		      <p>I regularly dive with a small group of experienced divers off a   privately owned boat. We are part of a group on Meetup.com, and   occasionally others join us for a dive. This time a diver with advanced   open-water (AOW) certification and about 30 dives joined us for an   after-work dive on the <em>Jim Torgerson (RSB-1)</em> wreck, which sits at   a depth of 36.5m. Visibility was 3-15m. The current   was strong enough that some divers familiar with this site complained   about it. We all wore wetsuits and breathed 32 percent nitrox on open   circuit.<br />
		        <br />
		        The new diver was struggling as he went down along the   anchor line with his buddy. I, along with several other divers, followed   shortly after. A few minutes into my dive, I heard a rapid tank   tapping, and another diver pointed to the new diver ascending from the   wreck far from the anchor line. He was buoyant and gaining speed. I was   diving with a SS Minnus dive propulsion vehicle (DPV, or underwater   scooter) and was able to quickly ascend from 27.5m to 20m and bring   him back to the anchor line at 27.5m. If I had not grabbed him, he   would have done an uncontrolled ascent and surfaced in current, unable   to get back to the boat on his own. On the anchor line, other divers   came over. We communicated that I'd take him up and that the other   divers would finish their dives.<br />
		        <br />
		        I had him hold at 27.5m to give   him time to settle down and for our bodies to have some time to adjust   to the rapid ascent and decent that we had just done. I also had him   switch to my long hose in the belief that my high-end regulator would be   easier to breathe from as he worked out the CO2 hit. He had about 1000 psi left, but I also wanted to save his gas for getting him back on the boat.<br />
		        <br />
		        We   worked up the anchor line, taking it extra slow and doing an extra-long   safety stop before I put him back on his regulator. By the time we were   ready to surface from the safety stop he looked much better, although   he had thrown up through the regulator during the safety stop. When we   made it back on the boat, I made sure there were no medical issues other   than seasickness and did a long assessment. <br />
		        <br />
		        We agreed that   despite his AOW certification and having 30 dives over the past two   years, he needed to gain more experience and work his way down to a dive   like this gradually. He is prone to seasickness, which was a   contributing factor along with poor buoyancy and not wearing gloves. He   should have communicated early on when he started to have problems.<br />
		        <br />
      Several   other divers saw him in distress from the wreck, but neither his dive   partner nor any of the other divers could get to him once he started   ascending. If not for having a powerful DPV, I would not have been able   to do anything either. I took into consideration the fact that I had   just started my dive, it was my first dive of the day and that quick   ascents deeper than 18.4m are less dangerous than close to the   surface. I am a technical diver with about 370 dives AN/deco and   studying trimix. </p>
              <h2>Comments</h2>
              <p>This   was a typical case of a diver diving beyond his training and   experience. Informal dive groups are likely to overlook this issue   because of respect for autonomy and freedom. While each diver is   responsible for himself, everybody in the group would be affected in   case of a dive accident; thus, divers in the group have the right to   check each other's competency. It is not impolite; it is a part of the   safe dive culture.<br />
                <br />
                Buddy systems among divers who are strangers   to each other often fail. It happened in this case, too. For the buddy   system to work, divers need to establish rules before the dive, be   comfortable about the other diver's skills and agree to a common dive   plan. When diving in a strong current such as in this case, a buddy   check at surface before descent is often impossible. Thus, it is more   important to have a good predive conversation and complete   cross-checking.<br />
                <br />
                Another diver in this group most likely saved the   life of this inexperienced diver. The rescuer probably would not have   succeeded without using the scooter, which enabled him to catch up with   the ascending diver before he got too shallow and bring him back to a   safe depth. An exerted diver experiencing an emergency ascent often ends   up with lung barotrauma, arterial gas embolism and drowning.<br />
                <br />
              Experienced   divers in informal groups should not keep to themselves. Instead, they   should volunteer their advice for the good of the entire group.</p>
