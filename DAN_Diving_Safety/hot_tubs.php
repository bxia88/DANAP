      <h1> Hot Tubs </h1>
		      <p align="justify"><em><strong>Diver Question: </strong></em></p>
	               
              <p><em>Is it safe to go into a hot tub or jacuzzi after diving? Is it also safe to take a hot shower?</em></p>
              <h6><strong>DAN Answer </strong></h6>
              <p>Getting into a hot tub immediately after diving does alter decompression   stress. As with many factors, the net response can be positive or   negative depending on the magnitude of the inert gas load and the heat   stress. A cold diver will have impaired peripheral circulation. The hot   tub (or hot shower) will warm the extremities and restore circulation   faster. If the inert gas load is small, this will facilitate and   increased rate of elimination because of the improved blood flow   (perfusion-based benefit). Larger inert gas loads can produce more   problematic responses. Since the solubility of gas is inversely related   to temperature, tissues will hold less in solution as they warm. Warming   tissue with significant loads can promote bubble formation. Since the   warming of the superficial tissues precedes the increase in blood flow,   such bubbles can become problematic before the circulation can remove   them harmlessly. <br />
                <br />
      There is no simple formula to compute what constitutes a minor,   significant or substantial peripheral inert gas load. The actual   conditions vary as a function of the individual, thermal protection,   physical activity and dive profile. Accepting the difficulty of   computation, I encourage a simple rule of thumb - delayed gratification.   Enjoy the thought of the hot tub or shower for a while instead of   jumping in immediately. The period of delay will likely be driven by   human nature. Those unwilling to wait will likely jump in regardless.   These would be the best candidates to practice more conservative dive   profiles. Those with more restraint may delay five to 30 minutes with   slightly less concern over the dive profiles. Another compromise would   be to employ a lower hot tub/show temperature. Much comes down to the   thoughtfulness of the diver. Decompression safety, as with many things,   is a matter of balancing strings of decisions so the net outcome is in   your favor. My approach is to stack as many factors as feasible in my   favor to compensate for the Murphy effect or chance that we see   frequently in decompression sickness.</p>
              <p><em>Answer provided by</em> <em> Neal W. Pollock, Ph.D</em><br />
