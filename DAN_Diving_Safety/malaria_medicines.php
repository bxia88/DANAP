      <h1>Malaria Medicines and Diving </h1>
		      <p align="justify"><em><strong>Diver Question: </strong></em></p>
	               
              <p><em>My husband and I are planning a trip this fall  to Papua New Guinea, where we expect to enjoy several days of diving and a  couple days of trekking through some heavily forested areas. We've been reading  about recommendations for anti-malaria medications. Are these drugs really  necessary?</em></p>
              <h6><strong>DAN Answer </strong></h6>
              <p>Malaria is indeed endemic to   Papua New Guinea, and the serious health risk posed by this disease   cannot be overstated. Anti-malarial drugs, as prescribed by your   physician, are a vital part of an overall plan to prevent infection   whenever you are traveling in a known malarial zone. <br />
                <br />
                The malaria parasite, a protozoan of the genus Plasmodium, is   transmitted to humans by the bite of an infected Anopheles mosquito,   usually between dusk and dawn. After an incubation period of 13-17 days,   symptoms develop; they are characterized by an abrupt onset of chills,   shivering, sweating and high fevers. Fevers may occur daily until   settling into a cyclical pattern. <br />
                <br />
                Remember the ABCD's of malaria prevention:<br />
      </p>
              <ul>
                <li>Awareness (Understand the risk and severity of malaria.)<br />
                </li>
                <li>Bite prevention (Reduce the risk of malaria through appropriate precautions.)<br />
                </li>
                <li>Chemoprophylaxis (Work with your doctor to select the most appropriate drug, and<br />
                  complete the course of pills.)<br />
                </li>
                <li>Diagnosis (Understand the signs and symptoms of malaria; should they occur, seek<br />
                medical help immediately.)</li>
              </ul>
              <p>Discuss your travel plans with your physician well before   departure. Many anti-malarial drugs require that you begin taking them   days, sometimes weeks, before departure. The drug options will vary   according to the strain of malaria found in your destination. All the   drugs used for malaria prophylaxis are considered safe and are generally   well tolerated, but side effects and toxic reactions can occur — some   of them serious. Nonetheless, the risk of malaria may warrant tolerating   temporary side effects. If a diver is unable to take a preventive   medication because of side effects, the trip should be canceled or the   medication changed to ensure coverage; the risk of contracting malaria   is simply too great. </p>
              <p> There have been no studies conducted on malaria medication use   while diving. In the absence of scientific data, we recommend caution   and stability on the drug before attempting any dive. Divers should also   note that the potential side effects of the anti-malarial drug   mefloquine (sometimes sold under the brand name Lariam) may mimic   symptoms of decompression illness. </p>
              <p> While drug prophylaxis is vital, it's even more important to   prevent the insect bites that transmit the disease. The use of   prescribed medication without complementary bite prevention through the   use of clothing and insect repellents is inadequate; it's like driving   without seatbelts while thinking your airbags will suffice in the event   of an auto accident. </p>
              <p> To avoid mosquito bites, stay in well-screened areas while   indoors, and wear clothes that cover most of the body when outdoors. Use   mosquito nets for sleeping, and when you go outside use insect   repellent containing at least 30 percent DEET. Stronger concentrations   can be used with caution. Permethrin, a synthetic chemical available in   over-the-counter strengths of 1 percent and 5 percent, is also commonly   used to treat clothing and mosquito nets. Use these with care and   according to manufacturers' instructions. </p>
              <p> Even with aggressive preventive means, it is still possible to   contract malaria. Symptoms may develop well after the trip is over, so   follow your physician's advice, and finish the complete course of   anti-malarial drugs prescribed. If flu-like symptoms (any illness with   chills, fever and headache) develop during a trip within a   malaria-endemic area or within several months after the last exposure,   seek qualified medical attention immediately.</p>
              <p><em>Answer provided by Daniel A. Nord, BFA, EMT-P, CHT, 
                Director of DAN Medical Service</em><br />
                </p>
