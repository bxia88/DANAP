      <h1>Exercises to Improve Body and Brain Power</h1>
      <p><em>By Jessica Adams, Ph.D., and Stephanie Deltufo, B.S, Photos by Stephen Frink. </em></p>
      <p align="center"><img src="../_images/Exercise_BodyBrain1.jpg" width="560" height="554" /></p>
<p align="justify">Exercise is a powerful tool to optimize brain function and your overall   scuba performance. Scientists used to believe that adult neurons   couldn't regenerate. Little did they know that aerobic exercises such as   the ones described here encourage the development of new neurons. In   fact, even people with dementia, a neurodegenerative disorder, have   shown a 34 percent decrease in cognitive degradation following   cardiovascular exercise three times a week.<br />
  <br />
  Complex aerobic   exercise increases cognitive flexibility and interneuron communication.   This is important for executive brain function, which allows the shift   of attention and the ability to produce creative problem-solving   strategies. Open-water diving takes place in a dynamic environment.   Improved cognitive flexibility may allow divers to recognize more   quickly any changes in their environment and to come up with novel   solutions to adapt to those changes. For a diver, this maximizes dive   efficiency by increasing the connections between memories of learning to   dive, previous dive experiences and muscle memory. This efficiency   allows a diver to respond to a challenging situation more quickly.<br />
  <br />
  The   following complex exercises should be completed in sequence with little   rest in between. Your ultimate goal is to maintain an elevated heart   rate for at least 30 minutes to achieve optimal cognitive and physical   benefits. Start with 10 minutes, and gradually increase the duration as   your exercise tolerance builds. Many of these movements have multiple   steps, so be patient. The sequences are designed to challenge your   coordination, improving visual and motor integration.</p>
