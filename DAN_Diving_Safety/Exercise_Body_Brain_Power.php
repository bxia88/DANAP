<h2 align="justify">Overhead Cross Jacks</h2>
<p align="center"><br />
  <img src="../_images/Exercise_BodyBrain2.jpg" width="598" height="315" /></p>
		      <p align="justify">This is similar to a traditional jumping jack, but arms go up only one   at a time, and when legs come together, they crisscross, alternating   which foot is placed in front of the other. Notice that both the hands   and feet cross the midline of the body while your arms are doing   different movements bilaterally.</p>
		      <p align="justify"><strong>Form:</strong></p>
		      <ol>
		        <li>Start with hands at your sides.</li>
		        <li>Jump both feet apart, swinging one arm low behind your buttocks and one arm high above your head.</li>
		        <li>Jump both legs in, crossing one in front of the other. Reverse arm position.</li>
		        <li>Jump both legs out.</li>
		        <li>Continue for 30 seconds. Once you have mastered the movement, see how many repetitions you can complete in 30 seconds.</li>
      </ol>
		      <h2 align="justify"><a href="https://www.onlineexambuilder.com/cardiovascular-health/exam-38346"></a>Lateral Squat with Cross</h2>
		      <h6 align="center"><img src="../_images/Exercise_BodyBrain3.jpg" width="598" height="313" /></h6>
		      <p align="left">Sit back into the squat position, <em>keeping your weight through your heels and eyes toward the sky.</em> Try to get as deep into the squat as you feel comfortable. Your range   of motion will naturally increase with each workout. Make sure you focus   on form. Complete 20 repetitions to each side.</p>
		      <p align="left"><strong>Form:</strong></p>
		      <ol>
		        <li>Start with your feet shoulder-width apart and arms straight at your sides.</li>
		        <li>Step laterally (to the side) as far as you can, and squat down as low as you can. Don't step or squat beyond what's comfortable.</li>
		        <li>Simultaneously, bring your arm across your chest. If you are squatting to the right, bring your right arm across your chest. </li>
		        <li>Return to starting position, and repeat on the opposite side.</li>
		        </ol>
		      <h2 align="left">Burpee to Side Plank</h2>
		      <p align="center"><br />
		        <img src="../_images/Exercise_BodyBrain4.jpg" width="294" height="602" /></p>
		      <p align="center"><img src="../_images/Exercise_BodyBrain5.jpg" width="596" height="152" /></p>
		      <p align="left">Try to hold each plank position for as long as you can while maintaining   a straight body. Work up to holding each plank pose for 30 seconds.   Complete 10 repetitions to each side.</p>
		      <p align="left"><strong>Form:</strong></p>
		      <ol>
		        <li>Start standing with both arms extended above your head.</li>
		        <li>Squat down, placing your hands on the floor.</li>
		        <li>Step or jump your feet back until you are in plank position.</li>
		        <li>Transfer weight onto one hand in side plank position.</li>
		        <li>Transfer weight back to plank.</li>
		        <li>Jump feet in and stand up, raising arms overhead.</li>
		        <li>Repeat on opposite side.</li>
      </ol>
		      <p align="left"><br />
	          <em><strong>DAN Note</strong></em><strong>:</strong> To avoid an increased risk of decompression sickness, DAN® recommends   that divers avoid strenuous exercise for 24 hours after making a dive.   During your annual physical exam or following any changes in your health   status, consult your physician to ensure you have medical clearance to   dive.</p>
