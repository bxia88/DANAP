      <h1>Does  your physical fitness level affect your risk of getting bent?</h1>
      <p>Poor medical  and physical fitness can compromise your safety in general and may increase  your risk of DCS. Definitive data are limited, but there is no question that it  is prudent to maintain a high level of physical fitness and to dive  progressively more conservatively as your fitness level declines. Safe diving  is possible throughout much of a normal life span, but it is important for all  divers to seek regular, objective evaluation of their capabilities and to adapt  their diving practices accordingly. But even for divers who have transitioned  from independent to more dependent forms of diving, in which they increasingly  rely on the support of others, there will ultimately be a point at which they  should hang up their fins.      </p>
      <p align="center"><img src="../_images/Exercise_BodyBrain1.jpg" width="560" height="554" /></p>
      <h2><strong>Physical Activity Recommendations</strong></h2>
      <p>        Adults need two types of regular activity to maintain or improve their  health—aerobics and strength training. At   least two and a half hours a week of moderate-intensity aerobic exercise is recommended to  achieve health benefits, and five hours a week to achieve additional fitness  benefits. And just as important as engaging in aerobic exercise is doing  muscle-strengthening activities at least two days a week.<br />
        <br />
        While good health and physical fitness will not solve all problems, the  foundation is an important one. An adequate physical reserve can allow a quick  response to keep a small problem from becoming a serious one. Relevant  scenarios can be easily imagined for almost any dive.<br />
        <br />
        Regular aerobic exercise has many positive benefits. Cardiac reserve is the  difference between the rate at which the heart pumps blood at rest and its  maximum capacity. An increase in this reserve may make it easier to meet the  physical demands of diving activity and stress. Blood values of cholesterol can  improve, reducing susceptibility to heart disease. Insulin sensitivity can  improve, reducing the risk of developing diabetes. While the data specific to  diving are much more preliminary, there is also some evidence that higher  levels of aerobic fitness may contribute to a reduced decompression stress.<br />
        <br />
        Most individuals are aware that being fit can improve quality of life. A major  problem, however, is that time takes a toll on us. The ease with which we  maintain our fitness level in our 20s can be very different from the reality as  decades pass. Aerobic fitness typically declines on an average of one percent per  year after age 30. The important point is that while some decline may be  unavoidable due to a gradual loss of muscle mass and a reduction in the  metabolic activity of aging muscle, the rate can be slowed and the reserve  range broadened by adopting healthy lifestyles as early as possible.<br />
        <br />
        The physical fitness needed for diving will vary with the demands of the  environment, the equipment, and the nature of the dive. The best strategy is to  incorporate regular physical activity into your life to improve or preserve  your capabilities, and to prolong your diving life. Do not count on diving to  keep you physically fit. If done properly, it should be your relaxing time in  the water. To maintain or build aerobic capacity and strength, swim, cycle,  run, or do whatever other physical activities you can enjoy. The more fit you  are, the longer you get to play. </p>
