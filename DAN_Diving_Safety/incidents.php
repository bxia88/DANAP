          <h2><strong>Who is the Divers Alert Network (DAN)?</strong> </h2>
          <p align="justify">            For  scuba divers worldwide, DAN means safety, health and peace of mind. DAN are the  experts in diving accident management. The organisation is committed to  improving the safety of diving for all divers. DAN strives to achieve this goal  via activities that include providing Worldwide Emergency  Evacuation Coverage and optional Dive Injury Insurance Services for Members; funding  and/or manning 24-hour diving emergency hotlines  throughout the region; offering non-emergency diving  medical advice, accident management training; and undertaking  data collection and research to enhance dive  safety. <strong>www.danasiapacific.org</strong>. </p>
			
