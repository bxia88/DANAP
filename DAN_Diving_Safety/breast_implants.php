      <h1>Breast Implants  </h1>
		      <p align="justify"><em><strong>Diver Question: </strong></em></p>
	               
              <p><em>I  have breast implants. I would like to learn to dive, but am afraid of what the  pressure will do to the implants. Are there any studies on this?</em></p>
              <h6><strong>DAN Answer </strong></h6>
              <p>Silicone and saline implants are used for cosmetic enhancement or   augmentation of the normal breast size and shape of reconstruction,   particularly after radical breast surgery for cancer or trauma.</p>
              <p>                In   one study, by Dr. Richard Vann, Vice President of Research at DAN,   mammary (breast) implants were placed in the Duke University Medical   Center hyperbaric chamber. The study did not simulate the implant in   human tissue. Three types were tested: silicone-, saline-, and   silicone-saline-filled. In this experiment, the researchers simulated   various depth/time profiles of recreational scuba diving.</p>
              <p>                Here's what they found:</p>
              <p>                There   was an insignificant increase in bubble size (one to four percent) in   both saline and silicone gel implants, depending on the depth and   duration of the dive. The least volume change occurred in the   saline-filled implant, because nitrogen is less soluble in saline than   silicone.</p>
              <p>                The silicone-saline-filled type showed the greatest volume   change. Bubble formation in implants led to a small volume increase,   which is not likely to damage the implants or surrounding tissue. If gas   bubbles do form in the implant, they resolve over time. <br />
                <br />
                <strong>Fitness and Diving Issues</strong><br />
                Once sufficient time has   passed after surgery, when the diver has resumed normal activities and   there is no danger of infection, she may begin scuba diving.
                Breast   implants do not pose a problem to diving from the standpoint of gas   absorption or changes in size and are not a contraindication for   participation in recreational scuba diving.
                Avoid buoyancy   compensators with constrictive chest straps, which can put undue   pressure on the seams and contribute to risk of rupture. <br />
                <br />
                <strong>Additional Considerations</strong><br />
                Breast implants filled with   saline are neutrally buoyant. Silicone implants are heavier than water,   however, and they may alter buoyancy and attitude (trim) in the water,   particularly if the implants are large. Appropriate training and   appropriate adjustment of weights help overcome these difficulties.<br />
              </p>
              <p><em>                Answer provided by the DAN Medical Team.</em></p>
