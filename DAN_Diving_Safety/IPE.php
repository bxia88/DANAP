      <h1> Immersion Pulmonary Edema </h1>
		      <p align="justify"><em><strong>Diver Question: </strong></em></p>
	               
              <p><em>I </em> <em>was  recently diving and while at depth I began to have a hard time breathing. I  made a controlled ascent and began coughing up pink frothy fluid. I was taken  to the hospital. The doctor there insisted it was a near drowning. I did not  inhale any sea water. I think it was something else.</em> <em>Can you help?</em> </p>
              <h6><strong>DAN Answer </strong></h6>
              <p>Originally, IPE was thought to occur almost exclusively in  cold water. While this condition seems to be more common in cold water, it has  also been reported in warm waters.</p>
              <p> During immersion in water, blood is redistributed from the  legs to the heart and blood vessels in the lungs. Usually the heart and lungs  compensate for this, but sometimes the resulting increased 
                pressure within the blood vessels in the lungs causes fluid to traverse the  small vessels (capillaries) and enter the gas containing spaces of the lungs. 
              Sometimes this is caused by an underwater myocardial  infarction (heart attack), abnormalities of the heart muscle or heart valves or  hypertension. Usually there is no obvious cause. </p>
              <p>                Cold water may be a predisposing factor because immersion  can cause the small arteries to constrict, increasing the resistance to the  flow of blood. Vasoconstriction in response to cold can be
              exaggerated in people with hypertension or those likely to develop it. But some  people who experience IPE are young and healthy, even military recruits.</p>
              <p>                Immediate treatment should include breathing oxygen while in  transit to a hospital. Some IPE cases have been fatal, although most cases  resolve on their own during observation in a hospital. A closer 
              examination may reveal hypertension, a heart attack, cardiac valve disease or impaired  contraction of the heart.</p>
              <p>                Divers who have experienced IPE are concerned with the  likelihood it will recur. Unfortunately, that likelihood is not known, so many  physicians recommend that people with a history of IPE not 
                return to diving. Still others recommend taking an antihypertensive medication before  each dive. Before considering a return to diving after IPE, DAN recommends the  diver consult with a doctor 
                knowledgeable about the condition.<br />
                <br />
                <em>Answer provided by</em> <em> the Divers Alert Network Medical Team </em><br />
