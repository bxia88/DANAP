      <h1> Inter-Island Flights </h1>
		      <p align="justify"><em><strong>Diver Question: </strong></em></p>
	               
              <p><em>While vacationing in St. Maarten, I plan to do a  day trip to Saba, with the intention of doing 3 dives. I would fly out in the  morning, returning in the evening. The first dive would be at 27m to 40m, and the two subsequent dives at 21m to 24m or  shallower. The dive operator has assured me that it would be safe to fly back,  that same day, as the aircraft would only reach an altitude of 2000 ft (610m).  for a few minutes during the 12 minute return flight. Will it be safe to fly  under these conditions and is 2000 ft. (610m) the cut-off altitude for flying  after diving? </em></p>
              <h6><strong>DAN Answer </strong></h6>
              <p>You are asking about a pattern of exposure for which there is very   limited empirical data. The guidelines in this low altitude range are   cobbled together from a combination of expert opinion and whatever   research can reasonably be applied. Knowing that there is a high   likelihood of some degree of vertical travel post-dive (with no huge   problem being identified) and appreciating the implications of research   data from  Switzerland based on 1700 ft exposures, the 2002 Divers Alert   Network flying after diving guidelines stipulated a cabin altitude   within the 2,000-8,000 ft range. Lesser altitudes are excluded.   Alternatively, the US Navy flying after diving table (9-6) begins with   an altitude table of 1000 ft although it only applies minimum surface   interval penalties for high repetitive groups. <br />
                <br />
                While the above may seem promising, the dives you propose to do   could easily achieve extremely high post-dive repetitive groups. The   remaining wild cards are the flight altitude and duration. I believe   that the inter-island flights in that region are frequently below 2500   ft, below 2000 ft may be common but certainly not guaranteed. Weather   and traffic conditions might well prolong flight time or result in a   higher flight altitude and/or a lower atmospheric pressure for any given   altitude. The person giving you the advice was describing an optimal   case which may or may not be realistic. <br />
                <br />
                Ultimately, the overall risk in the day you describe is probably   fairly small, but it is certainly not a plan I would encourage or   choose to do for recreation. The 'cutoff' guaranteeing safety that you   were hoping for simply does not exist. The net risk is determined by   multiple factors - those directly concerning the diving (depth, time,   thermal stress, exertion level, post-dive surface interval),   environmental conditions (weather, atmospheric pressure, geography),   pilot discretion, other air traffic, and individual susceptibility. You   are planning to stack enough variables against safety that a few other   factors going in the wrong direction could end up producing a bad day. <br />
                <br />
                Safety margins could be added through the use of nitrox with   a decompression algorithm set to air (but you would have to be very   cautious in respecting the maximum limits of the mix in your deeper   dives) and/or diving more conservative profiles and/or delaying the   return time. The problem with planning such an ambitious day, though, is   that, once set in motion it is hard to stop. And, again, a compromise   here or there or in any number of ways could easily end up producing in a   bad day. <br />
                <br />
              Ultimately, you have to decide what risk you are willing to   assume. If you are not diving alone, you partner or partners would have   to make similar decisions. In thinking of this, remember that   decompression insults tend to be probabilistic events. Getting away with   something once, twice or ninety-nine times does not make it safe. My   advice is to focus on all the ways to increase your margin of safety so   that good habits can make diving as worry-free as an enjoyable activity   should be.</p>
