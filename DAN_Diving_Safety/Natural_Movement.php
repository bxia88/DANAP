      <h1>Natural Movement</h1>
      <p><em>By Jessica B. Adams, Ph.D</em></p>
      <p>A variety of natural-movement exercises have recently entered mainstream   fitness. In the fitness industry the exact definition of <em>natural movement</em> ranges from activities and movements that occur between the body and   the natural world to multijoint exercises that can be accomplished using   traditional weights.<br />
        <br />
        Most natural-movement professionals agree   on a scientifically sound premise: The exercises require that several   large muscle groups work together to perform a task through a full range   of motion. Most exercises are completed at various levels in   three-dimensional space (in contrast to the single-plane movements of   traditional weight machines). Large muscle groups working together   performing movements in multiple planes is inherently life-specific   training. This sort of training prepares your body for the activities of   daily living — in addition to the joys of the dynamic underwater world.<br />
        <br />
        Many   of these movements may feel anything but natural at first. The   exercises are natural only in the sense that your body once had —   perhaps many years ago — the aptitude to develop these skills. The   unfortunate truth is that in modern society few of us have the   opportunity to physically reach our full potential unless we take the   initiative to prioritize our fitness.<br />
        <br />
Train   smarter, not harder. The images shown in this article illustrate a   substantial range of motion for each exercise. Do not try or expect to   reach this range during the first day, week or even month of doing the   exercise. Full range of motion is a long-term goal. The key is to be   patient, pay attention to your body and gradually increase mobility.   Your muscles work together to develop strength through an increasing   range of motion, which amounts to improved flexibility. It is not smart   to &quot;push through the pain&quot;; gradual progression is a fitness principle   that yields long-term results with minimal risk. If at any point you   feel pain, take a break, reassess and adjust your form, or move on to a   different exercise.<br />
<br />
Attempt to complete the stated number of   repetitions for three or four rounds, depending on your training state   and the amount of time you have available. Remember, every repetition   counts, so if you have time only for a single round, that's better than   nothing. Initially you may need breaks between rounds, but as your   training progresses, you will complete the exercises through a greater   range of motion at a faster (yet still controlled) pace with less rest   in between.<br />
<br />
The exercises purposely alternate between low and   high levels to make you get up and down between exercises. You may feel   slightly light headed when you transition from low to high, so   transition slowly until you feel comfortable. Focus on how you feel, and   always listen to your body.</p>
      <h2>Deep Air Squats (20 repetitions) </h2>
      <p>&nbsp;</p>
      <p align="center"><img src="../_images/Nat_Move1.jpg" alt="DAN Safety Campaign" width="560" height="524" /></p>
		      
		      <p align="justify">These are traditional body-weight squats. A full-range deep squat is   shown, but your full-range squat may be just a quarter of that range of   motion. Descend as low as you feel comfortable. If you pay attention to   your body, each time you do the routine your comfortable range of motion   will slowly but surely increase.<br />
		        <br />
		        1.	Start in an athletic stance with your feet shoulder-width apart (or slightly wider) and your toes pointing slightly outward.<br />
		        2.	Flex (bend) your knees and hips simultaneously while consciously pushing your rear end back and down.<br />
		        3.  	Keep your eyes toward the horizon, and push through your heels to   minimize stress on your knees while maintaining the natural curve of   your spine.<br />
		        4.	Fully extend your hips through their full range of motion in a controlled manner at the end of each repetition.<br />
		        <br />
		        <em>Tips:</em><br />
		        <em>•  	Squats should not put pressure on your knees. If you feel any stress on   your knees, you probably need to lift your chest higher.</em><br />
		        <em>•	  Squats should not put pressure on your lower back. If you feel any   stress on your lower back, you are probably focusing your eyes too high   in the sky.</em><br />
		        <em>•	Adjust until each repetition feels right.</em></p>
		      <h2 align="justify">Bear Crawl (10 forward then 10 backward; repeat twice)</h2>
		      <p align="justify">&nbsp;</p>
		      <p align="center"><img src="../_images/Nat_Move2.jpg" alt="DAN Safety Campaign" width="560" height="345" /></p>
		      <p align="justify">The bear crawl is not just for kids. It is a total body exercise that   improves balance, flexibility and muscular strength throughout the body.<br />
		        1.	Begin in a quadruped (crawling) position with your hands and knees about shoulder-width apart.<br />
		        2.	Raise both knees slightly off the floor.<br />
		        3.	Move one hand and the opposite foot, keeping your hips and shoulders square with one another.<br />
		        <br />
		        <em>Tips:</em><br />
		        <em>•	Gradually increase the speed of the movement within your zone of comfort and control.</em><br />
		        <em>•	Try not to raise your hips too high.</em></p>
		      <h2 align="justify"><a href="https://www.onlineexambuilder.com/cardiovascular-health/exam-38346"></a> Travelling Squat (four steps forward then four steps back; repeat five times)</h2>
		      <h6 align="center"><img src="../_images/Nat_Move3.jpg" alt="DAN Safety Campaign" width="560" height="362" /></h6>
		      <p align="left">1.	Start in a low but comfortable squat position. Low is a relative term; listen to your body.<br />
		        2.	While holding this position, take four steps forward and then four steps backward.<br />
		        <br />
		        <em>Tips:</em><br />
		        <em>•  	You may not be able to go as low as is shown in the photo. A low squat   is a position in which your body feels comfortable. It's OK if your   thigh muscles are uncomfortable, but any stress on your knees or lower   back is unacceptable.</em><br />
		        <em>•	Keep your weight on your heels. (You should be able to wiggle your toes.)</em></p>
		      <h2 align="left">Crabwalk (10 backward; 10 forward; repeat twice)</h2>
		      <p align="center"><br />
		        <img src="../_images/Nat_Move5.jpg" alt="DAN Safety Campaign" width="560" height="751" /></p>
		      <p align="left">The crabwalk is not just for kids. This is a total body exercise.<br />
		        1.	Start in a seated position on the ground with your hands slightly behind you.<br />
		        2.	Lift your rear end off the ground.<br />
		        3.	Travel backward for 10 steps and then forward for 10 steps.<br />
		        4.	Challenge: Add a kick with each foot prior to each change of direction.<br />
		        <br />
		        <em>Tips:</em><br />
		        <em>•	Try to keep your hips up — this will make the movement more challenging.</em><br />
		        <em>•	Start slowly, and gradually increase speed as you become more comfortable with the movement.</em></p>
		      <p align="left"><em>DAN Note</em>: To avoid an increased risk of decompression sickness, DAN® recommends   that divers avoid strenuous exercise for 24 hours after making a dive.   During your annual physical exam or following any changes in your health   status, consult your physician to ensure you have medical clearance to   dive.</p>
