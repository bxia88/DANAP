      <h1> Healthy But Overweight</h1>
		      <p align="justify"><em><strong>Diver Question: </strong></em></p>
	               
              <p><em>I have a friend who wants to learn to scuba dive. Although he does   not have any major illnesses, he is overweight - he is 168cm's tall and weighs about 112.5 kilograms. He   is on a diet recommended by his doctor, does not take any medication and   he walks a mile or more every evening. Before I encourage him to dive,   what are the chances he can be approved for diving?</em></p>
              <h6><strong>DAN Answer </strong></h6>
              <p>Fitness for scuba diving is a complex issue. Certainly, excessive weight   can be a reason to restrict diving. <br />
                <strong><br />
                Consider two questions:</strong><br />
                1. Will an overweight individual suffer any ill health effects by diving? and <br />
                2. Will this individual be able to perform all of the necessary skills to dive successfully? <br />
                <br />
                There is no strong body of evidence to suggest that overweight   individuals have a greater risk of DCI or that they suffer more   dive-related injuries that divers who are within 10 percent of their   ideal body weight. Obesity by itself does not restrict diving. </p>
      <p> The best indicator of diving fitness is the individual's general   health and level of physical fitness. Keep in mind that divers who are   overweight can have a greater risk for cardiac incidents. Consider the   exercise regimen - or lack of it - in an overweight individual. Diving   requires a diver to lift and carry scuba equipment, swim both underwater   and on the surface. </p>
              <p> When evaluating a candidate for scuba, a dive physician will   consider these factors as well as the &quot;reserve&quot; factor: the increased   cardiovascular and respiratory response required when a sudden need   arises. In an emergency situation, unfit divers may end up in a   near-drowning or fatal dive incident.</p>
              <p> The second question considers an individual's ability to perform   self-rescue in the open water and assist a buddy. Both skills are vital   to the scuba buddy system. Will your partner's physical conditioning   and stamina allow him or her to provide assistance to you at the   surface? </p>
              <p> These are good questions to ask any dive buddy. Remember, it is   difficult to get a heavy person out of the water and onto the back of a   boat for cardiopulmonary resuscitation (CPR): you can lose vital   minutes. Morbidly obese individuals with no exercise tolerance can dive   in a tranquil sea: it's when things go wrong or when circumstances   change that they have to call upon their reserves. </p>
              <p> Fitness for diving depends on more than just one criterion like   weight. Physical fitness and ability are major factors to consider.   Other experiences in life don't always prepare a new diver for the   potential physical exertion he or she can encounter underwater. For   some, lacking physical fitness and endurance and being overweight may be   a barrier to dive training. Also, divers who have gained weight over   the years might now be considered unfit. These divers do have the   advantage of experience, however. Perhaps this can help them avoid   situations that can stymie new divers. </p>
              <p> Each individual is different, and that's the way diving fitness   decisions should be made. Individuals who want to learn to dive should   join introductory scuba programs through local dive stores: they could   see how they handle themselves with equipment in the water. Equally   important, they can get the opinion of a professional instructor who can   speak realistically about the physical abilities needed for diving. <br />
                <br />
                <em>Answer provided by</em> <em> Joel Dovenbarger, Vice President, DAN Medical Services</em><br />
      </p>
