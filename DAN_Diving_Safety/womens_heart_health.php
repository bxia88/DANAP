      <h1> Women's Heart Health </h1>
		      <p>Heart disease is the leading cause of death in  women, and myocardial infarction (heart attack) is the leading cause of their  hospitalization. The characteristics of the disease in women may differ from  those in men; the age of onset, presence of risk factors, probability of  aggressive diagnosis and even likelihood of appropriate treatment vary in men  and women.<br />
                  <br />
      For example, heart disease develops 7 to 10 years later in women than in men  (possibly because of the protective effect of estrogen). Myocardial infarction  is less frequent in young women than in young men, but young women who have a  heart attack are at greater risk of dying within 28 days of their attack. The  common risk factors for heart disease have a similar predictive value for men  and women; however, men more frequently have smoking as a risk factor, whereas  women more frequently have hypertension, diabetes, hyperlipidemia or angina.  Although women typically smoke less than men, the relative risk for myocardial  infarction in women who do smoke is 1.5 to 2 times greater than in men who  smoke, especially in those less than 55 years old. A higher prevalence of  diabetes also contributes to higher mortality rates from heart attacks among  women.</p>
		      <p>		        Women receive fewer advanced diagnostic tests such  as coronary angiography and fewer interventions such as coronary artery bypass  grafts. These differentials may be due to the fact that acute heart attacks are  likely to occur at an older age in women, or to the presence of other  associated diseases, but could also be due to delays in admitting women to the  hospital.<br />
                <br />
		        The symptoms of a heart attack in women are usually the same as those in men,  with chest pain (angina) being the leading symptom. However, women are more  likely to attribute their symptoms to acid reflux, the flu or normal aging. In  addition, the chest pain that women experience does not necessarily occur in  the centre of the chest or the left arm; instead, women may feel pressure in  their upper back — a sensation of squeezing or as if a rope is tied around  them.<br />
                <br />
		        Although 90 percent of women who suffer a heart attack later admit that they intuitively  knew that was the cause of their symptoms, at the time they often discount  them, attribute them to something else, take an aspirin or just delay calling emergency services. This decreases the opportunity to preserve their heart from damage and  lowers their chance of survival.</p>
		      <h2><strong>These are the most common  symptoms of a heart attack in women:</strong> </h2>
		      <p>- Uncomfortable pressure,       squeezing, fullness or pain in the centre of the chest; it lasts more than       a few minutes or goes away and comes back.</p>
		      <p>- Pain or discomfort in one or       both arms, the back, neck, jaw or stomach.</p>
		      <p>- Shortness of breath, with or       without chest discomfort.</p>
		      <p>- Other signs, such as       breaking out in a cold sweat, nausea or lightheadedness.</p>
		      <p>- As with men, women's most       common heart attack symptom is chest pain or discomfort — but women are       somewhat more likely than men to experience some of the other common       symptoms, particularly shortness of breath, nausea/vomiting or back or jaw       pain.</p>
