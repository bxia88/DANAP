      <h1>Air Usage </h1>
		      <p align="justify"><em><strong>Diver Question: </strong></em></p>
	               
              <p><em>I seem to run  out of air much faster than everyone else in my group. I thought it might be  because I was a light smoker, so I quit. I then thought maybe I wasn’t fit  enough, so I started working out at the gym to improve my cardio fitness. On my  most recent trip to the Similans, I dived with people that were either chain  smokers, overweight, or much older than me and I still ran out of air first.</em></p>
              <p><em>Are there any  exercises I can do to improve my breathing or could it be related to me being a  fairly new diver, and that I need to relax more? If so, are there any exercises  I could do to relax more?</em></p>
              <h6><strong>DAN Answer </strong></h6>
              <p>The  most common cause of running out of air quickly is anxiety. When we are  stressed our breathing rate increases and can even double so that our air will  only last half the time of others. Our anxiety may have several causes. Common  ones include:</p>
              <ul>
                <li>                Fear  that we are not using our equipment properly;</li>
                <li> Fear  of being judged by other divers as incompetent or inexperienced;</li>
                <li> Fear  of our equipment malfunctioning and we cannot breathe;</li>
                <li> Fear  of our masks flooding and panicking;</li>
                <li> Fear  of the unknown, of sharks/creatures grabbing hold;</li>
                <li> Fear  of being in the blue, out of sight of the bottom.</li>
              </ul>
              <p>Experience  will help overcome these common fears. It’s important to learn to relax  underwater, to concentrate on simply breathing regularly and not too deeply.  Don’t try to cover too much distance, dive deep, or take lots of photographs.  Plan on enjoying a small area, look at all the small details and life forms,  spend time in one spot. It’s important not to be concerned what others think.  Ask questions when in doubt. If others think you’re stupid it’s their problem,  not yours.</p>
              <p>                Another  cause of using lots of air is poor buoyancy control. If you’re too light or too  heavy you can waste a lot of energy and air just staying down or stopping  yourself from crashing into the bottom. If this is the case, get some  assistance to show you how to become and maintain neutral buoyancy.<br />
                Diving  is meant to be a relaxed and enjoyable activity where you can float around and  be in awe of the wonder of nature. Stop trying to do everything, go everywhere,  impress others. Then your dives will be longer and more fun. </p>
              <p><em>                Answer provided by Dr John Parker, who is  a Diving physician and Senior Dive Medical Consultant for DAN Asia-Pacific.</em></p>
