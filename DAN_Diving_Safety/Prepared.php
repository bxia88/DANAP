      <h1>Be Prepared </h1>
      <p><em>By Dan Orr</em></p>
      <p>Preparation for scuba diving begins long before you arrive at the dive   site. In addition to the skills required to enjoy the dive, it is   important to be in good health and prepared to meet the physical demands   of the sport.<br />
        <br />
In reviewing the data DAN Research has compiled on   dive accidents, almost one-third of the diving fatalities were   attributed to cardiac incidents; approximately half of these incidents   occurred in divers 40-59 years old. What is really disturbing is that 60   percent of those fatalities had signs and symptoms they or others   around them recognised as cardiac-related before or during the dive, but   they continued to dive anyway.<br />
<br />
DAN medics recommend all divers   get an annual physical from a physician familiar with diving medicine   beginning at age 35 or whenever there is any significant change in   health status. An annual physical can help identify heart-related issues   and corrective actions to take to reduce the likelihood of experiencing   a cardiac-related issue while scuba diving. If you suspect someone is   exhibiting signs or symptoms that could indicate a cardiac problem, call   the dive or notify the divemaster, trip leader or other authority.</p>
      <p align="center"><img src="../_images/DAN_Tank.jpg" width="340" height="284" /></p>
      <h2 align="justify"><a href="https://www.onlineexambuilder.com/cardiovascular-health/exam-38346"></a>Maintain Your Gear</h2>
      <p align="justify">Thorough preparation also involves making sure your equipment is   maintained properly and has been inspected annually by a qualified   maintenance technician. In addition, when travelling to a dive site it   is good to pack a &quot;Save-A-Dive&quot; kit that includes critical replacement   parts. The basic &quot;Save-A-Dive&quot; kit typically includes rubber goods   likely to break or wear out such as mask and fin straps, &quot;O&quot; rings and a   regulator mouthpiece with tie wraps. If any of your equipment requires   batteries, replacements are a good addition. Depending upon the type of   diving you do, the equipment you use and how remote your travel is, your   &quot;Save-A-Dive&quot; kit may need to be more extensive. If you are a technical   or rebreather diver, it would be wise to incorporate redundant   replacement parts for any critical or essential equipment.</p>
      <h2 align="left">Preventative Precautions</h2>
      <p align="left">In order to make sure you don't forget to pack everything you need, it   is wise to make a comprehensive checklist. This same checklist will also   be helpful when repacking at the end of your trip to ensure nothing is   left behind.<br />
        <br />
        Once you arrive at the dive site, it is important   for you and your diving partners to follow a regular predive ritual in   preparation of each dive. Predive rituals and the use of checklists are   essential and underutilised components in proper dive preparation; they   reduce the likelihood of equipment errors that could transform an   otherwise enjoyable dive into an emergency situation.<br />
        <br />
        If you are   planning a boat dive, it is advisable to configure, assemble and check   your equipment before the boat ever leaves the dock. You don't want to   be preparing your equipment when the boat is rocking at the dive site   and other divers are rushing to enter the water.<br />
        <br />
        Part of your   predive checklist should include ensuring your dive location has all the   necessary emergency and first aid equipment such as emergency   communications (mobile phone, marine radio, satellite phone, etc.), first   aid and emergency oxygen equipment. If you are not bringing first aid   equipment to the dive site yourself, you should ask about the   availability of this equipment when you travel. When I board a dive   boat, part of my predive ritual is to take a look at the first aid   supplies and emergency oxygen equipment before we depart.</p>
      <p align="left">Predive rituals include configuring, assembling and checking your equipment, review of the dive plan and contingencies, review of hand signals (especially communicating breathing gas management signals), and a   review of common emergency procedures such as the removal of weights,   managing out-of-air emergencies and buddy separation. Unfortunately,   lives have been lost because divers were unfamiliar with critical   emergency processes such as how to jettison weights. </p>
      <p align="left">Your predive ritual should also include a check of your partner's   equipment to make sure everything is in place and working. This means a   thorough inspection to ensure breathing gas is on and the regulator is   working, the regulator and octopus are configured and functioning   properly, the power inflator is attached and working, and emergency signaling equipment (safety sausages, whistle, etc) is working and in place. Once you've done all of this, you're ready to dive.<br />
        <br />
        Although   preparation may not prevent an accident from happening, an effective   predive ritual and comprehensive checklist can reduce the likelihood   that an otherwise enjoyable dive will turn into a tragedy. As they say   in The Royal Air Force, &quot;Learn from your mistakes, better still, learn   from others.&quot;<br />
        <br />
      Dive safely.<br />
      </p>
