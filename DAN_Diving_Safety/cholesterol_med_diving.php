      <h1>Cholesterol Medications &amp; Diving</h1>
      <h3><strong>Diver Question: </strong>I'm a scuba instructor and in the   last few years I've noticed more open-water and advanced students taking   medication to lower their cholesterol. Are these medications safe to   take when scuba diving? Is there anything else I should be concerned   about?</h3>
      <h6><strong>DAN Answer </strong></h6>
              <p>At this time, there is no known interaction between the effects of   cholesterol-lowering (hypolipidemic) agents and diving. But this is not   the complete answer. Some cholesterol-lowering medications can cause   side effects which might impair diving performance; others may cause   symptoms similar to decompression illness, such as dizziness, fatigue,   paresthesia (a burning, tickling or tingling sensation), peripheral   neuritis (inflammation of a nerve) and muscle and joint pain.</p>
              <p>                Patients   who take these medications regularly are usually aware of such side   effects and, in the event of a dive accident, should be specifically   asked about them. Another important issue for individuals taking   hypolipidemic medication is cardiovascular fitness. High cholesterol is   one of several well-known risk factors for cardiovascular disease. Other   risk factors include diabetes, hypertension, cigarette smoking, being   overweight, a family history of heart disease before the age of 55 and   simply being male (cardiovascular disease is more common in males). Any   individual taking cholesterol-lowering medication should therefore   receive the OK from his personal physician that he is physically fit and   capable of sustaining exercise levels which are likely to be   experienced in diving.<br />
              </p>
              <p><em>                Answer provided by the DAN Medical Team.</em>      </p>
