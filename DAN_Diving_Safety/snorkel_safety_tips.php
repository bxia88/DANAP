          <h1>Snorkelling Safety Tips </h1>
	      <p align="justify">Snorkelling is a wonderful pastime that provides a relatively easily accessible window to the underwater world and that is enjoyed safely by millions of people of a wide variety of ages. However, snorkelling does present some risks to the unwary, and sometimes to the experienced. </p>
	      <p align="justify">Snorkelling is conducted in a potentially hostile and changeable environment that can sometimes feature rough seas, cold water and strong currents. It also brings you into close contact with marine animals, which, in defending themselves, can sometimes cause you harm. Powerboats can also present potential danger</p>
	      <p align="justify">In addition, you need to be in good health, and fit enough to safely meet the physical demands of snorkelling. Certain medical conditions may lead to complications while snorkelling. If you have breathing problems such as asthma, a cardiac condition, problems with your ears or sinuses, or any other medical problems, seek advice from your doctor before taking up snorkelling.</p>
	      <h2>GETTING STARTED</h2>
	      <div align="justify"><br clear="all" />
          </div>
	      <div>
                <div align="justify">
                  <ul>
                      <li><strong>Snorkelling can be physically strenuous.</strong> Ensure you have an adequate level of fitness.<br />
                    </li>
                    <li><strong>You should generally be a competent swimmer to take up  snorkelling. </strong>A poor swimmer  can sometimes  snorkel relatively safely with the aid of a buoyancy jacket, but this requires  care and close supervision. <br />
                    </li>
                    <li><strong>Certain medical conditions would disqualify you from  snorkelling on grounds of safety. </strong>Consult  a doctor who understands the demands of snorkelling to ensure you are medically  fit.<br />
                    </li>
                    <li><strong>Consider a formal snorkelling course. </strong>Such a course will  ensure that you are taught the necessary skills, are informed of the risks  associated with snorkelling, and it will advise on the selection of equipment.<br />
                    </li>
                    <li><strong>Before you progress from shallow water (1.5m) you should be  able to comfortably perform these skills: </strong><br />
                      - Snorkel  breathing and clearing; <br />
                      - Mask  clearing; <br />
                      - Duck  diving; <br />
                      - Ear  clearing; and <br />
                      - Weight belt  ditching (if a weight belt is necessary). <br />
                    </li>
                    <li><strong>Know the marine life in your region that could pose  problems;</strong> what potential risks  they present,  how to minimize these risks, and what to do if an injury occurs. <br />
                    </li>
                    <li><strong>Don&rsquo;t snorkel if you are unwell.</strong> Even a cold will seriously impair you. <br />
                    </li>
                    <li><strong>Be well-rested, well-fed, and well-hydrated. <br />
                    </strong></li>
                    <li><strong>Snorkel with a buddy. </strong><br />
                    </li>
                    <li><strong>Your breathhold time should be well within your limits of  comfort.</strong> Do not force it, as it  is possible to  blackout without warning. <br />
                    </li>
                    <li><strong>Do not hyperventilate.</strong> (A series of forceful deep breaths prior to holding your  breath.) You risk blackout without any warning. <br />
                    </li>
                    <li><strong>Do not breathhold dive into or under any overhead  environment</strong> (cave, wreckage,  overhand) where you  risk being trapped. <br />
                    </li>
                    <li><strong>Never snorkel at night unless adequately prepared with equipment and the conditions are stable.</strong><br />
</li>
                    <li><strong>Inform others that you are going snorkelling. </strong>Tell them your intended location and approximate return time. <br />
                    </li>
                    <li><strong>Do not snorkel in areas where obvious dangers exist.</strong> Examples are areas with strong current, water with  poor visibility, places where others are fishing, or areas with a lot of boat  traffic. <br />
                    </li>
                    <li><strong>Follow a &ldquo;look but don&rsquo;t touch&rdquo; policy.</strong> In this way you minimise the risk of injury from  marine organisms,  and you will avoid damaging fragile marine growth.<br />
                    </li>
                    <li><strong>If you  begin to feel tired, cold or uncomfortable, terminate your dive.</strong> <br />
                    </li>
                  </ul>
                </div>
            <ul>
              <p><em>But, most of all, enjoy the fantastic  opportunities that snorkelling has to offer.</em></p>
              <p>              (Tips provided by Stan Bugg and John Lippmann)           </p>
            </ul>
