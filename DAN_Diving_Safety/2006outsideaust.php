          <h1>2006 Diving Fatalities  </h1>
          <h2>          Fatalities Outside Australia </h2>
          <h3>CHINA</h3>
          <h6>            CH SC 06/01</h6>
          <p align="justify">            The victim was a 52-year-old male who was working on a  documentary project. He was killed when a valve blew from his tank and hit his  chest and head. <em>Cause of death: </em>Trauma </p>
          <h3>FIJI</h3>
          <h6>            FI SS 6/01</h6>
          <p align="justify">            This 30-year-old male was diving for beche-de-mer (sea  cucumber) at Nase Reef near Lekutu in Fiji. The victim and several others were  diving using a compressor when the victim was noticed to be motionless on the  sea bed. Resuscitation attempts were unsuccessful. <em>Cause of death: </em>Unknown </p>
          <h3>INDONESIA</h3>
          <h6>            IN BH 06/01</h6>
          <p align="justify">            An 84-year-old Australian woman died while snorkeling  a few metres from a beach in Bali. <em>Cause  of death: </em>Unknown.</p>
          <h6>IN SC 06/01</h6>
          <p align="justify">            Two tourist divers were separated from their dive  group while diving in strong currents in Bali and were left at sea. One diver  was found 72 hours later. The two divers had been strapped together but on the  last night, the survivor unstrapped his buddy, who he believed to have died, as  his body was dragging him down. </p>
          <p align="justify">The survivor was picked up about 200 km from the  original dive site but the body of the victim was never found. Although they  disappeared at 4pm the dive operator did not notify others until 8pm by which  time it was too late to organise an effective search that day. <em>Cause of death: </em>Drowning. </p>
          <h3>MALAYSIA</h3>
          <h6>MA SC 06/01</h6>
          <p align="justify">            A male victim died while scuba diving in Jarak. He was  reportedly certified as an Advanced Open Water Diver (AOWD) and had joined a  group of AOWD students and their instructor for Deep Dive training. It was  reported that the victim was sighted amongst the group six minutes into the  dive, when 
            they were already at a depth of about 30 msw. He then  disappeared and was next seen unconscious on the surface three minutes later. <em>Cause of death: </em>Unknown.</p>
          <h6>MA SC 06/02</h6>
          <p align="justify">            This victim was a female tourist aged &ldquo;in her  thirties&rdquo;. She was hit by a boat propellor and died from the injuries  sustained. No further details are available. <em>Cause of death: </em>Trauma.</p>
          <h6>MA SC 06/03</h6>
          <p align="justify">            A group of seven tourists went on a Discovery Dive  with three dive staff. However, there is some doubt whether some or all of the  staff were appropriately qualified to supervise this activity. When the group  arrived back at the beach, one tourist was missing. </p>
          <p align="justify">A search was conducted and the victim&rsquo;s body was found  at the bottom on the reef, motionless, with weights on and BCD deflated. <em>Cause of death</em>: Unknown.</p>
          <h6>MA SC 06/04</h6>
          <p align="justify">            This 28-year-old male was diving with a group of 6  tourists under the supervision of an instructor. During the dive, the group saw  manta rays at a depth of 20-23m and, during the excitement, the victim 
            became separated from the group and was later found  unconscious without his mask and one fin. There was still 80 bar of air  remaining in his tank. <em>Cause  of death</em>: Unknown.</p>
          <h6>MA SC 06/05</h6>
          <p align="justify">            This 58-year-old male had already completed one dive  to 20m for 20 minutes that ended with him rising to the surface while trying to  take a photograph. During the surface interval of 1:40 he appeared to be fine. </p>
          <p align="justify">While on the second dive to 18.5 msw, the victim had  an unknown incident. The divemaster leading the dive noticed something was  wrong but she was panicking herself and didn&rsquo;t help. The 
            victim made a rapid ascent to the surface. </p>
          <p align="justify">He was found at the surface a few minutes later by the  dive boat crew and taken to a local island where CPR was performed during the  40-minute trip to a regional hospital. The hospital reported that it was a  drowning death. <em>Cause of death: Drowning?</em></p>
          <h6>MA SC 06/06</h6>
          <p align="justify">            The victim was a 34-year-old tourist who  had completed 28 logged dives. The last 
            of these was five days prior to this fatal  dive.</p>
          <p>The group had finished the first dive of  the day and was climbing back on board the boat when the victim descended again  for an unknown reason. Others submerged to look for 
          him and he was found on the bottom at 15  msw, still with 60 bar of air in his tank. CPR with oxygen was attempted for  more than 50 minutes but without success.  <em>Cause  of death: </em>Unknown.</p>
          <h3>THAILAND</h3>
          <h6>TH BH 06/01</h6>
          <p align="justify">            A 70-year-old tourist was snorkelling off  a popular beach when she was struck by a long-tail boat. She sustained massive  head injuries, from which she died.            <em>Cause  of death: </em>Trauma.</p>
          <h6>TH RB 06/01</h6>
          <p align="justify">            A very experienced rebreather diver with  over 30 years of commercial saturation dive experience was diving on a wreck at  a depth of 50 msw. He was using a closed circuit rebreather on which he had  logged over 300 hours (he had logged over 2000 hours on CCR units). </p>
          <p align="justify">It is believed that he experienced an  unknown equipment malfunction causing him to receive 100% Oxygen at depth,  leading to CNS oxygen toxicity, which caused him to become unconscious and  subsequently drown.              <em>Cause of death: </em>Drowning (as a result of CNS O2 toxicity).</p>
          <h6>TH SC 06/01</h6>
          <p align="justify">            This 26-year-old female diver was with a  group of close friends who were diving at a coral reef around a small island.  For an unknown reason she decided to surface without the remainder of the  group. On the surface she was struck by a speedboat, which quickly sped away.  She suffered massive head injuries. <br />
            <em>Cause of death: </em>Trauma.</p>
          <p>&nbsp;</p>
          <p align="justify"><strong>Note:</strong> <strong>The information DAN AP has on the  fatalities outside Australia is usually scant on detail and we would be  grateful to receive any information available about the following incidents, or  others not listed here. We are certain that there are others. Our sources remain confidential and all reports are  de-identified. Please send information to (research@danasiapacific.org).          </strong></p>
