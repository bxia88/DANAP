      <h1>Immersion Diuresis (Urge to Urinate)</h1>
      <p>This underwater phenomenon can strike you even if you do not have a   weak bladder .</p>
      <h5><strong>Diver Question </strong></h5>
      <p><em>As an inexperienced diver, with less than 50 dives since   being certified last year, I have an unrelenting dilemma. No matter how   many times I void, I always end a dive having to desperately go to the   bathroom. My stomach will be bloated and I can't get my tank and wetsuit   off quickly enough. I purposely don't drink before a dive, except two   cups of morning coffee, and it seems I should be properly hydrated. I go   to the bathroom before I get in the water and immediately after the   dive (I refuse to urinate in my wetsuit). How can I control this? I   don't have a weak bladder or any form of incontinence.</em></p>
		      <h6>              DAN Answer</h6>
              <p>The answer to this interesting dilemma is based on an understanding of   dive physiology. The phenomenon you describe is known as immersion   diuresis and occurs whenever the body is emerged in water. Immersion,   along with a water temperature that is colder than air, causes narrowing   of the blood vessels in the extremities. This vasoconstriction occurs   primarily in the skin and superficial tissues of the body as well as in   the muscles of the arms and legs. The result: An increased volume of   blood is sent to the central organs of the body such as the heart, lungs   and large internal blood vessels. <br />
                <br />
                The hormone that controls the production of urine by the kidneys   is called antidiuretic hormone (ADH). It controls when and how much   urine your kidneys make. The increased blood volume to the major vessels   is interpreted by your body as a fluid overload. This overload causes   ADH production to stop, which in turn allows the kidneys to immediately   produce urine to lower the centrally circulating blood volume - the   body's automatic response to preserve blood volume. <br />
                <br />
                Once you exit the water, circulating blood volume returns to   near normal - less the fluid taken to produce urine, which is quickly   replaced as the body draws fluid from body tissues, such as muscles.   Unfortunately, you probably will also leave the water with a full   bladder. <br />
                <br />
                Since we are all subject to the same phenomenon underwater, this   is probably your normal response to immersion. If this situation causes   problems like urinary tract infections, see your doctor. If your coffee   is caffeinated, you may wish to switch to a decaffeinated brand, as   caffeine is a known diuretic that also interferes with the production of   ADH.</p>
