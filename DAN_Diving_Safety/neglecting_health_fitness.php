      <h1>Mistakes Divers Make &amp; How to Avoid Them </h1>
		      <h2 align="justify"><strong>Neglecting Your Health &amp; Fitness </strong></h2>
		      <h6><strong>Health</strong></h6>
              <p>Few people enjoy perfect  health for their entire lives. Adopting a healthy lifestyle early in life can  help postpone ailments associated with ageing. </p>
              <p>When health issues  present, it’s important to discuss them with your healthcare provider, pursue appropriate  interventions and adopt modifications. With ongoing awareness of your personal  health status and timely adjustments to maintain your health, diving can be a  lifelong recreational activity. </p>
              <p>Prior to diving, you  should take an honest assessment of whether you are medically fit to dive. Be vigilant  for signs of acute illness (like congestion) and familiarize yourself with the  risks and essential precautions associated with any chronic diseases. </p>
              <p><strong>Acute illness </strong>that lasts more than a few days or leaves you  feeling exhausted should prompt a delay to diving. </p>
              <ul>
                <li>Do not dive when ill. </li>
                <li>Wait until you regain  your normal strength and stamina. </li>
                <li>If you are not ready to  exercise at your pre-illness level, you should postpone your dive. </li>
                <li>The best course of  action is to consult with your physician. </li>
              </ul>
              <p><strong>Chronic diseases </strong>may affect your fitness to dive risks even if you  perform well in other activities. </p>
              <ul>
                <li>Some health conditions,  especially in advanced stages, may make the risks to you and your 
                  dive buddies  unacceptably high. </li>
                <li>In less advanced or more  stable medical conditions, divers may continue safe and enjoyable 
                  diving with proper  guidance from their physician, medical controls and wise choices.</li>
              </ul>
              <p>During your annual  physical exam or following any changes in your health status, consult your physician  to ensure you have a medical clearance to dive. </p>
              <h6><strong>Fitness</strong></h6>
              <p>Being a physically fit  diver means that you have sufficient aerobic capacity, cardiovascular health  and physical strength to meet the demands of the diving environment. Can you  fight a current? Perform a long surface swim? Help a buddy in an emergency? All  divers need
                to be physically able to  perform these essential tasks. </p>
              <p>Here are some tips to  help you enhance your fitness for diving:</p>
              <ul>
                <li>While diving itself can  be physically demanding, it is not enough to maintain fitness overall. </li>
                <li>Divers must stay fit  with additional activities outside of diving, such as swimming laps, strength  training and flexibility and balance exercises. </li>
                <li>Regular physical  activity, including aerobic activity and muscle-strengthening activity, is  essential to maintain physical fitness. </li>
                <li>If you are over 40 and  do not exercise regularly, consult your physician before you start exercising. </li>
                <li>Divers should dedicate  time to fin swimming. Fitness for swimming against a current includes fin-swimming  skills. Divers without these skills may not be able to create sufficient  propulsion to overcome a strong current even if they have high aerobic capacity  for muscular work. </li>
                <li>At least six weeks prior  to a dive trip, gradually increase the level of your physical activities. <br />
                  Join your local dive  club for more specific training opportunities. </li>
                <li>To avoid an increased  risk of decompression sickness, DAN recommends avoiding strenuous exercise for  24 hours after diving. </li>
              </ul>
