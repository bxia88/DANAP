      <h1>Sea Urchin Spines </h1>
		      <p><em>Diver Question: I  was chasing a big marble ray underwater near Cocos Island - I wanted to  photograph it - and wasn't paying attention to my buoyancy. I brushed by a rock  wall and suddenly felt severe burning in my arm and elbow. There were 15 black  sea urchin spines sticking out of my forearm. The spines had gone right through  my diveskin. I remembered hearing that it helps to urinate on a sea urchin  sting, so I tried it, but it didn't help. Most of the black spots on my arm have  disappeared, but I still can see two, and my wrist is starting to swell. What  should I do?</em></p>
              <h6>              DAN Answer:  </h6>
              <p>Some sea urchins are covered with sharp venom-filled spines  that can easily penetrate and break off into the skin. Others (found in the  South Pacific) may have small pincerlike appendages that grasp their victims  and inoculate them with venom from a sac within each pincer.</p>
              <p>                Sea urchin punctures or stings are painful wounds, most often of the hands or  feet. If a person receives many wounds simultaneously, the reaction may be so  severe as to cause extreme muscle spasm, difficulty in breathing, weakness and  collapse. <br />
                <br />
                <strong>The Treatment</strong></p>
              <ol>
                <li>Immerse the wound in non-scalding hot water to tolerance  (110 to 113 F / 43.3 to 45 C). This frequently provides pain relief. Other field  remedies, such as application of vinegar or urine, are less likely to diminish  the pain. If necessary, administer pain medication appropriate to control the  pain.</li>
                <li>Carefully remove any readily visible spines. Do not dig  around in the skin to try to fish them out - this risks crushing the spines and  making them more difficult to remove. Do not intentionally crush the spines.  Purple or black markings in the skin immediately after a sea urchin encounter  do not necessarily indicate the presence of a retained spine fragment. The  discoloration more likely is dye leached from the surface of a spine, commonly  from a black urchin (Diadema species). The dye will be absorbed over 24 to 48  hours, and the discoloration will disappear. If there are still black markings  after 48 to 72 hours, then a spine fragment is likely present. </li>
                <li> If the sting is caused by a species with pincer organs, use  hot water immersion, then apply shaving cream or a soap paste and shave the  area. </li>
                <li>                  Seek the care of a physician if spines are retained in the  hand or foot, or near a joint. They may need to be removed surgically, to  minimize infection, inflammation and damage to nerves or important blood  vessels. </li>
                <li>If the wound shows any sign of infection (extreme redness,  pus, swollen regional lymph glands) or if a spine has penetrated deeply into a  joint, the injured person (particularly one with impairment of his or her  immune system) should be started by a qualified health professional on an  antibiotic, taking into consideration the possibility of a Vibrio infection. </li>
                <li>If a spine puncture in the palm of the hand results in a  persistent swollen finger(s) without any sign of infection (fever, redness,  swollen lymph glands in the elbow or armpit), then it may become necessary to  treat the injured person with a seven- to 14-day course of a non-steroidal  anti-inflammatory drug (e.g., ibuprofen) or, in a more severe case, oral  prednisone, a corticosteroid medication.<br />
                </li>
              </ol>
