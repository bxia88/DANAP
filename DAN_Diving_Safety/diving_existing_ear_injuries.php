      <h1> Diving With Existing Ear Injuries </h1>
		      <h2 align="justify">Diver Question</h2>
		      <p>I am in good health and have had occasional problems   clearing my ears because I had a broken nose with a deviated septum. I   was diving recently and had a little trouble clearing my ears during my   dives. I also had sinus congestion from a recent cold. 
                   After   surfacing from my final dive, my ears felt blocked (like they had cotton   in them), and voices seemed muffled. I began taking decongestants. They   helped, but as the blocked/pressure sensation got better I noticed a   ringing in both of my ears.</p>
		      <p>		        I saw an ear, nose and throat physician   who put me on prescription medication, including steroids and a strong   decongestant. I have had excellent hearing in the past, but a recent   test showed that I had severe to moderate hearing loss in both ears. The   doctors told me that the ringing may be permanent and that I should not   continue scuba diving, but he is not a diver. Can I continue to dive   with ringing in my ears?</p>
              <h2 align="justify">DAN Answer</h2>
              <p align="justify"> Your doctor is probably correct - you should consider discontinuing   diving. Sinus and middle ear barotrauma are the most common dive-related   injuries. Injury to the soft tissue lining of these air spaces in the   body is an inherent problem in scuba diving because of the changing   volumes of gas while descending and ascending. Improper clearing   (equalizing technique) can lead to these types of injuries and is the   most common contributing factor among new divers.</p>
              <p align="justify">                Other contributing factors may be a history of sinusitis,   allergies and childhood ear infections. Any chronic irritations or   infections may damage the soft tissue lining (mucous membrane) of the   sinuses and especially the Eustachian tube, connecting the middle ear   with the pharynx. A deviated septum can also cause difficulty in   clearing. Divers who continue to descend when they cannot equalize   pressure risk several consequences: pain, fluid accumulation and   bleeding into the cavity, whether it is the ears or the sinuses.   Infection of the fluid can ensue; and persistence of fluid in the middle   ear is one possible reason for impaired hearing. Rupture of the eardrum   can also occur, and more serious is damage to the small membranes in   the inner ear: the round or oval windows. </p>
              <p align="justify">                Although spontaneous healing of these structures can occur,   and surgical repair may be possible, permanent loss of hearing can   result. Over-the-counter medication used for the symptoms of congestion   can be very effective for some divers and can help with pressure   equalization. However, the mucous membrane may not fully recover from a   viral infection or head cold for several weeks after initial symptoms   begin - this is why DAN advises divers not to dive with any symptoms of a   cold. </p>
              <p align="justify">                When barotrauma has occurred in the middle ear, it is not   unusual for the diver to have a sensation of pressure or fullness and   decreased hearing. Ringing (tinnitus) represents the stimulation of the   nerve fibers responsible for interpreting sound. In your case, a similar   episode of barotrauma could lead to further damage and additional   impairment. Your physician may probably feel that this risk is not worth   taking, and you will have to consider whether some degree of permanent   hearing loss is an acceptable risk for you. DAN recommends that divers   wait until symptoms of viral illnesses and head colds have completely   resolved before planning a dive. Although decongestant medications may   be helpful in salvaging a diving vacation if symptoms are mild, they do   not necessarily prevent serious sinus or ear barotrauma. If you cannot   equalize pressure in the middle ear cavities or sinuses, you should not   continue to descend. </p>
      <h6 align="justify">                DAN Safety Tip</h6>
              <p align="justify">                It's always a good idea to &quot;pre-clear&quot;   before you go under: hold your nose and swallow to see if you get that   characteristic release.<br />
                                                                                                  </p>
