      <h1>Stinging Coral </h1>
		      <p align="justify"><em><strong>Diver Question: </strong></em></p>
	               
              <p><em>When  I was diving two weeks ago, I inadvertently brushed up against a reef and  apparently came in contact with a type of stinging coral. As soon as I got out  of the water I noticed that I had a few small cuts and scrapes on the outside  of my left wrist and forearm. Later that evening, I felt an itching and burning  sensation around the affected skin. It became red and tender to touch. I washed  it with soap and hot water, which actually seemed to help. However, the  redness, itching and burning lasted for three to four days. The local doctor  prescribed a steroid cream twice a day, and everything seemed to heal during  the next four or five days. <br />
              <br />
Since I returned from my dive trip, the redness and itchy skin sensation have  come back. The area is uncomfortable and not that pleasant to look at. I've  never had trouble with any marine life before, so I am unsure if this reaction  is to be expected or if this is unusual. Do I need special care?</em></p>
              <h6><strong>DAN Answer </strong></h6>
              <p>Of the 12,000 to 13,000 information calls DAN receives each  year, a little more than 2 percent (250-300 calls) are related to marine  envenomation, bites or stings. Out of those calls, DAN gets 40 or 50 calls,  about one a week, relating to someone who has come in contact with coral. <br />
                <br />
It is not uncommon for a diver to have a red raised rash resulting from a coral  encounter. A burning sensation, pain and itching may also accompany a rash if  the coral is actually a hydroid, such as fire coral, which is not a true coral,  but a stinging creature. The extent of the reaction to fire coral depends on  the amount of exposure to toxins, the extent of the abrasion and pre-existing  sensitivity of the victim. <br />
<br />
You did the correct thing to clean the wound with soap and water: reactions may  be reduced by immediately cleansing the affected area. The most frequent  complications from non-stinging coral scrapes are inflammation, which leads to  poor healing, and, less commonly, a secondary infection. Proper cleansing,  therefore, is very important. <br />
<br />
If fire coral is the culprit, then dilute acetic acid solution, such as  household white vinegar, is a reasonable topical decontaminant and should be  used as a soak to reduce the pain. It's inexpensive and readily available.  Immersion in hot water can reduce the symptoms. If hot water is not available  then application of instant hot packs may be used. If heat sources are not  available instant cold packs or ice packs may be used. <br />
<br />
Treatment for the inflammatory response is symptomatic. Steroid creams are  rarely helpful, and they can prolong a skin infection. If the inflammation is  severe, systemic steroids in a moderate, tapering dose may be administered  under the supervision of a trained medical provider. Oral antihistamines can  sometimes, but not always, help reduce the itching or burning sensation. <br />
<br />
When underwater, try to avoid contact with coral or any other living creature.  Whenever possible, wear a wetsuit or diveskin to protect yourself during those  times when you are pushed into coral by another diver or by a current. <br />
<br />
Ocean divers should consider a marine animal first aid kit for their travels.  This will speed up the time to properly administer first aid for injuries. Additionally,  for divers who want to learn more about the various marine life injuries, there  are courses in marine life identification, first aid courses and a variety of  books and publications available.<br />
