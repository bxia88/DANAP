      <h1> Getting &quot;Dive Season&quot; Fit </h1>
		      <p align="justify"><em><strong>Diver Question: </strong></em></p>
	               
              <p><em>I'm  a 48-year-old diver and I have been diving for 12 years. I don't dive much  during the winter, but starting in the spring, I usually do a lot of weekend  diving in local lakes and quarries. I also take at least two dive vacations  each year. My questions: what constitutes a good dive physical for an active  48-year-old diver, male or female? What fitness guidelines should I be  following at my age, and what should I be concerned about?</em></p>
              <h6><strong>DAN Answer </strong></h6>
              <p>                Fitness is not only a product of a healthy lifestyle, such as   limiting fats and carbohydrates in our daily diets, but also a function   of how we address health issues. Lack of fitness poses two risks in   scuba diving: the first risk is what we assume ourselves; the second   involves the degree of risk to our buddies and potential rescuers. </p>
              <p> Scuba diving requires a level of fitness that allows us to   respond to the sudden need for a cardiovascular or respiratory exertion.   Because we are in a seemingly weightless environment (with proper   buoyancy control), and often pushed along by a current, we can easily   underestimate the level of physical fitness required to perform safely   in the water during an emergency situation. </p>
              <p> But even a relaxed dive can produce sufficient cardiovascular   and blood flow changes to stress a heart that already has some baseline   cardiovascular disease (CVD). An emergency situation may require even   more physical exertion. Several apparently healthy divers die each year   from sudden cardiac death during a dive. Of those, some appear   relatively healthy. </p>
              <p> A good way to monitor your health is to have a regular check-up   with your primary physician. This provides information that can help you   avoid illness and disease and maintain a lifestyle that promotes good   health. </p>
              <p> Why get periodic physical examinations? As we get older, our   chances of acquiring diseases such as diabetes, cancer and heart disease   are significantly higher. Most significant is the increased risk of   heart disease. </p>
              <p> Risk factors for CVD include a diet high in cholesterol and   saturated fat, smoking, lack of exercise, diabetes and high blood   pressure. A certain amount of risk is also passed along in genes.   Unfortunately, we may never realise we have a problem until we have the   first symptoms, such as chest and arm pain, shortness of breath and a   sudden loss of consciousness. Although normally associated with exertion   and exercise, symptoms can occur during periods of emotional stress. </p>
              <p> The centerpiece of a dive physical for divers 45 years or older   is a cardiac stress test. Determining how well your heart can perform   during this diagnostic test is helpful in determining heart health.   Health problems such as irregular heart rhythms or blood pressure   problems may show up during your evaluation. This can help to detect   some future problems. Don't downplay your symptoms just to be a good   patient; talk to your doctor about your health concerns. </p>
              <p> Tell your doctor what aches and pains you may have had since you   last check-up. Have you had episodes of lightheadedness, ear ringing,   bowel or bladder problems? </p>
              <p> To get a healthy stamp of approval from your doctor, you will   undergo other procedures in addition to a stress test. While many aren't   necessary for you to continue diving, such procedures are part of a   thorough physical examination. </p>
              <p> Take healthy eating advice to heart, and use an exercise program   to build endurance and cardiovascular fitness. Determine if a   combination of diet and exercise will work to lower cholesterol or if   drug therapy may be required. Follow your physician's advice, and if he   or she has dive-related questions, have your doctor call DAN. Take the   time to take care of yourself, and scuba diving will be in your future. <br />
                <br />
                <em>Answer provided by</em> <em> Joel Dovenbarger (Vice President, DAN Medical Services) </em> <em>and Dr James Caruso.</em> <br />
                      </p>
