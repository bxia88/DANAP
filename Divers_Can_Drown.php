        <h1>Divers Can Drown Awareness Campaign</h1>
        <p align="justify">DAN  is heavily involved in the investigation and reporting of diving accidents as  part of its mission to improve diving safety. It is important that we learn  from the mistakes and mishaps of other divers so that we can minimise similar  situations.</p>
        <p align="justify">In  examining 30+ years of Australian diving fatality statistics we found that in  44% of cases &ldquo;drowning&rdquo; was the key factor associated with these accidents.</p>
        <p align="justify">From  this, it is important as part of our research to try to determine the factors  behind these drowning deaths. </p>
        <p align="justify">Unsurprisingly,  equipment problems (e.g. BCD misuse, broken straps, regulator failure), gas  supply problems (e.g. out-of-gas, inappropriate gas) and rough water (e.g.  chop, current, surge) were highly associated with drowning. Gas supply-related  and buoyancy-related problems were the predominant factors that lead to the  diver drowning. </p>
        <p align="justify">It is important to note that the majority  of drowning deaths can be prevented with appropriate education and vigilance.  The following factors are especially significant:</p>
        <ol start="1" type="1">
          <li>Ensuring you are <em>fit       and healthy</em> enough for diving as exhaustion is a key factor that can       lead to drowning.</li>
          <li>Having good <em>general       aquatic skills</em> that will enable you to better handle challenging       situations should they arise.</li>
          <li><em>Choosing dive sites and       conditions compatible with your abilities</em>. Sometimes, this will mean withdrawing from a       planned dive.</li>
          <li>Using <em>good and       well-maintained equipment</em>.</li>
          <li>Ensuring <em>correct       weighting and buoyancy control.</em> Drowning victims are often       over-weighted and poor buoyancy control is a common problem. Almost three       quarters of victims were found with their weights in place and this       highlights an ongoing problem of divers being reluctant or unable to ditch       their weights when they get into trouble.</li>
          <li>Maintaining <em>close contact with your buddy</em> also provides       some security. Sixty-five percent of the victims were alone at the time of       their accident. A buddy&rsquo;s presence usually increases the likelihood of       help when required and reduces the time to rescue and first aid       management; both important to the chances of survival.</li>
          <li><em>Gas supply. </em>Fifteen per cent of       victims were low on gas and one quarter were completely out of gas.</li>
        </ol>
        <p align="justify">DAN  Asia-Pacific has launched a campaign to highlight this issue and has created a  &ldquo;Divers Can Drown&rdquo; poster to raise awareness of this issue and highlight  important factors in drowning prevention.</p>
        <p align="justify"><strong>Can you help DAN to promote this  important campaign?</strong> We  encourage Dive Operators and Dive Clubs to request a copy of this poster and to  display it prominently to assist DAN in creating awareness for this important  issue. To  obtain a copy, send an email to <a href="mailto:marketing@danasiapacific.org">marketing@danasiapacific.org</a>.</p>
