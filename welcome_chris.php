        <h1>Welcome Chris Wachholz</h1>
         
        <h2><strong>New  DAN Asia-Pacific CEO</strong></h2>
        <p align="justify">Chris began diving in 1971 and was an active wreck diver in the U.S.' Great Lakes and North Carolina. </p>
        <p align="justify">Chris is a Registered Nurse, with a Bachelor of Science in Nursing from Marquette University. In Nursing School, Chris volunteered as a research subject in decompression table experiments under Dr.Eric Kindwall, one of the world's leading experts in diving and hyperbaric medicine. </p>
        <p align="justify">Upon graduation, Chris worked as a staff nurse in Intensive Care, an Emergency Department and was a hyperbaric chamber staff nurse and chamber operator.</p>
        <p align="justify"> In 1983, Chris became the Assistant Director of Divers Alert Network (DAN) at Duke University Medical Centre becoming DAN America's first full-time employee.</p>
        <p align="justify">During a 25-year career at DAN America, Chris fielded non-emergency dive medical questions, took emergency Decompression Illness (DCI) calls and performed hands-on nursing care for DCI patients in the Duke Hyperbaric chamber. Chris also managd DAN's financial and business activities including: fundraising, marketing and staff and board recruitment.</p>
        <p align="justify">Chris co-developed DAN's first oxygen kit and training course and facilitated the development of International DAN.</p>
        <p align="justify">Chris has an M.B.A and a Masters of Environmental Management from Duke University.</p>
