          <h1>Dive Injury Insurance Considerations </h1>
	      <p align="justify">When  deciding on the level of optional Dive Injury (Treatment) Insurance to add to your DAN AP  Membership (Emergency Evacuation Coverage), we encourage you to consider:</p>
	      <h2><strong>The type of diving you are certified to undertake </strong> </h2>
	      <p align="justify">Consider that the Master Plan includes coverage to a maximum depth of 50m and the Preferred Plan has no depth limit, but in the event of an accident,  you need to be aware that you might be required to demonstrate that you had appropriate certification and/or experience for the depth, and were using appropriate equipment &amp; breathing gas. </p>
	      <p align="justify"><em>Note:</em> <em>If you dive deeper  than the Depth Conditions of your plan you risk voiding your coverage.</em></p>
	      <h2>We  strongly encourage you to review the <strong>Dive Injury Insurance Comparison Table and Policy Document</strong>.</h2>
	      <p align="justify">Given  that diving incidents often extend beyond decompression illness, it is  important to consider a level of coverage that will offer you the best  protection.</p>
	      <p align="justify"><em>Finally, please note that once your coverage has been implemented it is a costly process to change throughout the 12-month coverage period. This is why we strongly encourage  you to consider your coverage needs at the time of application.</em></p>
