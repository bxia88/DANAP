	      <p><em><br />
              <strong>Who Can Apply for DAN&rsquo;s Dive Injury  Insurance Plans?</strong></em> DAN  dive injury insurance plans are available to any recreational diver or snorkeller  (including instructors and divemasters supervising recreational diving  activities), who is a Member of DAN Asia-Pacific and is 12 years of age or  older. They are also available to certain research divers and underwater  photographers. Coverage is not available for commercial diving other than those  activities specified. </p>
	      <h6><strong>Good to Know</strong></h6>
	      <ul>
	        <li>	        Your DAN Membership Includes emergency evacuation benefits, it does not  cover treatment costs following a diving accident.</li>
            <li>              For  Members electing to take out both Membership and Insurance the 80km evacuation  restriction that applies to Membership only applicants is waived in the case of a Covered Diving  Accident only.</li>
	        <li>	          Dive  Injury Insurance is underwritten by Accident &amp; General Insurance (AGI) for  residents outside Australia.</li>
	        <li>If you have any other pre-existing insurance  policies your DAN policy becomes secondary coverage and will pay up to 100% of  all remaining eligible expenses. If DAN is your only insurance policy it will  pay up to 100% of all eligible expenses.</li>
	        <li>	          The  Standard Plan covers DCI ONLY. To be covered for all in-water  accidents/illnesses you need to consider the Master, Preferred or Preferred  Plus Plans.</li>
