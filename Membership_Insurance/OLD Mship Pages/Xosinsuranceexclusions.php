          <h1>Exclusions for Dive Injury Insurance</h1>
          <p class="emstrong"><strong>No benefits will be paid under any plan for  charges in connection with:</strong></p>
          <ol>
            <li class="ordered">Services or supplies for which a Member is not  required to pay.</li>
            <li class="ordered">Charges made only because Insurance exists. </li>
            <li class="ordered">An Injury for which benefits are paid or payable under  Workers&rsquo; Compensation or any Occupational Disease or similar law whether such  benefits are insured or self-insured. </li>
            <li class="ordered">Any act of war, if declared or not. </li>
            <li class="ordered">Custodial Care. </li>
            <li class="ordered">Drugs and medicines that may be obtained without  written prescription and/or not furnished by and administered during a Hospital  confinement as an Inpatient. </li>
            <li class="ordered">Charges that are more than the Reasonable and  Customary Charges for the services and supplies furnished. </li>
            <li class="ordered">Hospital services and supplies when confinement is  solely for diagnostic testing purposes; or costs incurred after 12 months from the  date of accident. </li>
            <li class="ordered">A dive, or one or more dives as part of a <a href="repdiveseriesdefn.php">Repetitive Dive Series</a> </strong>which exceeds the maximum depth specified under the  policy taken out by the Member. </li>
            <li class="ordered">Any medical charges incurred later than 12 months  immediately following the date of the illness or injury. </li>
            <li class="ordered">Nervous, emotional or mental disorders. </li>
            <li class="ordered">An Injury or Illness that occurs after drug and  alcohol use except those prescribed by a physician. </li>
            <li class="ordered">Medical exams not required for treatment of any Injury  or Illness. </li>
            <li class="ordered">Routine eye or hearing exams, eye refractions, eye  glasses contact lens, hearing aids or any type of external appliances used to  improve visual or hearing acuity and their fittings. </li>
            <li class="ordered">Cosmetic or reconstructive procedures, and any related  services or supplies, which alter appearance but do not restore or improve impaired  physical function.</li>
            <li class="ordered">Care, treatment, services or supplies: (a) not  prescribed by a Physician; or (b) not Medically Necessary; or (c) resulting  from a <a href="preconditiondefn.php">Predisposing  Medical Condition</a> that was not declared in writing to DAN at the time  of application for the insurance or communicated to DAN in writing if the  condition arose during the period of the policy; or (d) mainly for the purpose  of medical or other research; or (e) received from a Nurse which do not require  the skill and training of a Nurse; or (f) to the extent that benefits are  payable under other provisions of this Group Policy; or (g) provided or paid  for by any government&rsquo;s civilian employees and their dependents; or (h) reordered  by a family member. </li>
            <li class="ordered">Any act of Nuclear, Biological or Chemical Terrorism.</li>
            <li class="ordered">Undertaking a dive, a Repetitive Dive Series, a scuba  diving activity or snorkelling activity against the advice of a physician. </li>
            <li class="ordered">  Flying within a Surface Interval shorter than the required interval specified  in the most recent Divers Alert Network <a href="../_pdf/flyingafterdiving.pdf">Flying  After Diving Guidelines</a></li>
            <li class="ordered">Flying within at 72 hours of Recompression Treatment  or within a longer period if so specified by the treating physician.</li>
            <li class="ordered">Undertaking a dive, a Repetitive Dive Series, a scuba diving  activity within a minimum period of six weeks after the completion of  Recompression Treatment. </li>
            <li class="ordered">Undertaking a dive, a Repetitive Dive Series, a scuba diving  activity without first obtaining a clearance to return to diving from the  treating physician after undergoing Recompression Treatment.</li>
            <li class="ordered">Undertaking a dive, a Repetitive Dive Series, a scuba diving  activity or snorkelling or &nbsp;breathhold  diving activity as part of, or preparation for, a contest, competition, record  attempt, trial or experiment related to achieving depth or endurance records on  compressed gas or breathhold. </li>
            <li class="ordered">Undertaking a dive, a Repetitive Dive Series, a scuba diving  activity or snorkelling or breathhold diving activity in a manner which the  Member knew, or reasonably should have known would expose them to an obvious  risk of suffering an injury or illness. </li>
            <li class="ordered">Charges in   connection with any injury  or illness arising directly or indirectly   from any Pre-existing  or Predisposing or Condition – the only exception   to this is a previous case of Decompression  Illness (DCI) that has   been declared to DAN <u>and accepted by the insurers</u>. </li>
          </ol>
          <p class="emstrong">No benefits will be paid under specific plans  for charges in connection with: </p>
		  <p class="sred">Standard Insurance Plan:                      </p>
		  <p>Services and supplies for any Injury or  Illness other than DCI;</p>
		  <p class="sred">Master/Preferred Insurance Plans:</p>
		  <ol>
		    <li class="ordered"> Service and supplies for an Injury or  Illness not due to:<br />
		      <span class="strong">(a)</span> Decompression Sickness (DCS), Arterial Gas Embolism  
		      (AGE), or Pulmonary Barotrauma caused by a scuba 
		      diving or snorkeling activity; <br />
		      <span class="strong">(b)</span> Any accidental Injury that occurs in the water and is a 
		      direct result of a scuba diving or snorkeling activity; <br />
		      <span class="strong">(c)</span> Diving within the depth, training and breathing gas 
	        requirements of the particular policy.<br />
              <br />
            No benefit payment shall be made for charges  incurred after the date the Group Policy is terminated except as provided under  any extended benefits provision of this Group Policy.</li>
          </ol>
		    <p><span class="strong">IMPORTANT:</span> By virtue of the National Health Act, 1953, the Medical  Insurance cover referred to in the DAN Diving Insurance plans is not available  to Australian Members in respect to hospital treatment, medical and health  services, medical, surgical and prosthetic equipment and appliances, drugs and  medicines or ambulance services provided in Australia.</p>
		    <h4>Additional Exclusions For Death & Disability Cover</h4>
		    <p>For the Accidental Death and Dismemberment Indemnity,  Permanent Total Disability Indemnity, the Group Policy does not cover any loss,  fatal or non-fatal; caused by or resulting from: </p>
		    <ol>
		      <li class="ordered"> Traveling against the advice of a physician;</li>
	          <li class="ordered"> Intentionally self-inflicted injury;</li>
	          <li class="ordered">Suicide or attempted suicide; while sane or insane;</li>
	          <li class="ordered"> War or any act of war; declared or undeclared;</li>
	          <li class="ordered"> Service in the military, naval, or air service of any country;</li>
	          <li class="ordered">Illness, disease, pregnancy, childbirth, miscarriage or any 
	            bacterial infection other than bacterial infection occurring 
              from an accidental cut or wound;</li>
              <li class="ordered">Piloting or acting as a crew member or riding in any aircraft; 
                except as a fare-paying passenger on a scheduled airline.</li>
	          <li class="ordered"> Diving with a <a href="preconditiondefn.php">Predisposing  Medical Condition</a> </u></strong>that was 
	            known by the Insured and not declared in writing to Divers Alert Network.</li>
	          <li class="ordered">Any act of Nuclear, Biological or Chemical Terrorism.</li>
	      </ol>
		    <p><span class="emstrong">IMPORTANT NOTICE</span><br />
	          <span class="em">Availability of services and benefits for evacuation or injury commences on  acceptance of an application for membership of DAN Asia-Pacific. The applicant  is required to complete the application fully and in good faith and with  appropriate and full disclosure of any predisposing medical condition,  pre-existing condition or previous decompression illness. No cover is provided  for any evacuation, medical, paramedical or other injury benefits resulting  from or materially contributed to, directly or indirectly by any diving or  other activity in which the applicant engaged prior to and up to the time of  acceptance of the application by DAN Asia-Pacific, regardless of the date on  which any claim for cover is made by or on behalf of the applicant.</span></p>
