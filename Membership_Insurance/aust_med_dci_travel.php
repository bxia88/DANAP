	      <h1>Are you a non-National of Australia?</h1>
	      <p>If you are not an Australian National you need to reside in Australia for  at least 3 months to be eligible for DAN Membership only. However, this period  is extended to 12 months if you wish to take out the optional Dive Injury  Insurance as well.</p>
	      <p class="em">Should you make a claim, you may be required to provide  evidence of your long-term residency in Australia for your claim to be  approved.</p>
