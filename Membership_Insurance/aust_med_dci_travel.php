	      <p>If you are an Australian National intending to travel  or live outside of Australia  for longer than 3 months during your insurance period you should contact the  DAN Asia-Pacific office to discuss your options as you may need to take out  additional coverage as the optional dive injury insurance will not cover you for the extended period.<span class="em">  It is essential that you are registered with DAN AP at the place where you are actually residing for extended periods.</span></p>
