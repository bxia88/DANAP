          <h1>Preferred Plus Plan Frequently Asked Questions</h1>
	      <h2><strong>Is the Preferred Plus Plan available to residents of Australia?</strong></h2>
	      <p>No.</p>
	      <h2 align="justify"><strong>Are illnesses covered under the Non-Diving Accident  Medical Coverage component of the Preferred Plus Plan?</strong></h2>
	      <p>No. This  component is limited to non-diving <u>accidents</u>.</p>
	      <h2 align="justify"><strong>I  recently had a diving trip cancelled as my connecting flight to the destination  was late and, as a result,  I missed my live aboard departure. Is this covered under  the Preferred Plus Plan &ldquo;Dive Trip Cancellation&rdquo;? </strong></h2>
	      <p align="justify">No. The Trip Cancellation benefit pertains to an <em>illness or inju</em>r<em>y</em> that occurred <em>after</em> the insurance had been purchased and was deemed by  a physician as sufficient in degree to recommend against travel.</p>
	      <h2 align="justify"><strong>Does the  Preferred Plus Plan Cover my lost or stolen diving equipment?</strong></h2>
	      <p align="justify">No. DAN AP recommends that divers also take out a travel  insurance policy to complement their DAN coverage as this will cover the likes  of lost/stolen equipment and belongings.</p>
	      <h2 align="justify"><strong>If I am  involved in a diving accident will my diving equipment be covered if it is lost  or damaged?</strong></h2>
	      <p align="justify">Yes, so long as your Diving Equipment is  accidentally damaged or lost due to a diving accident that involves urgent  hospitalisation or transportation. The present market value at the time of the  loss or damage will be paid up to US$2,500 per covered Diving Accident. AGI has  the option to repair or replace the equipment in lieu of a cash payment.</p>
	      <h2 align="justify"><strong>Does the Non-Diving Accident Medical Coverage include injury  resulting directly, or indirectly, from an accident involving a motorcycle,  motor scooter or moped?</strong></h2>
	      <p>No.</p>
	      <h2 align="justify"><strong>If I have  a non-diving accident when I am overseas on a diving holiday will my medical  expenses be covered under the Preferred Plus Plan?</strong></h2>
	      <p align="justify">Yes. Up to US$10,000 (Lifetime Maximum Benefit)  necessary medical expenses will be covered subject to us US$250 deductible. It  is important to note that the diving holiday must take place outside your &ldquo;home  country&rdquo;. The trip you are on must not exceed 3-months and must be for  recreational purposes only.</p>
	      <p align="justify"><em>IMPORTANT: Excluded from coverage  is any injury, resulting directly or indirectly, from an accident involving a  motorcycle, motor scooter or moped.</em></p>
	      <h2><strong>Does the Preferred Plus Plan cover lost or stolen mobile phones,  camera’s, or other personal equipment?</strong></h2>
	      <p>	        No. As indicated in the Policy Document, coverage is provided  only for the “Loss of Diving Equipment in the event of a Diving Accident”. </p>
	      <p>DAN AP recommends that divers also take out a travel insurance  policy to complement their DAN coverage as this will cover the likes of  lost/stolen equipment and personal belongings, such as phones and cameras.</p>
	      <h2 align="justify"><strong>If I get  sick in the lead-up to a booked and paid overseas diving holiday that my  Physician advises I do not go on, will the cancellation of this trip be  covered?</strong></h2>
	      <p align="justify">If prior to the time and date of departure for a  diving vacation an Insured person suffers an illness or injury that would  substantially impair the Insured Persons ability to dive and requires the  cancellation of a diving vacation, then Yes. Up to US$5,000 per injured person  for losses that the Insured person incurs as a result of the cancellation  (subject to a US$250 deductible).</p>
	      <h2 align="justify"><strong>What  happens if I am on my diving vacation and I become ill or get injured and  cannot complete the remainder of my planned diving vacation?</strong></h2>
	      <p align="justify">If after a diving vacation commences the Insured  Person suffers an illness or injury that prevents the Insured Person from  diving up to US$2,500 (Lifetime Maximum Benefit per Insured Person for losses that  the Insured Person incurs as a result of such interruption, subject to a US$250  deductible, will be paid.</p>
	      <h2 align="justify"><strong>If I am  delayed from returning home due a diving accident will my extra accommodation  be covered until I am well enough to return home?</strong></h2>
	      <p align="justify">On written advice from your attending physician  following a diving accident up to US$200 per day to a Maximum Benefit of  US$3,000 for a covered condition. Bills and receipts of actual expenses must be  provided as well as the letter from the attending physician.</p>
	      <h2 align="justify"><strong>What about  if I am delayed from returning from due a non-diving accident or illness, will  my accommodation be covered? </strong></h2>
	      <p>No.</p>
	      <h2 align="justify"><strong>If I miss my return  flight home after a Diving Accident am I covered under the Extra Transportation  benefit?</strong></h2>
	      <p align="justify">Yes. There is a maximum benefit of up to US$2,000. The delay  must be on the advice of the attending Physician and the note must be provided  as part of a claim. The benefit payable is equal to the difference between a  new economy-class ticket and the remaining value of the original ticket.</p>
	      <h2 align="justify"><strong>Am I covered for pregnancy under the  Non-Diving Medical Coverage component of the plan and/or the Vacation  Cancellation/Interruption component?</strong></h2>
	      <p>No coverage is provided for medical  expenses, which are due to pregnancy, childbirth or miscarriage under the  Preferred Plus plan.</p>
	      <h2 align="justify"><strong>I am a European National, currently  living in the Asia-Pacific. If I return to my home country to visit family and  I have a non-diving accident, will my medical expenses be covered under the  non-diving accident medical component?</strong></h2>
	      <p>If you are returning to the country  for which you hold a Primary Passport, you will not be covered for a non-diving  accident. &nbsp;<br />
	        <br />
Non-diving accident coverage shall  not apply to an Insured Person who holds his/her primary passport from a  country other than the country of his/her Residence, when the Insured Person is  on a Trip to the country that issued that passport. </p>
