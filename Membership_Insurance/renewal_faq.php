          <p><span class="faq">1. When will my new coverage period commence?<a name="faq1" id="faq1"></a></span> </p>
          <p>If you submit your  renewal application prior to your current period expiring, your new coverage  period will commence upon expiration of your existing coverage, to ensure there  is no lapse in your coverage.</p>
          <p class="b2t"><a href="#1faq">Back to Top</a></p>
          <p><span class="faq">2. What if my DAN coverage has lapsed, when will my new coverage commence?<a name="faq2" id="faq2"></a></span><br />
          If your DAN coverage  has lapsed your new coverage period will start from the date your application  is processed. It will NOT be backdated.</p>
          <p class="b2t"><a href="#2faq">Back to Top</a></p>
          <p><span class="faq">3. If my DAN coverage has lapsed can I still select the &quot;Renew Now&quot; option?<a name="faq3" id="faq3"></a></span><br />
          Yes, we encourage you  to use the &ldquo;Renew Now&rdquo; option, as this will identify you as having been a DAN  AP Member.  </p>
          <p class="b2t"><a href="#3faq">Back to Top</a></p>
          <p class="faq">4. How early can I renew my coverage?<a name="faq4" id="faq4"></a></p>
          <p>We cannot process you  renewal more than one month in advance. If you do submit a renewal earlier than  this it will be held on file until we are able to process it.</p>
          <p class="b2t"><a href="#4faq">Back to Top</a></p>
          <p class="faq">5. Why do I have to complete a renewal application each year?<a name="faq5" id="faq5"></a></p>
          <p>DAN AP needs to review and renew its policies with its insurance providers on an annual basis. For this reason, we cannot offer automatic renewals. In addition, as a dive safety  association, we encourage you to review your coverage options each 12-month  period to ensure you are taking out the level of coverage that will best meet  your diving needs for the coming 12-months.</p>
          <p class="b2t"><a href="#5faq">Back to Top</a></p>
          <p class="faq">6. Why does my Membership Number change each year?<a name="faq6" id="faq6"></a></p>
          <p>A new  policy is being created and as such a new member number is allocated. This  number is unique and provides information for rapid verification.</p>
