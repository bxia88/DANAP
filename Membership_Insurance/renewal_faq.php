          <h1>Renewing Member Frequently Asked Questions</h1>
          <p><strong><em>Note:</em></strong> <em>As of May 1, 2015 DAN Asia-Pacific has removed the Standard Plan as one of our Dive Injury Insurance Plan options. As a dive health and safety organisation, removing the  Standard Plan was determined to be most beneficial for Members as the plan is limited in the coverage it provides and the price is only slightly less than the Master Plan, which is more comprehensive in the coverage provided for in-water diving incidents. For all Members who have a current Standard Plan, DAN will honour the coverage  provided under the terms of the policy until it expires. Please contact the DAN  office (Phone: +61-3-9886 9166 or <a href="mailto:info@danap.org">email</a>) if you have any questions.</em></p>
