        <h2>Incidents</h2>

		<dl class="faqs">

		  <dt>Who do I call in the event of an accident or emergency? </dt>

		  <dd>Please review DAN’s Emergency Procedures <a href="../emergency.php">here</a> </dd>

		  <dt>Does DAN TravelAssist need to arrange evacuations? </dt>

		  <dd>Yes. DAN TravelAssist MUST arrange all evacuations if they are to be paid for by DAN TravelAssist.  The determination of the need for an emergency evacuation or medical repatriation will be made by DAN TravelAssist in consultation with a local attending physician and payment will be made direct to the evacuation provider by DAN TravelAssist.</dd>

		 

		  <dt>What is a Covered Diving Accident?</dt>

		  <dd>A Covered Dive means a recreational dive or diving while a scuba instructor, dive master, underwater photographer, or while performing research under the auspices and following the diving safety guidelines equivalent to the American Academy of Underwater Scientists (AAUS).   A Covered Dive begins upon entry into the water and ceases upon exit from the water.  A Covered Dive must begin while Insurance is in force.

A Diving Accident means an Illness or Bodily Injury that occurs during a Covered Dive.

An Accident means a sudden, unexpected, unusual, specific event that occurs at an identifiable time and place during a Covered Dive.

</dd>

		</dl>

