<h1>Frequently Asked Questions</h1>

<p>Questions are provided under various categories. Scroll to the category to find the answer to your question. If you question has not been addressed send an email to info@danap.org or call +61-3-9886 9166.</p>
				   				   

		<div class="faq_contaner">

        <h2>DAN Membership & Insurance</h2>

		<dl class="faqs">

		  <dt>What is DAN Membership?</dt>

		  <dd>DAN Membership covers the cost of an Emergency Medical Evacuation and provides a range of other Travel Assistance benefits. It does not cover any associated treatment costs. DAN Membership also includes a range of non-travel benefits including a subscription to Alert Diver magazine, access to our Member-Portal, a subscription to the e-newsletter, Deeper with DAN, and more.</dd>

		  <dt>What is DAN Insurance? </dt>

		  <dd>DAN AP offers a range of Dive Injury Insurance plans, which are offered in addition to DAN Membership. These plans provide coverage for treatment and certain other costs associated with a covered diving accident as per terms and conditions of the policy.  DAN Asia-Pacific exclusively offers Members access to these dive injury insurance plans only at the time of joining or renewing their Membership.</dd>

		  <dt>What is the evacuation service?  Isn’t that the insurance?</dt>

		  <dd>A benefit of DAN Membership is the Emergency Evacuation service, DAN TravelAssist. It is a Member benefit that, in the event of an emergency you need to be airlifted to a medical facility, and you are more than 80km from home, you can access this service through DAN without a fee. You can use this service during any emergency - even a non-diving one. In order to use this Member benefit you MUST first contact DAN Travel Assist.

		  

No, the Evacuation Coverage is part of your DAN Membership, it is not insurance.</dd>

		  <dt>Does the TravelAssist Emergency Evacuation benefit cover both diving and non-diving incidents? </dt>

		  <dd>Yes, so long as you are 80km from your residential address. The 80km is measured as a direct route by air (i.e. as the crow flies).</dd>

		  <dt>Can I apply for DAN Membership only?  </dt>

		  <dd>Yes.</dd>

		  <dt>Can I apply for Insurance only? </dt>

		  <dd>No. It is a prerequisite that you are a DAN Member to be eligible to apply for DAN Dive Injury (Treatment) Insurance.</dd>

		  <dt>Can I add Insurance once my Membership has started? </dt>

		  <dd>No.  If you would like to add Dive Injury Insurance to your Membership it must be applied for at the same time that your DAN membership application is lodged. Insurance cannot be added at a later date.</dd>

		  <dt>What is the coverage period for DAN Membership and the Dive Injury Insurance Plans?  </dt>

		  <dd>12-Months.</dd>

		  <dt>What are the main differences between the DAN Dive Injury Insurance Plans? </dt>

		  <dd>Please refer to the comparison table in the Membership and Insurance section, which summarises the differences between each plan. <a href="insurancecountrylink.php">CLICK HERE</a></dd>

		  <dt>Are there any countries excluded from Emergency Evacuations? </dt>

		  <dd>It is the responsibility of the Member to inquire as to whether a country is "open" for assistance prior to his/her departure. The following list will provide some guidance but must not be used as a definitive listing as it will change from time to time.

		  

Tibet, Antarctica, the Arctic Circle, Mongolia, Muritania, Guinea, Sierra Leone, Liberia, Nigeria, Rwanda, Angola, Bosnia, Cuba, Bhutan, Haiti, Somalia, Yemen, Afghanistan, Kyrgystan, Tajikistan, and Russia east of the Urals.



Please contact DAN TravelAssist to confirm which countries are excluded at a particular point in time. TravelAssist can be contacted on +1-919-684-3483 (Collect).</dd>

		  <dt>What does DAN mean when it states that DAN Insurance is secondary insurance cover? </dt>

		  <dd>If you have any other pre-existing insurance policies, e.g. travel insurance, health insurance, ambulance cover, your DAN policy becomes secondary coverage and will pay up to 100% of all remaining eligible expenses. Please provide DAN with your existing insurance policy details. If, however, you do not have any other insurance policies DAN will pay up to 100% of all eligible expenses.</dd>

		  <dt>Are there any travel distance restrictions on my policy? </dt>

		  <dd>Distance restrictions apply for divers taking out Membership ONLY. This restriction states that the illness or injury must occur at least 80km (measured as a direct route by air) from their registered home address in order to receive the TravelAssist benefits such as Emergency Evacuation. However, this restriction is waived for a dive accident involving a Member who has taken out any level of dive injury insurance coverage.</dd>

		  <dt>Can I change my policy mid-year, for example change my Insurance Plan option  or add family members? </dt>

		  <dd>No. As such please take your time to consider the year ahead and what coverage both you and your applicable family members may need.</dd>

		  <dt>Are Snorkelling, Freediving and Spearfishing covered by DAN AP's Dive Injury (Treatment) Insurance?</dt>

		  <dd>Treatment for injuries resulting from recreational snorkelling, freediving and spearfishing is covered under the Master, Preferred and Preferred Plus Plans as per terms and conditions of the policy, this excludes any competition-related activity. IMPORTANT: The Standard Plan does not cover snorkelling, freediving or spearfishing. </dd>

		  <dt>Should I also take out Travel Insurance?</dt>

		  <dd>Yes. DAN always encourages divers to take out travel insurance in addition to their DAN coverage when they travel. Travel Insurance offers coverage for things that are not covered by DAN Membership or Insurance, such as medical treatment for non-diving illnesses and accidents and lost luggage.</dd>

		  <dt>Do I also Need General Medical Insurance? </dt>

		  <dd>If you live in a country where you are not covered by an adequate National Medical Health Scheme, then you definitely should have General Medical Insurance to provide coverage for non-diving accidents and illnesses occurring in your country of residence.

		  

The DAN Dive Injury Treatment Insurance plans provide coverage for covered diving accidents only. The Standard, Master and Preferred Plans do not cover medical expenses for non-diving medical problems.



The Preferred Plus Plan (available only to Members who live outside Australia) provides coverage for non-diving accident medical coverage only if you are outside your country of residence for a maximum period of 3 months. It does not provide coverage if the non-diving accident occurs within your country of residence.</dd>

		  <dt>Does DAN offer liability insurance for diving professionals? </dt>

		  <dd>No. </dd>

		  <dt>Does DAN cover lost/stolen dive equipment? </dt>

		  <dd>No.</dd>

		  <dt>Can I apply for coverage if I live on a boat? </dt>

		  <dd>If you are living on a boat and travelling on the boat for periods greater than three-months you cannot join DAN Asia-Pacific, rather you will need to join DAN World: <a href="http://www.danworld.ky">www.danworld.ky</a>, <a href="mailto:info@danworld.ky">Info@DANWorld.ky</a></dd>

		  <dt>If I want to dive deeper than 50 metres on the Preferred Plan, what certification do I need? </dt>

		  <dd>With the Preferred Plan it is stated that a person is covered for a Diving Accident sustained on a dive to any depth provided that the Insured held appropriate certification for the dive, and was using appropriate breathing gas mixes and equipment during the dive or Repetitive Dive Series.

		  

The Preferred Plan is partly designed to provide higher coverage limits for those who desire these. It is also designed to provide coverage for a ‘technical’ diver who has the required training and/or experience to dive in reasonable safety beyond 50m, as long as they are doing so using appropriate equipment and breathing gas. This limitation was introduced to encourage divers to think carefully and be appropriately prepared before they undertake very deep dives.



In the event of a claim for a dive beyond 50m, the appropriateness of a particular dive may be assessed by suitable diving medical and/or technical experts looking at the divers training and certification, as well as their experience at or around the depth of the dive where the accident occurred, to determine if the dive plan was reasonable for that diver. Please  refer to the Policy Wording for full details. </dd>

		  <dt>If I am lost at sea or otherwise stranded or lost on land or at sea, is "Search and Rescue" covered with my Membership Evacuation benefit? </dt>

		  <dd>No, Emergency Evacuation does not include efforts to locate a person (injured or un-injured) whose location is unknown, or efforts to rescue such persons from a dangerous situation or location.</dd>

		  <dt>A number of insurance companies offer insurance.  What is the DAN advantage? </dt>

		  <dd>DAN are the experts in diving accident management, with a vast network and experience in dealing with dive emergencies throughout the world.  DAN is Your Dive Safety Association and because of this you receive more than just an insurance  policy.  You receive all the benefits of Membership, such as TravelAssist benefits, subscription to Alert Diver magazine, and access to Medical Information.  Plus you will be helping support the funding/provision of 24-hour Diving Emergency Hotlines throughout the Asia-Pacific region.</dd>

		  <dt>Is DAN owned by any of the training agencies? </dt>

		  <dd>No.  DAN Asia-Pacific is an independent not-for-profit organisation with its head office in Melbourne, Australia.  We are part of the International Network of DAN Dive Safety Associations with worldwide membership exceeding 300,000.  We endeavour to work co-operatively with each of the training agencies in our quest to improve safety in recreational diving for all divers.  We are not owned by any training agency, business or shareholders, we are an association.</dd>

		  <dt>What happens when a government or international agency issues a travel safety warning about a particular country or region – is DAN Membership and Insurance benefits still valid? </dt>

		  <dd>Regarding evacuations, transportation arrangements and other TravelAssist benefits that are a part of every DAN Membership, DAN’s Assistance company will make every effort to evacuate or assist an injured member as long as it is deemed safe to do so at the time the Member requires it.  

		  

Delays however may occur when locations are experiencing civil unrest or infrastructure facilities are impaired by natural disasters, war or other violence or catastrophes. Periodic,  government or international agency travel warnings alone would not negate an appropriate attempt to provide an emergency medical evacuation or repatriation.  For example, if a travel warning was issued and the nearest airport was closed due to war, terrorism, violence, riots, natural disasters, etc., preventing a Members evacuation, DAN TravelAssist would attempt to provide assistance via other locations or other means or at the next earliest available time, subject to TravelAssist standard provisions.   



DAN’s optional dive accident insurance is valid for dive medical treatment expenses that occur as a result of a covered diving accident. If a travel advisory were issued for a country or area and the Member was treated at a medical facility during the period that the travel advisory was in effect, the Member would still be able to file a claim with their DAN insurance upon returning home or at a later date up to 1 year following the accident.</dd>
		  </dl>

