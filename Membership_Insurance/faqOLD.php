        <h2>Application Process</h2>

		<dl class="faqs">

		  <dt>Who can apply for DAN Insurance?</dt>

		  <dd>Any recreational diver or snorkeller, (including instructors and divemasters supervising recreational diving activities), who is a Member of DAN Asia-Pacific and is 12 years of age or older. It's also available to certain research divers & underwater photographers. Coverage is not available for commercial diving other than those activities specified.</dd>

		  <dt>Who is eligible for a family membership?</dt>

		  <dd>A Family Membership is defined as you and your Spouse or Domestic Partner or dependent; Child if unmarried and under 18 yrs old or; your unmarried child who is 18 yrs old or until his or her 24th birthday if that child, a) is enrolled as a full-time student at an accredited school or college and; b) is not employed on a full-time basis; and c) has the same home permanent address as the parent.</dd>
		  		  <dd>DOMESTIC PARTNER means a person who is at least eighteen years of age and the Eligible Person can show: 1) evidence of financial interdependence, such as joint bank accounts or credit cards, jointly owned property, and mutual life insurance or pension beneficiary designations; 2) evidence of cohabitation for at least the previous {6} months; and 3) an affidavit of domestic partnership if recognized by the jurisdiction within which they reside.</dd>

		  <dt>Can I take out insurance cover for all my family members?</dt>

		  <dd>If all the members in your Family Membership are divers then 'yes'. Dive Injury Plans are per person. Each family member is permitted to take out a different Dive Injury Insurance Plan depending on his/her individual needs. If you have members under your family membership who are not divers they need not apply for dive insurance cover, however they will be covered for TravelAssist benefits, including Emergency Evacuation, under your Family Membership.</dd>

		  <dt>I’ve applied on-line, now what?</dt>

		<dd><ul>

		  <li>An automated “Thank you” response will be generated once you submit your. <strong>THIS DOES NOT MEAN YOUR COVERAGE HAS BEEN IMPLEMENTED. YOUR COVERAGE IS NOT ACTIVE UNTIL YOU HAVE RECEIVED A CONFIRMATION EMAIL WITH YOUR MEMBERSHIP NUMBER.</strong></li>

		  <li> If there is a problem with your application the DAN AP Membership Services Team will endeavour to make contact with you by phone and/or email in order to get the required information in order to finalise your application. </li>

		  <li> Once processed you will receive an email from the DAN Head Office advising you of your Membership Number and Optional Dive Injury Insurance coverage (where applicable) as well as identifying the relevant DAN 24-Hour Hotline Numbers.

		    IF YOU HAVE APPLIED FOR COVERAGE CLOSE TO A DIVE TRIP, WE ENCOURAGE YOU TO PRINT OFF YOUR CONFIRMATION EMAIL AS PROOF OF YOUR COVERAGE. </li>

		  <li> Following processing, you can expect to receive your Membership Pack by post anywhere between 1-3 weeks depending on where you live.</li>

		  </ul></dd>

		  <dt>How long will it take to process my application?</dt>

		  <dd>The normal processing time is within 1 week unless Fast Processing (see below) is selected. Please allow sufficient time for your local postal services to deliver your Membership Pack, which may take several weeks depending on your location.		  </dd>

		  <dt>If I select Fast Processing how long will it take to process my application? Does this option mean I will receive my Membership Pack quicker?</dt>

		  <dd>For an additional cost of $10 (+GST in Australia) your application can be Fast Processed. Fast processing means your application will normally (excluding payment/medical issues) be processed within 24 hours from the time of receipt at the DAN Asia-Pacific Office providing it is received on a week day before 2pm Friday. If received on a weekend your application will be processed within 72 hours from the time of receipt at DAN Asia-Pacific’s Head Office.

Fast Processing will not speed up the delivery of your Membership Pack in the post.</dd>
		  <dt>What information must  I disclose about a pre-existing / pre-disposing medical condition, serious  injury or previous decompression illness (DCI)?</dt>
		  <dd>It is important that you notify the DAN team of any  significant injury or medical condition you have suffered or are currently  suffering from, as well as regular medications you are taking. This  requirement is not onerous and is there to help guide our members to discuss  any potential problems with a diving doctor, if appropriate. You  can do this directly from the online application form or by downloading a  declaration form <a href="../_pdf/medicaldeclaration.pdf">(click  here)</a>. You need to mark the relevant section of the form and submit it with  your application. In some cases, the diver may be required to obtain a Fitness  to Dive certificate in accordance with the guidelines of the South Pacific  Underwater Medicine Society (SPUMS). However, in most cases to date, no  further action is necessary. The information also provides us with some valuable  information about the types of conditions that people are diving with. </dd>
		  <dt>Are any exclusions applicable to Pre-existing or Predisposing Medical Conditions?</dt>
 <dd>No benefit will be paid under any plan for charges in connection with any injury or illness arising directly or indirectly from any Predisposing or Pre-existing Condition – the only exception to this is a previous case of Decompression Illness (DCI) that has been declared to DAN and accepted by the insurers.</dd>
 <dd><strong>Predisposing conditions </strong>are any condition that you have prior to taking out the insurance which may put you at risk of having a diving accident. These include, but are not limited to, epilepsy, diabetes, asthma, cardiovascular disease or cardiac condition, previous DCI or major surgery.   </dd>
 <dd><strong>Pre-existing conditions</strong> are medical conditions which existed and for which diagnosis, treatment and/or medication was received within the 12 months prior to taking out the insurance.
   It is your duty to disclose any predisposing or pre-existing conditions to DAN. </dd>
 <dt>I am not a resident of the Asia-Pacific region what do I do?</dt>

		  <dd>•	If you do not intend to reside in the Asia-Pacific region for longer than 3 months then you will need to join the DAN organisation that relates to your place of permanent residency.

            <br />

            •	If you intend to reside in Australia for greater than 3 months then you can join DAN Asia-Pacific.  However, if you are also planning to purchase the optional dive injury (treatment) insurance then you will need to be intending to reside in Australia for a period of 6 months or longer.

            <br />

            •	If you intend to reside in another country within the Asia-Pacific region (excluding Japan) for a period greater than 3 months then you will need to join DAN Asia-Pacific and you have the opportunity to purchase the optional dive injury (treatment) insurance, providing that your stay is 3 months or longer.



Please note should you make a claim on your optional dive injury (treatment) insurance you may be required to provide evidence of your long-term residency in the region for your claim to be approved.</dd>
		  </dl>

