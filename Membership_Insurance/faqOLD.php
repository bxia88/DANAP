        <h2>Travelling</h2>

		<dl class="faqs">

		  <dt>What happens if I am moving or travelling for an extended period away from the address of my DAN registration?</dt>

		  <dd>Whenever you move from the address of residence used when you enrolled as a DAN Asia-Pacific Member, you must inform DAN Asia-Pacific of your new address: phone +61 3 9886 9166 or email <a href="mailto:info@danap.org">info@danap.org</a>. This is important for insurance regulation purposes and to make sure you receive all the important Member notices and safety information benefits you are entitled to. If you will be moving out of the country of your DAN Registration, or away for an extended period (3 months or more), it is important that you notify the DAN team in order to ensure a claim is approved in the event of an accident, and, that you have the appropriate cover for your new country of residence (or where you will be traveling most of the time).</dd>

		  

		  <dt>I have a residence in the Asia-Pacific region but I travel a lot for work.  Can I still apply for DAN AP coverage?</dt>

		  <dd>DAN coverage is based on where you are physically living the majority of the time. Contact the DAN Office to discuss your personal situation: <a href="mailto:info@danap.org">info@danap.org</a> or call +61-3-9886 9166.</dd>

		 

		  <dt>If DAN is a worldwide organisation why do I need to change region when I move to other parts of the world?</dt>

		  <dd>If you live in another country (other than from where you joined DAN) for an extended period of time you might fall under the insurance laws set out by that country which could be different in some ways to those on which your policy is structured.

		  

As such, it is essential that you inform DAN Asia-Pacific when you are planning move to another country for a period or three or more months as some of your benefits may be effected.

</dd>

		</dl>

