        <h1 align="left">Divers Alert Network</h1>
          <p align="justify">As the conclusion of your complimentary DAN Asia-Pacific Short Term Coverage from Dive Damai, you may like to consider joining the Divers Alert Network as an annual member, in order to be protected for all your diving throughout the year, worldwide. </p>
          <h2><strong>Who is DAN?</strong></h2>
	      <p align="justify">	        The Divers Alert Network (DAN)  is a global network of not-for-profit, member-based, dive safety organisations  working for the safety of all divers through education, research and training. The organisation has 30+ years of experience and worldwide membership presently exceeds 300,000. As a diver it's important to be prepared for unexpected diving accidents and illnesses.  Being a Member of DAN means that&nbsp;wherever you  live or dive around the world&nbsp;you have peace of mind knowing that DAN, the  experts in diver accident management, is available 24-hours a day, 7 days a week  to assist in the event of an accident/illness. </p>
	      <h2 align="justify">Why is it so  important to be protected with DAN Membership &amp; Dive Injury  (Treatment) Insurance?</h2>
	      <p align="justify">Essentially, it&rsquo;s all about being prepared  for the unexpected, namely dive accidents and illnesses. It&rsquo;s essential that  divers have a reputable organisation to call, that is highly experienced and knowledgeable in the management of dive accidents, and will be able to co-ordinate a  rescue effort and get them to the most appropriate treatment for their injury  or illness. And then there is the issue of cost. DAN protection ensures the  diver will not be lumbered with the burden of a potentially hefty bill in  the event of an accident or illness.</p>
	      <h2><strong>Example</strong>s of Divers Requiring Emergency Evacuation &amp; Treatment </h2>
	      <h6 align="justify"><strong>Solomon Islands </strong></h6>
	      <p align="justify"><strong>A paralysed diver was evacuated from the Solomon Islands to Australia. The  evacuation cost was around AUD$90,000 (as an aircraft had to be sourced from  far away). In addition, the treatment costs were in excess of US$30,000, plus  on-going expenses.</strong></p>
	      <h6 align="justify"><strong>Truk Lagoon </strong></h6>
        <p align="justify"><strong>A diver was evacuated from Truk Lagoon to Guam as the local  chamber did not have staff ready to treat this diver requiring urgent care. The  short flight cost $60,000. The diver underwent one 5-hour treatment and then  spent a further 5-days in hospital at an additional cost of US$10,000.<br />
        </strong></p>
