      <h1>我的潜水事件</h1>

        <p><strong>地点：</strong>菲律宾的马拉帕斯卡岛。</p>
        <p><strong>潜水员：</strong>经验丰富，完成超过700次潜水。 </p>
        <h2><strong>事发当天之前的潜水纪录</strong></h2>
        <p><strong> </strong>第一天：第一次潜水深度为25公尺，水面休息时间为2小时57分。第二次潜水深度为24公尺，水面休息时间为3小时47分。第三次潜水深度为20公尺，水面休息时间为2小时51分。第四次潜水深度为9公尺。 </p>
        <p>第二天：第一次潜水深度为24公尺，水面休息时间为3小时35分。第二次潜水深度为21公尺，水面休息时间为1小时33分，第三次潜水深度为22公尺。 </p>
        <h2><strong>事发当天</strong></h2>
        <p>这位潜水员不想错过再次欣赏长尾鲨的机会，所以连续第三天一大早就开始潜水，当时她觉得很疲倦。那次潜水深度是28公尺，时间为59分钟，呼吸高氧EAN32。 </p>
        <p>当这位潜水员疲惫时，她有时会经历窦压问题，并在下潜时感到不适。在那天早上，她并未感到任何不适。 </p>
        <p><strong>这位潜水员表示：</strong><em>「这次潜水的不寻常之处，就是我是在上升时感到窦压，所以为了应对窦压问题，我们上升时间比平常更慢，但还是感到不适。」</em> </p>
        <p align="center"><img src="_images/psh_photo.jpg" width="423" height="317" /></p>
        <h2><strong>症状发生</strong></h2>
        <p>在这位潜水员回到岸边的途中，她的双膝开始感到微微刺痛 ，所以她要求进行纯气急救。幸好船上就备有纯气，所以她开始进行纯氧呼吸，并且持续到船靠岸为止（约25分钟）。上岸后潜水店业主检查她的症状，然后立刻拨打DAN潜水紧急服务热线。 </p>
        <p>DAN建议这位潜水员继续进行几小时的100%纯氧呼吸，并且好好休息，观察症状是否减轻。她在这段期间喝了很多水，并且依照DAN医师建议，服用布洛芬止痛消炎药（Ibuprofen）。可惜这位潜水员的状况并因此未改善。她说：<em>「我开始出现奇怪感觉，彷彿自己在腾云驾雾。我虽然醒着跟别人交谈，但却觉得非常不真实。」</em></p>
        <p>DAN建议她必须进行减压舱治疗，这位潜水员经由潜水中心的协助前往菲律宾宿雾省，因为那裡有距离最近的合适设备。她先搭船，然后再搭车。在她前往减压舱的路上，她都获得纯氧供给。 </p>
 	    <p>整趟路程花费将近4小时，因为当地正在进行庆典活动，所以才比平常处理时间来得久。这位潜水员在晚间10点抵达减压舱，多亏有DAN的协调安排，医生、护士和减压舱操作员都已就绪待命。这位潜水员开始接受检验，一位军方义工护士进入减压舱，在为期4小时50分钟的治疗中照顾她。身处狭小封闭的环境，而且又不能入睡（为了确保氧气能正确给予），这位潜水员确实很不好受。幸好她只需要进行一次治疗。 </p>
 	    <h2><strong>这位潜水员表示</strong></h2>
 	    <p><em>我从没想过自己会罹患减压疾病，也没想过自己得进减压舱进行治疗。我的潜水压力纪录总是很保守：我只在限制范围内潜水，而且还有潜水电脑表辅助，大部份的潜水经验中也使用</em><em>EAN  Nitrox</em><em>。我现在领悟到一件事，那就是对于身体的反应，谁也没有百分之百的把握。我当时非常疲倦，如果我决定不要潜水，或许我就不会罹患减压疾病。我奉劝各位要小心注意身体警讯。如果你的身体有疲倦现象，请你无论如何都不要忽略这警讯。否则你最后不会欢享假期，而是待在减压舱裡接受治疗…。</em></p>
 	    <p><em>谢谢帮助过我的大家。多亏有了他们，让我今天身体健康良好。自从</em><em>DAN</em><em>接到电话，得知我的状况之后，他们就对我提供许多帮助。他们的专业精神及照顾效率良好，让人感动。</em></p>
 	    <h2><strong>DAN</strong><strong>亚太区分部执行长约翰李普曼的结论</strong></h2>
 	    <p> 	      当医师接到这通热线电话，他听完这位潜水员描述的事发经过，就诊断她的头痛可能是由窦气压伤所造成。医师认为她的刺痛疑似减压疾病。布洛芬（Ibuprofen）及其他相似的非类固醇抗发炎药物都有助于控制这些症状。她起初藉由纯氧呼吸改善症状，这种情况其实并不罕见，因为纯氧呼吸有助于消除气泡及身体组织内的过多氮气，但随后出现的症状让人担忧，所以我们马上建议她前往减压舱。在取得减压舱的使用上本来遭遇困难，但后来还是排除困难了。所幸她在接受治疗时反应非常良好。 </p>
 	    <p>这位潜水员本人就提供了良好建议，若我们感到过度疲惫或身体不适，就不应该进行潜水。她可能在先前潜水时就已罹患非常轻微的减压疾病，只是在这次潜水时恶化。</p>
 	    <p>&nbsp;</p>
 	    <h6><strong>想出国潜水吗</strong><strong>? </strong><strong>别在没有</strong><strong>DAN</strong><strong>的保护下出国。</strong><strong> </strong></h6>
 	    <p> 	      在国外，发生潜水事故的紧急撤离与治疗费用相当昂贵。 因此，最好的办法是加入潜水事故管理专家的潜水员警示网，成为DAN的会员。 </p>
 	    <p><em>为意料之外的事故与疾病做好准备是潜水员的必备责任。成为DAN的会员，不管你生活在世界何处或潜水，都能安心仰赖DAN每周7日，每天24小时的潜水意外伤害救援服务。<u><a href="http://www.danap.org/chinese">www.danap.org/Chinese</a><a href="httph://www.danap.org/chinese"></a></u></em></p>
