	      <h1>Not Your Typical Honeymoon  </h1> 
          <h3><em>By  John Lippmann, DAN Asia-Pacific's Founder, Chairman and Director of Research</em></h3>
          <p><em>Here  a DAN AP Member recounts what turned out to be a very memorable honeymoon, for  all the wrong reasons.</em></p>
          <p>On  the second day after arriving in the Maldives for my Honeymoon I undertook my  first dive. It was an incredible boat dive, the water was a&nbsp;lovely&nbsp;28  degrees and as a bonus&nbsp;I was diving in my board shorts. </p>
          <p>The  next day I booked in for two&nbsp;dives and&nbsp;from then on I did at least  one dive per day over the next five days. As well as diving every day&nbsp;I  was also snorkelling a few hours at a time throughout the day and&nbsp;probably  not drinking as much as water as I should have. I was&nbsp;also lying out in  the sun&nbsp;in between&nbsp;diving or snorkelling.</p>
          <p>Halfway  through our honeymoon I started a Nitrox course as the dive instructor advised  that it would allow me to undertake more dives, which sounded great to me!  During the dives on Nitrox I was getting bad headaches. On what turned out to  be my last dive, I descended and my headache started straight away. On previous  dives the headaches were occurring at the end of the dives. </p>
          <p>During  the dive my face felt like I had a rash on it, but there was nothing visible.  My legs were numb and I felt very weak so I decided to ascend.&nbsp;I stayed at  15m and my symptoms seemed to improve, not completely, but not as serious as  before, so I stayed at 10m.&nbsp; </p>
          <p>For  the rest of the day I stayed out of the sun, rested and drank lots of water.  Later that day&nbsp;I went back to the dive centre and advised them of my  symptoms: I still felt tingles in my face&nbsp;and my whole right arm, and my  shoulder felt like it had pins and needles.&nbsp; </p>
          <p>The owner of the dive centre  advised that he could give&nbsp;me oxygen but he didn't think that I needed it  or that I had DCI. He also mentioned there was a medical centre on the next  island. I felt I was suffering from DCI so I made the trip to the medical  centre. It was quite basic but it had recompression chamber, and the doctors  were divers themselves, which put me at ease. Upon seeing me the doctor put me  straight onto oxygen and asked if I had insurance. Fortunately, I was a DAN  Asia-Pacific Member (with the additional Treatment Insurance Coverage).&nbsp; </p>
          <p>The  doctors called DAN straight away&nbsp;and got onto John&nbsp;Lippmann <em>(DAN  Asia-Pacific Executive Director)</em>. I was re-assured that DAN would cover all  my expenses and I didn't have to worry about anything, which made me feel a lot  better about my situation. I was so happy that I was with DAN.&nbsp;It is not  a&nbsp;good feeing to be in that kind of situation and&nbsp;having to worry  about whether you can afford the treatments and who is paying for what. <br />
            Within an hour I was put in the recompression chamber  for six hours. Once the treatment commenced the tingling in my arm stopped. <br />
  &nbsp;<br />
            The next day I returned to the Medical Centre for a three hour treatment in the  chamber. This was more of a precautionary measure from the doctors, as they  wanted to ensure I was fit to fly home in the next few days.&nbsp;It took a few  days for the&nbsp;feeling in my face and arm to return to normal. Once back  home in Australia I was advised by a Diving Doctor not to dive for four weeks.  Fortunately, I am&nbsp;diving again and loving it just as much as before.</p>
          <p><em>What have I learnt from this experience?</em>  If I go on  another diving holiday I will regulate the number of dives I undertake and make  sure I have a break in between.</p>
        <h2><strong>Important Considerations for Divers </strong></h2>
          <p><em>Individual  susceptibility to DCI varies greatly.</em> Never assume that because others doing the same  dives as you don’t have symptoms that you cannot be suffering from DCI. Call a  DAN-supported hotline for advice.</p>
          <p><em>Most  instructors and divemasters have inadequate training and experience to diagnose  DCI</em> (and  most other medical conditions). If you have any signs or symptoms after diving,  call a DAN hotline for expert diving medical advice.</p>
          <p><em>Divers  experiencing symptoms commonly associated with DCI should immediately be given  oxygen first aid.</em> It is a good idea to check with your dive operator whether they are  equipped and trained to help you in an emergency situation.</p>
          <p><em>Divers should  not continue diving if they exhibit symptoms consistent with DCI without first  getting expert dive medical advice.</em> Why? Continuing to dive often worsens symptoms and  therefore the seriousness of a situation. It can mean the difference between a  speedy resolution of the problem, considerably more treatment and occasionally  a permanent life-changing injury. Don’t risk it.<br />
		  
          </p>
