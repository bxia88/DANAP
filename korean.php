        <h1>왜 <strong>DAN </strong>보장이 중요할까요<strong>?</strong> </h1>
        <p>국내 다이빙 사고 발생시 치료를 위한 병원비 보다 응급 후송에 더 큰 비용이 
지출 될 수 있습니다. 해외에서 사고 발생시 가까운 의료 시설로 비상 후송과 <br />
치료에 상상 하기 힘든 수준의 비싼 금액이 청구 되며 이는 가정 경제에 큰 
부담으로 다가 올 수 있기 때문입니다.</p>
        <p>          DAN은 지 난 3  0 년 이 상 전 세 계 어 디에서,  언제나 다 이버들을 도 와 왔 습니다 . 
          DAN은 다이빙 사고 전문가로써 회원들에게 가치를 따질 수 없는 <br />
          안전을  위한 
          보호막  입니다.<br />
        </p>
