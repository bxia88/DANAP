        <p><br />
          The Divers Alert Network Asia-Pacific (DAN AP)은 다이빙 안전을 추구하는 
비영리 단체이며 한국, 중국, 인도와 동남 아시아 그리고 호주를 포함하는 <br />
광범위한 아시아 태평양 지역에 서비스를 제공 합니다.</p>
        <p>          전 세계적으로 300,000 명 이상의 다이버들이 DAN 회원으로 가입되어 
          있으며 60,000 건 이 넘는 응급 상황을 처리해 왔습니다.</p>
