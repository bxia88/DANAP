        <h1>Cardiac-Related Diving Fatalities</h1>
        <h2><strong>Did You Know that more than 30% of  Diving Deaths are Cardiac-Related?</strong></h2>
        <p align="justify">Although the median age of divers certified by PADI each  year is 29 years, there are many &ldquo;older divers&rdquo; participating in recreational  diving. Some of us have been diving for a long time, while others have learnt  to dive later in life when the opportunity arose.</p>
        <p align="justify">&nbsp;DAN and other diving  fatality data show  that divers over about 45 years old have a substantially increased risk of  death while diving and many of these are the result of cardiac-related problems  that were triggered by the cardiac stressors in the diving environment.</p>
        <p>          Diving  includes many stressors on the heart, which include, but are not limited  to:</p>
        <ul>
          <li>Immersion</li>
          <li>Exertion</li>
          <li>Breathing Restriction from a wetsuit and BCD</li>
          <li>Wearing heavy Equipment</li>
          <li>Breathing denser gas at increased partial  pressures</li>
          <li>Dehydration</li>
          <li>Gas Changes Within the Body </li>
        </ul>
        <p align="justify">Sadly,  these incidents often occur in divers who were unaware of any pre-existing heart  problems or disease. <em>So how can we work  to reduce the prevalence of such fatalities?</em></p>
        <p align="justify">          At DAN  Asia-Pacific, we encourage male divers aged 45 and over and female divers of 55  and over to visit a doctor, preferably one trained in diving medicine. A doctor  who is trained in diving medicine should be more aware of the stressors of the  diving environment and how these can affect a susceptible person. Such a  consultation provides an opportunity to discuss the diver&rsquo;s diving practices  and health and determine whether any particular medical testing is advisable.  Often a good medical history and examination will be sufficient. However,  sometimes an exercise stress test and/or an ultrasound of the heart may also be  appropriate. We also recommend that such medical assessments be done at least  every two years, depending on the overall health of the person.&nbsp; </p>
        <p align="justify">          Other simple prevention tips include being reviewed by a  diving doctor after any significant change in health, and not diving if you  feel unwell. The latter can sometimes be a hard choice if you are on a planned  and much-anticipated diving holiday, or you feel you will be letting your dive  buddy down.&nbsp; However, safety really is  the best policy. </p>
        <p align="justify">          Remember that DAN is always available 24/7 to provide  immediate advice in a dive emergency, and during our office hours to provide  feedback in non-emergency situations. </p>
        <h2 align="justify">Can You Help Create Awareness for this Safety Campaign?</h2>
        <p align="justify">          For this safety campaign, DAN has created a poster. We  hope that sharing the reality that 30% of diving fatalities are cardiac-related  will encourage more &ldquo;older divers&rdquo; to pay attention to their health, so they  can continue diving for many years to come. We encourage you to request a copy  of this poster to promote in your dive centre or dive club. To obtain a copy send  us an <a href="mailto:info@danasiapacific.org">email.</a></p>
