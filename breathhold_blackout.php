        <h1>Breathhold Blackout Safety Campaign</h1>
        <p align="justify">The Divers Alert Network (DAN) has initiated a Breath-holding  Blackout Safety Campaign, which is supported by Royal Life Saving Society -  Australia (RLSSA). The campaign was created to reinforce that extended breath-holding  can cause sudden unconsciousness, and in the water this is often fatal.</p>
        <p align="justify">          DAN data show that, in Australia, 10-12 people die each  year while breath-hold diving or surface snorkelling. An increasing number of  the snorkelling-related deaths occur in older divers due to heart problems  (some of these can be avoided by appropriate health checks). In addition, there  is an average of two deaths per year in predominantly younger breath-hold  divers as a result of extended breath-holding, with or without prior  hyperventilation. </p>
        <p align="justify">          It has been known for years that pre-breath-hold  hyperventilation can cause a diver to blackout without warning. Despite this  knowledge, hyperventilation is still widely-practiced in the misguided belief  that &ldquo;if I don&rsquo;t do more than a couple of breaths if won&rsquo;t happen&rdquo;. The reality  is that it does happen; and it happens to both inexperienced as well as  highly-capable breath-hold divers.</p>
        <p align="justify">          To promote this message, DAN has developed a poster  that simply highlights how breath-holding blackout can occur and how it can be  avoided. </p>
        <p><strong>Can you help DAN to promote this  important campaign?</strong> Breath-holding Blackout campaign posters are available upon  request by emailing <a href="mailto:marketing@danasiapacific.org">marketing@danasiapacific.org</a>. </p>
