        <h1>Member Question</h1>
         
        <h2><strong>Diving Medical</strong>s</h2>
        <p><em><strong>Question: </strong>I just had my yearly  physical examination and was wondering if there is a yearly (or every other  year) &ldquo;diving physical&rdquo;? In other words, if I wanted to make sure I was fit for  diving what would I specifically want checked that wasn&rsquo;t looked at in the normal  physical? </em></p>
        <h1>DAN Answer</h1>
        <p>The need for diving medicals has been an ongoing  controversy. Many consider them to be unnecessary for most divers and that a  comprehensive questionnaire will identify anyone with a medical condition that  increases risk, and only the people so identified should have a diving medical.  I disagree.</p>
        <p align="justify">I believe anyone contemplating  scuba diving should ideally have a medical with a doctor with special training  in diving medicine. No other sport gives such a variety of physical and  psychological stresses, so frequently, in such complex combinations and over  such short periods of time. The diving population is ever widening in age and  fitness level.</p>
        <p align="justify">A thorough medical with special  emphasis on ears, sinuses, lungs and heart is merited. Risk factors for heart  disease especially need to be noted and stress tests of the heart performed in  anyone with several factors. Many diseases that seem to be irrelevant to diving  may indeed have special significance. Any added risk, whether major or minor,  can be identified, its implications and ways to minimise it discussed.</p>
        <p align="justify">This is the true purpose of a  diving medical. It&rsquo;s not a question of being fit or unfit but of risk  identification, risk minimisation, and risk acceptance or refusal.</p>
        <p align="justify"><em>Answer is provided by Dr John Parker who is  a Diving Physician and Senior Dive Medical Consultant for DAN Asia-Pacific.</em></p>
