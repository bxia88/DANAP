      <h1>A Different Kind of Souvenier </h1>

        <p>By Marty McCafferty, EMT-P, DMT</p>
        <h2><strong>The Diver </strong> </h2>
        <p>The diver was a 49-year-old female with more than 200 lifetime dives.   She had no known medical history, took no medications regularly and was   generally healthy and fit.</p>
        <h2><strong>The Dives</strong></h2>
        <p>Her trip was to a popular Caribbean island for a weeklong dive vacation.   Over six days she completed 21 problem-free dives, all of which were on   air and were within the no-decompression limits of her computer. She   performed a safety stop at the end of each dive. The maximum depth of   her deepest dive was 34m; the rest of the dives ranged from 9m to   27.5m deep. The diver refrained from excessive consumption of alcohol   and other behaviors that might have negatively affected her health or   safety while on vacation.</p>
        <h2><strong>The Symptoms </strong></h2>
        <p>On the day  of her departure from the island, the diver's pre-flight surface interval was  greater than 24 hours. She had no specific complaints but did have a general  sense of feeling unwell. <br />
          <br />
During the first of her two flights she began to experience deep muscle and  joint aches. The discomfort was in multiple areas and was accompanied by nausea  and a severe headache. The symptoms did not resolve on the ground between  flights, and they did not worsen during the second flight. She arrived home and  hoped to recover by getting some rest. After a less-than-recuperative night's  sleep her symptoms did not diminish, and, concerned about the possibility of  decompression sickness (DCS), she decided to contact DAN®.</p>
        <p align="center"><img src="_images/joint_ache.jpg" width="350" height="529" /></p>
        <p>The DAN medic to whom she spoke suggested that she seek medical   attention at the closest emergency department. While the possibility of   decompression injury could not be excluded, the timing and the nature of   her symptoms made other possible explanations more likely. When she   arrived at the emergency department she had a fever of 103°F (39.44° Celsius). She   underwent a variety of diagnostic procedures, including several   laboratory tests and thorough physical and neurological examinations.   All indications pointed to a diagnosis of dengue fever, not DCS.<br />
          <br />
        The   hospital staff administered appropriate supportive care, which included   IV fluids and medications to help manage her pain and nausea. She was   ultimately discharged, and the doctor instructed her family on how to   monitor her condition and continue her care. The fever and nausea   subsided within three days, and the aches and headache finally resolved   after two weeks.</p>
        <h2><strong>The Discussion</strong></h2>
        <p>This case should serve as a reminder that travel to tropical areas   exposes divers to risks beyond those associated with diving. The timing   and nature of this diver's symptoms justifiably led her to suspect the   symptoms may not have been related to diving. However, she wisely   deferred to the expertise of medical professionals and, in doing so,   received prompt evaluation and care for the illness she had. (It is   worth noting that the established flying-after-diving guidelines are   associated with a consistently high safety record.)<br />
          <br />
          The   importance of receiving a medical assessment that considers multiple   possible diagnoses cannot be overstated. While localised pain and severe   fatigue are common symptoms of DCS, generalized muscle pains and fever   are not. The evaluating physician recognised this and expanded the   differential diagnosis list accordingly. The diagnosis of dengue fever   is made on the basis of clinical findings (signs and symptoms) as well   as history of travel to known risk areas. Fortunately, this tropical   disease is not generally life-threatening, and full recovery is expected   within a few weeks, as occurred in this case.<br />
          <br />
          Most divers are   familiar with the signs and symptoms of DCS, but it is important to   remember that these signs and symptoms are not exclusive to DCS and may   indicate other maladies.</p>
        <p align="center"><img src="_images/mosquito.jpg" width="270" height="170" /></p>
        <h2><strong>About Dengue </strong></h2>
        <p>Dengue fever is a mosquito-borne infection. The infection is  characterised by an acute onset of a fever three to 14 days after being bitten  by an infected mosquito. The classic dengue fever presents, after this  incubation period, as follows:<br />
          <br />
• Acute fever<br />
• Muscle and joint aches/pain <br />
• Severe frontal headache <br />
• Acute pain behind the eyes<br />
• Nausea and loss of appetite<br />
• Rash<br />
• In rare cases, bleeding<br />
<br />
The pain associated with the disease can be severe, especially in subsequent  infections. This severe pain gave rise to dengue's nickname, &quot;breakbone  fever.&quot; Medical care for infected people is aimed at symptom reduction, as  there is no cure (nor is there a vaccine). The fever and pain should be managed  with acetaminophen (Tylenol); avoid aspirin, ibuprofen, naproxen and other nonsteroidal  anti-inflammatory drugs (NSAIDs) as these can promote bleeding.<br />
<br />
In most cases the fever resolves within a few days, but fatigue may persist for  days to weeks. As always, if you develop symptoms after diving, do not hesitate  to contact DAN.</p>
