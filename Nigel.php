      <h1 class="style4">Incident Analysis for Nigel's Incident in Kadavu, Fiji </h1>
      <p><em>By DAN AP's John Lippmann and Scott Jamieson </em></p>
      <p>Following is a review of some of the issues associated with Nigel's incident and CAGE (Cerebral Arterial Gas Embolism) diagnosis: </p>
      <h2><strong>Oxygen Availability</strong></h2>
      <p>        It was  fortunate that the dive operator was well equipped with oxygen  and staff trained in oxygen provision so that oxygen first aid could  be commenced immediately. In addition, the operator had a non-rebreather mask  available, which provides a high level of oxygen to a breathing diver: 70-90%.  The flow rate was also in the recommended range of 10-15 lpm, albeit at the  lower end. It is likely that the outcome of this incident would have been very  different without the immediate and prolonged oxygen first aid. </p>
      <h2><strong>Immediate  Call to DAN DES Hotline</strong></h2>
      <p>        Fast action  in calling the DAN DES Hotline is to be commended as it meant that emergency  evacuation procedures were investigated immediately. It also meant the  instructions of the DES Doctors could be followed from the outset, such as the  instruction for the diver to continue breathing oxygen all night at the highest  possible flow rate. The hospital, in an effort to conserve oxygen, suggested  lowering the flow rate to 4 lpm, however the DAN DES Doctor and DAN On-Call  Staff rejected this as high concentration oxygen was vital to help reduce the  divers symptoms. </p>
      <h2><strong>Issues  Associated with Remoteness</strong></h2>
      <p>The diver in  this instance actually chose Fiji because it was “not too remote”.  Unfortunately, even being close to a major city and international airport can  still be considered remote depending on the associated circumstances.  Whilst Suva has a recompression chamber  (which is something many places don’t),  no technician was available to run the chamber until the following day. As  such, even if DAN had been able to evacuate the diver to this chamber that first  evening he would not have been treated. </p>
      <h2><strong>Arranging an Emergency Medical Evacuation </strong></h2>
      <p>Even  though DAN are the experts in diving accident management with 30+ years of  experience helping sick and injured divers, the unique set of circumstances  that present for each case can and do create challenges. In this case, whilst  the island of Kadavu has a landing strip, it doesn’t have lights in order to  guide a plane in for a safe landing. Despite an urgent evacuation to a nearby  chamber being our best option, the member was forced to remain in Kadavu until  the following morning. He was fortunate that the island had a medical centre  with oxygen, although the supply was limited and would have run out if not  supplemented by some from local dive shops. </p>
      <h2><strong>Coverage for  Diving Incidents</strong></h2>
      <p>In this case,  the air evacuations alone cost in excess of US$110,000 and that doesn’t include  the cost of the Chamber treatments! As a DAN Member, the evacuation costs were  fully covered. And in this highly stressful situation, it meant neither Nigel or Sandra had to worry about how these costs would be covered and who was going  to arrange the two evacuations. </p>
