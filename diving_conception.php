 	  <h1>Diving Whilst Trying to Conceive </h1>
 	  <p><em><strong>Member Question: </strong>I know that women are advised not to dive while pregnant, but is it a good idea to take a dive trip while trying to conceive?</em></p>
 	  <p><strong>DAN Answer</strong>: Due to the strong ethical concerns raised by prospective study of   this issue, the literature that addresses this topic is limited. What is   available comes from case reports and surveys, which are summarized by   Dr. Maida Beth Taylor in the book, <em>Women and Pressure: Diving and Altitude</em> (Dr. Caroline Fife and Marguerite St. Leger Dowse, eds., Best Publishing, 2010).<br />
 	    <br />
 	    A   few studies suggest an increased risk of birth defects associated with   diving while pregnant. One retrospective questionnaire surveyed 109   women who dived before and during pregnancy; 69 of those women stopped   diving once pregnancy was confirmed. Among the 40 who continued to dive,   the survey suggested higher rates of low birth weight, birth defects   and neonatal respiratory difficulties relative to the group that stopped   diving. Taylor wrote, &quot;Of particular interest is the list of defects   reported: multiple hemivertebrae, absence of a hand, ventricular septal   defects, possible coarctation of the aorta, hypertrophic pyloric   stenosis and a birth mark.&quot; These reports must be taken in context, and   when other data on this topic are aggregated, &quot;[a] calculated rate for   these limited reports suggests a 2.7 percent rate of serious anomalies,   consistent with the estimated incidence of major congenital anomalies (2   to 3 percent) reported in the USA, Canada, the United Kingdom and   Wales.&quot;<br />
 	    <br />
 	    According to Taylor, the developmental risk to a fetus   when the mother dives during the first trimester appears to be   &quot;vanishingly small.&quot; As more women avoid diving when pregnant or when   trying to conceive, the availability of cases will continue to decline,   and data will remain limited. Despite the lack of hard evidence, concern   about potential links to birth defects leads to the reasoned   recommendation to<br />
 	    refrain from diving during the periconceptional period as well as during pregnancy.</p>
 	  <p><em>Answer provided by <em> Nicholas Bird, M.D., MMM</em></em><br />
