	      <h1>Party, Party, Party!   </h1> 
          <p><em>Divers  often gather for a beer or two after a long day of diving, and some, let’s face  it, drink more than just one or two! But what are the risks involved in doing  this, especially when we are likely to get back in the water early the next  morning?</em></p>
          <p>Drinking  heavily over a short time is well known to cause nausea, vomiting, memory loss,  unsteadiness and slowed reaction time, slurred speech, poor co-ordination,  impaired judgement and of course the lack of inhibition. </p>
          <p>To  go diving after drinking alcohol is very risky, even the following day. It can  take more than 12 hours for the blood alcohol to fall to normal. A hangover is  disabling; headache and nausea increase risk of seasickness. Dizziness, poor  co-ordination and judgement will impede physical and mental performance.  Dehydration can be severe and increases the risk of decompression illness.</p>
          <p>Nitrogen  narcosis symptoms may be amplified and brought on at a shallower depth than  normal. You not only put yourself at higher risk but possibly also your buddy.</p>
          <p>Dr  John Parker (a diving  physician and Senior Dive Medical Consultant for DAN AP) recommends limiting your alcohol intake to no more than two standard  drinks the night before a dive and retiring to bed at an early time. There  should be no alcohol intake the day of diving until all dives have been  completed. Before taking any alcohol ensure you have drunk plenty of water and  are well hydrated.</p>
