        <h1><strong>자주</strong><strong> </strong><strong>묻는</strong><strong> </strong><strong>질문</strong><strong> </strong><strong> </strong>(FAQ's)<br />
        </h1>
        <h2><u> </u><strong>댄 회원</strong><strong> &amp; 보험</strong><strong> </strong>(Membership &amp; Insurance) </h2>
        <h6><strong>댄 회원이 무엇입니까</strong><strong>?</strong><strong> </strong>(What is DAN Membership?) </h6>
      <p>댄 회원은 제일 가까운 곳에 위치한 적절한 의료 시설<a href="_pdf/TravelAssist_Korean.pdf">(더 자세한 내용은 여기를 클릭하세요)</a>로의 응급 의료 후송 비용을 보장 받을 수 있습니다. </p>
      <p> 또한 트레블 어시스턴트(Travel  Assistance)에서 제공하는 다양한 혜택도 보장 가능합니다. 이는 치료와 관련된  비용은 보장되지 않음을 주의하여 <br />
      주시기 바랍니다.</p>
      <p> 댄 회원은 또한 댄에서 발행하는 얼러트 다이버(Alert  Diver)잡지, 회원 전용 구간(Member-Portal) 그리고 이메일로 발행되는 정기 <br />
        소식지(Deeper with DAN 그리고 Safety  Stop) 등의 혜택을 받으실 수 있습니다. <br />
      </p>
      <h6><strong>댄 사고 보험이 무엇입니까</strong><strong>?</strong><strong> </strong>(What is DAN Dive Injury Insurance?) </h6>
        <p>댄은 약관에 따른 보험 규정 범위 내 다이빙 사고와 관련된 치료와 다른 특정 비용을 보장하는 폭 넓은  다이빙 사고 보험을 제공합니다. <br />
        (보험은 댄 회원과 함께 묶어 패키지로 제공 됩니다) </p>
        <p><a href="_pdf/korean_comparison_table.pdf">댄 사고  보험 비교표를 위해 여기를 클릭하세요. </a><br />
        </p>
        <h6><strong>댄 회원와 사고 보장 기간이 얼마 입니까</strong><strong>?</strong><strong> </strong>(What is the Coverage Period?) </h6>
        <p>12개월입니다. <br />
        </p>
        <h6><strong>댄 다이빙 상해 보험에는 어떤 차이가 있나요</strong><strong>?</strong><strong> </strong>(What are the main difference between the Dive Injury Insurance Plans?) </h6>
        <p><a href="_pdf/korean_comparison_table.pdf">보험 비교표를 참고하여 주십시오. 여기를 클릭하세요.</a><br />
        </p>
        <h6><strong>여행 보험을 추가로 들어야 할까요</strong><strong>?</strong><strong> </strong>(Should I also take out Travel Insurance?)</h6>
      <p>댄은 다이빙 여행을 가실 때 댄 사고 보험뿐만 아니라 여행보험을 추가로 가입하시라 권장해 드립니다. 여행보험은 수화물 분실 등 댄 회원 혹은 댄 다이빙 <br />
        사고보험으로 보장되지 않는 내역의 보장을 제공하기 때문입니다. </p>
      <p>&nbsp;</p>
      <h2><strong>가입</strong><strong> </strong><strong>신청서</strong><strong> </strong><strong>처리</strong><strong> </strong><strong>절차</strong><strong> </strong>(The DAN Application Process) </h2>
        <h6><strong>온라인으로 가입 신청을 완료 했습니다</strong><strong>. 그 다음은 어떻게 되나요?</strong><strong> </strong>(I've applied for DAN Coverage,now what?) </h6>
        <p>이메일이나 팩스로 가입 신청서를 댄 아시아-태평양으로 보내셨다면 가입 신청서가 처리되기 까지 5일(주말, 공휴일 제외)이 
          걸립니다.      </p>
        <p>댄 회원 번호를 받으신 후에 댄 회원&amp;보험 보장을 받으실 수 있음을 주의 바랍니다. 회원 번호는 가입 신청서가 처리 된 후 고객님의 이메일로 
        전송됩니다. </p>
      <p> 후 댄 회원자료를 우편으로 받으시게 됩니다. 회원님의 거주지역에 따라 배송 기간에 차이가 나게 되며 대략적으로 일주일에서  
            늦게는 삼 주의 시간이 예상됩니다. </p>
            
            
      <p>
      
                회원 가입 승인 이메일을 받지 못하셨다면 <a href="mailto:korea@danap.org"><strong>korea@danap.org</strong></a> 로 이메일을 보내주시거나 +61 3 9886 9166으로 전화 주시기 
        바랍니다.<br />
      </p>
           
          <h6><strong>기존 질병이나  소인요인에 대해 배제요인이 적용 되나요?</strong> (Medical Exclusions) </h6>
              </h5>
              <p>기존 질병이나 소인요인으로 인해 직접적 혹은 간접적으로 일어난 사고나 질병과 관련해서는 보험금이 지급되지  않습니다. 하지만 댄에 고지하였고 보험 회사로부터 인정된 과거의 감압질환(DCI)에 대해서만 예외가 인정됩니다. 
              </p>
              <p>&nbsp;</p>
      <h2><strong>여행</strong><strong> </strong>(Travelling)</h2>
        <h6><strong>이사 혹은 장기 여행으로 인하여 댄에 등록된 거주지 주소로부터 장기간 떠나 있게 된다면 어떻게 하나요</strong><strong>?</strong><strong> </strong>(Moving or Travelling for an Extended Period) </h6>
        <p>댄 아시아-태평양  회원 가입 시 등록한 거주지를 떠나게 될 경우에는 댄 아시아-태평양에 새 거주지 주소를 반드시 통보하여야  합니다. +61 3 9886 9166으로 전화 하시거나 <a href="mailto:korea@danap.org">korea@danap.org</a> 로 이메일을 보내시면 됩니다.  이는 보험 법규를 따르기 위하기 만이 아니라 댄 회원이 중요한 회원 관련 정보와 안전 관련 정보를 항시 제공받고 보상  혜택을 최대한 누리기 위함 입니다. 만약 댄 아시아-태평양 보장 지역  내에 있는 나라 밖으로 이주를 하시거나 3개일 이상을 댄 아시아-태평양  보장 지역 밖으로 떠나 계신다면 반드시 댄 아시아-태평양에게 연락을 취하여 이를 통보 해야 합니다.  이는 만약의 사고 발생시 보험 혜택을 받으실 수 있게 함이고 새로 거주하시는 나라(혹은 장기여행 시 대부분의 시간을 보낼 나라)에서 적절한 보험을 가지실 수 있게 함입니다.    </p>
        <p>&nbsp;</p>
        <h2><strong>사고</strong><strong> </strong>(Incidents)        </h2>
        <h6><strong>사고가 나거나 응급 상황 발생 시 어디에 연락을 해야 하나요</strong><strong>? </strong><strong> </strong>(Who do I call in the event of an accident or emergency?) </h6>
        <p><a href="korean_emergency.php">댄 응급 절차를 확인 해 주십시오. 여기를 클릭하세요. </a><br />
        </p>
        <h6><strong>댄 트레블어시스트</strong><strong>(TravelAssist) 가 응급후송을 전담해야 하나요?</strong><strong> </strong>(Does TravelAssist Need to Arrange an Evacuation?) </h6>
        <p>네. 그렇습니다. 관련 비용이 댄으로 청구가 되기 위해서는 댄 트레블어시스트가 모든 응급  후송을 관할해야 합니다. 응급후송이나 의료 송환이 필요한지의 여부는 댄 트레블어시스트가 현지 의료진과 상의를  통하여 결정될 것입니다. 이 관련 비용은 댄이 응급후송을 담당한 회사에게 직접 지불할 것입니다.  <br />
        </p>
        <h6><strong>보험 규정 내  다이빙 사고가 무엇입니까? (What is a Covered Diving Accident)? </strong></h6>
        <p>보험 규정 내 다이빙이란 여가적 다이빙 혹은 다이빙 강사, 다이브 마스터, 수중 사진사 혹은American Academy of  Underwater Scientists(미국수중과학자학회) (AAUS)나 기타 관련된  기관의 다이빙 안전 수칙을 수행하며 연구 다이빙을 하는 다이빙 입니다. 보험 규정 내 다이빙은  입수  시 시작이 되며  퇴수 시 만료가 됩니다. 다이빙 사고란 질병이나 신체 부상으로 보험 규정 내 다이빙  중 일어나는 경우를 말합니다. 사고란 보험 규정 내 다이빙 중 확인 가능한 시간과 장소에서 일어나는 갑작스런,  예기지 않은, 드문, 특정한 사건입니다.</p>
