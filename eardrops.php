        <h1>Eardrops</h1>
        <p align="justify"><a href="http://www.chinadiveshow.com"></a>Eardrops are a solution of medications intended for  application in the external ear canal.<strong> </strong></p>
        <h2><strong>Prophylactic Use of Eardrops</strong></h2>
        <p>For most  divers, eardrops are not necessary after diving. The purpose of most eardrops  is to prevent external ear canal infections (known as otitis externa or  swimmer's ear). Infections of the ear canal are associated with persistent  moisture as well as local skin trauma, which can result from inserting cotton  swabs or other objects into the ears that can damage the thin skin lining the  ear canal. As DAN medical information specialists are fond of saying,  &quot;Don't put anything smaller than your elbow in your ear.&quot; Persistent  moisture and local skin trauma can enable bacterial overgrowth and infection.<br />
            <br />
          Eardrops are formulated to help dry the ear after exposure and lower the  acidity (pH), making the external ear canal an unfriendly environment for  bacterial or fungal colonization and infection. Common ingredients include  acetic acid (the active ingredient in vinegar), boric acid, aluminum acetate,  sodium acetate, isopropyl alcohol and glycerin. The acids alter pH, which  retards bacterial growth; aluminum acetate and sodium acetate are astringents,  which shrink tissues. Isopropyl alcohol helps dry the tissues, and glycerin may  help lubricate the skin to prevent excessive drying.<br />
  <br />
          For divers plagued by swimmer's ear, gently rinsing the ears with freshwater  after each dive may help. Drying the ears with a hair dryer may also be  helpful, but take care to ensure the air is not too hot.</p>
        <h2><strong>Therapeutic Use of Eardrops</strong></h2>
        <p>Eardrops  can be prescribed by your physician to treat infection or inflammation of the  external ear canal. These drops may contain antibiotics and/or steroids.<br />
            <br />
            <em>Note: It is important to never put drops into the ear canal if eardrum  rupture is suspected. Normally the eardrum serves as a barrier to the  middle-ear space. If ruptured, contamination or medications harmful to the  inner ear can gain access.</em></p>
        <h2><strong>Fitness to Dive</strong></h2>
        <p>Prophylactic ear drops are used to prevent external canal infections  during repetitive multiday diving. If you feel ear pain, you should stop diving  and have your ear evaluated. Divers diagnosed with an ear infection or ear  injury should not dive before fully healed and cleared by a physician.</p>
